<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The Sabbatical Year is characterized by the releasing of debts that are owed
to us,<sup><small><a name=fr2 href=#fn2>2</a></small></sup>
and by allowing our land to rest - that is, not cultivating the land or
harvesting its crops. On the one hand, this <i>Mitzvah</i> can be viewed as
one of benevolence ("<i>so that the poor among your people can eat ...</i>"
<u>Exodus 23:11</u>) because (1) crops that grow of their own accord during
the <i>Sh'mittah</i> are available to the poor for gleaning, and (2)
Israelite debtors who cannot repay what they have borrowed can be released
from a lifetime of bondage. On the other hand, I believe that the primary
reason for the Sabbatical Year is revealed in <u>Deuteronomy 25:20-22</u>,
which promises God's supernatural provision to those who will obey Him and
trust Him.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
A question that naturally comes to mind is how releasing loans and resting
our land in the Sabbatical Year applies today. I am of the opinion that it
continues to apply where today's circumstances are similar to those under
which the ancient Israelites lived. Furthermore, when it does apply, it
applies not only to Jews, but also to <i>K'rov Yisrael</i> Gentiles. How
similar the circumstances need to be are revealed to us by the <i>Ruach
HaKodesh</i> as we pray for guidance.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
As for most Gentiles (not <i>K'rovei Yisrael</i>), I believe that their
compliance is a blessing but not a requirement; I draw this conclusion (as an
analogy) from <i>Isaiah 56:1-7</i>, which promises blessing to the foreigner
who keeps God's Sabbaths, but apparently does not require it of him. One
thing that is certain, however, is that God continues to expect all of us to
look to Him for provision, rather than to think that we can cause provision
to come to us through our own efforts.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
We ought to consider the Sabbatical Year (and God's other Sabbaths - both
weekly and annual) to be a test of our faith, because our willingness to
release loans and to rest our land in the Sabbatical Year (as well as rest
ourselves on the other Sabbaths), is an indication that we trust God, and are
willing to rely upon Him.
</font></p><hr width=150px style="margin-left: 10px">
<p align=left style="margin-left: 30px; margin-right: 30px">
<font size=2>
<a name=fn2 href=#fr2>2.</a>
There is a rabbinical document called a <i>pruzbul</i>,
by which a private debt is made public by transferring it to a <i>beit
din</i>, thus making it redeemable during and after a
<i>Sh&#039;mittah</i>. It is a legal contrivance to circumvent the
discharge of a debt in the <i>Sh&#039;mittah</i>.
</font>
</p>
<hr width=150px align=left style="margin-left: 10px">
<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary by Daniel C. Juster</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
In modern societies that are not primarily agricultural, the <i>Sh'mittah</i>
command (like th Jubilee command) is difficult to apply. However, there are
underlying principles that have been noted by many Bible scholars. First, the
command shows the importance of renewing and preserving the land so that it
is not worn out. Secondly, the command shows that God does not want people
to be forever burdened with debt, but rather to be able to have a new start.
So, the command encompasses a principle of their needing to be a way of
release from debts that lead to destruction. Bankruptcy laws are probably
the modern social equivalent because the same underlying principle is
involved. The Biblical text provides that assistance should be made
available for those truly in need, and the Sabbatical year's discharge of
debts, coupled with the requirement that we not harvest the corners of our
fields (thereby allowing some of our crops to be gleaned) are special
applications of God's requirement of us that we care for the poor.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides, Meir, and HaChinuch agree that all debts must be cancelled during
the Sabbatical Year, and that loans to needy Jews may not be withheld because
an approaching <i>Sh'mittah</i> will preclude their collectability. Although
<u>Deuteronomy 15:3</u><i>permits</i> the repayment of loans made to
foreigners to be demanded during the <i>Sh'mittah</i>, Maimonides and
HaChinuch interpret the Scripture as <i>requiring</i> that loans to idolaters
be collected. Meir's compilation does not reference <u>Deuteronomy 15:3</u>
at all, and does not promulgate any such requirement. Also, none of the
commentators connect trusting God with the <i>Sh'mittah's</i> cancellation of
debts, or with Scripture's prohibition against our refusing to make
uncollectable loans to needy Jews prior to a <i>Sh'mittah</i>.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Similarly, neither Maimonides, nor Meir, nor HaChinuch connect trusting God
with Scripture's requirement that we allow our land to rest during the
Sabbatical Year. Meir alone asserts that agricultural commandments
pertaining to the <i>Sh'mittah</i> are only applicable in <i>Eretz
Yisrael</i>, and he therefore places them in an appendix, and lists them
separately from his other <i>mitzvot</i>.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
