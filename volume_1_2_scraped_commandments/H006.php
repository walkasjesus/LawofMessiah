<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
How can <u>Romans 13:1-6</u> say:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>For there is no authority that is not from God, and the existing
authorities have been placed where they are by God.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
And how can <u>Hebrews 13:17</u> say without qualification:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>Obey your leaders and submit to them.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Are there not evil rulers that would command us to do ungodly things and yet
we must obey them? It is indeed a conundrum unless we consider that the
leaders spoken of are those referred to in <u>Deuteronomy 17:14-20</u> -
leaders approved by God, and governing with knowledge of <i>Torah</i>, and
with intent to do God's will. These rulers may be of our secular governments
or they may be the elders of our congregations but, if we do what we ought to
do - choose rulers that are approved by God and accountable to Him, all will
be well. This requires that we be involved in the appointing process of our
leaders, in order to make sure that whether king, or president, or governor,
or congregational elder - our rulers will be of the mind and character to
govern in godly ways. Yes, things go awry in this fallen world and, when
they do, we must take our instruction from God (through the Holy Spirit) and
rely on Him to set things right. And until things are set right, if we are
commanded against God's <i>Torah</i>, must obey God and not man (see <u>Acts
5:26-29</u> & <u>Revelation 13:6-10</u>).
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides and HaChinuch refer to <u>Deuteronomy 17:16-17</u> to support
their <i>mitzvot</i> stating only that a king must not amass great personal
wealth. The quintessential example of a king that did amass great wealth is
King Solomon who, according to <u>2 Chronicles 9:13-29</u>, received
twenty-two tons of gold each year in addition to other items of worth, and
according to <u>1 Kings 11:3</u>, had seven hundred wives and three hundred
concubines who eventually turned his heart away from God. Meir wrote no
<i>mitzvah</i> on the subject.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
