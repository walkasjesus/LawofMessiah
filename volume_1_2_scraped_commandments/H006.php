<font size=4 face="Times New Roman, Times, serif">
<b><i>If we appoint a ruler over us, he is to be one whom God chooses - one
who rules according to the Word of God, and does not prefer himself over his
people.</i></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This precept is derived from His Word (blessed be He):
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Key Scripture</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Deuteronomy 17:14-20</u>
(Maimonides RN365; Chinuch C502)
<br>
<i>When you have entered the land ADONAI your God is giving you, have taken
possession of it and are living there, you may say, &#039;I want to have a
king over me, like all the other nations around me.&#039; In that event,
you must appoint as king the one whom ADONAI your God will choose. He must
be one of your kinsmen, this king you appoint over you- you are forbidden to
appoint a foreigner over you who is not your kinsman. However, he is not to
acquire many horses for himself or have the people return to Egypt to obtain
more horses, inasmuch as ADONAI told you never to go back that way again.
Likewise, he is not to acquire many wives for himself, so that his heart
will not turn away; and he is not to acquire excessive quantities of silver
and gold. When he has come to occupy the throne of his kingdom, he is to
write a copy of this Torah for himself in a scroll, from the one the cohanim
and L&#039;vi&#039;im use. It is to remain with him, and he is to read in
it every day, as long as he lives; so that he will learn to fear ADONAI his
God and keep all the words of this Torah and these laws and obey them; so
that he will not think he is better than his kinsmen; and so that he will
not turn aside either to the right or to the left from the mitzvah. In this
way he will prolong his own reign and that of his children in
Isra&#039;el.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Romans 13:1-6</u>
<br>
<i>Everyone is to obey the governing authorities. For there is no authority
that is not from God, and the existing authorities have been placed where
they are by God. Therefore, whoever resists the authorities is resisting
what God has instituted; and those who resist will bring judgment on
themselves. For rulers are no terror to good conduct, but to bad. Would you
like to be unafraid of the person in authority? Then simply do what is good,
and you will win his approval; for he is God&#039;s servant, there for your
benefit. But if you do what is wrong, be afraid! Because it is not for
nothing that he holds the power of the sword; for he is God&#039;s servant,
there as an avenger to punish wrongdoers. Another reason to obey, besides
fear of punishment, is for the sake of conscience. This is also why you pay
taxes; for the authorities are God&#039;s public officials, constantly
attending to these duties.</i>
