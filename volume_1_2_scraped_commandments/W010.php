</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The key Scripture supporting this <i>Mitzvah</i> is Yeshua's rebuke to the
<i>Torah</i> teachers and Pharisees of his time for portraying themselves as
righteous by meticulously complying with minor <i>Torah</i> requirements that
others could observe them doing, while disregarding weightier requirements of
the <i>Torah</i> (e.g. justice, mercy, trust, justice, love, etc.),
violations that they could keep hidden. It is a lesson to us that
<i>Torah</i> is not to be pursued by observable self-sacrifice and
rule-keeping, but rather by searching the depths of God's heart for how He
wants us to mirror His image in relating to Him and to our fellow man.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
