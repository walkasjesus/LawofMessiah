</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This <i>Mitzvah</i> joins together into one, all the commandments in
Scripture that address the hunting and slaughtering (<i>sh'chitah</i>) of
animals and birds for
food<sup><small><a name=fr3 href=#fn3>3</a></small></sup>.
It illuminates the compassion and mercy God has for His created creatures,
and the holiness he requires of His people. Although hunting and
slaughtering for food are permitted, the harvested creature's blood must be
poured out at the time of its death in order for its meat to be considered
<i>kosher</i>. This is specifically commanded in <u>Leviticus 17:13</u> in
the case of hunting, and in <u>Deuteronomy 12:15-16</u>, <u>23</u> and
<u>15:20-23</u> in the case of slaughtering; its purpose is to comply with
the Scriptures that forbid us from eating
blood.<sup><small><a name=fr4 href=#fn4>4</a></small></sup>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Each way of harvesting an animal or bird has its own challenges. The
<i>kosher</i> way of <i>sh'chitah</i> (practiced by the ancients for
centuries) is to cut the neck of the creature in such a way that its blood
pours out, thereby fulfilling this <i>Mitzvah</i> automatically. The wound
that results from hunting, on the other hand, rarely causes the animal's or
bird's blood to pour from its body, so it must be bled as a separate and
intentional act. An animal or bird that dies of its own accord
(<i>n'veilah</i>) or is killed by a wild beast in the field (<i>t'reifah</i>)
may not be eaten because there is no way to bleed it, and no way to know how
long it has been dead nor the state of its former health. If it died of its
own accord, the latter is of obvious concern.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The prohibition against slaughtering an animal together with its young on the
same day (<u>Leviticus 22:28</u>) is a <i>mitzvah</i> of compassion that is
reflective of God&' compassion for His creatures. An analogous act of
compassion is commanded by <u>Deuteronomy 22:6-7</u>, which forbids us from
taking a mother bird who is sitting on chicks or eggs; the chicks may be
taken, but the mother bird must be let go.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
In today's developed world, most of us do not slaughter or hunt for our meat;
instead, we rely on professional slaughterers and butchers and, unless they
are rabbinically supervised, we have little control over what methods of
slaughter and bleeding may have been used. One solution is to only purchase
<i>kosher</i> meat; if that is not feasible, we ought to make some inquiry
into how the animals we purchase are slaughtered and their meat processed.
While certain procedures (such as shock or bolt stunning) used to render an
animal senseless prior to slaughtering might not satisfy rabbinical standards
of <i>sh'chitah</i>, bleeding it immediately after it is stunned would
probably satisfy the biblical standard.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Acts 15:27-29</u> reinforces <u>Genesis 9:3-4</u> (part of the Noahic Law)
in making it clear that Gentiles are not to eat meat with the blood still in
it, in much the same way as Jews are commanded to not to eat blood under the
Law of Moses<sup><small><a name=fr5 href=#fn5>5</a></small></sup>. There is a
common misinterpretation of these Scriptures which are often said to mean
that one must not eat the limb of a living animal. Certainly one must not,
but the prohibition is much broader than that; it is against the eating of
any blood<sup><small><a name=fr6 href=#fn6>6</a></small></sup>.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
In my opinion, both Jews and Gentiles are held to the same biblical standard
of not eating blood, which includes not eating meat from an animal in which
the blood has not been poured out. As for the other requirements of this
<i>Mitzvah</i>, they are mandated for Jews and <i>K'rovei Yisrael</i>,
and strongly recommended for other Gentiles. Ritual slaughter (other than
bleeding) is not biblically required for either Jews or Gentiles.
</font></p><hr width=150px style="margin-left: 10px">
