<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><i>We may eat from either a hunted or a slaughtered bird or animal, but
are forbidden to eat from an animal that was killed by beasts in the field or
that died of its own accord. Whether slaughtered or hunted, the
creature's blood must be poured out and, if it is an animal, its young must
not be slaughtered with it on the same day. If it is a mother bird sitting
on chicks or eggs, the chicks may be taken, but the mother bird must be
released.<sup><small><a name=fr1 href=#fn1>1</a></small></sup></i></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This precept is derived from His Word (blessed be He):
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Key Scripture</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
