<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides, Meir, and Chinuch agree that <u>Genesis 9:3-4</u> and
<u>Deuteronomy 12:23</u> forbid us to eat the limb or flesh cut from a living
animal. They acquire this interpretation from <i><u>Chulin 102b</u></i> of
the <i>Talmud</i> despite the fact that the Scriptures do not say that at
all. They also associate these Scriptures with <u>Exodus 22:30(31)</u>,
which commands us to not eat the flesh of an animal that has been torn by
wild beasts in the field.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides, Meir, and Chinuch state that we are forbidden to eat an animal
that has died of its own accord (<i>n'veilah</i>), and Meir adds that
<i>n'veilah</i> includes meat from an animal that was not properly
slaughtered.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
All three commentators take the position that <u>Deuteronomy 12:21</u>
teaches that ritual slaughter is required of all animals or birds that one
wishes to eat, and none of the commentators allow for eating a hunted animal
or bird - even if its blood is first poured out. This is probably one of the
most extreme of the rabbinical fences placed around the <i>Torah</i> in that
it adversely affects many in the world who must hunt in order to eat.
Consistent with this view, the commentators interpret <u>Leviticus 17:13</u>
as applying to ritual slaughter when, in fact, the actual subject of the
Scripture is hunting. HaChinuch says that fish and locusts need not be
ritually slaughtered. How one slaughters a locust he does not say.
