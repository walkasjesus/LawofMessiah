<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><i>Cohanim (priests) are to pray blessing over God's people.</i></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This precept is derived from His Word (blessed be He):
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Key Scriptures</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Numbers 6:22-27</u>
(Maimonides RP26; Meir MP58; Chinuch C378)
<br>
<i>ADONAI said to Moshe, "Speak to Aharon and his sons, and tell them that this
is how you are to bless the people of Isra&#039;el: you are to say to them,
&#039;Y&#039;varekh&#039;kha ADONAI v&#039;yishmerekha. [May ADONAI bless
you and keep you.] Ya&#039;er ADONAI panav eleikha vichunekka. [May ADONAI
make his face shine on you and show you his favor.] Yissa ADONAI panav
eleikha v&#039;yasem l&#039;kha shalom. [May ADONAI lift up his face toward
you and give you peace.]&#039; In this way they are to put my name on the
people of Isra&#039;el, so that I will bless them."</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Matthew 16:19</u>
<br>
<i>I will give you the keys of the Kingdom of Heaven. Whatever you prohibit on
earth will be prohibited in heaven, and whatever you permit on earth will be
permitted in heaven.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The Levitical <i>Cohanim</i> (Aaron and his sons) were commanded to bless the
"People of Israel" (the Jewish People) who, at that time, were the only
people group in covenant with God. The blessing (referred to today as the
<i>Birkat Cohanim</i> or the Aaronic Benediction) was to be given using the
exact words recorded in <u>Numbers 6:24-26</u>, the purpose of which was for
the <i>Cohanim</i> to put God's Name on the Jewish People, thereby
identifying whom God Himself would bless. Clearly, God did not need the
<i>Cohanim</i> to tell Him who the Jewish People were, so the commandment was
no doubt intended more as an impartation of authority, informing the
<i>Cohanim</i> that what they declared on earth (in the way of blessing)
would be made a reality in heaven - a concept that was to be reiterated years
later by Messiah Yeshua (see <u>Matthew 16:19</u>).
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
In the New Covenant, all believers in Yeshua are elevated to the status of
priests (<i>Cohanim</i>) under Yeshua (<u>1 Peter 2:5-12</u>) who, Scripture
teaches, is our High Priest (<u>Hebrews 4:14</u>; <u>Revelation 1:4-6</u>).
Extrapolating from <u>Numbers 6:22-27</u>, I therefore conclude that, as was
required of Aaron and his sons, we who are New Covenant <i>Cohanim</i>,
through our belief in Yeshua, are also required to pray blessing upon the
Jewish People; and not only upon them, but also upon the newest "People of
God" as well - all who believe in Yeshua - both Jews and non-Jews. And there
is good biblical evidence that our responsibility to bless does not stop
there, but extends even to the world's unbelievers through our bringing them
the Good News of Yeshua and praying for their salvation (<u>Matthew 9:36-38,
28:16-20</u>; <u>James 5:16</u>).
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Although <u>Hebrews 8:13</u> indicates that the Mosaic Covenant is
diminishing and is in the process of vanishing, it is never said, in
Scripture, to have already vanished. Without the Temple, Levitical
<i>Cohanim</i> today have few functions, but those remaining include
ceremonially blessing the Jewish People with the <i>Birkat Cohanim</i>
(<u>Numbers 6:22-27</u>), receiving special <i>aliyot</i> to read from the
<i>Torah</i>, and participating in redeeming a Jewish family's firstborn son
(<i>Pidyon HaBen</i> - <u>Numbers 18:15</u>). In Orthodox Judaism, only
Levitical <i>Cohanim</i> are authorized to minister the <i>Birkat
Cohanim</i>, but in other denominations, others may do so. In Messianic
Judaism, for example, it may be prayed over a congregation by a Levitical
<i>Cohen</i> if one is available, but often the Rabbi, the <i>Chazan</i>
(Cantor), or other officiating leader ministers it at a service's conclusion
in reliance upon his New Covenant priestly status. Since the New Covenant
priesthood is not limited to Jews, Gentile leaders are also authorized to
minister it.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The matter of women leading synagogue services is controversial; in my
opinion, women should not be in that position of authority and should
therefore not minister the <i>Birkat HaCohanim</i>. Those that believe
otherwise, however, rely on the undisputed fact that the New Covenant
priesthood extends to women as well as to men.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides says that <i>Cohanim</i> must bless Israel every day, but does not
say it has to be with the <u>Numbers 6:24-26</u> text. Meir does not say
that the blessing must be given every day, but does say that it must be given
during a service and the text in <u>Numbers 6:24-26</u> must be used.
HaChinuch concurs with Maimonides that the <i>Cohanim</i> must bless Israel
every day, and gives many more particulars on how it is to be done. Two of
the particulars are that the Birkat HaCohanim that must be imparted is just
"<i>Sim Shalom</i>", and that the <i>Cohe</i>n delivering the blessing must
position his hands in a special way.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
