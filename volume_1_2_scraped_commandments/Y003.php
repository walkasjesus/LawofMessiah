<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The various Scriptures supporting this <i>Mitzvah</i> speak of trusting him,
and others speak of believing in him; both infer having faith in him and in
his Father in heaven who sent him. It is important to understand that the
kind of faith to which we are called is not mere intellectual belief for we
read in <u>James 2:19</u>:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>You believe that "God is one"? Good for you! The demons believe it too -
the thought makes them shudder with fear!</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Our belief must be more than cognitive - it must be a full embracing of
Yeshua as our Lord and Messiah - no less than what is stated in <u>Romans
1:8-9</u>:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>What, then, does it say? "The word is near you, in your mouth and in your
heart." - that is, the word about trust which we proclaim, namely, that if
you acknowledge publicly with your mouth that Yeshua is Lord and trust in
your heart that God raised him from the dead, you will be delivered.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Although pursuing eternal life (salvation) is not commanded, it is a clear
benefit and result of trusting and believing in Yeshua.
