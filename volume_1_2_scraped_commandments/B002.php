<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><i>We are to return an item we are holding in pledge when the owner is in
need of it.</i></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This precept is derived from His Word (blessed is He):
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Key Scriptures</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Exodus 22:25(26)</u>
(Maimonides RP199; Meir MP63; Chinuch C587)
<br>
<i>If you take your neighbor&#039;s coat as collateral, you are to restore it
to him by sundown.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Deuteronomy 24:12</u>
(Maimonides RN240; Meir MN61; Chinuch C586)
<br>
<i>If he is poor, you are not to go to bed with what he gave as collateral in
your possession;</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Deuteronomy 24:13</u>
(Maimonides RP199; Chinuch C587)
<br>
<i>rather, you must restore the pledged item at sunset; then he will go to
sleep wearing his garment and bless you. This will be an upright deed of
yours before ADONAI your God.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Ephesians 4:31-32</u>
<br>
<i>Get rid of all bitterness, rage, anger, violent assertiveness and slander,
along with all spitefulness. Instead, be kind to each other, tenderhearted;
and forgive each other, just as in the Messiah God has also forgiven you.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Colossians 3:12</u>
<br>
<i>Therefore, as God&#039;s chosen people, holy and dearly loved, clothe
yourselves with feelings of compassion and with kindness, humility,
gentleness and patience.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Although historically commanded to Israelites concerning lending money to
(and taking security from) other Israelites, compassion suggests that this
<i>Mitzvah</i> be applied to everyone. It in fact raises the question of
whether it is proper, in the first place, to take possession of any pledged
object which is likely to be needed in the future.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
In modern times, it is unlikely that a person who is not in the pawn business
will take security of chattel for a loan, and it is even less likely that
chattel security would involve clothing. Yet, there might be a moral analogy
if one takes possession of an item such as a person's tools if the tools are
needed by him to earn a living.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides presents a more literal interpretation, requiring the pledged item
to be returned during the day or night as needed. "Need", in this context,
is on the level of survival, protection, earning capacity, health, and basic
comfort. HaChinuch is consistent with Maimonides and Meir.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
