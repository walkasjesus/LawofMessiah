</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Supportive Scripture</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>2 Corinthians 6:16-17</u>
<br>
<i>What agreement can there be between the temple of God and idols? For we are
the temple of the living God - as God said, "I will house myself in them ...
and I will walk among you. I will be their God, and they will be my
people." Therefore ADONAI says, "Go out from their midst; seperate
yourselves; don&#039;t even touch what is unclean. Then I myself will
receive you".</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This <i>Mitzvah</i> historically applies to having reverence for the
Tabernacle of God and later for the Temple, for these were, according to
Scripture, most holy. Today, we may apply this <i>Mitzvah</i> to any
biblically authorized sanctuary in which God dwells or in which we worship
Him. This includes edifices such as synagogues, churches and chapels, and
may be extended to their furnishings and accoutrements that are set aside as
holy objects used in worship. We must treat even the space of these
sanctuaries with respect and not conduct ourselves in ways that demean their
purpose.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Some synagogue and church sanctuaries are temporary or convertible spaces
that are used for other things besides worship. In such cases, the space
ought to be ceremonially commissioned when it is about to be used as a
sanctuary, and decommissioned, and its holy objects put away, when its use as
a sanctuary is concluded.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The <u>1 Corinthians 6:19-20</u> Scripture is interesting in that it applies
to our personal bodies and also to our corporate body, the body of believers.
Having reverence for these bodies is not very different from how we are to
treat sanctuaries of brick and mortar, in that we are to care for them and
not subject them to, or use them for, unholy purposes.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides and HaChinuch treat their respective <i>mitzvot</i> as only
applying to the historical sanctuary of the Temple, whereas Meir applies it
in a more contemporary way. He refers to synagogues and places where
<i>Torah</i> is studied as "little sanctuaries" and states that we must not
do ordinary and frivolous things in them like sleep, and engage in laughter
and idle conversation.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
