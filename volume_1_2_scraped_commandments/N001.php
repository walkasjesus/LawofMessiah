<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides, Meir, and HaChinuch treat loving neighbors, <i>gerim</i>, and
enemies as separate <i>mitzvot</i>, and draw distinctions between neighbors
who are Jews and those who are not. Also, the term <i>ger</i> refers to
non-Jewish foreigners who have come to live within the cities of Israel
(within Jewish communities), but it more certainly refers to proselytes who
have converted to Judaism. The classical commentators' concepts of "love",
as expressed in their respective <i>mitzvot</i>, are very similar to how
"love" is defined in this <i>Mitzvah</i>.
