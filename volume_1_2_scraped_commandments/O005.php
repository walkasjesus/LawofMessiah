<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
In <u>Matthew 10:5-8</u> we read of how Yeshua's disciples were sent out to
minister to the "lost sheep of the house of Israel" with Holy Spirit power
that included power to cast out demons. Earlier in the Scriptures, we see
numerous examples of Yeshua casting out demons and expecting his disciples to
do the same. For example, in <u>Matthew 17:20</u> (<u>verse 21</u> in NKJ)
we read of how, after seeing Yeshua cast out a demon, his disciples were
perplexed by not being able to cast it out themselves:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>And he said to them: "Because you have such little trust! Yes! I tell you
that if you have trust as tiny as a mustard seed, you will be able to say to
this mountain, 'Move from here to there!' and it will move; indeed, nothing
will be impossible for you!"</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
In another Scripture (<u>Mark 9:28-29</u>), we read:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>After Yeshua had gone indoors, his talmidim asked him privately, "Why
couldn't we drive it out?" He said to them "This is the kind of spirit that
can be driven out only by prayer."</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Because the Holy Spirit is with us today as he was during and after the time
of Yeshua, we too have the power to cast out demons in Yeshua's name. That
notwithstanding, we are cautioned by the Scriptures that underlay this
<i>Mitzvah</i> not to do so if we perceive that the person who is the subject
of the ministry cannot hold the deliverance due principally to lack of faith
or continued or un-repented sin.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
