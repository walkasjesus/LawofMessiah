<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<i>Chametz</i> is any food that contains leaven, the quintessential one being
bread leavened with yeast. This <i>Mitzvah</i> states that we must not eat
<i>chametz</i>, from Passover through the seventh day of the Feast of
Unleavened Bread. Each of the supporting Scriptures above states it a little
differently. <u>Exodus 13:3</u> instructs abstention from <i>chametz</i>,
<u>Exodus 12:19-20</u> prohibits eating food that contains <i>chametz</i>,
and <u>Deuteronomy 16:2-3</u> prohibits eating <i>chametz</i> along with the
sacrificed Passover lamb, all of which amount to the same thing which is to
not eat <i>chametz</i> during the seven days of Unleavened Bread.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The particulars of what should be considered leaven and <i>chametz</i> are
controversial among Jewry. Sephardic and Ashkenazic communities follow
different rules on the subject, and Messianic Jewish communities are equally
diverse. Is leaven anything that can cause food to rise, or must there be
fermentation in order for it to qualify? What grains must be removed from the
house because they are subject to spontaneous fermentation during storage?
Rice? Barley? Beans? Discussions of such particulars are found in the
<i>Talmudic</i> literature, but not in any of the classical <i>mitzvah</i>
codifications. I do not think it would be judicious for me to express my
opinion of these particulars here. The general idea in the Scriptures is
that the Israelites' bread that would otherwise have fermented with yeast did
not have time to rise. Therefore, a commonly held view that our not keeping
casually-stored grains that would have had time to ferment and rise is what
Scripture intends. An alternative view discounts the effect of airborne yeast
and calls for discarding only those items that contain added leaven. The
commercially prepared foods found in today's homes pose a special problem of
identification in that some contain leaven where you would not expect it.
Soups, for example, often contain yeast. My personal practice is to read all
of the labels on packaged and canned foods and discard those that contain the
words "leaven" or "yeast".
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This <i>Mitzvah</i> is mandated for Jews and <i>K'rov Yisrael</i> Gentiles,
but not for Gentiles generally. <u>Exodus 12:19-20</u> makes this clear in
that most Gentiles were not part of the community of Israel in the first
place, so their being cut off from it for eating <i>chametz</i> is
meaningless.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The classical commentators' <i>mitzvot</i> and this <i>Mitzvah</i> are in
agreement except for terminology. They use the word "<i>Pesach</i>" as
inclusive of both Passover and the 7 days of the Feast of Unleavened Bread,
whereas my use of the word refers only to the evening period from
<i>Nisan</i> 14 to 15 when God "passed over" the homes of those Israelite
families that applied the blood of the sacrificed lamb to their doorposts.
Maimonides, Meir, and HaChinuch agree that <i>chametz</i> is not to be
eaten after the middle of the 14<sup><small>th</small></sup> day of
<i>Nisan</i> because the Passover lamb was slaughtered at dusk.
