<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This <i>Mitzvah</i> directs us to honor and respect persons of advanced age
for two important reasons. First, those who have lived longer than we are
assumed to have acquired wisdom through their greater life experiences, and
presumably also through walking with the Lord for a longer period of time.
Second, whatever knowledge and wisdom younger people may have, they have
likely acquired (in large measure) through the life experiences and God-given
wisdom of their
elders<sup><small><a name=fr2 href=#fn2>2</a></small></sup>
including from their parents (<u>Proverbs 22:6</u>).
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Why is this <i>Mitzvah</i> needed? Regrettably, it is because, too
frequently, persons of advanced age are not respected. I suggest that the
reason for this is because older persons, over time, lose some of their
physical attributes that are visible, while the ways in which they gain
(knowledge, wisdom, and experience) are less visible and less tangible.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
When Moses needed help governing Israel, he chose seventy men of advanced age
who were already respected leaders. It is also why <u>1 Timothy 3:2-12</u>
instructs us that when we ordain elders to govern our New Covenant
communities, each appointee must (among other things) be a man who is "above
reproach", "faithful to his wife", "self-controlled", "able to teach", be a
good manager "of his own household", have "children who obey him with all
proper respect", and he must not be a new believer. All of these come to a
man over time and with age, and it is why they are called "elders".
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Although the Scriptures that pertain to this subject refer mainly to men,
women are included as well. Women are, of course, to be honored and obeyed as
mothers regardless of their age (<u>Exodus 20:12</u>;
<u>Ephesians 6:1-3</u>), but older women, in particular, are exhorted to
conduct their lives in holiness so that they are able to train younger women
in the ways of the Lord (<u>Titus 2:3-5</u>). Because men, regardless of
their abilities, are not competent (nor is it appropriate for them) to train
women in many areas, women who have wisdom and experience that comes with
advanced age and who are also filled with the Holy Spirit, are exceedingly
valuable and are needed in every community.
</font></p><hr width=150px style="margin-left: 10px">
<p align=left style="margin-left: 30px; margin-right: 30px">
<font size=2>
<a name=fn2 href=#fr2>2.</a>
"Elders" here means persons who are older.
</font>
</p>
<hr width=150px align=left style="margin-left: 10px">
<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Daniel C. Juster</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
During biblical times, a man could attain maturity and be an elder at an age
that we, today, would consider quite young. Therefore, though given the
title "elder" and "overseer", the test is maturity and not chronological age.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides', Meir's, and HaChinuch's <i>mitzvot</i> that are based on
<u>Leviticus 19:32</u>, command that we honor persons of advanced age and
<i>Torah</i> scholars despite the fact that the Scripture says nothing about
scholars. Most of what they write has to do with the particulars of how we
extend honor, and their writings on this are fairly complex and legalistic.
To give a sense of it, here is a brief quote from Meir's MP17 from Charles
Wengrove's English adaptation of "<u>Concise Book of
Mitzvoth</u>":<sup><small><a name=fr3 href=#fn3>3</a></small></sup>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
The term sevah ("a man of hoary head") means someone of very advanced age,
even if he is not a man of wisdom. One is duty-bound to stand before him -
even a wise scholar who is very young; but he is not obligated to stand up to
his full height before him [the old man], but only enough to honor him. The
term <i>zaken</i> means <i>zeh she-kanah chochmah</i>, one who has acquired
wisdom. Even if he is very young, one is to stand up to his full height
before him, from the time he [the scholar] comes within four cubits until he
has left his presence.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Meir's treatment of the subject goes on to speak of honoring to one's
<i>Torah</i> teacher, and concludes by saying:
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Whoever disgraces such scholars will have no share in the world-to-come.
</font></p><hr width=150px style="margin-left: 10px">
