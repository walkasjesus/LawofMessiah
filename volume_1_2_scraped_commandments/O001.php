<b><i>We are to be salt for the earth and a light for the world.</i></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This precept is derived from His Word (blessed be He):
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Key Scriptures</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Isaiah 49:5-6</u>
<br>
<i>So now ADONAI says - he formed me in the womb to be his servant, to bring
Ya&#039;akov back to him, to have Isra&#039;el gathered to him, so that I
will be honored in the sight of ADONAI, my God having become my strength -
he has said, "It is not enough that you are merely my servant to raise up
the tribes of Ya&#039;akov and restore the offspring of Isra&#039;el. I will
also make you a light to the nations, so my salvation can spread to the ends
of the earth."</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Matthew 5:13-16</u>
<br>
<i>You are salt for the Land. But if salt becomes tasteless, how can it be made
salty again? It is no longer good for anything except being thrown out for
people to trample on. You are light for the world. A town built on a hill
cannot be hidden. Likewise, when people light a lamp, they don&#039;t cover
it with a bowl but put it on a lampstand, so that it shines for everyone in
the house. In the same way, let your light shine before people, so that
they may see the good things you do and praise your Father in heaven.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Matthew 6:22-23</u>
<br>
<i>The eye is the lamp of the body. So if you have a "good eye" [that is, if
