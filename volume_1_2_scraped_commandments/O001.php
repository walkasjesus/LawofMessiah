<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Because salt is essential for life, it is used in the Bible to symbolize
life. Related to it, light is used in the Bible to symbolize godly knowledge
and understanding that leads to life. Yeshua said of himself in <u>John
9:5</u>:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>While I am in the world, I am the light of the world.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
And yet, in <u>Matthew 5:14</u> he told his disciples:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>You are light for the world.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Could both be true? Yes, both were true, for he told his disciples in
<u>John 12:35</u>:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>The light will be with you only a little while longer. Walk while you have
the light, or the dark will overtake you; he who walks in the dark doesn't
know where he's going.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Yeshua was letting his disciples know that he was going away, but that he was
going to transfer his light to them so that when he left, his light would
remain. The Bible uses "light" to symbolize revelation and understanding as
far back as <u>Genesis 1:3</u> and, in <u>Isaiah 49:5-6</u>, Israel is said
to be a "light to the Gentiles" (i.e. the rest of the world). Therefore, when
Yeshua imparted his light to the original Jewish disciples (today all who
believer in Yeshua are his disciples), he did so with the expectation that
they/we would disseminate it throughout the world in his behalf.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
"Salt" is also used symbolically in the Bible. In addition to salt being an
intrinsic part of our bodies and therefore necessary for life, it is used to
preserve food and to make food flavorful. By Yeshua saying that we (his
disciples) are to be salt for the earth, and by his warning to us not to lose
our saltiness, he is sending us forth to minister the truth and life of God
through both our words and through our "enlightened" examples that others who
have need of knowing God would want to emulate.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
