<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary of Daniel C. Juster</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
I think binding on the hand and head are very likely intended to be
metaphoric - dedicating our minds and actions. However, I think affixing to
the doorpost could be meant as a literal reminder, and the fringe is
unquestionably literal. Also, a blue fringe is a symbol of royalty, so it
reminds the Jewish wearer of God's Word in the context of Israel being a
royal priesthood.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides, Meir, and HaChinuch interpret these Scriptures ultra-literally,
and most of their writings about them consists of details of construction and
usage. I believe they are wrong to associate the <u>Exodus 13</u> Scriptures
with <i>t'fillin</i>. There the sign on the hand and reminder on the
forehead refers to eating <i>matzah</i> during the Feast of Unleavened Bread.
