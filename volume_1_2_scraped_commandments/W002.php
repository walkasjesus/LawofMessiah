(See also <u>Matthew 9:20</u>,
<u>14:36</u>; <u>Luke 8:44</u>)
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This <i>Mitzvah</i> combines several commandments, each employing a physical
object to remind the Israelites of the commandments of God, and His having
brought us out of Egypt. There are four kinds of these reminders: (a)
something worn on the hand; (b) something worn on the forehead; (c) something
affixed to the doorpost and gates of one's house; and (d)
<i>tzitziyot</i><sup><small><a name=fr1 href=#fn1>1</a></small></sup>
containing a blue thread worn on the clothing. The Orthodox position and that
of the classical commentators is that each of these is intended by God to be
done literally, but some argue that they are metaphoric.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
I am inclined to view binding to the hand and forehead as metaphoric and
placing on the doorposts of houses as literal, but they are found in adjacent
verses of Scripture and, by normal principles of exegesis, the three of them
should therefore be viewed all one way or all the other. On the other hand,
I cannot say that the three of them are metaphoric and that wearing
<i>t'fillin</i> is a misapplication of Scripture because, in
<u>Matthew 23:5</u>, Yeshua criticizes the Pharisees for how they construct
and wear their <i>t'fillin</i>, but He does not admonish the Pharisees for
wearing them. Nevertheless, an argument for believing that binding to the
hand and forehead is not meant to be literal is that the expression "as a
sign on your hand and as a reminder between your eyes" is found in Scripture
as a sign of several different things. <u>Deuteronomy 6:4-9</u> appears to
be saying that God's commandments are a sign, <u>Exodus 13:5-10</u> appears
to be saying that <i>matzah</i> is a sign, and <u>Exodus 13:11-16</u> appears
to be saying that redeeming the firstborn is a sign.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Wearing <i>tzitziyot</i> containing a thread of blue is clearly intended to
be literal because of its specificity, and its consistency with the simple
four-cornered style of garments worn during the time of Moses; also, Yeshua
apparently wore them (<u>Matthew 9:20, 14:36</u>; <u>Luke 8:44</u>). The
question that modern Jews must answer for themselves is whether God intends
<i>tzitziyot</i> to be worn today when the type and style of clothing is not
as before. In Orthodox Jewish communities all men wear <i>tzitziyot</i>, and
there is discussion about whether they are also appropriate for women to
wear, but women normally do not wear them. In communities that have not
adopted a position on the wearing of <i>tzitziyot</i>, each individual must
make his own decision about whether to wear them as described in Scripture or
to substitute some other article of clothing or accessory that will
accomplish the same thing - reminding anyone seeing them of the commandments
of God.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
What fringes are is self-evident, so the remaining decisions (other than
whether to wear them at all) are what to construct them of, whether and how
to knot them, what the color and origin of the blue dye for the thread should
be, and what articles of clothing to attach them to. Since the only
requirement in Scripture is that they be placed on the corners of garments,
unless one's community defines the particulars of acceptable
<i>tzitziyot</i>, the wearer can use his own judgment on all of these. The
orthodox Jewish community has adopted detailed rules for manufacturing
<i>tzitziyot</i> which their male members wear under their shirts or vests
and attached to a special four-cornered garment called a <i>tallit katan</i>.
<i>Tzitziyot</i> are also worn on the traditional rectangular prayer shawl,
the <i>tallit gadol</i>.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The way to make and use <i>t'fillin</i> is not defined in Scripture, so one
who chooses to wear them either adopts the Orthodox construction and usage,
or exercises individual creativity. One contemporary kind of <i>t'fillin</i>
I have seen consisted of a cloth bracelet and headband, both embroidered with
words from Scripture; the bracelets were worn the entire day, and the
headband only during prayer. The same latitude exists for making and placing
<i>m'zuzot</i>. I have seen them made from tubes of diverse materials
containing hand-written parchments or pieces of paper, and also made in the
form of wood and ceramic plaques on which were engraved words of Scripture.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
It is important not to get so involved in the details of these "reminders"
that we forget what they are supposed to remind us of, which are the
commandments of God. We are not only to be reminded by them, but to be made
continually aware of God's Word as we proceed through each day. This is the
sense I receive from (1) the <u>Numbers 15</u> Scripture that speaks of not
going around after where our own hearts and eyes lead us, and from (2) the
<u>Deuteronomy 6</u> Scripture that speaks of talking about God's words as we
walk by the way.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
These commandments were given to the Israelites thousands of years ago, and
those who heard them directly from Moses no doubt knew exactly what God
meant; if they didn't, they could simply have asked Moses for clarification.
We are living in an age where many things have changed - clothing styles,
home styles, life styles, work schedules, etc., and we do not have Moses with
us to ask. We do, however, have God Himself with us and in us in the person
of the <i>Ruach HaKodesh</i>, so it is to Him that each of us should appeal
for clarification. I came to my own conviction about wearing
<i>tzitziyot</i> in this very way. Because I understood the principle behind
wearing them, I considered wearing a necklace of the tablets of the
<i>Torah</i> instead, reasoning that they too would remind me and others of
the Word of God. However, after praying for guidance, the guidance I
received from God was that He wanted me to wear <i>tzitziyot</i> because His
plans for me (as well as for other Jews), made it important that I be
perceived as a Messianic Jew who respected the community's traditions. I
recommend this "New Covenant" approach of seeking guidance from the <i>Ruach
HaKodesh</i> for not only these matters, but for all of life's questions;
God's <i>Ruach</i> was made available to us for that very purpose.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
In summary, I lean toward believing that the commandment to place God's words
on our hands and, foreheads are meant as metaphors to impress on us that
God's Word must form what we do and what we think. I cannot say so for sure,
however, because of the <u>Matthew 23:5</u> Scripture I mentioned earlier. I
can go either way regarding <i>m'zuzot</i>, but it is clear to me that the
commandment to wear <i>tzitziyot</i> is meant to be literal (see the
Scriptures indicating that Yeshua wore them), but that God may allow
substitutions on an individual basis.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
That notwithstanding, I consider the traditional literal ways of observance
to be valid implementations of the Scriptures so long as their broader
message is understood and pursued. One likely case for God allowing or even
desiring substitution in all of these may be for Gentiles living among Jews.
The principles are applicable to Gentiles, but the traditional ways of
observing the commandments have become almost completely associated with
being Jewish. Another case for substitution would be for women, for whom the
commandments are also applicable, but the traditional ways of observing them
have become almost completely associated with being male.
