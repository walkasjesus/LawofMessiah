<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This <i>Mitzvah</i> commands against seeking, engaging in, or utilizing
occult supernatural powers. The term "occult" covers a wide range of
prohibited activities that include (but are not limited to) divining,
fortune-telling, sorcery, astrology, magic, wizardry, witchcraft, enchanting,
soothsaying, spell-casting, or consulting the dead or communicating with
spirits other than the Spirit of God. This <i>Mitzvah</i> is relevant today
because many (if not all) of the occult practices mentioned in Scriptures are
still practiced in certain quarters. Some more recent occult activities that
are not mentioned in Scripture are hexing, water witching, numerology,
<i>Kabbalah</i>, voodoo, new age spiritism, mindreading, channeling, energy
healing (and there are others). It is not necessary to know the definitions
of each of these or how each is different from the others. All that one
needs to know is that it is a serious sin to attempt to communicate with any
spirit other than the Holy Spirit, or to seek power from any source other
than from God.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides, Meir, and HaChinuch each wrote ten <i>mitzvot</i> that
collectively express the principle of this <i>Mitzvah</i>. Curiously and
uncharacteristically, HaChinuch's <i>mitzvah</i> C514 (having to do with
a kind of wizardry) that is analogous to Maimonides' <i>mitzvah</i> RN37 and
Meir's <i>mitzvah</i> MN173, does not reference a Scripture from the
<i>Torah</i>, but only tractate <i>Kil'ayim</i> (viii, 4) of the Jerusalem
Talmud, and <u>Job 5:23</u> (see * above) that seemingly speaks to an
unrelated subject.
