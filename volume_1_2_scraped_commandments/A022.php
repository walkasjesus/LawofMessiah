<u>Proverbs 6:23-25</u>
<br>
<i>For the mitzvah is a lamp, Torah is light, and reproofs that discipline are
the way to life. They keep you from an evil woman, from a loose
woman&#039;s seductive tongue. Don&#039;t let your heart lust after her
beauty or allow her glance to captivate you.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Matthew 18:7-9</u>
<br>
<i>Woe to the world because of snares! For there must be snares, but woe to the
person who sets the snare! So if your hand or foot becomes a snare for you,
cut it off and throw it away! Better that you should be maimed or crippled
and obtain eternal life than keep both hands or both feet and be thrown into
everlasting fire! And if your eye is a snare for you, gouge it out and
fling it away! Better that you should be one-eyed and obtain eternal life
than keep both eyes and be thrown into the fire of Gei-Hinnom.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Mark 9:43-47</u>
<br>
<i>If your hand makes you sin, cut it off! Better that you should be maimed but
obtain eternal life, rather than keep both hands and go to Gei-Hinnom, to
