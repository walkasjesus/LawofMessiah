you should be lame but obtain eternal life, rather than keep both feet and
be thrown into Gei-Hinnom! And if your eye makes you sin, pluck it out!
Better that you should be one-eyed but enter the Kingdom of God, rather than
keep both eyes and be thrown into Gei-Hinnom ...</i>
