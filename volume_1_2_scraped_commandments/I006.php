</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Supportive Scriptures - The High Places Were Finally Destroyed</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>2 Kings 18:1-5</u>
<br>
<i>It was in the third year of Hoshea the son of Elah, king of Isra&#039;el,
