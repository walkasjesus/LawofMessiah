done. He removed the high places, smashed the standing-stones, cut down the
asherah and broke in pieces the bronze serpent that Moshe had made; because
in those days the people of Isra&#039;el were making offerings to it,
calling it N&#039;chushtan. He put his trust in ADONAI the God of
Isra&#039;el; after him there was no one like him among all the kings of
Y&#039;hudah, nor had there been among those before him.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>2 Kings 23:4-16</u>
<br>
<i>Then the king ordered Hilkiyahu the cohen hagadol, the cohanim of the second
rank and the doorkeepers to remove from the sanctuary of ADONAI all the
articles that had been made for Ba&#039;al, for the asherah and for the
entire army of heaven; and he burned them up outside Yerushalayim in the
fields of Kidron and carried their ashes to Beit-El. He deposed the
idolatrous priests the kings of Y&#039;hudah had ordained to offer on the
high places by the cities of Y&#039;hudah and in the places surrounding
Yerushalayim; he also deposed those who offered to Ba&#039;al, the sun, the
moon, the constellations and the whole army of heaven. He took the asherah
from the house of ADONAI to Vadi Kidron outside Yerushalayim and burned it
in Vadi Kidron, stamped the ashes to powder and threw the powder onto the
burial-ground for the common people. He smashed the houses of the cult
prostitutes that were in the house of ADONAI, where the women also wove
garments for the asherah. He removed the cohanim from the cities of
Y&#039;hudah; then, from Geva to Be&#039;er-Sheva, he desecrated the high
places where the cohanim had been making offerings. He also smashed the High
Places of the Gates that were at the entrance of the Gate of Y&#039;hoshua
the governor of the city, on the left as one enters the city. But although
the cohanim who had been at the high places did not come up to the altar of
ADONAI in Yerushalayim, nevertheless they did share matzah with their
kinsmen. He desecrated the Tofet fire pit in the Ben-Hinnom Valley, so that
no one could cause his son or daughter to pass through fire [as a sacrifice]
to Molekh. He confiscated the horses which the kings of Y&#039;hudah had
given to the sun, at the entrance to the house of ADONAI near the room of
the officer N&#039;tan-Melekh, in the side-courtyard; and he burned up the
chariots of the sun. The king smashed the altars on the roof of the upper
room of Achaz, which the kings of Y&#039;hudah had made, and the altars
which M&#039;nasheh had made in the two courtyards of the house of ADONAI.
He broke them into pieces and threw the rubble into Vadi Kidron. The king
desecrated the high places facing Yerushalayim south of the Mount of
Destruction, which Shlomo the king of Isra&#039;el had built for
&#039;Ashtoret the abomination of the Tzidonim, K&#039;mosh the abomination
of Mo&#039;av and Milkom the abomination of the people of &#039;Amon. He
