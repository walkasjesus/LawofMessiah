</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Key Scriptures</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b>DESTROYING OBJECTS & PLACES OF IDOLATRY</b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Exodus 23:23-24</u>
<br>
<i>When my angel goes ahead of you and brings you to the Emori, Hitti,
P&#039;rizi, Kena&#039;ani, Hivi and Y&#039;vusi, I will make an end of
them. You are not to worship their gods, serve them or follow their
practices; rather, you are to demolish them completely and smash their
standing-stones to pieces.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Leviticus 26:30</u>
<br>
<i>I will destroy your high places, cut down your pillars for sun-worship, and
throw your carcasses on the carcasses of your idols; and I will detest
you.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Numbers 33:50-53</u>
<br>
<i>ADONAI spoke to Moshe in the plains of Mo&#039;av by the Yarden, across from
Yericho. He said to tell the people of Isra&#039;el, "When you cross the
Yarden into the land of Kena&#039;an, you are to expel all the people living
in the land from in front of you. Destroy all their stone figures, destroy
all their metal statues and demolish all their high places. Drive out the
inhabitants of the land, and live in it, for I have given the land to you to
possess."</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Deuteronomy 7:5</u>
(Maimonides RP185; Chinuch 436)
<br>
<i>No, treat them this way: break down their altars, smash their
standing-stones to pieces, cut down their sacred poles and burn up their
carved images completely.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Deuteronomy 7:25-26</u>
<br>
<i>You are to burn up completely the carved statues of their gods. Don&#039;t
be greedy for the silver or gold on them; don&#039;t take it with you, or
you will be trapped by it; for it is abhorrent to ADONAI your God.
Don&#039;t bring something abhorrent into your house, or you will share in
the curse that is on it; instead, you are to detest it completely, loathe it
utterly; for it is set apart for destruction.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Deuteronomy 12:1-3</u>
(Maimonides RP185; Chinuch C436)
<br>
<i>Here are the laws and rulings you are to observe and obey in the land
ADONAI, the God of your ancestors, has given you to possess as long as you
live on earth. You must destroy all the places where the nations you are
dispossessing served their gods, whether on high mountains, on hills, or
under some leafy tree. Break down their altars, smash their standing-stones
to pieces, burn up their sacred poles completely and cut down the carved
images of their gods. Exterminate their name from that place.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Deuteronomy 13:17-19(16-18)</u>
(Maimonides RP186; Chinuch C464)
<br>
<i>Heap all its spoils in an open space, and burn the city with its spoils to
the ground for ADONAI your God; it will remain a tel forever and not be
built again - none of what has been set apart for destruction is to stay in
your hands. Then ADONAI will turn from his fierce anger and show you mercy,
have compassion on you and increase your numbers, as he swore to your
ancestors - provided you listen to what ADONAI says and obey all his mitzvot
that I am giving you today, thus doing what ADONAI your God sees as right.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b>DESTROYING PERSONS WHO ARE IDOLATERS (Not authorized in the New
Covenant)</b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Exodus 22:19(20)</u>
<br>
<i>Anyone who sacrifices to any god other than ADONAI alone is to be completely
destroyed.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Numbers 24:12-20</u>
(Chinuch C603)
<br>
<i>Bil&#039;am answered Balak, "Didn&#039;t I tell the messengers you sent me
that even if Balak would give me his palace full of silver and gold, I could
not of my own accord go beyond the word of ADONAI to do either good or bad?
that what ADONAI said is what I would say? But now that I am going back to
my own people, come, I will warn you what this people will do to your people
in the acharit-hayamim. So he made his pronouncement: "This is the speech
of Bil&#039;am, son of B&#039;or; the speech of the man whose eyes have been
opened; the speech of him who hears God&#039;s words; who knows what
&#039;Elyon knows, who sees what Shaddai sees, who has fallen, yet has open
eyes: "I see him, but not now; I behold him, but not soon - a star will step
forth from Ya&#039;akov, a scepter will arise from Isra&#039;el, to crush
the corners of Mo&#039;av and destroy all descendants of Shet. His enemies
will be his possessions - Edom and Se&#039;ir, possessions. Isra&#039;el
will do valiantly, From Ya&#039;akov will come someone who will rule, and he
will destroy what is left of the city." He saw &#039;Amalek and made this
pronouncement: "First among nations was &#039;Amalek, but destruction will
be its end."</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Deuteronomy 3:21-22</u>
(Maimonides RN58; Chinuch C525)
<br>
<i>Also at that time I gave this order to Y&#039;hoshua: &#039;Your eyes have
seen everything that ADONAI your God has done to these two kings. ADONAI
will do the same to all the kingdoms you encounter when you cross over.
Don&#039;t be afraid of them, because ADONAI your God will fight on your
behalf.&#039;</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Deuteronomy 7:1-4</u>
(Chinuch C425)
<br>
<i>ADONAI your God is going to bring you into the land you will enter in order
to take possession of it, and he will expel many nations ahead of you - the
Hitti, Girgashi, Emori, Kena&#039;ani, P&#039;rizi, Hivi and Y&#039;vusi,
seven nations bigger and stronger than you. When he does this, when ADONAI
your God hands them over ahead of you, and you defeat them, you are to
destroy them completely! Do not make any covenant with them. Show them no
mercy. Don&#039;t intermarry with them - don&#039;t give your daughter to
his son, and don&#039;t take his daughter for your son. For he will turn
your children away from following me in order to serve other gods. If this
happens, the anger of ADONAI will flare up against you, and he will quickly
destroy you.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Deuteronomy 7:16-21</u>
(Maimonides RN58; Chinuch C525)
<br>
<i>You are to devour all the peoples that ADONAI your God hands over to you -
show them no pity, and do not serve their gods, because that will become a
trap for you. If you think to yourselves, &#039;These nations outnumber us;
how can we dispossess them?&#039; nevertheless, you are not to be afraid of
them; you are to remember well what ADONAI your God did to Pharaoh and all
of Egypt - the great ordeals which you yourself saw, and the signs, wonders,
strong hand and outstretched arm by which ADONAI your God brought you out.
ADONAI will do the same to all the peoples of whom you are afraid. Moreover,
ADONAI your God will send the hornet among them until those who are left and
those who hide themselves perish ahead of you. You are not to be frightened
of them, because ADONAI your God is there with you, a God great and
fearsome.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Deuteronomy 13:7-16(6-15)</u>
<br>
<i>If your brother the son of your mother, or your son, or your daughter, or
your wife whom you love, or your friend who means as much to you as
yourself, secretly tries to entice you to go and serve other gods, which you
haven&#039;t known, neither you nor your ancestors - gods of the peoples
surrounding you, whether near or far away from you, anywhere in the world -
you are not to consent, and you are not to listen to him; and you must not
pity him or spare him; and you may not conceal him. Rather, you must kill
him! Your own hand must be the first one on him in putting him to death, and
afterwards the hands of all the people. You are to stone him to death;
because he has tried to draw you away from ADONAI your God, who brought you
out of the land of Egypt, out of a life of slavery. Then all Isra&#039;el
will hear about it and be afraid, so that they will stop doing such
wickedness as this among themselves. If you hear it told that in one of
your cities which ADONAI your God is giving you to live in, certain
scoundrels have sprung up among you and have drawn away the inhabitants of
their city by saying, &#039;Let&#039;s go and serve other gods, which you
haven&#039;t known,&#039; then you are to investigate the matter, inquiring
and searching diligently. If the rumor is true, if it is confirmed that such
detestable things are being done among you, you must put the inhabitants of
that city to death with the sword, destroying it completely with the sword,
everything in it, including its livestock.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Deuteronomy 17:2-5</u>
<br>
<i>If there is found among you, within any of your gates [in any city] that
ADONAI your God gives you, a man or woman who does what ADONAI your God sees
as wicked, transgressing his covenant by going and serving other gods and
worshipping them, the sun, the moon, or anything in the sky - something I
have forbidden - and it is told to you, or you hear about it; then you are
to investigate the matter diligently. If it is true, if it is confirmed that
such detestable things are being done in Isra&#039;el; then you are to bring
the man or woman who has done this wicked thing to your city gates, and
stone that man or woman to death.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Deuteronomy 18:20</u>
<br>
<i>But if a prophet presumptuously speaks a word in my name which I didn&#039;t
order him to say, or if he speaks in the name of other gods, then that
prophet must die.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Deuteronomy 20:16-18</u>
(Maimonides RP187; Chinuch C425)
<br>
<i>As for the towns of these peoples, which ADONAI your God is giving you as
your inheritance, you are not to allow anything that breathes to live.
Rather you must destroy them completely - the Hitti, the Emori, the
Kena&#039;ani, the P&#039;rizi, the Hivi and the Y&#039;vusi - as ADONAI
your God has ordered you; so that they won&#039;t teach you to follow their
abominable practices, which they do for their gods, thus causing you to sin
against ADONAI your God.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Deuteronomy 25:17-19</u>
(Maimonides RP188-189; Meir MP76-77; Chinuch C603-604)
<br>
<i>Remember what &039;Amalek did to you on the road as you were coming out of
Egypt, how he met you by the road, attacked those in the rear, those who
were exhausted and straggling behind when you were tired and weary. He did
not fear God. Therefore, when ADONAI your God has given you rest from all
your surrounding enemies in the land ADONAI your God is giving you as your
inheritance to possess, you are to blot out all memory of &039;Amalek from
under heaven. Don&039;t forget!</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Jeremiah 48:10</u>
(Chinuch C525)
<br>
<i>A curse on him who does the work of ADONAI carelessly! A curse on him who
withholds his sword from blood!</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Colossians 3:5</u>
<br>
<i>Therefore, put to death the earthly parts of your nature - sexual
immorality, impurity, lust, evil desires and greed (which is a form of
idolatry)</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b>IDOLATRY DEALT WITH IN THE NEW COVENANT</b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Acts 17:22-24</u>
<br>
<i>Sha&#039;ul stood up in the Council meeting and said, "Men of Athens: I see
