</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Merriam-Webster defines sexual "lust" as "an unusually intense or unbridled
sexual desire." Since the Bible unambiguously says that "lust" is sin,
Merriam-Webster&#039;s first alternative ("an unusually intense sexual
desire") cannot be the one meant because mere feelings, no matter how strong,
are never sin. The second definition ("an unbridled sexual desire") is
closer to what is meant, the inclusion of "unbridled" making the difference.
Restated, the sexual sin of lust comprises the prurient desire for a person
that is not our spouse, coupled with one or more wrongful actions designed to
gratify the desire; ergo, the title of this Mitzvah: "Giving Ourselves to
Unlawful Sexual Desires." The reason that Yeshua (in <u>Matthew 5:28</u>)
was able to say that "a man who even looks at a woman with the purpose of
lusting after her has already committed adultery in his heart," is that the
man he is speaking of is not only feeling sexual desire for the woman, but
has already begun to pursue her either in his mind or by his actions. With
this understanding, David&#039;s adultery with Bat-Sheva did not begin when
they had sexual intercourse; it began when David sent his messengers to fetch
her (<u>2 Samuel 11:1-4</u>).
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary by Daniel C. Juster</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Lust includes allowing one&#039;s imagination to entertain sexually immoral
thoughts without immediately renouncing them and refocusing. It includes
masturbating while entertaining thoughts of a person who is prohibited to us,
as well as any other act designed to gratify our desire. Our sexual thoughts
are to be only toward our spouse or (within strict constraints) toward one we
are legally pursuing for marriage.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
