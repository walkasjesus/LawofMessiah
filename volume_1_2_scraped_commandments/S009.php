<br>
<i>You have heard that our fathers were told, &#039;Do not commit
adultery.&#039; But I tell you that a man who even looks at a woman with
the purpose of lusting after her has already committed adultery with her in
his heart. If your right eye makes you sin, gouge it out and throw it away!
Better that you should lose one part of you than have your whole body thrown
into Gei-Hinnom.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
