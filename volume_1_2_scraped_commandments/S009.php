<font size=4 face="Times New Roman, Times, serif">
<b><i>We are not to have give ourselves to unlawful sexual desires.</i></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This precept is derived from His Word (blessed be He):
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Key Scriptures</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Exodus 20:14(17)</u>
<br>
<i>Do not covet your neighbor&#039;s house; do not covet your neighbor&#039;s
wife, his male or female slave, his ox, his donkey or anything that belongs
to your neighbor.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Proverbs 6:23-29</u>
<br>
<i>For the mitzvah is a lamp, Torah is light, and reproofs that discipline are
the way to life. They keep you from an evil woman, from a loose
woman&#039;s seductive tongue. Don&#039;t let your heart lust after her
beauty or allow her glance to captivate you. The price of a whore is a loaf
of bread, but the adulteress is hunting for a precious life. Can a man
carry fire inside his shirt without burning his clothes? Can a man walk
[barefoot] on hot coals without scorching his feet? So is he who has sex
with his neighbor&#039;s wife; anyone touching her will be punished.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
