<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
It is a common misunderstanding to believe that God's covenant with Abraham
(repeated through Isaac and Jacob) applies only to Jews. The Abrahamic
Covenant defines the Jewish people generationally, but it is also the
framework for Gentiles to willfully connect themselves to the Jewish people,
thereby enlarging the "people of God" that comprises both Jews and Gentiles.
It is why God told Abraham that he would be the "<i>father of many
nations.</i>" (<u>Genesis 17:4</u>). That notwithstanding, Gentiles (even
<i>K'rovei Yisrael</i> - those closely connected to a Jewish community) are
not themselves Jews because their priestly identity and covenantal
responsibilities are not inherited, but are rather, voluntarily derived from
their connection to the Jewish People through their covenant with the Jewish
Messiah. In the Mosaic Covenant, Gentiles who wanted a special covenantal
relationship with God received it by physically moving into the Jewish camp,
becoming circumcised, sacrificing at the Tabernacle or Temple, and becoming
part of the Jewish community. In the New Covenant, Gentiles who want a
special covenantal relationship with God receive it by becoming grafted into
the "Jewish" olive tree through faith in Messiah Yeshua (see Romans
the "Jewish" olive tree through faith in Messiah Yeshua (see <u>Romans
11</u>).<sup><small><a name=fr1 href=#fn1>1</a></small></sup>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<i>B'rit milah</i> (covenant circumcision) was (and still is) required of all
Jewish males who are eight days old lest they be cut off from the covenant
they have inherited. In the Mosaic Covenant, circumcision was required of
any Gentile male who lived in a Jewish household and wished to covenantally
connect to the Jewish people. Such a Gentile became subject to most of the
commandments of <i>Torah</i>, but it did not result in his becoming a Jew or
a Jewish
convert,<sup><small><a name=fr2 href=#fn2>2</a></small></sup>
and it is clear from <u>Acts 15:1-20</u> that circumcision was not required
for their salvation.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Circumcising a Jewish child has historically been considered the duty of the
child's father, but an experienced circumciser (a <i>mohel</i>) is often
employed. The surgery can be performed by a non-Jew, but only a Jew can
receive the child into the covenant. The prevailing view is that <i>b'rit
milah</i> should be done on the eighth day after birth when possible. So
important is this eighth day principle, that <i>halachah</i> requires that
circumcision be performed on that day even if it falls on a Sabbath.
</font></p><hr width=150px style="margin-left: 10px">
<p align=left style="margin-left: 30px; margin-right: 30px">
<font size=2>
<a name=fn1 href=#fr1>1.</a>
Jewish individuals who have been broken off of their
olive tree through unbelief have to be re-grafted. Jews that remain
unconnected to their tree retain their covenantal responsibilities but not
their salvific relationship with God.
</font>
</p>
<p align=left style="margin-left: 30px; margin-right: 30px">
<font size=2>
<a name=fn2 href=#fr2>2.</a>
Uncircumcised Gentiles can participate in
today&#039;s Passover <i>seders</i> because today&#039;s <i>seders</i> do
not include eating a sacrificed Passover animal (see <u>Exodus
12:43-49</u>). In <u>1 Corinthians 5:7-8</u>, Paul makes it clear that
Yeshua is our sacrificed lamb, and he thereby encourages the Gentiles to
whom he is speaking to celebrate the <i>seder</i>. We do not require that
<i>K&#039;rov Yisrael</i> Gentiles be circumcised.
</font>
</p>
