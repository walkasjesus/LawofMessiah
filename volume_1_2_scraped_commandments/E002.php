<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary by Daniel C. Juster</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
We note that participating at Passover <i>seders</i> has special meaning for
believers in Yeshua because the fullness of the meaning of Passover is in the
death of Yeshua as our Passover Lamb. As such, in the New Covenant order,
both Jews and Gentiles commonly participate in the meaning of the Passover
sacrifice in its New Covenant fulfilment form. Regarding Gentile
participants in particular, since the Passover meal practiced by Jewish
people today is a memorial without the sacrificial lamb, it is common for
Gentiles to participate, even in traditional Jewish <i>seders</i>, without
violating the Scriptures.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides, Meir, and HaChinuch are in agreement concerning the requirement
that a Jewish baby be circumcised on the eighth day if possible. HaChinuch
and Meir state that if a Jewish baby is not circumcised, then there is a
continuing obligation on the part of the uncircumcised individual to become
circumcised when he is older. It is interesting that Maimonides and HaChinuch
state that mothers have no responsibility to circumcise their sons in view of
Moses' wife Zipporah having circumcised her son (<u>Exodus 4:24-26</u>). Meir
states that circumcision should be done after sunrise.
