<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Most of the <i>mitzvot</i> promulgated in the Mosaic era appear to command
against worshiping foreign gods of the supernatural kind - sometimes
attributed to animals and natural objects (e.g. the Nile river god and the
frog god of the Egyptians), and sometimes man-made objects (e.g. heathen
carved statues and the golden calf). While not spoken of directly in the
Mosaic Law, Satan and satanic influence is at the core of all idolatry and of
all gods other than the God of Abraham, Isaac, and Jacob. It was the
archangel Lucifer (later named Satan) who first rebelled against God, and who
was cast out of heaven and allowed to become the god of the earth to the
extent that man would allow him. Satan's first attempt at seducing man away
from God was successful (i.e. Adam and Eve), and the rest of the earth's
history has been his war against God for the heart of mankind.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
There were (and still are) pagans who were/are susceptible to worshiping
foreign gods, such as the Canaanites and Egyptians of old, and the Hindus and
Satanists of today. The religions of the Canaanites and Egyptians severely
impacted the Israelites in years gone by but, today, Satan's attack on God's
people is more subtle. We get a hint of the kinds of idols that impact us
most today from the following Scriptures:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>1 Samuel 15:23:</u>
<i>For rebellion is like the sin of sorcery, stubbornness like the crime of
idolatry. Because you have rejected the word of ADONAI, he too has rejected
you as king.</i>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Ephesians 5:5:</u>
<i>For of this you can be sure: every sexually immoral, impure or greedy
person - that is, every idol-worshipper - has no share in the Kingdom of the
Messiah and of God.</i>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Colossians 3:5:</u>
<i>Therefore, put to death the earthly parts of your nature - sexual
immorality, impurity, lust, evil desires and greed (which is a form of
idolatry)</i>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>1 Timothy 6:10:</u>
<i>For the love of money is a root of all the evils; because of this craving,
some people have wandered away from the faith and pierced themselves to the
heart with many pains.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
These Scriptures indicate that stubbornness, sexual immorality, impurity,
lust, evil desires and greed for money and property are all idols. Other
common idols are money, property, power, and security. An idol is therefore
anything (or anyone) that we desire more strongly than we desire God, and
idolatry is any giving-in to such a desire, such that we put that thing or
person ahead of God. This <i>Mitzvah</i> commands us to put away all
idolatry, both the worship of foreign gods, as well as those other idols that
draw us away from the God of Abraham, Isaac, and Jacob. Anyone who allows
such things to dominate his or her life is guilty of idolatry.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides, Meir, and HaChinuch each wrote nine <i>mitzvot</i> that
collectively express the principle of this one <i>Mitzvah</i>. Most of the
commentators' <i>mitzvot</i> are self-explanatory, but there is one that
bears special mention. In his <i>mitzvah</i> C27, HaChinuch criticizes
Maimonides for making no distinction of culpability between making an idol
with one's own hands, and commissioning one's agent to make an idol for him.
HaChinuch writes that there should be such a distinction because of an
established Talmudic rule (<i>TB <u>Kidushin 42b</u></i>) that a person who
commissions another to commit a wrong is not himself guilty of the wrong. In
saying this, HaChinuch may be referring to Maimonides' RN3 and not RN2 which
is described in this <i>Mitzvah</i>.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides, Meir, and HaChinuch wrote separate <i>mitzvot</i> for bowing down
to an idol (Maimonides RN5; Meir MN11; HaChinuch C28), and for worshiping an
idol (Maimonides RN6; Meir MN 12; HaChinuch C29), which seems to me to be
splitting hairs.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
In their <i>mitzvot</i> RN14, MN13, and C86 respectively, Maimonides, Meir,
and HaChinuch interpret <u>Exodus 23:13</u> narrowly to mean that we are not
to swear in the name of idols, and in their <i>mitzvot</i> RN26, MN27, and
C518 respectively, they interpret it to mean that we are not to prophesy in
the name of an idol.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
In his <i>mitzvah</i> RN10, Maimonides admonishes the reader to not study or
pay attention to idolatry, whereas Meir and HaChinuch (MN16 and C213
respectively) command the reader to not turn toward idolatry. While there
is, indeed, the possibility that a spiritually weak person could become
attracted to idolatry through learning about it, I do not believe that the
Scriptures cited support such a broad restriction. <u>Deuteronomy 12:30</u>
is clear that the prohibition is against inquiring about foreign gods for the
purpose of following them.
