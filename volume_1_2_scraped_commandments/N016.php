<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Psalm 4:4(3)</u> says: "<i>You can be angry, but do not sin!</i>", and
similarly, <u>Ephesians 4:26</u> says: "<i>Be angry, but don't sin.</i>"
Clearly, anger itself is not sinful, but what we do as a result of our anger
often is, and that is why there are so many Scriptures that warn us against
anger. Notice that both of the Scriptures above command us to not sin as a
result of our anger - a comforting reminder that we can control our anger,
and not sin on account of it.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Anger can be directed toward a circumstance or even against our self, but
this <i>Mitzvah</i> has to do with anger directed against a person (our
neighbor), that we allow to persist. That, <u>Matthew 5:22</u> says is sin,
and <u>Ephesians 4:26-27</u> says that we must not let the sun go down on our
anger which means that in order to avoid sin, any anger we have toward our
neighbor must be short-lived.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Leviticus 19:18</u> cited above is the basis for Maimonides', Meir's and
HaChinuch's <i>mitzvot</i> on loving, and on not taking vengeance or bearing
a grudge. While they could be related to holding onto anger (the subject of
this <i>Mitzvah</i>), holding onto anger is not necessarily involved.
