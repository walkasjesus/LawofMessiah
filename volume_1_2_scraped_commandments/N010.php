<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Kidnapping (abduction) is essentially stealing a person. Coveting a person
such as a servant or another man's wife is a state of mind that precedes the
act of stealing or kidnapping. The Scriptures speak for themselves regarding
the wrongfulness of kidnapping Israelites and all others as well.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary by Daniel C. Juster</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
There is a special case of kidnapping - kidnapping for ransom - where it is
not the person who is desired, but a reward for the return of the person. The
reward sought might be monetary or something else, such as the release of a
prisoner. In addition, there is the kidnapping of children, again for
reward, but also for people who simply want a child and take someone else's.
This was the case in Solomon's famous decision of the woman who lost her own
baby and wanted to steal the other's. Both of these are serious crimes and
require serious penalties.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The <i>mitzvot</i> of Maimonides, Meir, and HaChinuch prohibit the
kidnapping of Israelites only, and consider kidnapping to be stealing.
