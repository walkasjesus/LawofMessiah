<font size=6>
<b>K5. </b>
</font>
<font size=5>
<b>Eating the Sinew of the Thigh.</b>
</font>
<font size=3 face="Times New Roman, Times, serif">
&nbsp;&nbsp;
<a href="comment.php?d=K005&h=K5"
onClick="return show_hide_box(this,400,280,'1px solid')">[Make a Comment]</a>
</font>
</p>
<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><i>It is halachically prescribed that we do not eat the sinew of an
animal's thigh.</i></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This precept is derived from His Word (blessed be He):
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Key Scriptures</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Genesis 32:25-33(24-32)</u>
(Maimonides RN183; Meir MN1; Chinuch C3)
<br>
<i>and Ya&#039;akov was left alone. Then some man wrestled with him until
daybreak. When he saw that he did not defeat Ya&#039;akov, he struck
Ya&#039;akov&#039;s hip socket, so that his hip was dislocated while
wrestling with him. The man said, "Let me go, because it&#039;s daybreak."
But Ya&#039;akov replied, "I won&#039;t let you go unless you bless me."
The man asked, "What is your name?" and he answered, "Ya&#039;akov." Then
the man said, "From now on, you will no longer be called Ya&#039;akov, but
Isra&#039;el; because you have shown your strength to both God and men and
have prevailed." Ya&#039;akov asked him, "Please tell me your name." But he
answered, "Why are you asking about my name?" and blessed him there.
Ya&#039;akov called the place P&#039;ni-El [face of God], "Because I have
seen God face to face, yet my life is spared." As the sun rose upon him he
went on past P&#039;ni-El, limping at the hip. This is why, to this day,
the people of Isra&#039;el do not eat the thigh muscle that passes along the
hip socket - because the man struck Ya&#039;akov&#039;s hip at its socket.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Romans 14:2-3</u>
<br>
<i>One person has the trust that will allow him to eat anything, while another
whose trust is weak eats only vegetables. The one who eats anything must
not look down on the one who abstains; and the abstainer must not pass
judgment on the one who eats anything, because God has accepted him</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The statement of this <i>Mitzvah</i> begins "It is <i>halachically</i>
prescribed ... rather than "We are to ..." as in other <i>Mitzvot</i> because
<u>Genesis 32:33(32)</u> that underlies it is not stated as a commandment; it
reads:
QuoteParagraph
<i>This is why, to this day, the people of Isra&#039;el do not eat the thigh
muscle that passes along the hip socket - because the man struck
Ya&039;akov&039;s hip at its socket.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Although, in Judaism, the above verse of Scripture is <i>halachically</i>
treated as a commandment, it cannot be proven to be so because it could
merely be saying that between the time of Jacob and the time of Moses (when
we assume Genesis was redacted), the community of Israel elected to remember
Jacob&#039;s struggle with the "man" by not eating the "sinew of the thigh"
(<i>gid hanasheh</i> or "thigh muscle" in the CJB) of an animal that passes
along the hip socket. It is because Jacob&#039;s own thigh muscle was
damaged when the "man" with whom he wrestled all night touched his hip socket
in order to force the contest to an end.<sup><small><a name=fr1 href=#fn1>1</a></small></sup>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The "man's" identity is shrouded in mystery, but it is apparent that he
contained "the fullness of all that God is
..."<sup><small><a name=fr2 href=#fn2>2</a></small></sup> because Jacob
said of him in <u>Genesis 32:31(30)</u>:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>"I have seen God face to face, yet my life is spared."</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Whoever the "man" was, it is clear that Jacob spent all night wrestling in
the pursuit of a blessing to which God had already committed Himself through
His earlier promise to Abraham; nevertheless, the "man" with whom Jacob
wrestled declared in <u>Genesis 32:39(28)</u>:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>"From now on, you will no longer be called Ya'akov, but Isra'el; because
you have shown your strength to both God and men and have prevailed."</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
While the apparent complement was given for Jacob's tenacious pursuit of the
godly objective of acquiring a blessing, it was unnecessary for him to be
injured in the process of acquiring it, and he wouldn't have been, had he
trusted God's promise (about which he knew) instead of trying to extract the
blessing through his own strength. It was a characteristic of Jacob to act in
the flesh, and it was not the first time that he sought God's blessing
through fleshly means (<u>Genesis 27:1-35</u>). It is worthy of note that
Jacob did not win the wrestling match, and the "man" (who was clearly a
supernatural being) did not lose it since he clearly could have killed Jacob
had he wanted to.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Many in today's Jewish community comply with Jewish law and custom by not
eating the sinew of an animal's hip (<u>Genesis 32:33(32)</u>), but we also
understand the Scripture's twofold lesson for us, which is: (1) like Jacob,
we must be zealous in our pursuit of godly things; but (2) unlike Jacob, we
must do so in faith, and in reliance on God's strength - not on our own. We
must submit to God and not wrestle with Him, lest He have to disable us as He
did Jacob. There are many Scriptures that teach this principle; I will list
but a few here:
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Zechariah 4:6b</u>
<br>
<i>&#039;Not by force, and not by power, but by my Spirit,&#039; says
ADONAI-Tzva&#039;ot.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Psalm 105:4</u>
<br>
<i>Seek ADONAI and his strength; always seek his presence.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Proverbs 3:5-6</u>
<br>
<i>Trust in ADONAI with all your heart; do not rely on your own understanding.
In all your ways acknowledge him; then he will level your paths.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Ephesians 6:10</u>
<br>
<i>Finally, grow powerful in union with the Lord, in union with his mighty
strength!</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Philippians 4:13</u>
<br>
<i>I can do all things through him who gives me power.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>James 4:7</u>
<br>
<i>Therefore, submit to God. Moreover, take a stand against the Adversary, and
he will flee from you.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
There is a practical problem to overcome when we seek to abstain from eating
meat from an animal's thigh unless we purchase our meat exclusively from a
<i>kosher</i> source (the origin of chopped beef or "hamburger" from
non-<i>kosher</i> sources is often not labeled and is unknown).
<i>Kosher</i> meat is not always available, so the next simplest solution in
attemptingto comply with <i>halachah</i> is to learn the cuts of meat that
come from an animal's hind quarter (sometimes referred to as
non-<i>kosher</i> cuts) and avoid them. There is, however, another but more
laborious solution, which is for the consumer or the butcher to search for
and remove all sinew-like structures in meat that comes from an animal's
thigh; these would include veins, nerves, tendons, and cartilage. God does
not expect the average person to be an anatomist or have the time to do it,
so I am confident that making a reasonable effort to avoid eating meat from
an animal's hind quarter is sufficient, although not required unless
ascribing to a community's standard that seeks to build a fence around the
<i>Torah</i>. Interestingly, Orthodox <i>halachah</i> sometimes establishes
an even greater fence around the <i>Torah</i> by prohibiting the consumption
of meat from behind an animal's 13th rib, which is the entire rear half of
the animal - not just the hind quarter.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
There is one more issue that needs to be thought of whenever we depart from
<i>halachic kashrut</i>, and that is how it may affect our hospitality to
guests who keep rabbinically <i>kosher</i>. There is no one solution I can
recommend other than to inquire of our guests&#039; needs and do our best to
comply. It is sometimes expedient to avoid serving meat altogether.
</font></p><hr width=150px style="margin-left: 10px">
<p align=left style="margin-left: 30px; margin-right: 30px">
<font size=2>
<a name=fn1 href=#fr1>1.</a>
Scholars argue over
exactly what the <i>gid hanasheh</i> was, some calling it "muscle", others
"nerve", and still others "tendon". For most practical purposes it does
not matter, because the laws of <i>kashrut</i> require that all visible
nerves, tendons, and veins be removed from the hind quarter of an animal if
the hind quarter is to be used for food. Because such preparation is often
cost-prohibitive, it is the common Jewish practice (except where food is
scarce) to not eat the hind quarter of an animal at all.
</font>
</p>
