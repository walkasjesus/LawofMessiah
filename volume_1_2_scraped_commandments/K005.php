<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The translators into English of Maimonides, Meir, and HaChinuch that I
consulted, all translate <i>gid hanasheh</i> as "the sinew of the thigh
vein." Meir says that there are two sinews - an inner one that is near the
hip socket, and another that is further away. He says that the inner one is
the subject of the <i>Torah</i> prohibition, and the other cannot be eaten
either as a result of rulings of the Sages. Of the three, HaChinuch is the
only one who attempts to explain the underlying reason for the
<i>mitzvah</i>, but draws conclusions that both exceed and contradict the
biblical text. He says, for example, that the man who wrestled with Jacob
was Esau's (not Jacob's) guardian angel, and he was attempting to extirpate
Jacob and his progeny from the world. When he did not succeed, he caused
Jacob pain by dislocating his hip. HaChinuch says that this is predictive of
Esau and his descendants (the
Arabs)<sup><small><a name=fr3 href=#fn3>3</a></small></sup>
seeking to eradicate and inflict pain upon the Children of Israel, that they
will not succeed, and that when the Messiah comes, Israel will be healed.
</font></p><hr width=150px style="margin-left: 10px">
