<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><i>We are to pay an employee his wages on the same day that he labors or
when contractually due.</i></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This precept is derived from His Word (blessed is He):
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Key Scriptures</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Leviticus 19:13</u>
(Maimonides RN238; Meir MN38; Chinuch C230)
<br>
<i>Do not oppress or rob your neighbor; specifically, you are not to keep back
the wages of a hired worker all night until morning.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Deuteronomy 24:14-15</u>
(Maimonides RP200; Meir MP66; Chinuch C588)
<br>
<i>You are not to exploit a hired worker who is poor and needy, whether one of
your brothers or a foreigner living in your land in your town. You are to
pay him his wages the day he earns them, before sunset; for he is poor and
looks forward to being paid. Otherwise he will cry out against you to
ADONAI, and it will be your sin.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Matthew 10:10b</u>
<br>
<i>... for a worker is worthy of his food.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The essence of this <i>Mitzvah</i> is that we must not oppress an employee by
withholding or delaying payment of his wages. There are many kinds of
employees today in many occupations. Some are day laborers and some are poor
and some are not. It is customary to pay a hired man or employee at the end
of a specified period of time that may be daily, weekly, bi-weekly, monthly,
or at the conclusion of a contract, so this <i>Mitzvah</i> should not be
interpreted to require that all workers be paid daily. That having been
said, <u>Deuteronomy 24:14-15</u> requires that an employer pay a hired man
daily if the man's impoverished condition makes it reasonable and
compassionate to do so.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The inference of <u>Deuteronomy 14</u> is that not paying wages to a poor
worker in a timely manner is akin to exploiting him, and there are
undoubtedly unscrupulous employers who would withhold or delay the payment of
wages to a poor worker in order to pressure the worker into giving the
employer some kind of advantage. This <i>Mitzvah</i> can, therefore, be
construed to have a broader meaning, which is to not exploit an employee's
weakness to gain advantage.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
