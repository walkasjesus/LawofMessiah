<b>C2. </b>
</font>
<font size=5>
<b>Paying a Hired Poor Man His Wages on the Day He Labors or When Due.</b>
</font>
<font size=3 face="Times New Roman, Times, serif">
&nbsp;&nbsp;
<a href="comment.php?d=C002&h=C2"
onClick="return show_hide_box(this,400,280,'1px solid')">[Make a Comment]</a>
