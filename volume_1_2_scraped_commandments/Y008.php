<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Although Yeshua instructs us to come to him for spiritual rest, God's rest
was offered earlier in biblical history as well. Spiritual rest and physical
rest (e.g. Sabbath rest) have similarities in that they are both times of
refreshing, they both require that we stop the various things we are doing,
they both involve seeking the shalom of God, and they both require faith
because rest is the opposite of striving. In the New Covenant, the source of
spiritual rest is Yeshua and none other.
