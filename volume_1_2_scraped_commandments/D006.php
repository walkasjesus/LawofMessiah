<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This <i>Mitzvah</i> requires that no leaven, whether alone or part of food
containing leaven (<i>chametz</i>), be in a Jew's possession or within his
control during the seven days of the Feast of Unleavened Bread. This implies
that he (or she) must, before sundown on the 14<sup><small>th</small></sup>
day of <i>Nisan</i>, remove it from his house, garage, shed, trash bin,
business property, territory domain, or anywhere else where he has
proprietary rights of control, and must not let it back in for seven full
days. This abstinence commemorates the Jews' hasty departure from Egypt,
when they had no time to allow their bread to rise and therefore had to eat
unleavened bread on the first leg of their journey. According to <u>Exodus
12:19</u>, obedience to this commandment is not an option for either a Jew or
a Gentile who is a "<i>citizen of the land</i>" (<i>K'rov Yisrael</i>).
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Leaven is analogized to sin in <u>1 Corinthians 5:6-8</u> and in <u>Galatians
5:7-9</u>, and its removal from our lives is directly linked to the Passover,
and to the Passover lamb - our Messiah. But leaven is not always symbolic of
sin, as we can see from <u>Leviticus 23:17</u>, which requires that the
firstfruits bread offering of <i>Shavuot</i> be made with leaven.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The particulars of what should be considered leaven and <i>chametz</i> are
controversial within Jewry. <i>Sephardim</i> and <i>Ashkenazim</i> follow
different rules on the subject, and Messianic Jewish communities are equally
diverse. Is leaven anything that can cause food to rise, or must there be
fermentation in order for it to qualify? What grains must be removed from
the house because they are subject to spontaneous fermentation during
storage? Rice? Barley? Beans? Discussions of such particulars are found in
the <i>Talmudic</i> literature, but not in any of the classical
<i>mitzvah</i> codifications. I do not think it would be judicious for me
to express my opinion of these particulars here. The general idea in the
Scriptures is that the Israelites&#039; bread that would otherwise have
fermented with yeast did not have time to rise. Therefore, our not keeping
casually-stored grains that would have had time to ferment and rise would
seem to be the general intent of the biblical text.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The legal fiction, in which people sometimes engage, of keeping control of
their <i>chametz</i> (whether stored in their house or not) by selling it to
a Gentile for a minimum amount of money with the understanding that the
Gentile will sell it back to them after the days of Unleavened Bread is not
in accord with the spirit of the Scriptures. People should gauge their
purchases and production of leavened products so as to minimize their
material loss, but they should not engage in subterfuge.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This <i>Mitzvah</i> is mandated for Jews and <i>K'rovei Yisrael</i>,
but not for Gentiles generally; the text of <u>1 Corinthians 5:6-8</u> cited
above is therefore metaphorical in its application as to them.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides, Meir, and HaChinuch distinguish between not possessing leaven and
not finding it in one's house. Meir and HaChinuch require that a Jew search
his house for leaven, whereas Maimonides does not mention such a requirement.
None of the commentators attempt to define "leaven", but HaChinuch says that
yeast is an example of it. Meir states that if a non-Jew places
<i>chametz</i> in a Jew&#039;s house and the Jew has not taken responsibility
for it, the <i>chametz</i> need not be destroyed; otherwise it must be.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
