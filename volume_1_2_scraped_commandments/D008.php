<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Exodus 12:3-8</u> gives details of the first Passover that occurred on the
14<sup><small>th</small></sup> day of the first month (on the Jewish lunar
calendar), the evening before the Israelites were led out of Egypt. God
subsequently commanded the Israelites to keep the Passover as an annual
remembrance of their deliverance (<u>Deuteronomy 16:1-2</u>), and
<u>Leviticus 23:5</u> clarifies that Passover is only part of the day,
between sundown and complete darkness.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
On the original Passover in Egypt, the Israelites were instructed to eat a
sacrificed lamb with <i>matzah</i> and bitter herbs (<i>maror</i>).
<u>Numbers 9:10-11</u> is significant here because, by commanding Passover
observance a month late under certain circumstances, the point is made that
Passover is to be kept each year as a lasting ordinance; it was not just a
one-time event.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Lambs were sacrificed on Passover until the destruction of the Second Temple,
after which, animal sacrifice could no longer be done. Even had the Temple
not been destroyed, it is doubtful that the Passover sacrifice would have
continued among the Jewish followers of Yeshua because they understood Him to
be their "once and for all" sacrificed lamb. On the other hand, they might
have partaken of it as part of a memorial to Yeshua, since we see Paul and
other disciples engaged in Temple sacrifices long after the time of Yeshua's
death (<u>Acts 21</u>). What remains of Passover observance for Jews today,
is that we partake of a <i>seder</i> meal on the evening of <i>Nisan</i> 14,
during which we eat lamb if we are Sephardic (we do not if we are Ashkenaz),
unleavened bread and <i>maror</i> (bitter herbs), and we abstain from eating
(or even possessing) leaven. The Sephardim look at the eating of lamb
differently from the Ashkenazim. The Sephardim eat it in remembrance of the
sacrificed Passover lamb, and the Ashkenazim abstain so that no one can
interpret it as constituting a present day sacrifice.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
There was one Passover meal (described in <u>Matthew 26:26</u>, <u>Mark
14:22</u>, and <i>Luke 22:19</i>), at which Yeshua picked up a piece of
<i>matzah</i> from the table, broke it, and gave it to the twelve disciples
who were with Him, to eat and make a memory they would later use as a
memorial of His body that was be broken during the sacrifice He was about
to endure. This, along with the wine He also spoke of, I consider to be a
new commandment to His disciples (present and future - Jew and Gentile).
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Observing the Passover (without sacrificing a lamb) is mandatory for Jews and
<i>K'rov Yisrael</i> Gentiles. It is optional for other Gentiles who may
also want to observe it (albeit in some adaptive manner) as a way of
identifying with the Jewish People, and also as an acknowledgement of
Passover's prophetic significance that points to Yeshua. Over the centuries,
various Jewish <i>halachic</i> practices have developed that define how
Passover is observed in Jewish communities. Gentiles who choose to observe
Passover may borrow from these, but are free to depart as well, and to
develop their own customs and expressions. It should be noted that the
historic churches sometimes call the period from Good Friday to Resurrection
Sunday "Passover" (<i>Pascha</i>) and do keep it to a degree, but usually
without adequate reference to its Exodus roots.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Finally, a word needs to be said about the prohibition to Gentiles recorded
in <u>Exodus 12:43-49</u>:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>ADONAI said to Moshe and Aharon, "This is the regulation for the Pesach
lamb: no foreigner is to eat it. But if anyone has a slave he bought for
money, when you have circumcised him, he may eat it. Neither a traveler nor
a hired servant may eat it. It is to be eaten in one house. You are not to
take any of the meat outside the house, and you are not to break any of its
bones. The whole community of Isra'el is to keep it. If a foreigner staying
with you wants to observe ADONAI's Pesach, all his males must be circumcised.
Then he may take part and observe it; he will be like a citizen of the land.
But no uncircumcised person is to eat it. The same teaching is to apply
equally to the citizen and to the foreigner living among you."</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This prohibition only applies to eating the sacrificed Passover lamb (which
we no longer do). It does not apply to the other aspects of Passover
observance, so Gentiles are completely free to attend and even conduct
Passover <i>sedarim</i>.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides, Meir, and HaChinuch all recognize the requirement to eat
<i>matzah</i> on Passover. Meir also says that we must eat <i>matzah</i>,
<i>maror</i>, and <i>charoset</i> (a sweet paste-like mixture), and that the
<i>matzah</i> we eat must be made of wheat, barley, spelt, oats, or rye. He
also says that we are prohibited from eating <i>matzah</i> the day before
Passover, so that our appetite for it will be increased. Maimonides and
HaChinuch do not mention eating <i>maror</i>, but refer their readers to
the Talmud's tractate <i>Pesachim</i> for other provisions of the
<i>mitzvah</i>. Maimonides and HaChinuch state that it is the evening of the
15<sup><small>th</small></sup> day of <i>Nisan</i> when we are to eat
unleavened bread; Meir says it is on the 1<sup><small>st</small></sup> night
of Passover when we must eat it, but does not indicate the date.
