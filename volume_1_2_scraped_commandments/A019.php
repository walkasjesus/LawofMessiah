<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
God's commandments that we assemble with each other and with Him, are
initially given during the time of the Mosaic Covenant. Early in the
Covenant, the Tabernacle served as the place of assembly for the Israelites
to receive communication from God, to pray, and to perform the commanded
sacrifices. The Tabernacle and later the Temple also served as places where
the Israelites could assemble with each other (e.g. <u>Joshua 18:1</u>), and
by the time of Yeshua, the synagogue and individual homes also had become
places of communal assembly - mainly for prayer, but also for study and
discussion.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
With the advent of the New Covenant, Jewish and Gentile followers of Yeshua
made increased use of their homes for worship, prayer, and fellowship
(<u>Acts 2:46</u>, <u>5:42</u>), and these home gatherings developed into
what eventually became known within Christianity as "home churches", and
within Messianic Judaism as "home fellowships", "<i>chavurot</i>" or, when
they have elder leadership, "congregations".
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Scripture enjoins believers<sup><small><a name=fr1 href=#fn1>1</a></small></sup> to be in regular congregational fellowship
with one another (<u>Hebrews 10:24-25</u>) as a means of providing mutual
spiritual protection and accountability. Being part of a believing community
enables us to observe one another's lives, share our burdens, and rebuke and
correct one another (<u>Galatians 6:1</u>) in the event that we observe any
of us falling into sin or straying from the faith. Congregational community
also provides us with leaders (elders) whose responsibility it is to protect
the community from outside evil encroachment (<u>Acts 20:28-29</u>), to stop
sin when it is observed (<u>Acts 20:30</u>), to bring correction to members
of the community, to judge disputes (<u>Matthew 18:17</u>), and to maintain
reconciliation among community members (<u>Luke 17:3</u>; <u>Colossians
3:13</u>).
</font></p><hr width=150px style="margin-left: 10px">
