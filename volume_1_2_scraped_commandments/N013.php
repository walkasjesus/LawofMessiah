<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The term "hypocrite" is often applied to persons who seek to correct others
while being in significant fault themselves. According to
<u>Galatians 6:1</u>, it is right for us to correct a brother who is caught
doing something sinful, provided that we do so "<i>in a spirit of humility,
keeping an eye on [ourselves] so that [we] won't be tempted too.</i>" A key
aspect of humility is our willingness to judge our own conduct against what
is taught in Scripture, and especially before seeking to correct our
neighbor's. Notice that I said "correct" and not "judge" when our neighbor
is not a brother or sister believer. Passing judgment is authoritative, and
goes further than merely seeking to correct. According to <u>1 Corinthians
5:12-13</u>, believers are authorized to judge one other, but are not to
judge those outside the body of believers:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>For what business is it of mine to judge outsiders? Isn't it those who are
part of the community that you should be judging? God will judge those who
are outside. Just expel the evildoer from among yourselves.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
