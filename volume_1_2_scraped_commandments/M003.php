<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
It is clear from the foregoing Scriptures (and others not cited) that
truth-telling is a commanded virtue, and that the opposite of truth-telling
(lying) is a sin. We know that truth-telling is a godly attribute because in
<u>Numbers 23:19</u>, when under the influence of the Holy Spirit, Balaam
said to Balak:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>God is not a human who lies or a mortal who changes his mind. When he says
something, he will do it; when he makes a promise, he will fulfill it.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
In complete contrast to God who does not lie, <u>John 8:44</u> quotes Yeshua
(scolding a group of Judeans out to kill Him) as saying that Satan is the
original liar; we read:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>You belong to your father, Satan, and you want to carry out your father's
desires. From the start he was a murderer, and he has never stood by the
truth, because there is no truth in him. When he tells a lie, he is speaking
in character; because he is a liar - indeed, the inventor of the lie!</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This appears to be a reference to <u>Genesis 3:2-4</u> where Satan (in the
form of a serpent) lies to Eve about eating from the tree "<i>in the middle
of the garden</i>" and says to her:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>It is not true that you will surely die ...</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
In my stating categorically that lying is a sin, I believe I should address
the often-debated Scripture passages in which Abraham and Isaac were less
than candid in admitting that Sarah and Rebecca respectively were their
wives; they are:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Genesis 20:2</u>
<br>
<i>Avraham was saying of Sarah his wife, "She is my sister"; so Avimelekh
king of G'rar sent and took Sarah.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
and
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Genesis 26:7</u>
<br>
<i>The men of the place asked him about his wife, and out of fear he said,
"She is my sister." He thought, "If I tell them she's my wife, they might
kill me in order to take Rivkah. After all, she is a beautiful woman."</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Apologists for Abraham sometimes use Abraham's convoluted explanation in
<u>Genesis 20:12</u> to try to show that Abraham didn't lie because Sarah was
not only Abraham's wife, but also his half-sister. The same explanation
cannot, however, be made for Isaac and, the truth be told, both men lapsed in
their faith, fearing for themselves and for their wives. Then there is the
occasion where Rebecca and Jacob conspired to deceive Isaac into blessing
Jacob instead of Esau. It is one of the unique aspects of the Scriptures,
that God allows us to see the foibles of even the greatest of the patriarchs.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides, Meir, and HaChinuch listed <i>mitzvot</i> on various aspects of
honesty, but did not write one that generalizes that we must tell the truth
and not lie. For example, Maimonides listed (1) not withholding evidence
from a court; (2) not bearing false witness and punishing those who do; (3)
not prophesying falsely; (4) not committing fraud by moving land boundaries;
(5) not swearing falsely to avoid paying our debts; and (6) not using or
keeping dishonest weights and measures.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The closest that the classical commentators come to writing a general
<i>mitzvah</i> on truth-telling is their use of <u>Leviticus 19:14</u> to say
that we must not cause anyone to stumble by giving misleading or wrong
advice. <u>Leviticus 19:14</u> actually speaks of not cursing a deaf person
or placing an obstacle in the path of a blind person; therefore, in
constructing their respective <i>mitzvot</i>, the commentators have not
treated deafness and blindness as physical disabilities, but rather as
conditions of lacking sufficient information to make safe and appropriate
judgments.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
