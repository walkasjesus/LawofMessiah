<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Clinging to God means that we are to tenaciously grab hold of Him and not let
go. We are to derive our strength from God, trust Him, and allow nothing to
separate us from Him or entice us away from Him. This becomes most
meaningful during times when we are tempted us to abandon God and follow the
path of self or sin. <u>James 4:7-8a</u> teaches that, during such times, we
must
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>submit to God. Moreover, take a stand against the Adversary, and he will
flee from you [us]. Come close to God, and he will come close to you
[us].</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
We do this by remembering God's Word in <u>2 Timothy 2:26</u>:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>that [we] may come to [our] senses and escape the snare of the devil,
having been taken captive by him to do his will.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Yeshua addresses this in <u>Matthew 6:13a</u>, in the prayer he recommends to
His disciples:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>And do not lead us into hard testing, but keep us safe from the Evil
One.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides states his <i>mitzvah</i> as our being commanded to cleave (or
cling) to God, and he goes on to say that the cleaving or clinging to God is
accomplished through associating with wise men. Meir's and HaChinuch's
<i>mitzvot</i> do not even mention clinging to God in the way they are stated
(although <u>Deuteronomy 10:20</u> is quoted as the operative Scripture);
instead, they command us to attach ourselves to <i>Torah</i> scholars and
their disciples. They then say that if we do so, it is as though we are
attaching ourselves to the <i>Shechinah</i>. It is ironic that all three
commentators choose to redirect Scripture's clear message of clinging to God,
to a different message - that of clinging to men. They derive this
equivalence from <i>Talmud</i>.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
