<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
If we understand that the "gods" in <u>Exodus 20:3</u> are not only false
deities, but rather all persons, matters, and things to which we "bow down"
(i.e. put ahead of God and allow to control our lives), we understand how it
is the quintessential Scripture that supports this <i>Mitzvah</i>. After
that, <u>Matthew 6:33</u> calls us to faith by exhorting us to "<i>... seek
first his Kingdom and his righteousness, and all these things will be given
to you as well.</i>" It is telling us that, despite our perceived needs and
natural inclination to dwell on them, if we put our attention on God instead
of ourselves, He will see to it that our needs are met.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides, Meir, and HaChinuch interpret <u>Exodus 20:3</u> as referring
only to false deities. This <i>Mitzvah</i> is not, therefore, addressed by
any of the Jewish classical commentators in the broader sense of putting God
ahead of all other persons, matters, and things.
