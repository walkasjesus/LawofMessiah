<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><i>We are to neither add nor subtract from the Written Word of God - the
Scriptures.</b></i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This precept is derived from His Word (blessed be He):
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Key Scriptures</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Exodus 13:1(12:32)</u>
(Maimonides RN313-314; Meir MN159-160; Chinuch C454-455)
<br>
<i>Everything I am commanding you, you are to take care to do. Do not add to it
or subtract from it.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Revelation 22:18-19</u>
<br>
<i>I warn everyone hearing the words of the prophecy in this book that if
anyone adds to them, God will add to him the plagues written in this book.
And if anyone takes anything away from the words in the book of this
prophecy, God will take away his share in the Tree of Life and the holy
city, as described in this book.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The foregoing Scriptures are sometimes used as proof texts that the canon of
Scripture is closed. They are also used to teach against New Covenant
prophecy or contemporary "Word gifts" by those who believe that such
manifestations of the <i>Ruach HaKodesh</i> ended with "the Apostolic age".
I do not agree that such gifts and benefits of the Holy Spirit have ended
and, rather than being a denial of prophecy, I maintain that these Scriptures
are a warning against false prophecy, and a warning to us to not add or
subtract from God's written Word.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Another purpose of these Scriptures is to warn us away from contradicting or
nullifying Scriptures through extra-biblical writings or oral traditions that
are claimed to be inspired. Examples can be found in Roman Catholic law and
interpretation, and also in Jewish <i>halachah</i> drawn from the
<i>Talmud</i>. In the case of the Catholic Church, its tradition and the
decisions of ecumenical Church councils and <i>ex cathedra</i> rulings of the
Pope (very rare since the 19<sup><small>th</small></sup> century), were
considered binding and almost inerrant; this is now being reevaluated. In
regard to Jewish <i>halakhah</i>, an authority is claimed for the Oral Law
that is equal to the Scriptures, and the decisions of the Talmudic and
Pre-Talmudic Rabbis have the weight of binding the conscience.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Building fences around the <i>Torah</i> - that is, making rules that are more
stringent than what God commands - is a part of Jewish <i>halachah</i> that
tends to create de facto contradictions of Scripture. For example, the
rabbinical prohibition of eating meat and drinking milk at the same meal
eliminates a liberty that God wants us to have (<u>Genesis 18:7-8</u>), and
it therefore adds to the Scriptures. The Scriptures need not affirmatively
state that God allows us to eat milk and meat in order for a constructive
addition to Scripture to exist.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides states his two <i>mitzvot</i> but does not comment on them. Meir
defines his <i>mitzvot</i> as applying to both the written law (contained in
the <i>Torah</i>) and the Oral Law (contained in the <i>Talmud</i>). He also
says that building protective fences around the written Law is permissible
and not a violation of either <i>mitzvah</i>. HaChinuch teaches that his two
<i>mitzvot</i> only apply to the written Law and, therefore, building fences
and declaring rabbinical rules is permissible because they only apply to the
"<i>Oral Torah</i>". He uses the prohibition of mixing milk and meat as such
an example and says that it adds to the "<i>Oral Torah</i>" but not to the
written one, so it is allowed.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
