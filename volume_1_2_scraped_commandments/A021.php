<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
"Love of money" is a short way of saying "love of what money can buy". One
naturally thinks of money buying goods and services and these alone are
enough to ensnare those who are susceptible. However, there is a far greater
evil in natural man's quest for money; it is that money can buy the power of
men's obedience, and preoccupation with power is the opposite of trusting God
and submitting to His power. Wanting to be powerful as God was what brought
down Lucifer, and the idolatry of it can bring us down as well. The draw of
money and what it can buy is so strong, so seductive, that we are commanded
to flee from it. Pursuing godly virtues is the opposite of (and the antidote
for) pursuing love of money, and is the reason that these two are linked in
<u>1 Timothy 6:10-11</u>.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary by Daniel C. Juster</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
There are some exceptional people who can buy anything they want, but just
love to see their financial worth grow and grow; they are in love with it.
The Bible does not say that it is wrong to get rich, but rather that one must
not be in love with money; money should primarily be used to advance the
Kingdom of God by the leading of the Holy Spirit. In Scripture, the standard
for acquiring money seems to favor modesty.
