<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Forgiveness in Jewish Tradition</u>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
While there is no explicit commandment in the <i>Torah</i> requiring that we
forgive our brother, there is one Scripture, <u>Leviticus 19:17</u>, that
implicitly means the same thing:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>Do not hate your brother in your heart, but rebuke your neighbor frankly,
so that you won&#039;t carry sin because of him. Don&#039;t take vengeance
on or bear a grudge against any of your people; rather, love your neighbor as
yourself; I am ADONAI.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Rabbinical literature endorses the principle of forgiving others, and gives
<u>Genesis 50:16-21</u> (Yosef's forgiveness of his brothers) as the example
we are to follow. Beyond that, all other references to forgiveness are
rabbinic, and include:
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<i><u>Mishnah</u>: Yoma 8:9; Baba Kama (92a) 8:5</i><br>
<i><u>Talmud</u>: Yoma 86b; Rosh HaShana 17a; Ta'anith 20a,b; Beitz 32b;
Shabbat 151b</i><br>
<i><u>Shulchan Aruch</u>: Yom Kippur 606:1</i><br>
<i><u>Mishneh Torah</u>: De'ot 6:6,9; Madda 6:6,9; Teshuvah 2:9,10,11; 43</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
It is clear, however, from the following New Covenant Scriptures, that there
are not one, but two kinds of forgiveness - unconditional and conditional -
and our responsibility to forgive others is different for each of them.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Unconditional Forgiveness<sup><small><a name=fr2 href=#fn2>2</a></small></sup></u>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
I call the kind of forgiveness commanded in <u>Mark 11:25<sup><small><a name=fr3 href=#fn3>3</a></small></sup></u> "<u>unconditional Forgiveness</u>":
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>And when you stand praying, if you have anything against anyone, forgive
him; so that your Father in heaven may also forgive your offenses.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Notice that this kind of forgiveness is truly without condition, and is to be
granted, not only to brothers in the Lord, but to anyone who has sinned
against us and hurt us. It does not seek to judge the offender, nor does it
require that the offender be repentant. It must be given as soon as possible
after the offense is committed, so that bitterness does not take root within
us and do spiritual damage. This kind of forgiveness is essentially a
releasing of the matter to God, and does not retain anger or a desire for
personal vengeance (see <u>Ephesians 4:31-32</u>). Unconditional forgiveness
is a manifestation of godly love that we are to give, even to our enemies
(<u>Luke 6:27-32</u>). Yeshua illustrated this kind of forgiveness when, in
<u>Luke 23:34a</u>, He beseeched His Father in heaven to forgive His Roman
soldier executioners who were, even at that moment, subjecting Him to an
unjust and painful death.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Our granting unconditional forgiveness, and not harboring bitterness against
those who have sinned against us, purges us of unholy attitudes that may have
resulted from our being victimized, and opens the way for God to forgive us
for our sins as well. Conversely, if we do not grant unconditional
forgiveness, <u>Mark 11:25<sup><small><a name=fr4 href=#fn4>4</a></small></sup></u>, <u>Matthew 6:14-15</u>,
and <u>Luke 6:37</u> convey the certainty that God will not forgive us
either. Granting unconditional forgiveness does not, however, mean that the
relationship or trust between persons is restored. It only means that the
person who has forgiven has freed himself from the anger of vengeance, and
has a heart to see the person who has sinned against him repent so that he
might be blessed by God.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Conditional Forgiveness</u>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
I call the kind of forgiveness commanded in <u>Luke 17:3-4</u>
"<u>Conditional Forgiveness</u>".<sup><small><a name=fr5 href=#fn5>5</a></small></sup>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>Watch yourselves! If your brother sins, rebuke him; and if he repents,
forgive him. Also, if seven times in one day he sins against you, and seven
times he comes to you and says, 'I repent,' you are to forgive him.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Conditional forgiveness is different from unconditional forgiveness in
several ways that the <u>Luke</u> Scripture illustrates. First, conditional
forgiveness applies only to brother believers.<sup><small><a name=fr6 href=#fn6>6</a></small></sup>
Second, whereas unconditional forgiveness must be given whether or not there
is repentance, conditional forgiveness is only granted if the offending
brother repents, and the brother who has been sinned against is required to
judge whether the offending brother has repented before the offended brother
forgives him. And it is different in another way as well, which is
illustrated in <u>John 20:23</u> which states:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>If you forgive someone's sins, their sins are forgiven; if you hold them,
they are held.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Here you see that if one who is sinned-against, judges that his offending
brother is repentant and forgives him, God also forgives the offending
brother. Conversely, if one who is sinned against withholds forgiveness
because, in his judgment, his brother is not repentant, then God honors the
judgment and does not forgive the offending brother either. One may ask why
<u>John 20:23</u> applies only as between believers since the Scripture
doesn't say that! It is because the verse involves judging, and <u>1
Corinthians 5:12-13</u> allows us to judge those who are within the body of
believers, but not those who are outside.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Forgiveness Has No Limits</u>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
There is no maximum number of times that we must forgive those who sin
against us, even if they do so repeatedly. There are no limits on our
granting unconditional or conditional forgiveness so that we might walk in
holy attitudes and continuous forgiveness ourselves. As previously stated in
<u>Luke 17:4</u>:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>Also, if seven times in one day he sins against you, and seven times he
comes to you and says, 'I repent,' you are to forgive him.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
And similarly in <u>Matthew 18:21-22</u>:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>Then Kefa came up and said to him, "Rabbi, how often can my brother sin
against me and I have to forgive him? As many as seven times?" "No, not
seven times," answered Yeshua, "but seventy times seven!"</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Scripture's requirement that we forgive does not negate our responsibility to
cooperate with public authorities when matters of justice or the protection
of others is involved.
