<font size=6>
<b>S8. </b>
</font>
<font size=5>
<b>Having Sexual Intercourse With a Woman During Her Menstruation.</b>
</font>
<font size=3 face="Times New Roman, Times, serif">
&nbsp;&nbsp;
<a href="comment.php?d=S008&h=S8"
onClick="return show_hide_box(this,400,280,'1px solid')">[Make a Comment]</a>
</font>
</p>
<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><i>We are not to have sexual intercourse with a woman during her
menstruation.</i></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This precept is derived from His Word (blessed be He):
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Key Scriptures</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Leviticus 18:19</u>
(Maimonides RN346; Meir MN132; Chinuch C207)
<br>
<i>You are not to approach a woman in order to have sexual relations with her
when she is unclean from her time of niddah.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Leviticus 20:18</u>
<br>
<i>If a man goes to bed with a woman in her menstrual period and has sexual
relations with her, he has exposed the source of her blood, and she has
exposed the source of her blood; both of them are to be cut off from their
people.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>1 Thessalonians 4:3-5</u>
<br>
<i>What God wants is that you be holy, that you keep away from sexual
immorality, that each of you know how to manage his sexual impulses in a
holy and honorable manner, without giving in to lustful desires, like the
pagans who don&#039;t know God.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This <i>Mitzvah</i> applies to the conduct of both men and women.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Under the Law of Moses, a woman (and all she touched) during her period of
menstruation was ceremonially unclean. Although being ceremonially clean or
unclean has little meaning today in the absence of the Tabernacle or Temple,
I believe that this <i>Mitzvah</i> not to have sexual relations during a
woman&#039;s period should be obeyed and the reason is twofold. First, a
woman cannot become pregnant during her menses, and a prime purpose for
sexual intercourse is to bear children. Second (which I believe is the
better reason), all through the Bible, blood is very special to God, and
<u>Leviticus 20:18</u> says that if a woman and a man have sexual relations
during her period:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>he has exposed the source of her blood, and she has exposed the source of
her blood; both of them are to be cut off from their people.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
We see that in the importance of blood sacrifice, and in the prohibition of
our eating blood. <u>Leviticus 17:11-14</u> states:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>For the life of a creature is in the blood, and I have given it to you on
the altar to make atonement for yourselves; for it is the blood that makes
atonement because of the life. This is why I told the people of
Isra&#039;el, "None of you is to eat blood, nor is any foreigner living with
you to eat blood." When someone from the community of Isra&#039;el or one of
the foreigners living with you hunts and catches game, whether animal or bird
that may be eaten, he is to pour out its blood and cover it with earth. For
the life of every creature - its blood is its life. Therefore I said to the
people of Isra&#039;el, "You are not to eat the blood of any creature,
because the life of every creature is its blood. Whoever eats it will be cut
off."</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
I do not believe that the similar penalty of being "cut off" for eating blood
and for having sexual intercourse with a woman during her period of blood is
coincidental.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Classical Commentators:</u>
<br>
Maimonides, Meir, and HaChinuch each wrote a <i>mitzvah</i> that prohibits
having sexual relations with a woman during her menstruation.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
