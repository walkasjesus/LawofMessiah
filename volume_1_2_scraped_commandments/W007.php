<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><i>We are not to believe or advocate unbiblical (false) doctrines, nor
pay heed to persons who do.</b></i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This precept is derived from His Word (blessed be He):
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Key Scriptures</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Numbers 15:38-40</u>
(Maimonides RN47; Meir MN156; Chinuch C387)
<br>
<i>Speak to the people of Isra&#039;el, instructing them to make, through all
their generations, tzitziyot on the corners of their garments, and to put
with the tzitzit on each corner a blue thread. It is to be a tzitzit for
you to look at and thereby remember all of ADONAI&#039;s mitzvot and obey
them, so that you won&#039;t go around wherever your own heart and eyes
lead you to prostitute yourselves; but it will help you remember and obey
all my mitzvot and be holy for your God.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Deuteronomy 13:1-4(12:32-13:3)</u>
<br>
<i>Everything I am commanding you, you are to take care to do. Do not add to it
or subtract from it. If a prophet or someone who gets messages while
dreaming arises among you and he gives you a sign or wonder, and the sign or
wonder comes about as he predicted when he said, &#039;Let&#039;s follow
other gods, which you have not known; and let us serve them,&#039; you are
not to listen to what that prophet or dreamer says. For ADONAI your God is
testing you, in order to find out whether you really do love ADONAI your God
with all your heart and being.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Galatians 1:8-9</u>
<br>
<i>But even if we - or, for that matter, an angel from heaven! - were to
announce to you some so-called "Good News" contrary to the Good News we did
announce to you, let him be under a curse forever! We said it before, and I
say it again: if anyone announces "Good News" contrary to what you received,
let him be under a curse forever!</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>2 John 1:9-11</u>
<br>
<i>Everyone who goes ahead and does not remain true to what the Messiah has
taught does not have God. Those who remain true to his teaching have both
the Father and the Son. If someone comes to you and does not bring this
teaching, don&#039;t welcome him into your home. Don&#039;t even say,
"Shalom!" to him; for the person who says, "Shalom!" to him shares in his
evil deeds.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This <i>Mitzvah</i> is among others that command obedience to the Word of
God, and is self-explanatory from its supporting Scriptures. It is noteworthy
that both the <u>Deuteronomy 13</u> and <u>Galatians 1</u> Scriptures
anticipate and warn us against prophetic individuals who may appear to us
(even with signs and wonders) and proclaim doctrines that are contrary to the
written Word, and to the Apostles' interpretation of the written Word. We
are not to listen to, follow, or even be hospitable to such individuals. This
<i>Mitzvah</i> amplifies our need to test all prophetic words (even our own)
against Scripture.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides states that we are not to accept opinions that are contrary to the
<i>Torah</i>. In fact, we are to put a fence around our minds, and not
exercise freedom of thought that would take us in those directions. Meir
agrees that we should constrain our thoughts and warns us against being drawn
away from God by worldly attractions and by Satan who attempts to lead us
into heresy. HaChinuch's emphasis is to warn against pursuits of the flesh.
