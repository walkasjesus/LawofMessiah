gods. Exterminate their name from that place. But you are not to treat
ADONAI your God this way. Rather, you are to come to the place where ADONAI
your God will put his name. He will choose it from all your tribes; and you
will seek out that place, which is where he will live, and go there.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
