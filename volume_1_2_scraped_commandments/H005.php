</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Key Scriptures</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Leviticus 17:3-9</u>
(Maimonides RN90; Chinuch C186)
<br>
<i>When someone from the community of Isra&#039;el slaughters an ox, lamb or
goat inside or outside the camp without bringing it to the entrance of the
tent of meeting to present it as an offering to ADONAI before the tabernacle
of ADONAI, he is to be charged with blood- he has shed blood, and that
person is to be cut off from his people. The reason for this is so that the
people of Isra&#039;el will bring their sacrifices that they sacrifice out
in the field- so that they will bring them to ADONAI, to the entrance of the
tent of meeting, to the cohen, and sacrifice them as peace offerings to
ADONAI. The cohen will splash the blood against the altar of ADONAI at the
entrance to the tent of meeting and make the fat go up in smoke as a
pleasing aroma for ADONAI. No longer will they offer sacrifices to the
goat-demons, before whom they prostitute themselves! This is a permanent
regulation for them through all their generations.&#039; Also tell them,
&#039;When someone from the community of Isra&#039;el or one of the
foreigners living with you offers a burnt offering or sacrifice without
bringing it to the entrance of the tent of meeting to sacrifice it to
ADONAI, that person is to be cut off from his people.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
