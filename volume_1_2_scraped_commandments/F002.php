<font size=4 face="Times New Roman, Times, serif">
<u>Deuteronomy 25:5-10</u> [obsolete]</u>
(Maimonides RP216-217, RN357; Meir MP45-46, MN135;
Chinuch C597-599)
<br>
<i>If brothers live together, and one of them dies childless, his widow is not
