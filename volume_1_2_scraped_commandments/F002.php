<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides, Meir, and HaChinuch make no reference to <u>Genesis 2:24</u> (or
any other Scripture) to support leaving one's father and mother and becoming
one flesh with one's wife when one marries. Maimonides' RP213, Meir's MP44,
and HaChinuch's C552, rely on <u>Deuteronomy 24:1</u> to support their
allegation that marriage must be by <i>kiddushin</i> (consecration) - a
binding marriage ceremony. The Scripture does not say that, nor does any
other Scripture.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides and HaChinuch refer to <u>Exodus 21:11</u>, and Meir refers to
<u>Deuteronomy 24:1</u> to support their general law of marriage, but I find
all three commentators ambiguous as to whether their respective
<i>mitzvot</i> command marriage or simply describe the <i>kiddushin</i>
(sanctification), of a marriage ceremony. Apparently, Rabbi Dr. Charles B.
Chavel, a prominent translator into English of Maimonides' "<i>Sefer
HaMitzvot</i>" had the same concern, for he found it necessary to introduce
the bracketed word [only] in the first sentence of his <i>mitzvah</i> RP213,
causing it to read:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
By this injunction we are commanded to take a woman to wife [only] by a
binding ceremony: either by giving her something [of
value]<sup><small><a name=fr2 href=#fn2>2</a></small></sup>,
or by handing her a writ of marriage, or by intercourse [accompanied by a
declaration of marriage].
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Meir discusses <i>kiddushin</i> through the payment of monetary value, and he
also discusses betrothal before <i>nissuin</i> (consummation) but not the
other ways of consecrating a marriage.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
HaChinuch presents the most detail of <i>kiddushin</i>. He states that if
marriage is consecrated by the giving of monetary value, the groom must say:
"You are now consecrated to me by this money (or thing of monetary value)."
If the marriage is consecrated by a document, the groom must say: "You are
consecrated to me by this document (or whatever it is)." If marriage is
consecrated by sexual intercourse, he must say to his bride before witnesses:
"You are consecrated to me by this act of intimacy." He then takes his bride
into seclusion and is seen doing so by the witnesses.
</font></p><hr width=150px style="margin-left: 10px">
<p align=left style="margin-left: 30px; margin-right: 30px">
<font size=2>
<a name=fn2 href=#fr2>2.</a>
The modern equivalent of paying the bride money is for
the groom to give the bride a wedding ring of significant monetary value
while stating: "You are hereby consecrated to me with this ring, according
to the law of Moses and Israel." It is analogous to a "bride price"
practiced in certain other cultures, and opposite to a "dowry" which the
bride or the bride&#039;s father is expected to pay to the groom.
</font>
