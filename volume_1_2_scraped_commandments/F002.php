him and speak to him. If, on appearing before them, he continues to say,
&#039;I don&#039;t want to marry her,&#039; then his brother&#039;s widow is
to approach him in the presence of the leaders, pull his sandal off his
foot, spit in his face and say, &#039;This is what is done to the man who
