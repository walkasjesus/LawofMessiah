<font size=4 face="Times New Roman, Times, serif">
<b>THE "CREATION" COVENANT OF MARRIAGE</b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Genesis 2:18-24</u>
<br>
<i>ADONAI, God, said, "It isn&#039;t good that the person should be alone. I
will make for him a companion suitable for helping him." So from the ground
ADONAI, God, formed every wild animal and every bird that flies in the air,
and he brought them to the person to see what he would call them. Whatever
the person would call each living creature, that was to be its name. So the
person gave names to all the livestock, to the birds in the air and to every
wild animal. But for Adam there was not found a companion suitable for
helping him. Then God caused a deep sleep to fall upon the person; and
while he was sleeping, he took one of his ribs and closed up the place from
which he took it with flesh. The rib which ADONAI, God, had taken from the
person, he made a woman-person; and he brought her to the man-person. The
man-person said, "At last! This is bone from my bones and flesh from my
flesh. She is to be called Woman, because she was taken out of Man." This
is why a man is to leave his father and mother and stick with his wife, and
they are to be one flesh.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Exodus 21:7-11</u> [obsolete]</u>
(Maimonides RP213; Chinuch 552)
<br>
<i>If a man sells his daughter as a slave, she is not to go free like the
men-slaves. If her master married her but decides she no longer pleases
him, then he is to allow her to be redeemed. He is not allowed to sell her
to a foreign people, because he has treated her unfairly. If he has her
marry his son, then he is to treat her like a daughter. If he marries
another wife, he is not to reduce her food, clothing or marital rights. If
he fails to provide her with these three things, she is to be given her
freedom without having to pay anything.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Exodus 22:15-16(16-17)</u> [obsolete]</u>
<br>
<i>If a man seduces a virgin who is not engaged to be married and sleeps with
her, he must pay the bride-price for her to be his wife. But if her father
refuses to give her to him, he must pay a sum equivalent to the bride-price
for virgins.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Leviticus 21:13-15</u> [obsolete]</u>
(Maimonides RP38; Chinuch 272)
<br>
