<font size=4 face="Times New Roman, Times, serif">
<u>Deuteronomy 21:10-13</u> [obsolete]</u>
(Maimonides RP221; Chinuch 532)
<br>
<i>When you go out to war against your enemies, and ADONAI your God hands them
over to you, and you take prisoners, and you see among the prisoners a woman
who looks good to you, and you feel attracted to her and want her as your
wife; you are to bring her home to your house, where she will shave her
head, cut her fingernails and remove her prison clothing. She will stay
there in your house, mourning her father and mother for a full month; after
which you may go in to have sexual relations with her and be her husband,
and she will be your wife.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Deuteronomy 21:15-17</u> [obsolete]</u>
<br>
<i>If a man has two wives, the one loved and the other unloved, and both the
loved and unloved wives have borne him children, and if the firstborn son is
the child of the unloved wife; then, when it comes time for him to pass his
inheritance on to his sons, he may not give the inheritance due the
firstborn to the son of the loved wife in place of the son of the unloved
one, who is in fact the firstborn. No, he must acknowledge as firstborn the
son of the unloved wife by giving him a double portion of everything he
owns, for he is the firstfruits of his manhood, and the right of the
firstborn is his.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Deuteronomy 22:13-21</u> [obsolete]</u>
(Chinuch C552)
<br>
<i>If a man marries a woman, has sexual relations with her and then, having
come to dislike her, brings false charges against her and defames her
character by saying, &#039;I married this woman, but when I had intercourse
with her I did not find evidence that she was a virgin&#039;; then the
girl&#039;s father and mother are to take the evidence of the girl&#039;s
virginity to the leaders of the town at the gate. The girl&#039;s father
will say to the leaders, &#039;I let my daughter marry this man, but he
hates her, so he has brought false charges that he didn&#039;t find evidence
of her virginity; yet here is the evidence of my daughter&#039;s
virginity&#039; - and they will lay the cloth before the town leaders. The
leaders of that town are to take the man, punish him, and fine him
two-and-a-half pounds of silver shekels, which they will give to the
girl&#039;s father, because he has publicly defamed a virgin of
Isra&#039;el. She will remain his wife, and he is forbidden from divorcing
her as long as he lives. But if the charge is substantiated that evidence
for the girl&#039;s virginity could not be found; then they are to lead the
girl to the door of her father&#039;s house, and the men of her town will
stone her to death, because she has committed in Isra&#039;el the
disgraceful act of being a prostitute while still in her father&#039;s
house. In this way you will put an end to such wickedness among you.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
