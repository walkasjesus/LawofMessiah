</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Supportive Scriptures - Marriage in the New Covenant</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Mark 12:25</u>
<br>
<i>For when people rise from the dead, neither men nor women marry - they are
like angels in heaven.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>1 Corinthians 7:1-6</u>
<br>
<i>Now to deal with the questions you wrote about: "Is it good for a man to
