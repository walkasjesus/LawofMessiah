family is to be known in Isra&#039;el as &#039;the family of the man who had
his sandal pulled off.&#039;</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b>MARRIAGE IN THE NEW COVENANT</b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>1 Corinthians 7:36-40</u>
<br>
<i>Now if a man thinks he is behaving dishonorably by treating his
fianc&eacute;e this way, and if there is strong sexual desire, so that
marriage is what ought to happen; then let him do what he wants - he is not
sinning: let them get married. But if a man has firmly made up his mind,
being under no compulsion but having complete control over his will, if he
has decided within himself to keep his fianc&eacute;e a virgin, he will be
doing well. So the man who marries his fianc&eacute;e will do well, and the
man who doesn&#039;t marry will do better. A wife is bound to her husband
as long as he lives, but if the husband dies she is free to marry anyone she
wishes, provided he is a believer in the Lord. However, in my opinion, she
will be happier if she remains unmarried, and in saying this I think I have
God&#039;s Spirit.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>1 Corinthians 11:3</u>
<br>
<i>But I want you to understand that the head of every man is the Messiah, and
the head of a wife is her husband, and the head of the Messiah is God.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Ephesians 5:22-33</u>
<br>
<i>Wives should submit to their husbands as they do to the Lord; because the
husband is head of the wife, just as the Messiah, as head of the Messianic
Community, is himself the one who keeps the body safe. Just as the
Messianic Community submits to the Messiah, so also wives should submit to
their husbands in everything. As for husbands, love your wives, just as the
Messiah loved the Messianic Community, indeed, gave himself up on its
behalf, in order to set it apart for God, making it clean through immersion
in the mikveh, so to speak, in order to present the Messianic Community to
himself as a bride to be proud of, without a spot, wrinkle or any such
thing, but holy and without defect. This is how husbands ought to love
their wives - like their own bodies; for the man who loves his wife is
loving himself. Why, no one ever hated his own flesh! On the contrary, he
feeds it well and takes care of it, just as the Messiah does the Messianic
Community, because we are parts of his Body. "Therefore a man will leave his
father and mother and remain with his wife, and the two will become one."
There is profound truth hidden here, which I say concerns the Messiah and
the Messianic Community. However, the text also applies to each of you
individually: let each man love his wife as he does himself, and see that
the wife respects her husband.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Colossians 3:18-19</u>
<br>
<i>Wives, subject yourselves to your husbands, as is appropriate in the Lord.
Husbands, love your wives and don&#039;t treat them harshly.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Hebrews 13:4a</u>
<br>
<i>Marriage is honorable in every respect; and, in particular, sex within
marriage is pure.</i>
