<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Waiting on God and having patience are obviously connected. As a matter of
fact, according to <u>Galatians 5:22</u>, patience is a fruit of the Spirit.
So why is exercising patience to wait on God sufficiently problematic so as
to warrant a <i>mitzvah</i> devoted to it? It is because natural man's way
is to be impatient and to seek immediate gratification, so that when natural
man petitions God for something in prayer, he judges whether or not God has
answered or will answer his prayer by whether or not he sees immediate
results.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This <i>Mitzvah</i> calls us to exercise faith for God's timeframe which is
not the same as ours; consider the following Scriptures:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>2 Peter 3:8-9</u>:&nbsp;&nbsp;<i>Moreover, dear friends, do not ignore
this: with the Lord, one day is like a thousand years and a thousand years
like one day. The Lord is not slow in keeping his promise, as some people
think of slowness; on the contrary, he is patient with you; for it is not his
purpose that anyone should be destroyed, but that everyone should turn from
his sins.</i>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Ecclesiastes 3:1</u>:&nbsp;&nbsp;<i>For everything there is a season, a
right time for every intention under heaven</i>.
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Acts 1:6-7</u>:&nbsp;&nbsp;<i>When they [Yeshua and his disciples] were
together, they asked him, "Lord, are you at this time going to restore
self-rule to Isra'el?" He answered, "You don't need to know the dates or the
times; the Father has kept these under his own authority."</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Waiting on God builds our faith and our dependence on Him, which are good
things. <u>Proverbs 3:5-6</u> expresses it well:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>Trust in ADONAI with all your heart; do not rely on your own
understanding. In all your ways acknowledge him; then he will level your
paths.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
