<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Belief in the God of Abraham Isaac and Jacob is not only belief in His
existence, but in the fact that He is the God who brought us out of Egypt
and is our only God (<u>Exodus 20:2</u>). Also, Scripture teaches that
believing in God is connected to trusting God, since one cannot trust in
whom one does not believe. In order to emphasize the equivalence of belief
and trust, several of the above Scriptures are rendered in two reputable
translations - the NKJ that speaks of belief, and the CJB that speaks of
trust in their handling the same Scriptures.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides, Meir, and HaChinuch concur that <u>Exodus 20:2</u> is a
Scripture that, by implication, commands that we believe in God. The
rationale for this is God's own statement that He is our God, and the
supreme means of our having been brought out of the slavery of Egypt. To
believe, therefore, that God is God, one has to trust His Word that He is
whom He says He is, thereby illustrating the inseparability of belief and
trust. Meir makes another cogent point by stating that belief in God is the
foundation of Jewish faith, and anyone who does not believe in God has no
place among the Jewish people.
