</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This precept is derived from His Word (blessed be He):
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Key Scripture</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Matthew 23:8-10</u>
<br>
<i>But you are not to let yourselves be called &#039;Rabbi&#039;; because you
have one Rabbi, and you are all each other&#039;s brothers. And do not call
anyone on earth &#039;Father.&#039; because you have one Father, and he is
in heaven. Nor are you to let yourselves be called &#039;leaders,&#039;
because you have one Leader, and he is the Messiah!</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The Greek words used in <u>Matthew 23:8-10</u> are
<img src=images/Y007-1.gif align=top height=23> for rabbi,
<img src=images/Y007-2.gif align=top height=23> for father,
<img src=images/Y007-3.gif align=top height=23> for teacher or leader. Since
other Scriptures refer to our earthly fathers, teachers, and leaders, and
since John does not object to being called "rabbi" in <u>John 3:26</u>,
Yeshua cannot be saying that we should not appropriately acknowledge our
earthly fathers, teachers, leaders, and ordained rabbis. He is instead
saying that we are to acknowledge our Father in heaven as our supreme father,
and only him (Yeshua) as our supreme rabbi, teacher, & leader. The context is
Yeshua criticizing <i>Torah</i>-teachers and the <i>P'rushim</i> who are
using titles to elevate their stature, and doing things in public in order to
be seen. This is a warning to us as well, to neither elevate our positions
higher than we ought, nor approve or affirm others who may seek to do the
same.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary by Daniel C. Juster</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Yeshua is warning us against gaining our identities through titles or other
means that give an idolatrous level of honor to human beings.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
