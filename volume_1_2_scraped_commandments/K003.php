<p align=left>
<font size=6>
<b>K3. </b>
</font>
<font size=5>
<b>Eating Fat, Meat from Strangled Animals, or Food Polluted by Idols.</b>
</font>
<font size=3 face="Times New Roman, Times, serif">
&nbsp;&nbsp;
<a href="comment.php?d=K003&h=K3"
onClick="return show_hide_box(this,400,280,'1px solid')">[Make a Comment]</a>
</font>
</p>
<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><i>We are not to eat fat, meat from strangled animals, or food polluted
by idols.</i></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This precept is derived from His Word (blessed is He):
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Key Scriptures</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Leviticus 3:16-17</u>
<br>
<i>The cohen will make them go up in smoke on the altar; it is food, an
offering made by fire to be a fragrant aroma; all the fat belongs to ADONAI.
It is to be a permanent regulation through all your generations wherever you
live that you will eat neither fat nor blood.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Leviticus 7:22-25</u>
(Maimonides RN185; Meir MN88; Chinuch C147)
<br>
<i>ADONAI said to Moshe, "Say to the people of Isra&#039;el, &#039;You are not
to eat the fat of bulls, sheep or goats. The fat of animals that die of
themselves or are killed by wild animals may be used for any other purpose,
but under no circumstances are you to eat it. For whoever eats the fat of
animals of the kind used in presenting an offering made by fire to ADONAI
will be cut off from his people.&#039;"</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Acts 15:19-20</u>
<br>
<i>Therefore, my opinion is that we should not put obstacles in the way of the
Goyim who are turning to God. Instead, we should write them a letter
telling them to abstain from things polluted by idols, from fornication,
from what is strangled and from blood.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The third chapter of <u>Leviticus</u> discusses the procedure for peace
offerings made by fire to the Lord and, in speaking of the fat of the
offerings, <u>Leviticus 3:16-17</u> states:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>The cohen will make them go up in smoke on the altar; it is food, an
offering made by fire to be a fragrant aroma; all the fat belongs to ADONAI.
It is to be a permanent regulation through all your generations wherever you
live that you will eat neither fat nor blood.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
So, the rationale for not eating the fat of an animal is that the parts of
the animal that have significant fat belong to God. That is why we offer up
the fat to God as food in the Peace Offering, and why we do not eat it
ourselves; it belongs to God.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
I am of the opinion that <u>Leviticus 3</u> prohibits Jews and
<i>K'rovei Yisrael</i> from eating any masses of fat that covers an
animal&#039;s inner organs, and any food in which animal fat is the main
ingredient.<sup><small><a name=fr1 href=#fn1>1</a></small></sup>
I express no opinion regarding the degree to which incidentally occurring
fat must be removed from meat, nor do I believe that Scripture prohibits
Jews and <i>K'rovei Yisrael</i> from eating marbleized meat (i.e. meat
containing embedded particles of fat). Gentiles generally are not prohibited
from eating fat, but many abstain for reasons related to health.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Although not biblically required, choosing to only purchase
rabbinically-certified meat is an easy and automatic way to comply with this
<i>Mitzvah</i> as well as with several others having to do with
<i>kashrut</i>.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The biblical prohibition in <u>Acts 15:19-21</u> against eating meat from
strangled animals and food polluted by idols is often thought of in its
context of applying to Gentiles. However, it is clearly applicable to Jews as
well; that is what is meant by:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>For from the earliest times, Moshe has had in every city those who
proclaim him, with his words being read in the synagogues every Shabbat.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
A matter that deserves some discussion, however, is the meaning of the
expression "<i>polluted by idols</i>". Some translations render this
"offered to idols", or "sacrificed to idols", bringing to mind the ritual
slaughter of an animal as part of pagan worship, and then bringing the meat
home to eat. This kind of idol worship is rare in the developed world, but
still may occur among pagan and satanic cults. We do sometimes encounter
restaurants and food markets where there is evidence that the owners engage
in idol worship (oriental restaurants especially), and we also encounter
"<i>halal</i>" restaurants and markets where the meats are blessed by Muslim
clerics in the name of <i>Allah</i>. In my opinion, the former is not a
violation of Scripture so long as the food being offered for sale has not
been involved in idolatrous rituals. The latter is more problematic and, in
my opinion, eating such food should be avoided unless there is nothing else
to eat.
</font></p><hr width=150px style="margin-left: 10px">
<p align=left style="margin-left: 30px; margin-right: 30px">
<font size=2>
<a name=fn1 href=#fr1>1.</a>
"Animal fat" does not include the fat in milk.
Consequently, butter, cheese, cream, and similar foods are permitted.
</font>
</p>
<hr width=150px align=left style="margin-left: 10px">
<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Meir and HaChinuch interpret Scripture&#039;s prohibition from eating fat as
only applying to certain "forbidden" fat (<i>chelev</i>). HaChinuch states
that there are three kinds: (1) fat attached to the entrails, (2) fat
attached to the kidneys, and (3) fat on the loins; he then goes on to make
anatomical distinctions in considerable detail. Maimonides makes no
distinction as to kinds of fat that are <i>chelev</i>, and kinds that are
permitted. HaChinuch explains the prohibition on eating fat by asserting its
unhealthy nature. Maimonides and Meir make no attempt at explanation.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
