<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Swearing, in the context of this <i>mitzvah</i>, is invoking the Name of God
in order to add a weight of truth to that which is spoken. Swearing can be
formal (such as in a court of law), in which case swearing falsely is known
as "perjury", and one can be punished by the court for committing it.
Swearing can also be informal such as when one makes a comment on the spur of
the moment and adds an oath to the comment as a way of attempting to assure
its truth. In either case, the Scriptures that underlay this <i>Mitzvah</i>
make it clear that one must never swear to that which is untrue.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u><b>Classical Commentators</b></u>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides, Meir, and HaChinuch take great pains to employ three Scriptures
(<u>Exodus 20:7</u>, <u>Leviticus 5:22(6:3)</u>, and <u>Leviticus
19:11-12</u>) and many words of explanation in order to produce three
<i>mitzvot</i> from what is essentially the same. Maimonides distinguishes
two of them by emphasizing a <i>shebuat bittui</i> (verbal oath) from a
<i>shebuat shav</i> (false oath), from an oath made to repudiate a debt, and
the other two commentators do similarly using various indicators of
distinction, and they distinguish the third Scripture by its reference to
theft.
