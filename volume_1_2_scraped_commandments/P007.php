<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><i>Levitical Priests are not to be in the vicinity of a dead body unless
the deceased is a close relative.</i></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This precept is derived from His Word (blessed be He):
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Key Scriptures</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Leviticus 21:1-4</u>
(Maimonides RP37; Meir MP59; Chinuch C264)
<br>
<i>ADONAI said to Moshe, "Speak to the cohanim, the sons of Aharon; tell them:
&#039;No cohen is to make himself unclean for any of his people who dies,
except for his close relatives - his mother, father, son, daughter and
brother; he may also make himself unclean for his virgin sister who has
never married and is therefore dependent on him. He may not make himself
unclean, because he is a leader among his people; doing so would profane
him.&#039;"</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Leviticus 21:6</u>
(Maimonides RN166; Meir MN141; Chinuch C263)
<br>
<i>Rather, they are to be holy for their God and not profane the name of their
God. For they are the ones who present ADONAI with offerings made by fire,
the bread of their God; therefore they must be holy.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Mark 5:39-41</u>
<br>
<i>On entering, he said to them, "Why all this commotion and weeping? The child
isn&#039;t dead, she&#039;s just asleep!" And they jeered at him. But he put
them all outside, took the child&#039;s father and mother and those with
him, and went in where the child was. Taking her by the hand, he said to
her, "Talita, kumi!" (which means, "Little girl, I say to you, get up!"). At
once the girl got up and began walking around; she was twelve years old.
Everybody was utterly amazed.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
It is natural for members of a community to want to be with, and comfort
members of a grieving family while they are mourning the loss of a loved
one. Nevertheless, <u>Leviticus 21:1</u> prohibits Levitical <i>cohanim</i>
from being anywhere near a dead body lest they become ritually unclean and
unfit for their Temple duties. In modern times, the Scripture has been
applied in several different ways by different rabbinical authorities,
including not permitting supposed male descendants of Aaron to be in the
same room as a dead person, nor even on cemetery grounds.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The prohibition of course had a practical purpose when the Tabernacle and
Temple functioned with the Levitical Priesthood conducting sacrifices, but it
is not the case today. Nevertheless, in anticipation of the Temple being
rebuilt (<u>Ezekiel 40-47</u>) and the sacrifices being restored, Orthodox
and Conservative Judaism continue the practice of not allowing men whom they
believe are descended from Aaron to come near to a dead body. Reform Judaism
does not follow the practice because Reform Judaism does not recognize the
continuity of priestly lineage.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Leviticus 21:2-3</u> is an exception to the prohibition when the deceased
person is a close relative. Not only is it an exception - it is considered a
positive commandment by the classical commentators, that a priest allow
himself to be rendered ritually unclean in that circumstance for the sake of
his relative.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Some Messianic Jewish congregations follow Conservative Judaism in this,
others do not, and still others have not developed a policy on these issues
at all. Messianic Jewish scholars and leaders that follow Conservative Jewish
practices interpret <u>Ezekiel</u> chapters <u>40-47</u> to be prophecy, that
the Holy Temple will eventually be rebuilt, and that certain sacrifices will
be resumed - possibly conducted by a restored Levitical priesthood. All
believers in Yeshua know that restored sacrifices cannot be for the
commission of sin since Yeshua is our sacrifice for sin (<u>Hebrews 9:11-14;
10:1-14</u>); furthermore, we do not know whether the priests conducting
sacrifices in a restored Temple will be "Levitical" or of some other priestly
order. Nevertheless, in compliance with Jewish tradition, Messianic Judaism
generally follows the practice of shielding "assumed" <i>cohanim</i> from
being near dead bodies, with the understanding that it is voluntary on their
part.<sup><small><a name=fr1 href=#fn1>1</a></small></sup>.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This <i>Mitzvah</i> applies only to males. It does not apply to females
because <u>Leviticus 21:1</u> refers to "sons of Aaron", and because only
males served in the Tabernacle and the Holy Temple as priests.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Messianic Judaism does not apply this <i>Mitzvah</i> to New Covenant priests
(i.e. to believers in Yeshua).
</font></p><hr width=150px style="margin-left: 10px">
<p align=left style="margin-left: 30px; margin-right: 30px">
<font size=2>
<a name=fn1 href=#fr1>1.</a>
Since the Temple&#039;s records have been lost, there is
no official record of whose unbroken patrilineal descent is traceable to
Aaron. Consequently, family names such as "Cohen", "Kohen", "Cone",
"Cowen", etc., in addition to family histories and traditions are relied
upon.
</font>
</p>
<hr width=150px align=left style="margin-left: 10px">
<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides, Meir, and Chinuch all take the Orthodox approach of considering
<u>Leviticus 21:1-4 and 6</u> to consist of two commandments - a negative one
prohibiting a <i>cohen</i> from being in the vicinity of a dead body, and a
positive one requiring that a <i>cohen</i> disregard the prohibition in the
case where the deceased person is his close relative. Also, all three
commentators agree that the commandments apply only to men.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
