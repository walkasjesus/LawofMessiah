<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
It should be apparent from the many Scriptures quoted above, that both the
<i>Tanakh</i> and the <i>Kitvey B'rit Chadasha</i> enjoin us to be thankful
to God, giving thanks to Him who created all and who continues to provide for
our every need. There exists, however, only one place in the <i>Torah</i>
that specifically commands thankfulness - <u>Deuteronomy 8:6-18</u>, <u>verse
10</u>, which is the basis for the traditional Grace after Meals, the
<i>Birkat Hamazon</i>. Notwithstanding that single occurrence, being
thankful and giving thanks to God are deeply ingrained in Judaism and in
Jewish practice. They are addressed in various places in the <i>Talmud</i>
(e.g. in <u><i>B'rakhot</i></u> and <u><i>Avot</i></u>), and appear in the
<i>Siddur</i> as the first blessing that is said upon awakening (<i>Modeh
Ani</i>), as the next-to-last benediction of the <i>Sh'moneh Esreh</i>
("<i>Modim anachnu lach ...</i>"), and as the central theme of the
<i>Aleinu</i> prayer ("<i>Va'anachnu korim umishtachavim umodim</i>"). They
are also implicit in the multitude of blessings that begin "<i>Baruch atah
adonai, eloheynu melech ha-olam ...</i>" ("Blessed are you Lord, our God,
King of the universe, who ...").
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
There are essentially two ways in which we can bless God. The first is by
expressing thankful praise to Him for the blessings He bestows on us. It may
be emoted spontaneously or recited ritually in what is termed a
"<i>b'rakhah</i>". The second way is by loving Him as our Heavenly Father,
Messiah savior, and Holy Spirit, and by fulfilling His highest expectation of
us, which is to be unswervingly obedient to His Word and to walk in His ways.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The totality of Scripture makes it plain that our being thankful to God and
our blessing Him should not be only for His provision of food, but for all of
His provisions and blessings that He bestows upon us, not the least of which
is His unconditional love. There is therefore a deeper way to understand and
employ the <i>Birkat Hamazon</i> than as merely "grace after meals". It is
that, in the fullness of our having eaten, we should allow its words to
permeate our soul while contemplating our blessings, and thankfully
remembering that God is our provider and sustainer, and not we ourselves.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
None of the commentators (Maimonides, Meir, or HaChinuch) wrote general
<i>mitzvot</i> commanding that we be thankful to God. That is no doubt
because they only sought to codify commandments they found in the
<i>Torah</i>, and concluded that <u>Deuteronomy 8:10</u> is the only
Scripture in the <i>Torah</i> that commands thankfulness.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Two English translations for <u>Deuteronomy 8:10</u> are included in the
Scripture list above. None of the commentators worked in the English
language, of course, but the way the New King James Version translates
<u>Deuteronomy 8:10</u> must have been close to how all three of them
understood the Hebrew. I conclude this because all three of their
<i>mitzvot</i> command that we bless God after we have eaten a meal (i.e. say
Grace). Although the context of the Scripture is the Israelites having come
into the Promised Land, Maimonides, Meir, HaChinuch and all of Judaism
interpret it broadly to mean that, after we have eaten and are satisfied, we
are to remember God who brought us out of Egypt. That is the purpose of the
<i>Birkat Hamazon</i>. Meir states that we are also to bless God before
eating. Using the logic of <i>kal va-chomer</i> (less to more), he states
that if we are to bless God when we are satisfied, how much more is our
