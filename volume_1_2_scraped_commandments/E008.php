<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
In his <i>mitzvah</i> RP212, Maimonides states that we are to marry in order
to multiply, and he cites <u>Yebamoth 65b</u> of the <i>Talmud</i> to support
his assertion that the duty to be fruitful and multiply applies to men and
not women. Meir does not state that it is only the man's duty, but implies
it by framing his <i>mitzvah</i> MP43 to address only the man. HaChinuch is
consistent with Maimonides and Meir and also cites <u>Yebamoth 6</u>. None of
the classical commentators treat being fruitful and multiplying as other than
marrying and reproducing.
