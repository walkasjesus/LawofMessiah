<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The first part of <u>Genesis 1:28</u> states: "<i>God blessed them: God said
to them, 'Be fruitful, multiply, fill the earth ...'</i>" Notice that the
commandment to "be fruitful" in <u>Genesis 1:28</u> comes before the command
to "multiply," and that the rest of the verse:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>and subdue it. Rule over the fish in the sea, the birds in the air and
every living creature that crawls on the earth.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
instructs us that we have an obligation to produce value from our multiplied
numbers - in other words, disseminate our fruitfulness.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The agricultural analogy is that fruit comes first and contains the seeds
from which reproduction or multiplication of the plant occurs. Then, the more
multiplication the more the fruit, so it is an unending cycle.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
We see God's expectation that we multiply (increase) in fruitfulness
elsewhere in Scripture as well. In <u>Genesis 35:10-11</u>, for example, God
repeats his commandment to Israel (Jacob) to multiply, but God also says that
a nations and kings will be descended from him, implying that Israel's
descendants will have functions to fulfill with their numbers. Of course we
know from <u>Exodus 19:6</u> that one of the areas of Israel's fruitfulness
is that it is destined to "<i>be a kingdom of cohanim</i>" - "<i>a nation set
apart.</i>"
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
In <u>Acts 9:31</u>, when we are told that the Messianic Community multiplied
in number, it is not to inform us of their reproductive fertility, but of the
fact that belief in Yeshua spread throughout the area. Similarly,
<u>Colossians 1:6</u> speaks of the Good News "<i>being fruitful and
multiplying</i>", and <u>Colossians 1:10</u> speaks of "<i>being fruitful in
every good work and multiplying in the full knowledge of God.</i>" It appears
clear that God's desire for Israel and for all of mankind is that we multiply
(increase) in both number and fruitfulness.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Now a word about reproduction and marriage. The <u>Genesis</u> commandment
to multiply was given after man was created to be male and female, but prior
to God having made Eve (<u>Genesis 2:18-24</u>). Although reproduction was
not mentioned in the context of marriage, after God made Eve, He caused her
and Adam to become "one flesh" (<u>verse 24</u>) and, since then, God
intended reproduction between man and woman to be only in the context of
covenant marriage. Reproduction is so implicitly part of the marriage
covenant, that a party who refuses to bear children against the wishes of his
or her mate may well be ruled to have abandoned the marriage covenant.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The controversy regarding the use of birth control measures by consenting
married persons cannot be definitively resolved within this <i>Mitzvah</i>.
I do, however, express my opinion that methods of birth control that do not
kill or abort fetuses are permitted because (1) Scripture allows unmarried
persons to avoid having children by remaining single (<u>Matthew 9:12</u>;
<u>1 Corinthians 7:8</u>), and (2) in Paul's warning to married persons that
they should not deprive each other of sexual relations (<u>1 Corinthians
7:3-5</u>), the reason he gives is not that deprivation violates a
commandment to reproduce, but rather "<i>because of your lack of
self-control, you may succumb to the Adversary's
temptation.</i>"<sup><small><a name=fr1 href=#fn1>1</a></small></sup>
And then, Paul follows up in <u>verse 6</u> with: "<i>I am giving you this as
a suggestion, not as a command.</i>" I deduce from this that since agreed-to
abstinence between married persons is not a sin, so neither is preventing
conception through other non-lethal means.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Finally, <u>Genesis 1:28</u> requires that mankind, as a species, reproduce,
but it does not command that all persons or couples reproduce. Certainly
unmarried persons should not, persons with certain medical conditions cannot,
and there may be some that God specifically calls to remain unmarried (<u>1
Corinthians 7:8</u>).
