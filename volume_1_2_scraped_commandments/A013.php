<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
God spoke to Israel through Jeremiah and promised them:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>I will put my Torah within them and write it on their hearts; I will be
their God, and they will be my people.</i> (<u>Jeremiah 31:32b</u>;
<u>Hebrews 8:10</u>)
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Later, God spoke through Ezekiel and said:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Ezekiel 36:26</u>
<br>
<i>I will give you a new heart and put a new spirit inside you; I will take
the stony heart out of your flesh and give you a heart of flesh.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This new heart with the <i>Torah</i> embedded in it was to be a clean heart
with a new Spirit, and it came to fruition as part of the New Covenant and
man's "new birth" in Yeshua:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>John 3:3</u>
<br>
<i>"Yes, indeed," Yeshua answered him, "I tell you that unless a person is
born again from above, he cannot see the Kingdom of God."</i>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>John 3:6</u>
<br>
<i>What is born from the flesh is flesh, and what is born from the Spirit is
spirit.</i>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>2 Corinthians 5:1</u>
<br>
<i>Therefore, if anyone is united with the Messiah, he is a new creation -
the old has passed; look, what has come is fresh and new!</i>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>1 Peter 1:23</u>
<br>
<i>You have been born again not from some seed that will decay, but from one
that cannot decay, through the living Word of God that lasts forever.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
