</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This <i>Mitzvah</i> touches a number of principles, as can be seen from the
large number of Scriptures and <i>mitzvot</i> of Meir, Maimonides, and
Chinuch that are involved. It affirmatively requires that we lend money to
our brother if he is in need, and to not charge him interest on the loan.
Although historically commanded only to Israelites concerning their lending
money to other Israelites, strangers, and sojourners who are in need, this
<i>Mitzvah</i> to lend without interest applies in the New Covenant to all
who are our brothers in Yeshua regardless of whether they are Jews or
Gentiles. While we have no obligation under this <i>Mitzvah</i> to lend
money to persons other than brothers, if we do lend money to other poor or
needy persons, this <i>Mitzvah</i> strongly suggests that, out of compassion
for their poverty, we not charge them interest either. Also, this
<i>Mitzvah</i>'s reference to the Sabbatical Year warns us that we must not
decline to lend money to needy brothers prior to the Sabbatical Year, even
though the debt will be legally discharged and may never be repaid. It
should also be noted that <u>Deuteronomy 15:7</u> (referenced above) implies
an obligation to give charity (<i>tz'dakah</i>) as well as to loan, and
<u>Exodus 22:24(25)</u> not only prohibits being a usurer of the needy, but
also participating in it as a borrower (even as a needy borrower), a surety,
a witness, or in any other way.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Luke 6:34-36</u> tells us that if we loan money to someone (presumably
someone in need), we ought not to expect anything in return; rather, our loan
should be motivated by compassion and not by the hope of profiting from the
transaction either directly or indirectly. In my opinion, several English
translations of the Scripture (including the CJB) render <u>verse 34</u>
incorrectly by suggesting that the lender should not expect the loan itself
to be repaid. Not only is such a notion inconsistent with other Scriptures,
it contradicts the very nature and definition of a loan as distinguished from
a gift.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Deuteronomy 23:21(20)</u> does not apply to loans made to needy persons;
we know this because it allows an Israelite to charge interest to a stranger,
whereas <u>Leviticus 25:35</u> prohibits it if the stranger is poor.
Nevertheless, it is included here because it indicates that an Israelite
brother may not charge interest to another Israelite, even in the case of a
general loan, e.g. a business loan. This is in direct conflict with the
modern-day practice of charging interest for commercial loans, mortgages, and
credit cards. It is possible that, in this anonymous, mixed, and
credit-dependant society, this prohibition is no longer applicable because it
would prohibit Jews from becoming bankers of any kind, and would prevent Jews
from borrowing for business and from acquiring mortgages from banks whose
officers were Jewish.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Regarding <u>Deuteronomy 23:20(19)</u>, HaChinuch states that, not only are
we not to lend money at interest to a brother, but we are not to borrow money
at interest from a brother either, thereby making it clear that the lender in
such a transaction is not the only guilty party. It is also noteworthy that
Meir's <i>mitzvot</i> limit this obligation to lending to Jews, while
Maimonides' <i>mitzvah</i> does not contain this limitation; Meir states that
his <i>mitzvah</i> (MP62) is greater and more of an obligation than mere
charity. It is also noteworthy that other <i>mitzvot</i> deal differently
with lending money in circumstances that do not involve poverty.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Chill (p 85) references the three-year tithe (<u>Deuteronomy 14:28</u>)
because it is used for the needy. He also references <u>Deuteronomy 15:3</u>
(Maimonides RP142) that allows an Israelite to charge interest to a Gentile
on a loan as well as <u>Exodus 22:24(25)</u> (RN234).
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
It is noteworthy that this is part of a whole genus of laws that provide a
humane and protective society - a society that does not reward laziness, but
helps those in genuine need and leads to a gracious caring for one another.
It is related to the more clearly social welfare rules to be discussed in
other <i>mitzvot</i>, such as a planter providing for the poor by not
harvesting the corners of his corn field, not double-thrashing olive trees,
and not picking up fallen fruit from other trees. A biblically humane
society does have a welfare component, though it may not be in redistribution
of wealth; that is a big debate. What is clear is that it has rules that
foster widespread charity and responsibility for oneself, one's family, and
others.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
