(Maimonides RP197, RN237; Meir MN53, MP62; Chinuch C66,
C68)
<br>
<i>If you loan money to one of my people who is poor, you are not to deal
with him as would a creditor; and you are not to charge him interest.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
