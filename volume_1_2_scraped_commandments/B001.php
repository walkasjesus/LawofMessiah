<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><i>We are to lend money to our brother, sufficient for his need and
without regard for the Sabbatical Year. In such a loan, interest must not be
charged nor given, and brothers are to take no part whatsoever in
interest-bearing loans involving other brothers.</i></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This precept is derived from His Word (blessed is He):
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Key Scriptures</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
