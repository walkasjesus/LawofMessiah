<font size=4 face="Times New Roman, Times, serif">
<b><i>We are to obey the biblical law concerning divorce and
remarriage.</i></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This precept is derived from His Word (blessed be He):
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Key Scriptures</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b>GOD HATES DIVORCE</b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Malachi 2:16a</u>
<br>
<i>"For I hate divorce," says ADONAI the God of Isra&#039;el.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Mark 10:9</u>
<br>
<i>So then, no one should break apart what God has joined together.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b>THE SINAI COVENANT&#039;S ALLOWANCE BUT ABHORRENCE OF DIVORCE</b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Exodus 21:7-11</u>
(Maimonides RP213; Chinuch C552)
<br>
<i>If a man sells his daughter as a slave, she is not to go free like the
men-slaves. If her master married her but decides she no longer pleases
him, then he is to allow her to be redeemed. He is not allowed to sell her
to a foreign people, because he has treated her unfairly. If he has her
marry his son, then he is to treat her like a daughter. If he marries
another wife, he is not to reduce her food, clothing or marital rights. If
he fails to provide her with these three things, she is to be given her
freedom without having to pay anything.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Deuteronomy 21:10-14</u>
<br>
<i>When you go out to war against your enemies, and ADONAI your God hands them
over to you, and you take prisoners, and you see among the prisoners a woman
who looks good to you, and you feel attracted to her and want her as your
wife; you are to bring her home to your house, where she will shave her
head, cut her fingernails and remove her prison clothing. She will stay
there in your house, mourning her father and mother for a full month; after
which you may go in to have sexual relations with her and be her husband,
and she will be your wife. In the event that you lose interest in her, you
are to let her go wherever she wishes; but you may not sell her for money or
treat her like a slave, because you humiliated her.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Matthew 19:7-8</u>
<br>
<i>They said to him [Yeshua], "Then why did Moshe give the commandment that a
man should hand his wife a get and divorce her?" He answered, "Moshe
allowed you to divorce your wives because your hearts are so hardened. But
this is not how it was at the beginning.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b>WE ARE NOT TO BREAK THE COVENANT OF MARRIAGE</b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Malachi 2:13-16</u>
<br>
<i>Here is something else you do: you cover ADONAI&#039;s altar with tears,
with weeping and with sighing, because he no longer looks at the offering or
receives your gift with favor. Nevertheless, you ask, "Why is this?"
Because ADONAI is witness between you and the wife of your youth that you
have broken faith with her, though she is your companion, your wife by
covenant. And hasn&#039;t he made [them] one [flesh] in order to have
spiritual blood-relatives? For what the one [flesh] seeks is a seed from
God. Therefore, take heed to your spirit, and don&#039;t break faith with
the wife of your youth. "For I hate divorce," says ADONAI the God of
Isra&#039;el, "and him who covers his clothing with violence," says
ADONAI-Tzva&#039;ot. Therefore take heed to your spirit, and don&#039;t
break faith.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Matthew 19:3-6</u>
<br>
<i>Some P&#039;rushim came and tried to trap him [Yeshua] by asking, "Is it
permitted for a man to divorce his wife on any ground whatever?" He
replied, "Haven&#039;t you read that at the beginning the Creator made them
male and female, and that he said, &#039;For this reason a man should leave
his father and mother and be united with his wife, and the two are to become
one flesh&#039;? Thus they are no longer two, but one. So then, no one
should split apart what God has joined together."</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Mark 10:6-9</u>
<br>
<i>However, at the beginning of creation, God made them male and female. For
this reason, a man should leave his father and mother and be united with his
wife, and the two are to become one flesh. Thus they are no longer two, but
one. So then, no one should break apart what God has joined together.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Luke 16:18</u>
<br>
<i>Every man who divorces his wife and marries another woman commits adultery,
and a man who marries a woman divorced by her husband commits adultery.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b>ABANDONMENT BREAKS COVENANT &amp; RELEASES THE INNOCENT PARTY FROM
COVENANT OBLIGATION</b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Deutronomy 31:16-17</u>
<br>
<i>ADONAI said to Moshe, "You are about to sleep with your ancestors. But this
people will get up and offer themselves as prostitutes to the foreign gods
of the land where they are going. When they are with those gods, they will
abandon me and break my covenant which I have made with them. Then my anger
will flare up, and I will abandon them and hide my face from them.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Jeremiah 11:10-11</u>
<br>
<i>They [Israelites] have returned to the sins of their ancestors, who refused
to hear my words, and they have gone after other gods to serve them. The
house of Isra&#039;el and the house of Y&#039;hudah have broken my covenant
which I made with their ancestors. Therefore ADONAI says, "I am going to
bring on them a disaster which they will not be able to escape; and even if
they cry to me, I will not listen to them.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Jeremiah 31:30-31(31-32)</u>
<br>
<i>Here, the days are coming," says ADONAI, "when I will make a new covenant
with the house of Isra&#039;el and with the house of Y&#039;hudah. It will
not be like the covenant I made with their fathers on the day I took them by
their hand and brought them out of the land of Egypt; because they, for
their part, violated my covenant, even though I, for my part, was a husband
to them," says ADONAI.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b>A BELIEVER IS NOT TO ABANDON HIS OR HER SPOUSE</b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>1 Corinthians 7:10-11</u>
<br>
<i>To those who are married I have a command, and it is not from me but from
the Lord: a woman is not to separate herself from her husband. But if she
does separate herself, she is to remain single or be reconciled with her
husband. Also, a husband is not to leave his wife.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>1 Corinthians 7:12-14</u>
<br>
<i>To the rest I say - I, not the Lord: if any brother has a wife who is not a
believer, and she is satisfied to go on living with him, he should not leave
her. Also, if any woman has an unbelieving husband who is satisfied to go
on living with her, she is not to leave him. For the unbelieving husband
has been set aside for God by the wife, and the unbelieving wife has been
set aside for God by the brother - otherwise your children would be
"unclean," but as it is, they are set aside for God.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
