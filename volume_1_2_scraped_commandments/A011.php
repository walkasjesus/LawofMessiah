<font size=4 face="Times New Roman, Times, serif">
<b><u>Supportive Scriptures - Neither Wine Nor Grapes Are to be Consumed by
Those Who Have Taken a Nazirite Vow</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Numbers 6:2-4</u>
<br>
<i>Tell the people of Isra&#039;el, &#039;When either a man or a woman makes a
special kind of vow, the vow of a nazir, consecrating himself to ADONAI; he
is to abstain from wine and other intoxicating liquor, he is not to drink
vinegar from either source, he is not to drink grape juice, and he is not to
eat grapes or raisins. As long as he remains a nazir he is to eat nothing
derived from the grapevine, not even the grape-skins or the seeds.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The above Scriptures speak for themselves. Drunkenness is prohibited;
drinking wine in moderation is not. Wine is used in various ways within
Messianic Judaism - as one of the elements in the <i>Shulchan Adonai</i> (the
Lord's Table), for <i>Kiddush</i>, for <i>Havdalah</i>, and as a celebratory
beverage.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Of the numerous Scriptures that command against becoming intoxicated through
drinking, Maimonides and HaChinuch quote only <u>Leviticus 10:8-11</u> that
prohibits priests from entering the Temple after drinking wine or other
intoxicating liquor. Meir does not address the subject at all.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
