<font size=4 face="Times New Roman, Times, serif">
<b><i>We may partake of wine but must not become intoxicated.</i></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This precept is derived from His Word (blessed be He):
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Key Scriptures</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Leviticus 10:8-11</u>
(Maimonides RN73; Chinuch C152)
<br>
<i>ADONAI said to Aharon, "Don&#039;t drink any wine or other intoxicating
liquor, neither you nor your sons with you, when you enter the tent of
meeting, so that you will not die. This is to be a permanent regulation
through all your generations, so that you will distinguish between the holy
and the common, and between the unclean and the clean; and so that you will
teach the people of Isra&#039;el all the laws ADONAI has told them through
Moshe."</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Matthew 26:27-29</u>
(see also <u>Mark 14:23-25</u>; <u>Luke 22:17-18</u>;
<u>1 Corinthians 11:25-27</u>)
<br>
<i>Also he took a cup of wine, made the b&#039;rakhah, and gave it to them,
saying, "All of you, drink from it! For this is my blood, which ratifies
the New Covenant, my blood shed on behalf of many, so that they may have
their sins forgiven. I tell you, I will not drink this &#039;fruit of the
vine&#039; again until the day I drink new wine with you in my Father&#039;s
Kingdom."</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>1 Timothy 3:2-3</u>
<br>
<i>A congregation leader [elder] must be above reproach, he must be faithful to
his wife, temperate, self-controlled, orderly, hospitable and able to teach.
He must not drink excessively or get into fights; rather, he must be kind
and gentle. He must not be a lover of money.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>1 Timothy 3:8</u>
