<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This <i>Mitzvah</i> bestows the term "Annual Sabbath" on seven of the
annually-occurring "designated times" listed in <u>Leviticus 23</u>. This is
because, as in the case of the weekly Sabbath, we are commanded to rest on
them, refrain from work, and assemble. Of these however, only one of them -
<i>Yom Kippur</i> - is actually termed a Sabbath in the Scriptures. Each of
these days has its unique theme and reason for being, but because they bear
a basic similarity to the weekly Sabbath, the related issues of work, rest,
and application to Gentiles will be dealt with here only briefly.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The first and seventh days of the Feast of Unleavened Bread remind us of
God's provision and our need to purge leaven (analogized to sin) from
our lives (<u>1 Corinthians 5:7-8</u>). Of all the feasts, Passover is the
one that has most caught the attention of the Christian Church. This is
because of the obvious analogy of the Passover lamb sacrifice to Yeshua's
sacrifice, where the blood of each of the sacrifices, saved those who
applied it.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The next "designated time" listed on the biblical calendar, <i>Shavuot</i>,
was historically commanded to the Israelites to commemorate the Spring
harvest, but it is relevant to Gentile followers of Yeshua as well, both as
an identification with the Jewish People, and also as a memorial of the
<i>Shavuot</i> that occurred centuries later, when God made the Holy Spirit
available on earth to all of mankind (<u>Acts 2:1-21, 10:45</u>). Christian
terminology for this day is
"Pentecost"<sup><small><a name=fr1 href=#fn1>1</a></small></sup>.
<i>Shavuot</i> is unique among the Annual Sabbaths in being the only one that
Scripture does not assign a particular date on the lunar calendar. Instead,
<u>Leviticus 23:10,15-16</u> states:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>Tell the people of Israel, 'After you enter the land I am giving you and
harvest its ripe crops, you are to bring a sheaf od firstfruits of your
harvest to the cohen ... From the day after the day of rest - that is, from
the day you bring the sheaf for waving - you are to count seven full weeks,
until the day after the seventh week; you are to count fifty days; and then
you are to present a new grain offering to ADONAI.'</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This fifty-day count from "the day after the day of rest" (or Sabbath) is
known in Jewish practice as "counting the <i>omer</i>", and which one of
several possible Sabbaths is "day zero" of the count is a subject of ongoing
dispute that involves two different days that Scripture refers to as
"firstfruits". The first of these is the one in <u>Leviticus 23:10</u> which,
for clarity, I will refer to as <i>Yom
Habbikurim</i>.<sup><small><a name=fr2 href=#fn2>2</a></small></sup>
The second occurance is in <u>Numbers 28:26</u>, where the term is again
used, but for <i>Shavuot</i>.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<i>Yom T'ruah</i> (day of blowing) is also known as the "Day of Trumpets" or
"<i>Shofarot</i>", and as "<i>Rosh Hashanah</i>" (head of the year, i.e. New
Year) by the Rabbinic community. It is followed ten days later by <i>Yom
Kippur</i> (Day of Atonement). Although these days were historically
commanded to the Israelites, Gentile followers of Yeshua may choose to
observe them as well, both to identify with the Jewish People, and also to
use as times of personal introspection and repentance that may lead to
joining with their Jewish brothers in praying for Israel on <i>Yom
Kippur</i>. This is especially appropriate when one considers the elevated
position of New Covenant believers (both Jews and non-Jews) as priests under
Messiah Yeshua (<u>1 Peter 2:5-10</u>; <u>Revelation 1:4-7</u>).
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Finally, <i>Sukkot</i> is particularly significant for Gentiles, because
Scripture prophecies the time when all the Gentile nations on earth will be
required to join with Israel in keeping the Feast referred to in <u>Zechariah
14:16-19</u>. This appears to be a nation-to-nation mandate and not one
directed to individuals.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Although "rest" is not specifically mentioned in the supporting Scriptures
for the Feast of Unleavened Bread and for <i>Shavuot</i>, it is strongly
implied by the requirement that we not work. Also, although in <u>Hebrews
4:2-11</u> it is the weekly Sabbath rest that is analogized to resting in
Messiah, its application to all of the annual Sabbaths is unmistakable.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
I am of the view that complying with these Annual Sabbaths by resting,
abstaining from work, and assembling, is mandatory for Jews and
<i>K'rovei Yisrael</i> Gentiles but, with the exception of the Feast of
<i>Sukkot</i> is not mandatory for Gentiles generally. That notwithstanding,
I believe that blessing comes to Gentiles who keep the Annual Sabbaths,
analogous to the blessing for keeping the Seventh-day Sabbath that is
promised in <u>Isaiah 56:1-7</u>. Keeping the Annual Sabbaths also serves to
connect Gentile believers to their Jewish brethren, and enhances
understanding of the events in history that led to the Messiah's coming
and to the New Covenant.
