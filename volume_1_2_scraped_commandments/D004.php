<font size=4 face="Times New Roman, Times, serif">
<b><i>We are to rest, refrain from work, and assemble on God's annual
Sabbaths; these are (1) the first and (2) seventh day of Unleavened
Bread, (3) Shavuot, (4) Yom T'ruah (Rosh HaShanah), (5) Yom Kippur, (6) the
