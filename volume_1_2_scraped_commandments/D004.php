<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides and Meir loosely refer to the "first day of Passover" when they
mean the first day of Unleavened Bread. This is a common Jewish practice,
but is at odds with the biblical definition which describes Passover as the
period from before sundown on the 14<sup><small>th</small></sup> day of the
first month (<i>Nisan</i> 14), to sometime early the next day (<i>Nisan</i>
15), when the Israelites began their exodus from Egypt. HaChinuch
specifically refers to <i>Nisan</i> 15, and all three commentators recognize
the exception from not doing work when food preparation is needed. HaChinuch
considers the first and seventh days of "Passover" to be a single festival,
unlike the case of <i>Sh'mini Atzeret</i>.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
In their writings, Maimonides, Meir, and HaChinuch assume the common
rabbinical practice in the Diaspora of doubling up on each of the Annual
Sabbaths except for <i>Yom Kippur</i>. The reason for this duplication is
interesting, but will not be explored further here.
