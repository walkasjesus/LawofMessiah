<p align=left>
<font size=6>
<b>B3. </b>
</font>
<font size=5>
<b>Limitations on Our Eating and Otherwise Partaking of the Produce of Our
Work.</b>
</font>
<font size=3 face="Times New Roman, Times, serif">
&nbsp;&nbsp;
<a href="comment.php?d=B003&h=B3"
onClick="return show_hide_box(this,400,280,'1px solid')">[Make a Comment]</a>
</font>
</p>
<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><i>We may eat of crops with which we work, but not take any of it away,
nor take to excess. If we lack a basic human need and our work involves
providing it to others, we may partake of the product or service of our labor
within the limits of survival but not of luxury.</i></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This precept is derived from His Word (blessed is He):
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Key Scripture</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Deuteronomy 23:25(24)-26(25)</u>
(Maimonides RP201, RN267, RN268; Meir MP65, MN186, MN187;
Chinuch C576, C577, C578)
<br>
<i>When you enter your neighbor&#039;s vineyard, you may eat enough grapes to
satisfy your appetite; but you are not to put any in your basket. When you
enter your neighbor&#039;s field of growing grain, you may pluck ears with
your hand; but you are not to put a sickle to your neighbor&#039;s grain.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>1 Corinthians 9:7</u>
<br>
<i>Did you ever hear of a soldier paying his own expenses? Or of a farmer
planting a vineyard without eating its grapes? Who shepherds a flock without
drinking some of the milk?</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>1 John 3:17</u>
<br>
<i>If someone has worldly possessions and sees his brother in need, yet closes
his heart against him, how can he be loving God?</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This <i>Mitzvah</i> is a form of charity known in the Jewish world as
<i>tz'dakah</i>. It speaks to the worker directly, but implies a clear duty
on the part of the owner of crops to allow his workers to eat of them while
they are at work. One may argue that this also applies to those who work
among food items which are not crops. For example, a restaurant worker may
eat his fill during his or her work shift, but may not take any of it home.
Also, because the <i>Mitzvah</i> is clearly one of compassion for the poor,
in modern times it extends to industries that provide human necessaries other
than edibles such as medical care, medicine, clothing basics, and fuel. It
seems to me that if one hires an unshod shoe salesman, it is inhumane and a
violation of <u>1 John 3:17</u> to require that he sell shoes while not
equipping him with a pair of his own.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
<br>
In their respective positive <i>mitzvot</i>, both Meir and Maimonides
emphasize the land-owner's obligation to allow workers to eat, rather than
the workers' permission to eat. In contrast, Meir's and Maimonides'
<i>mitzvot</i> MN186 and RN267 respectively, state that workers are not to
eat while performing their work, but only at the end of their
work.<sup><small><a name=fr1 href=#fn1>1</a></small></sup>
Meir's <i>mitzvah</i> MN187 emphasizes that the worker is not to take food
away and give it to others, while Maimonides' <i>mitzvah</i> RN268 states
that a laborer is not to pick more crops than he needs for his immediate
meal.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
HaChinuch says that the root of <i>mitzvah</i> C576 is that Israelites are to
have good character and exhibit good will. He is in basic agreement with
Maimonides and Meir, and all three are very detailed regarding situations and
exceptions.
</font></p><hr width=150px style="margin-left: 10px">
<p align=left style="margin-left: 30px; margin-right: 30px">
