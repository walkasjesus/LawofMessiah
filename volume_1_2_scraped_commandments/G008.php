<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Despite the numerous Scriptures in both the <i>Tanakh</i> and the <i>Kitvey
B'rit Chadasha</i> that exemplify and exhort to worship, praise, and prayer,
there is no explicit commandment in the <i>Torah</i> that requires any of
them. The overall message of Scripture embracing them is unmistakable,
however, so the early Rabbis and codifiers of the Law decided to list (in
their <i>mitzvah</i> compilations) Scriptures that command giving service to
God, and to treat "service" as encompassing prayer. I chose to do something
similar in this <i>Mitzvah</i> by joining worship, praise, prayer, and
service, and letting "worship" be the umbrella that encompasses the other
three. Scripture (in particular <u>Zechariah 4:6</u>, <u>Romans 8:26-27</u>
and <u>Philippians 3:3</u>) is clear that true worship and its components can
only be entered into with the power and assistance of the Holy Spirit.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
If one understands worship as being a complete giving over of oneself to
another, it is easy to see how praise, prayer, and service can be components
of worship. It is not always the case, however; whether they are part of,
and contribute to worship, depends upon the degree of "giving over", and
their intensity. For example, merely complementing someone (praising him)
does not constitute worship, and neither does asking an ordinary favor of
someone (old English usage of praying), or serving someone as an employee.
That notwithstanding, requesting something of a person while continuously
praising him, showering him with unsolicited service, and pursuing him with
an attitude of submission and adulation, may very constitute worship.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
In common modern usage, the word "prayer" is generally understood to mean
petitioning God but, as I have indicated, in "old English" usage, it was a
request made to anyone. For example, in <u>Job 33:1</u> (rendered in the
King James Version of the Bible) we read of God saying::
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>Wherefore, Job, I pray thee, hear my speeches, and hearken to all my
words.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
God was certainly not "praying" to Job as we understand Job to have prayed to
God.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Serving another is similar, in that whether or not it constitutes worship
depends upon the degree to which the service is rendered and the attitude
that accomanies it. We read in <u>1 Peter 4:10</u>:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>As each one has received some spiritual gift, he should use it to serve
others, like good managers of God's many-sided grace.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This exhortation to serve others is clearly not an invitation to worship
them, whereas Yeshua's answer to Satan in <u>Luke 4:8</u>:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>The Tanakh says, 'Worship ADONAI your God and serve him only.'</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
illustrates how giving service to God does (and should) rise to the level of
worship. Serving another with abandon (as a willing slave) is what
constitutes worship, and it is only appropriate in our rendering service to
God.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
An additional word concerning prayer: It has been taught, in certain
circles, that <u>Mark 11:24</u> (and similar Scriptures) can be used as a way
of getting God to give us what we want, the theory being that, if we believe
for something enough, God will give it to us. It is called "faith" by those
who advance this view, but it is a mistake because true faith requires first
hearing from God, and then believing and praying for what God wants - not for
what we want - unless the two coincide. A Scripture that must be applied
when interpreting <u>Mark 11:24</u> is <u>Jude 1:20</u>:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>But you, dear friends, build yourselves up in your most holy faith, and
pray in union with the Ruach HaKodesh.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Praying in union with the Holy Spirit is the key (see also, <u>Hebrews
11:1</u>).
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Whereas Maimonides, Meir, and HaChinuch express themselves somewhat
differently, they essentially agree that prayer is linked to service. Because
they cannot find anything in the <i>Torah</i> explicitly commanding prayer,
they treat praying to God and rendering service to God as being essentially
the same and only quote Scriptures that command service.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The only reference to worship is found in the caption of Maimonides'
Commandment #RP5, as translated by Charles B. Chavel in his two volume work,
"The
Commandments."<sup><small><a name=fr1 href=#fn1>1</a></small></sup>
Meir captions his Commandment #MP7 as "Praying Every Day to God," and
HaChinuch captions his Commandment #C433 as "The Precept of Prayer to the
Almighty." Despite Chavel's caption, Maimonides does not discuss worshiping
God at all, but rather exhorts us only to serving Him and to prayer.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Of the three, Meir is the only one of the commentators that connects praising
God to prayer and service. He states that the commandment requires a
sequence in addressing God: (1) praising Him; (2) petitioning Him; and (3)
both praising and thanking Him. He also says that, in praying to God, it is
necessary to do so with "focused intention of the heart." This is known as
praying with <i>kavanah</i>.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
HaChinuch begins his <i>mitzvah</i> #C433 by referring to the Scriptures
commanding that we give service to God, and then quotes Maimonides:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
Now, R. Moses b. Maimon of blessed memory wrote: Although this
<i>mitzvah</i> is one of the all-inclusive precepts - in other words, those
which include the entire <i>Torah</i>, since the service of God includes all
the <i>mitzvot</i> - it likewise contains a specific detail: namely, that God
commanded us to pray to Him ...
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
HaChinuch then proceeds to explain our responsibility to pray three
times a day, corresponding to the times when sacrifices were conducted in the
Temple. He uses the term "prayer services", which links prayer with service,
and expresses that predominant Jewish understanding that "prayer services"
are meant to give "service" to God. As in the case of the other
commentators, HaChinuch does not refer to worship even once.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
All three of the commentators, Maimonides, Meir, and HaChinuch, rely on
<i>Talmud Bavli Ta'anith 2a</i> as their authority for linking prayer to the
Scriptures commanding service to God.
