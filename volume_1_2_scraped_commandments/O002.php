<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Amazing as it seems, disciples of Yeshua have been given spiritual authority
to permit or prohibit things to happen on earth that are reflected in heaven.
This is sometimes referred to as "binding" and "loosing", and is conditioned
upon our using the authority in a way that is consistent with God's will. The
authority is given to us as servants of Yeshua, and whereas it is our
decision when to use it, it must be understood that we do so "in the name
of Yeshua", and that it is Yeshua and not we that brings the result to
pass.<sup><small><a name=fr1 href=#fn1>1</a></small></sup>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
"Loosing" from disease and "binding" of healing is a spiritual authority we
have been given among others. According to the Scripture, we have authority
(in the name of Yeshua) to declare healing, to cast out demons, and even to
raise from the dead. In the western world we experience our authority to
cast out demons fairly often, to heal not so often, and to raise from the
dead almost never. We do, however, receive testimonies from believers of
these happening in other parts of the world where all three appear to be
substantially active. Why not also here in the western world is speculative.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
A special explanation is needed concerning our authority to forgive sins
pursuant to <u>John 20:23</u>, as we generally consider that in Yeshua's
exclusive domain. <u>John 20:23</u> states:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>If you forgive someone&#039;s sins, their sins are forgiven; if you hold
them, they are held.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Clearly, this is an extension and secondary application of our authority to
"bind" and "loose", but it is limited to sins that are committed against us
personally and is not authority to forgive sins generally. One way to
understand this is to first consider <u>Luke 17:3-4</u>, which commands us to
forgive sins that are committed against us if the perpetrator of the sins
repents (<u>Luke 17:3-4</u>). God gives that authority to the victims of sin
in order to put a burden on the perpetrator to approach the person he or she
injured and try to make it right. The <u>John 20:23</u> Scripture should be
looked at in the same way.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Finally, <i>batei din</i> have authority to "bind" and "loose". Reminiscent
of <u>Deuteronomy 17:8-13</u>, God gives authority to our <i>batei din</i> to
hear and decide cases, and He honors those <i>beit din</i> judgments that are
appropriately rendered:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Matthew 18:17-18:</u>&nbsp;&nbsp;<i>If he refuses to hear them, tell the
congregation [the beit din]; and if he refuses to listen even to the
congregation [beit din], treat him as you would a pagan or a tax-collector
[unbeliever]. Yes! I tell you people that whatever you prohibit on earth
will be prohibited in heaven, and whatever you permit on earth will be
permitted in heaven.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
So, therefore, if a <i>beit din</i> makes such a finding we are to treat the
adjudged person as no longer a believer.<sup><small><a name=fr2 href=#fn2>2</a></small></sup>
</font></p><hr width=150px style="margin-left: 10px">
<p align=left style="margin-left: 30px; margin-right: 30px">
