<u><b>Commentary</b></u>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
There are three kinds of fear that are recognized in the Scriptures. The
first kind (exemplified by "fear of God and parents") has to do with having
profound respect (reverence), and is appropriate - even commanded in those
and similar applications. The second kind (also appropriate) is the kind
that is built into us by God, in order to protect us from imminent danger.
Examples of this kind of fear are our ducking for cover upon being startled
by a loud discharge, and our holding back from touching a hot object after
having been previously burned by it. The third kind, the one that the
Scriptures command against, is the kind where we seek to protect ourselves in
circumstances where we ought instead to be relying on God. This kind is
exemplified by fear of man and of unwanted circumstances (e.g. death).
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
We sometimes think of fear as a feeling, and indeed there is a physiological
component to it. However, God does not command us regarding feelings, but
rather regarding the choices that we make (in faith) in spite of our
feelings. That is the teaching of <u>2 Corinthians 5:7</u>:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>... for we live by trust, not by what we see.</i> (rendered in the New
King James: <i>For we walk by faith, not by sight.</i>)
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The Greek word for "sight" in this Scripture is
<img src=images/word-eidos-1.gif height=22 align=top> (pronounced
"<i>aye-dos</i>"), and refers not only to eyesight, but to all of
our senses, including those that imbue us with feelings.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Fear, worry, and anxiety are similar in that they are the opposite of
trusting God for our welfare and protection. All three are debilitating and
open us to spiritual attack, and that is why there are so many Scriptures (in
both the <i>Tanakh</i> and the <i>Kitvey B'rit Chadasha</i>) that warn us
against giving in to fear, and exhort us to have faith instead.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u><b>Classical Commentators</b></u>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The <i>mitzvot</i> of Maimonides and HaChinuch address only three kinds of
fear - the reverent kind (not addressed in this <i>Mitzvah</i>), fear of
idolaters, and fear (by a judge) of a party to a lawsuit. Meir addresses
only reverent fear and a judge's fear.
