took a papyrus basket, coated it with clay and tar, put the child in it and
placed it among the reeds on the riverbank. His sister stood at a distance
to see what would happen to him. The daughter of Pharaoh [a Gentile] came
down to bathe in the river while her maids-in-attendance walked along the
riverside. Spotting the basket among the reeds, she sent her slave-girl to
get it. She opened it and looked inside, and there in front of her was a
crying baby boy! Moved with pity, she said, "This must be one of the
Hebrews&#039; children." At this point, his sister said to Pharaoh&#039;s
daughter, "Would you like me to go and find you one of the Hebrew women to
nurse the baby for you?" Pharaoh&#039;s daughter answered, "Yes, go." So
the girl went and called the baby&#039;s own mother. Pharaoh&#039;s daughter
told her, "Take this child away, and nurse it for me, and I will pay you for
doing it." So the woman took the child and nursed it. Then, when the child
had grown some, she brought him to Pharaoh&#039;s daughter; and she began to
raise him as her son. She called him Moshe [pull out], explaining, "Because
I pulled him out of the water.</i>
