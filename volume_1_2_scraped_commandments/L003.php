&amp; Gentiles</b>]. They were standing in front of the throne and in front
of the Lamb, dressed in white robes and holding palm branches in their
hands; and they shouted, "Victory to our God, who sits on the throne, and to
the Lamb!"</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This <i>Mitzvah</i> prohibits excluding Gentiles from Jewish communities. A
Jewish community that is the kind of which I am speaking has Jewish members,
governing and pastoral leaders, organized prayer that is culturally Jewish,
Bible and/or <i>Torah</i> studies, table fellowship, an adherence to biblical
<i>halachah</i> as it applies to Jews, and a shared Jewish style of life.
Even a casual perusal of the Scriptures presented in this <i>Mitzvah</i> will
demonstrate that, throughout Jewish history, there have always been Gentiles
that resided alongside, and participated with, their Jewish brothers within
Jewish communities. So close was the communal relationship between Jews and
<i>gerim</i> (committed Gentiles) during the Mosaic Covenant era, that God
provided for there to be an inheritance of land given to Gentiles that was
within the territory of a tribe with whom a Gentile was living (<u>Ezekiel
47:21-23</u>).
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The relationship gets even closer in the New Covenant where, according to
<u>Romans 11:16-18</u>, Gentile believers in Yeshua become connected to their
Messianic Jewish brothers and to the Jewish people globally by figuratively
being grafted into, and becoming branches on, a cultivated olive tree that is
owned by Israel (the Jewish people):
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>Now if the hallah offered as firstfruits is holy, so is the whole loaf.
And if the root is holy, so are the branches. But if some of the branches
were broken off, and you - a wild olive - were grafted in among them and have
become equal sharers in the rich root of the olive tree, then don't boast as
if you were better than the branches! However, if you do boast, remember that
you are not supporting the root, the root is supporting you.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
We know that the olive tree into which Gentile believers are grafted belongs
to Israel because we read in <u>Romans 11:24</u>:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>For if you were cut out of what is by nature a wild olive tree and
grafted, contrary to nature, into a cultivated olive tree, how much more will
these natural branches be grafted back into their [Israel's] own olive
tree!</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
What is more, we read in <u>Ephesians 2:11-14</u>:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>Therefore, remember your former state: you Gentiles by birth - called the
Uncircumcised by those who, merely because of an operation on their flesh,
are called the Circumcised - at that time had no Messiah. You were estranged
from the national life of Isra'el. [Commonwealth of Israel in the New King
James translation] You were foreigners to the covenants embodying God's
promise. You were in this world without hope and without God. But now, you
who were once far off have been brought near through the shedding of the
Messiah's blood. For he himself is our shalom - he has made us both one and
has broken down the m'chitzah [middle wall of separation] which divided us
...</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Another reason that Gentiles should not be excluded from Jewish communities
is the matter of table fellowship and hospitality of Jews to Gentiles that is
exemplified in the following Scriptures:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>John 4:7-10</u>
<br>
<i>A woman from Shomron [<b>a Gentile</b>] came to draw some water; and
Yeshua said to her, "Give me a drink of water." (His talmidim had gone into
town to buy food.) The woman from Shomron said to him, "How is it that you,
a Jew, ask for water from me, a woman of Shomron?" (For Jews don't associate
with people from Shomron.) Yeshua answered her, "If you knew God's gift,
that is, who it is saying to you, 'Give me a drink of water,' then you would
have asked him; and he would have given you living water."</i>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Acts 10:25-28</u>
<br>
<i>As Kefa entered the house, Cornelius [<b>a Gentile</b>] met him and fell
prostrate at his feet. But Kefa pulled him to his feet and said, "Stand up!
I myself am just a man." As he talked with him, Kefa went inside and found
many people gathered. He said to them, "You are well aware that for a man
who is a Jew to have close association with someone who belongs to another
people, or to come and visit him, is something that just isn't done. But God
has shown me not to call any person common or unclean"</i>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Hebrews 13:2</u> (NAS)
<br>
<i>Do not neglect to show hospitality to strangers [<b>presumably includes
Gentiles</b>], for by this some have entertained angels without knowing
it.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Still another reason that Gentiles should not be excluded from Jewish
communities is the application of <u>Matthew 7:12</u> (see also, <u>Luke
6:31</u>):
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>Always treat others as you would like them to treat you; that sums up the
teaching of the Torah and the Prophets.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
And even another (and perhaps the best) reason for Jews to not exclude
Gentiles from their communities is that it is God's plan for Jews and
Gentiles to worship side-by-side at the end of times. We see this in
<u>Revelation 7:9-10</u>:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>After this, I looked; and there before me was a huge crowd, too large for
anyone to count, from every nation, tribe, people and language [<b>Jews &
Gentiles</b>]. They were standing in front of the throne and in front of the
Lamb, dressed in white robes and holding palm branches in their hands; and
they shouted, "Victory to our God, who sits on the throne, and to the
Lamb!"</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Would we think it proper for a Gentile community to exclude Jews? There were
many such communities in the United States in the past, but thankfully there
no longer are - at least officially.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
While Gentiles should not be excluded from Jewish congregations and other
Jewish community entities, there are some situations where Jews caucusing
with Jews may be appropriate - a Jewish discussion group on Jewish topics for
example. I do believe it is important for Jewish disciples of Yeshua to
fellowship in communities where their Jewish identities, life patterns, and
covenant responsibilities are encouraged. For that reason, Gentiles that
join and commit themselves to a Messianic Jewish congregation should adopt
the congregation's Jewish ways of being, and not try to influence it to
become Christian in culture. That notwithstanding, some aspects of
<i>Torah</i> apply differently to Jews and Gentiles.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Perhaps this <i>Mitzvah</i> can best be summarized by <u>Psalms 133:1</u>:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>Oh, how good, how pleasant it is for brothers to live together in
harmony.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
In the New Covenant, Jewish and Gentile believers in Yeshua are brothers and
sisters in the Lord and should therefore live together in harmony wherever
they choose to worship.<sup><small><a name=fr1 href=#fn1>1</a></small></sup>
</font></p><hr width=150px style="margin-left: 10px">
