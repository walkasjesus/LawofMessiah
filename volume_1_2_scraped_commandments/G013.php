<b>NAZIRITE DEDICATION</b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Numbers 6:1-3</u>
(Maimonides RN202-204; Chinuch C368-370)
<br>
<i>ADONAI said to Moshe, "Tell the people of Isra&#039;el, &#039;When either a
man or a woman makes a special kind of vow, the vow of a nazir, consecrating
himself to ADONAI; he is to abstain from wine and other intoxicating liquor,
he is not to drink vinegar from either source, he is not to drink grape
juice, and he is not to eat grapes or raisins.&#039;"</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Numbers 6:4</u>
(Maimonides RN205-206; Chinuch C371-372)
<br>
<i>As long as he remains a nazir he is to eat nothing derived from the
grapevine, not even the grape-skins or the seeds.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Numbers 6:5</u>
(Maimonides RP92, RN209; Chinuch C373-374)
<br>
<i>Throughout the period of his vow as a nazir, he is not to shave his head.
Until the end of the time for which he has consecrated himself to ADONAI he
is to be holy: he is to let the hair on his head grow long.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Numbers 6:6-8</u>
(Maimonides RP93, RN207-208; Chinuch C375-376)
<br>
<i>Throughout the period for which he has consecrated himself to ADONAI, he is
not to approach a corpse. He is not to make himself unclean for his father,
mother, brother or sister when they die, since his consecration to God is on
his head. Throughout the time of his being a nazir he is holy for ADONAI.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Numbers 6:9-15</u>
(Maimonides RP93; Chinuch C377)
<br>
<i>If someone next to him dies very suddenly, so that he defiles his
consecrated head, then he is to shave his head on the day of his
purification; he is to shave it on the seventh day. On the eighth day he is
to bring two doves or two young pigeons to the cohen at the entrance to the
tent of meeting. The cohen is to prepare one as a sin offering and the
other as a burnt offering and thus make atonement for him, inasmuch as he
sinned because of the dead person. That same day he is to re-consecrate his
head; he is to consecrate to ADONAI the full period of his being a nazir by
bringing a male lamb in its first year as a guilt offering. The previous
days will not be counted, because his consecration became defiled. This is
the law for the nazir when his period of consecration is over: he is to be
brought to the entrance of the tent of meeting, where he will present his
offering to ADONAI - one male lamb in its first year without defect as a
burnt offering, one female lamb in its first year without defect as a sin
offering, one ram without defect as peace offerings, a basket of matzah,
loaves made of fine flour mixed with olive oil, unleavened wafers spread
with olive oil, their grain offering and their drink offerings.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Numbers 6:16-18</u>
(Chinuch C377)
<br>
<i>The cohen is to bring them before ADONAI, offer his sin offering, his burnt
offering, and his ram as a sacrifice of peace offerings to ADONAI, with the
basket of matzah. The cohen will also offer the grain offering and drink
offering that go with the peace offering. The nazir will shave his
consecrated head at the entrance to the tent of meeting, take the hair
removed from his consecrated head and put it on the fire under the sacrifice
of peace offerings.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Numbers 6:19-21</u>
<br>
<i>When the ram has been boiled, the cohen is to take its shoulder, one loaf of
matzah from the basket and one unleavened wafer, and place them in the hands
of the nazir, after he has shaved his consecrated head. The cohen is to
wave them as a wave offering before ADONAI; this is set aside for the cohen,
along with the breast for waving and the raised-up thigh. Following that,
the nazir may drink wine. This is the law for the nazir who makes a vow and
for his offering to ADONAI for his being a nazir - in addition to anything
more for which he has sufficient means. In keeping with whatever vow he
makes, he must do it according to the law for the nazir.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Numbers 8:5-7</u>
(Maimonides RP93; Chinuch C377<sup><small><a name=fr1 href=#fn1>1</a></small></sup>)
<br>
<i>ADONAI said to Moshe, "Take the L&#039;vi&#039;im from among the people of
Isra&#039;el and cleanse them. Here is how you are to cleanse them:
