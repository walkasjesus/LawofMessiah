<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
In the foregoing Scriptures, we encounter categories of creatures (and also
specifically named creatures), some of which are clean and may be eaten, and
others of which are unclean and may not. Eatability and cleanness generally
coincide, but they are separate attributes as <u>Leviticus 11:46-47</u>
explains:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>Such, then, is the law concerning animals, flying creatures, all living
creatures that move about in the water, and all creatures that swarm on the
ground. Its purpose is to distinguish between the unclean and the clean, and
between the creatures that may be eaten and those that may not be eaten.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The Mosaic commandments specifying which creatures may and may not be eaten
are generally complied with as an unchallenged part of Messianic Jewish life
and practice. Nevertheless, the question sometimes arises as to God's
rationale for these restrictions and whether today, in the absence of the
Holy Temple (ceremonial cleanness had to be maintained in the Temple), the
food restrictions still apply. Those who believe they still apply may offer
their personal opinions as to why, but <u>Leviticus 20:24b-26</u> gives us
God's reason:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>I am ADONAI your God, who has set you apart from other peoples. Therefore
you are to distinguish between clean and unclean animals and between clean
and unclean birds; do not make yourselves detestable with an animal, bird or
reptile that I have set apart for you to regard as unclean. Rather, you
people are to be holy for me; because I, ADONAI, am holy; and I have set you
apart from the other peoples, so that you can belong to me.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Scripture's reason for the food laws is clearly that Israelites (today called
Jews) are a holy people set apart, and are therefore commanded to a unique
lifestyle (including restrictions as to what we are allowed to eat) so that
we will be seen as set apart. This reason is not Temple-dependent, and is
therefore as valid today as it was at the time the food commandments were
given to Moses. Perhaps it is even more important today, because so many of
the world's Jews are integrated into the general society and have lost
visibility. It is my opinion, therefore, that it is God's will that today's
Jews (and the <i>K'rovei Yisrael</i> who dwell in community alongside us)
continue to obey the biblically commanded restrictions on what may and may
not be eaten, and that it is sin for us not to do so.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
There are those who would say that my position is not correct - that Jewish
believers in Yeshua who aspire to keep the food laws (in fact any laws) are
rejecting Messiah's sacrifice and God's grace, and are putting themselves in
an unholy bondage. This false theology is discussed in general terms in my
essay "<u>Elephants in the Room: What Paul Really Meant by His Comments on
the Law</u>" that is found in the preliminary section of this book.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
It is beyond the scope of this <i>Mitzvah</i> to discuss the various New
Covenant Scriptures that are typically used to come against obeying
God&#039;s food laws in the Torah, but I will mention the main ones for the
reader&#039;s convenience; they are <u>Mark 7:18-19</u>; <u>Acts 10:9-15</u>
and <u>11:1-9</u>; <u>1 Corinthians 8:8</u> and <u>10:25-27</u>;
<u>Colossians 2:16</u>; and <u>1 Timothy 4:1-5</u>.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
How then should today's Gentile believers respond to the dietary
commandments? Some say that they should not seek to keep them because doing
so will diminish Israel's visible uniqueness as a people set apart
(<u>Leviticus 20:24b-26</u>). Others say: "If God gave these dietary
restrictions to his 'chosen people' they must be good, so I will adopt them
as well." My opinion is that if Gentile believers voluntarily adhere to the
biblical food laws as a witness to their grafted-in connection to the Jewish
people, they are acting appropriately. On the other hand, if they profess
that the commandments are directed to all believers in Yeshua in order to
mark them as a holy people analogous to the Jews (<u>Ephesians 2:11-22</u>)
and that they are in sin if they do not keep them, then they would be wrong
(in my opinion), and would indeed be obscuring Israel's intended uniqueness.
It is therefore my opinion that, in general, obedience to the food laws
(other than those mentioned in <u>Acts 15:19-20</u> and <u>Acts 21:25</u>) by
Gentiles is optional.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
In closing, I should like to say a few words about the term "<i>kosher</i>"
as it applies to the dietary laws - a term that I have thus far intentionally
not used in this discourse. The word means "allowed", and it is correctly
used in describing whether the Bible permits a creature to be eaten by an
Israelite (e.g., "A goat is a <i>kosher</i> animal, whereas a pig is not.").
The reason I have avoided its use in this <i>Mitzvah</i>, is that the word
"<i>kosher</i>" has another connotation as well, and that is in stating
whether the flesh of a particular creature (or a particular substance) is
allowed to be eaten according to rabbinical standards. So, for example, the
meat of a goat (which is a <i>kosher</i> animal) is not <i>kosher</i>,
according to rabbinical standards, if the goat was not slaughtered
appropriately, or that the slaughter was not conducted under rabbinical
supervision. One ought to therefore be alert to how the word "<i>kosher</i>"
is used in context, so as to elicit its intended meaning.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary by Daniel C. Juster</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
For those not satisfied that the only rationale for this <i>Mitzvah</i> is
that it is a standard created by God to make a distinction between Israel and
the nations, they may consider these others:
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
First is that it is for health. This is argued in "<i>None of These
Diseases</i>" by S. I. Macmillan. The problem with his position is that not
all the creatures prohibited in Scripture are clearly unhealthy to eat.
Certainly, God's best for Israel is that it be healthy. Second, is the idea
that the clean-unclean laws (beyond the food laws) show the nature of sin in
a symbolic way in that certain animals feed off death or, like pigs, are
unclean because they are filthy (Keil and Delitzsch, "<i>Commentary on the
Old Testament</i>", first published in 1866). Third, is "<i>Purity and
Danger</i>" by Mary Douglas, in which she argues that there was a reasoned
sense of the ancients as to what was fitting or not fitting - what was whole
or not whole - in categories of living creatures. It was seen as natural that
fish should have fins and scales. In her last years before her death, she
was no longer convinced of her position, and came to believe, instead, that
there simply were animals that God valued as being free from men's use.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
While there is truth in all of these views, it would seem best that we
embrace the idea that God&#039;s reason for making distinctions is part of
his making the Jewish people distinct, and that not all of the creatures
named in Scripture are subject to a human rationale.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides, Meir, and HaChinuch are unified in their <i>mitzvot</i> that
prohibit the eating of certain creatures but, whereas Maimonides and
HaChinuch have written positive <i>mitzvot</i> requiring that we examine
creatures for signs of their cleanness and consequent eatability, Meir has no
such corresponding <i>mitzvah</i>.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
In their mitzvot #RP152 and C155 respectively, Maimonides and HaChinuch
combine <u>Leviticus 11:9-12</u> and <u>Leviticus 20:25</u> to defend their
positive <i>mitzvot</i> that we are to examine creatures of the sea for
tokens (fins and scales) of their acceptability for food. <u>Leviticus
11:9-12</u> describes the sea creatures that we may and may not eat, but
<u>Leviticus 20:25</u> makes no reference to fish whatever.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
