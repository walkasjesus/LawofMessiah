<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides comments very little. HaChinuch states that the underlying
meaning of the <i>mitzvah</i> to not work on the Sabbath is to refrain from
the work of our normal occupations. In our rest, we are to remember each day
of the creation of the world as well as the miracle of Israel's exodus from
Egypt. HaChinuch and Meir state that the sages list 39 activities that are
prohibited on the Sabbath (<u>Shab. 73a</u>); these are intended as a fence
around the <i>Torah</i>. Of the three commentators, Meir is the only one who
states that it is forbidden for a Jew to ask a Gentile to do work in his
behalf on the Sabbath, and he also states that doing work on the Sabbath is
permitted if it is to save a human life. None of the traditional
<i>mitzvot</i> addresses the requirement of <u>Leviticus 23:3</u> that the
Sabbath should be a day of "holy convocation".
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Special mention is made of Maimonides' RN322 and HaChinuch's C114, in which
they profess that we must not inflict punishment on Shabbat. The Scripture
upon which they rely is <u>Exodus 35:3</u>, which states:
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<i>You are not to kindle a fire in any of your homes on Shabbat.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The logic of relying on this seemingly inapplicable Scripture is that one of
the ways of punishing according to the Mosaic Law is burning.
