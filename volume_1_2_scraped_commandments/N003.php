<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This <i>Mitzvah</i> is a natural consequence of the <i>Mitzvah</i> "Loving
Our Neighbor, the Stranger, and Even Our Enemy." Scripture requires that we
help our neighbor who is in need whether or not we are asked, and whether or
not he is a friend. Scripture also commands that help be given even at a
cost to ourselves, and therein lies the greatest challenge, which is, "How
far must we go to help, and at how great a cost?" Do we give to our neighbor
financially to the extent of impoverishing ourselves and our family? Do we
rise from our battlefield trench to help our fallen enemy only to get shot
ourselves? Throughout life, we encounter situations of these kinds, usually
not as extreme, but similar in that helping our neighbor risks something, or
takes something away from us with certainty. It may be money, it may be
property, it may be time, and it may occasionally be life. There is no
biblical formula for when to help, for how much to help, or for whom to help
in a given circumstance. We must be tuned into and rely on the Holy Spirit
to guide us.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
There are some differences among Maimonides, Meir, and HaChinuch regarding
one's obligation to unload and reload an animal that belongs to a Jew and
that has fallen as a result of its burden. All agree that both unloading and
reloading are obligations but in regard to unloading, Meir states that an
elder is exempt because it is beneath the dignity of his status as an elder.
HaChinuch agrees with the elder exemption in general, but states that the
elder is not exempt if he would have unloaded his own animal under similar
circumstances. HaChinuch also states (but Meir does not) that if one
encounters a friend and a non-friend with a fallen animal at precisely the
same time, one must first assist the non-friend in unloading in order to
avoid impure intent. Strangely, in regard to the loading of a fallen animal,
Meir and HaChinuch state these obligations somewhat in reverse. Meir states
that an elder is exempt from loading and reloading unless he would have done
similarly for himself, and he states that one must help a non-friend before a
friend. HaChinuch, on the other hand, makes no mention of friends,
non-friends, and elders being exempt in the case of loading, whereas
Maimonides does not allude to these secondary matters at all.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Of significance is that, in the New Covenant Scriptures, <u>Matthew 23:11</u>
and <u>20:26-27</u>, Yeshua speaks against Meir's and HaChinuch's
interpretation that the dignity of an elder causes him to be exempt from
helping his neighbor with the neighbor's fallen animal. In fact, Yeshua
teaches the opposite when He says:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
"<i>The greatest among you must be your servant</i>"
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
and
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
"<i>Whoever among you wants to be a leader must become your servant, and
whoever wants to be first must be your slave!</i>"
