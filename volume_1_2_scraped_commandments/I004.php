<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The Scriptures are clear that we are not to entice anyone into idolatry.
Although they refer to physical idols and false gods, <u>Matthew 6:24</u> and
<u>Colossians 3:5</u> consider anything to be a "god" or an idol that
competes with the one true God by becoming man's master or that we put ahead
of the one true God in any other way. Such idols can be money, sexual
immorality, impurity, lust, evil desires, property, power, security, greed,
and others. Consequently, anyone who entices others to pursue such things is
guilty of violating the Scriptures that underlie this <i>Mitzvah</i>.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides, Meir, and HaChinuch published similar <i>mitzvot</i> on the
subject of this <i>Mitzvah</i>. Maimonides' RN15, Meir's MN14, and
HaChinuch's C87 declare that we must not call any person to idolatry, and
Maimonides' RN16, Meir's MN23, and HaChinuch's C462 state that we must not
persuade an Israelite to engage in idolatry. Maimonides supports the
Scripture's prescribed death penalty for leading an Israelite into idolatry,
and states that the victim - the one who was persuaded - should be the one to
put the enticer to death. Capital punishment by the governing authorities is
not authorized in the New Covenant but, where there is unrepentance,
excommunication and expulsion from the community of believers is authorized
(see <u>Proverbs 22:10</u>, <u>Matthew 18:15-17</u>, <u>Romans 16:17</u>,
<u>1 Corinthians 5:11</u>, <u>2 Thessalonians 3:6</u>, and <u>Titus
3:10-11</u>).
