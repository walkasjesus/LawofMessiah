<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><i>On the evening of Passover, we are to ceremonially tell our children
that we eat matzah and remove leaven and chametz from our domain as a
reminder of what God did for us when we left Egypt.</i></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This precept is derived from His Word (blessed be He):
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Key Scriptures</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Exodus 12:15-20</u>
(Maimonides RP156, RP158, RN198; Meir MP22, MP23, MN5;
Chinuch C9, C10, C12)
<br>
<i>For seven days you are to eat matzah - on the first day remove the leaven
from your houses. For whoever eats hametz [leavened bread] from the first to
the seventh day is to be cut off from Isra&#039;el. On the first and
seventh days, you are to have an assembly set aside for God. On these days
no work is to be done, except what each must do to prepare his food; you may
do only that. You are to observe the festival of matzah, for on this very
day I brought your divisions out of the land of Egypt. Therefore, you are to
observe this day from generation to generation by a perpetual regulation.
From the evening of the fourteenth day of the first month until the evening
of the twenty-first day, you are to eat matzah. During those seven days, no
leaven is to be found in your houses. Whoever eats food with hametz in it is
to be cut off from the community of Isra&#039;el - it doesn&#039;t matter
whether he is a foreigner or a citizen of the land. Eat nothing with hametz
in it. Wherever you live, eat matzah.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Exodus 12:25-27</u>
<br>
<i>When you come to the land which ADONAI will give you, as he has promised,
you are to observe this ceremony. When your children ask you, &#039;What do
you mean by this ceremony?&#039; say, &#039;It is the sacrifice of
ADONAI&#039;s Pesach [Passover], because [[ADONAI]] passed over the houses
of the people of Isra&#039;el in Egypt, when he killed the Egyptians but
spared our houses.&#039;" The people of Isra&#039;el bowed their heads and
worshipped.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Exodus 12:41-49</u>
(Maimonides RN126-128; Chinuch C13-14, C17)
<br>
<i>At the end of 430 years to the day, all the divisions of ADONAI left the
land of Egypt. This was a night when ADONAI kept vigil to bring them out of
the land of Egypt, and this same night continues to be a night when ADONAI
keeps vigil for all the people of Isra&#039;el through all their
generations. ADONAI said to Moshe and Aharon, "This is the regulation for
the Pesach lamb: no foreigner is to eat it. But if anyone has a slave he
bought for money, when you have circumcised him, he may eat it. Neither a
traveler nor a hired servant may eat it. It is to be eaten in one house.
You are not to take any of the meat outside the house, and you are not to
break any of its bones. The whole community of Isra&#039;el is to keep it.
If a foreigner staying with you wants to observe ADONAI&#039;s Pesach, all
his males must be circumcised. Then he may take part and observe it; he will
be like a citizen of the land. But no uncircumcised person is to eat it.
The same teaching is to apply equally to the citizen and to the foreigner
living among you.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Exodus 13:3-8</u>
(Maimonides RP157, RN197, RN200; Meir MP24; Chinuch C21)
<br>
<i>Moshe said to the people, "Remember this day, on which you left Egypt, the
abode of slavery; because ADONAI, by the strength of his hand, has brought
you out of this place. Do not eat hametz. You are leaving today, in the
month of Aviv. When ADONAI brings you into the land of the Kena&#039;ani,
Hitti, Emori, Hivi and Y&#039;vusi, which he swore to your ancestors to give
you, a land flowing with milk and honey, you are to observe this ceremony in
this month. For seven days you are to eat matzah, and the seventh day is to
be a festival for ADONAI. Matzah is to be eaten throughout the seven days;
neither hametz nor leavening agents are to be seen with you throughout your
territory. On that day you are to tell your son, &#039;It is because of
what ADONAI did for me when I left Egypt.&#039;</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Matthew 2:13-15</u>
<br>
<i>After they had gone, an angel of ADONAI appeared to Yosef in a dream and
said, "Get up, take the child and his mother, and escape to Egypt, and stay
there until I tell you to leave. For Herod is going to look for the child in
order to kill him." So he got up, took the child and his mother, and left
during the night for Egypt, where he stayed until Herod died. This happened
in order to fulfill what ADONAI had said through the prophet, "Out of Egypt
I called my son."</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Hebrews 8:9</u>
<br>
<i>&#039;It will not be like the covenant which I made with their fathers on
the day when I took them by their hand and led them forth out of the land of
Egypt; because they, for their part, did not remain faithful to my covenant;
so I, for my part, stopped concerning myself with them,&#039; says ADONAI.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This ceremony (that we now call a <i>seder</i>) was commanded for us
(Israelites) to do after we emerged from Egypt and entered the Land that the
Lord had promised our ancestors. Although Israel's exodus from Egypt
occurred many centuries ago, we retell its events in the first person as
though "we" (not "they") left Egypt. I believe it is also applicable to
Gentiles who permanently live within the Jewish community (<i>K'rovei
Yisrael</i>) because those whom God brought out of Egypt were a mixed
multitude that consisted, not of Jews only, but also of Gentiles (<u>Exodus
12:38</u>).
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
At our modern Passover <i>seder</i>, it is customary for a child to ask four
questions, and for us to answer the questions by telling him (or her) the
story of when we left Egypt. Through this retelling, we are reminded of
God's miracles in our behalf, that He brought us out of Egypt "by the
strength of His hand", and that we left in such haste that we brought no
leaven with us so the bread we had to eat was un-risen. Although our living
conditions in the desert were strenuous, God provided for us with
<i>manna</i><sup><small><a name=fr1 href=#fn1>1</a></small></sup>,
quail, water, and shoes that did not wear out. His greatest provision for
us, however, was His divine presence, for He accompanied us and guided us
through the desert in a column of cloud by day and a column of fire by night.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
While this <i>Mitzvah</i> to recount Israel's exodus from Egypt is clearly
directed to Jews and <i>K'rov Yisrael</i> Gentiles, the remembrance of what
God did for Israel should be a joy and encouragement for all believers. All
Gentile parents who are believers may therefore want to tell the story of the
Exodus, and explain about <i>matzah</i>, leaven, and <i>chametz</i> to their
children as well.
</font></p><hr width=150px style="margin-left: 10px">
