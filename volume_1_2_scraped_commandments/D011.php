<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides, Meir, and HaChinuch all state that we are to tell about the
exodus from Egypt. However, Maimonides and HaChinuch say that we are to tell
it on the evening of the 15<sup><small>th</small></sup> of <i>Nisan</i>, and
Meir says it should be told on the night before the
15<sup><small>th</small></sup> of <i>Nisan</i>. These are probably not
disagreements, but rather different ways of referring to the Passover
<i>seder</i> event.
