<b>D11. </b>
</font>
<font size=5>
<b>Ceremonially Recounting What God Did for Us When We Left Egypt.</b>
</font>
<font size=3 face="Times New Roman, Times, serif">
&nbsp;&nbsp;
<a href="comment.php?d=D011&h=D11"
onClick="return show_hide_box(this,400,280,'1px solid')">[Make a Comment]</a>
</font>
