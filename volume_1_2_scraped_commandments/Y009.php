<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
It is held by some that the New Covenant is a covenant of grace (i.e. not of
law), unlike the Mosaic Covenant which is a covenant of commandments (law).
The Scriptures that underlie this <i>Mitzvah</i> show that Yeshua issued
commandments as binding as those that were given through Moses - proof that
the New Covenant is also a covenant of law. In my opinion, God's grace
abounds in both covenants.
