<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The Sabbatical Year is known as the "Year of Release" (<i>Sh'mittah</i>)
because loans made to Israelites, by Israelites, are cancelled every seventh
year. Also, Israelites are prohibited from denying loans to needy brothers
when the Sabbatical Year approaches, despite the possibility that the loan
will be discharged and not repaid voluntarily. The release of such loans
does not (according to <u>Deuteronomy 15:3</u>) apply to
<img src=images/word-hanachriy.gif align=top height=24> - foreigners. I
believe that one of the reasons God gave Israel the Sabbatical Year was to
test well-to-do Israelites' faith and reliance on Him.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
A question that naturally comes to mind is how releasing loans in the
Sabbatical Year applies today in the New Covenant. I am of the opinion that
it continues to apply where the circumstances are similar to those of ancient
Israel, and that it applies not only to Jews but also to <i>K'rov Yisrael</i>
Gentiles; <u>how</u> similar the circumstances need to be will be revealed by
the <i>Ruach HaKodesh</i> as we pray for guidance. As for other non-Jewish
followers of Yeshua, I am inclined to the position that as New Covenant
brothers, literal compliance is a blessing for them but not a requirement; I
partially draw this conclusion from <i>Isaiah 56:1-7</i>, which promises
blessing to the foreigner who keeps Gods Sabbaths, but does not require it of
them (although admittedly most Sabbaths do not involve the release of debts).
One thing that is certain, however, is that God continues to expect all of us
to look to Him for provision rather than to think that we can cause provision
to come to us by our own efforts.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This <i>Mitzvah</i> has some interesting ramifications in modern times. One
of these is that the enforced collection of debts today is generally
accomplished through civil courts - not by <i>batei din</i>. Therefore, as a
practical matter, there is no way to enforce the <i>Mitzvah</i> against
suing a brother in a secular court if a creditor decides to violate it and
sue on the debt. Also, because this <i>Mitzvah</i> is so closely connected
to Jewish life in <i>Eretz Yisrael</i>, it is arguable as to whether it
applies among brothers (brother Jews and brothers in Yeshua) in the Diaspora.
The weight of rabbinical opinion is that it is valid for Jews everywhere, and
it is my opinion that it also applies to Jewish and <i>K'rov Yisrael</i>
members of Messianic Jewish communities in the Diaspora if the loans are
given with the understanding that observing the <i>Sh'mittah</i> is part of
the community's <i>halachah</i>.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
It should not be surprising that this <i>Mitzvah</i> is unpopular among
creditors and that it has encountered much resistance. During the latter
years of the Second Temple, the sage Hillel became concerned by the growing
number of Jews whose weak faith was such that they were declining to loan
money to poor Jewish brothers when the Sabbatical Year was near. To urge
them to do so, he invented a legal procedure that, in effect, circumvented
God's intent in giving the <i>Mitzvah</i>. The procedure, still used today,
is known as a <i>Pruzbul</i>, and works this way: Some time before
<i>Sh'mittah</i>, a lender gives ownership of his uncollected debt to a
<i>bet din</i> which, in turn, appoints the lender to collect the debt for
the <i>bet din</i>. The debt is not discharged by <i>Sh'mittah</i> because
the <i>bet din</i> is a corporate entity, not a Jewish brother, so
<u>Deuteronomy 15:2</u> does not apply. When the lender collects the debt
and attempts to give the proceeds to the <i>bet din</i>, he is told that the
<i>bet din</i> does not want the money and he is instructed to keep it.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
I do not agree with using such casuistic ploys to collect debts or, for that
matter, to avoid complying with God's commandments in any other circumstance;
I believe it is this kind of thinking that Yeshua was referring to when He
said to the Pharisees:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>Thus, with your tradition which you had handed down to you, you nullify
the Word of God! And you do other things like this.</i> (<u>Mark 7:13</u>)
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Nevertheless, I do not believe there is anything wrong with a lender
accepting return of his money after <i>Sh'mittah</i> if it is offered
voluntarily.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The application of this commandment in modern credit societies requires some
adjustment that is legitimate. First of all, commercial loans which are used
to increase a person's wealth, purchase mortgages, make investments, etc.,
are not the intended application of this <i>Mitzvah</i>, although some of
these loans are discharged as well. This <i>Mitzvah</i> is meant to apply to
loans given by individuals, to individuals, because the borrower (who is a
Jewish brother to the lender) is in some kind of personal need. Need-based
loans must be given with generosity, and with willingness that the loan will
be cancelled on the Sabbatical Year. Modern societies often have something
like this in built into their civil laws that is called bankruptcy, where a
person who becomes financially insolvent can receive a judicial discharge of
his debts; in the United States, this is possible every six years.
Bankruptcy laws are the modern alternative to indentured servanthood and
Debtor's Prison, and they reflect something of the same spirit as this
<i>mitzvah</i> and the Year of Jubilee.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides states that a purpose of the <i>Sh'mittah</i> commandment is to
teach kindness to the poor, and he also puts forth the case that it trains us
against coveting what our neighbor owns. The logic is that, by not being
allowed to recover money he has lent, how much more must the lender keep from
coveting what belongs to the borrower. HaChinuch agrees with this <i>kal
va-chomer</i> logic.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
HaChinuch connects complying with <u>Deuteronomy 15:9</u> (the requirement to
lend even with <i>Sh'mittah</i> approaching) with trusting God by citing
<u>Proverbs 11:24-25</u> as God's promise of blessing to those who are
generous. The other commentators make no such connection and, in fact,
Maimonides exhibits severity toward foreigners by interpreting the permissive
language in <u>Deuteronomy 15:3</u> ("<i>you may demand that a foreigner pay
his debt</i>") into his <i>Mitzvah</i> RP142 which states that we are
required to collect debts from heathen with the same zeal as we forgive the
debts of Israelites. Meir does not deal with the issue at all, but
HaChinuch, in his <i>Mitzvah</i> C476, agrees with Maimonides, and says that
it is so that an idolater will not profit from an Israelite's generosity and
possibly entice the sympathetic Israelite to follow after idolatrous
practices.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Meir is of the opinion that if a borrower offers return of a loan voluntarily
after <i>Sh'mittah</i>, the lender should not accept it. However, if it is
offered a second time, then he may.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
