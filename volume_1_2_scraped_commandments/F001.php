<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Honoring our father and mother involves respecting them for their parental
position both privately and in public. It also implies obeying them when we
are young, but never in a way that disobeys or dishonors God. Although,
by promising long life in the land, it is clear that <u>Exodus 20:12</u> and
<u>Ephesians 6:1-3</u> are addressing Israelites, the instructions they
convey are morally universal and are therefore appplicable for Gentiles as
well.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The King James Version of the Bible translates "<i>tira'u</i>" (derived
from "<i>yara</i>") in <u>Leviticus 19:3</u> as "<i>Ye shall fear</i>",
whereas other versions translate the word "revere" or "respect". We are not
to be frightened by our parents (that is not the kind of "fear" of them we
are to have). We are rather to regard them with awesome respect, for God
holds them accountable to train us up in righteousness (<u>Proverbs
22:6</u>), and He holds us accountable for how we treat them - how we speak
of them, and how we speak to them. We are not to usurp their authority,
speak for them (unless asked), contradict them before others in a way
that would cause them embarrassment or dishonor and, of course, not curse
them or strike them. Our obligation to honor and revere God (<u>Proverbs
3:9</u>; <u>Leviticus 24:15</u>; <u>Deuternomy
6:13</u>)<sup><small><a name=fr4 href=#fn4>4</a></small></sup>,
and it is incumbent upon us, even should they do wrong to us or follow
ungodly paths in aspects of their personal lives.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The relevance of <u>1 Timothy 5:8</u> is that providing materially for
one's parents, especially when they are at an advanced age, is one way of
honoring and revering them, and not supporting them when there is a need to
do so is the opposite. It is commonly thought that <u>1 Timothy 5:8</u> is
only a reference to husbands supporting their wives and children, but the
context in the ancient world (as well as in many places in the world today)
included one's parents, because aging parents commonly lived with (or close
by) their children. This principle is verified in Meir's commentary to his
<i>mitzvah</i> MP41.
</font></p><hr width=150px style="margin-left: 10px">
<p align=left style="margin-left: 30px; margin-right: 30px">
<font size=2>
<a name=fn4 href=#fr4>4.</a>
Similar to the King James Version&#039;s translation
of "tira&#039;u" in <u>Leviticus 19:3</u>, most translators of
<u>Deuteronomy 6:13</u> translate "tira" (also derived from "yara") as
"fear".
</font>
</p>
<hr width=150px align=left style="margin-left: 10px">
<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides, Meir, and HaChinuch have not combined honoring and revering
parents into a single <i>mitzvah</i>. Instead, they wrote separate
<i>mitzvot</i> for each, the combined essence of which agrees entirely with
this <i>Mitzvah</i>.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
In regard to honoring our parents, all three make reference to places in the
<i>Talmud</i>, most prominently, tractate <i>Kiddushin</i>, and also to the
<i>Sifra</i> (<u>Leviticus
19:3</u>)<sup><small><a name=fr5 href=#fn5>5</a></small></sup>.
Maimonides adds that we are required to care for our parents when they become
old; Meir agrees and goes into some detail of our obligation to financially
support our parents when necessary. He further says that we are to attend
our father as a servant would attend his master, but we are not to sin in
following our parents' instructions.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
In regard to revering our parents, all three commentators link reverence to
fear, and instruct against standing or sitting in one's father's place. Meir says that we are not to contradict our father's words or refer to him by his
name. HaChinuch is detailed in other ways, and raises the question of how
far reverence of parents should go. He indicates that we must be able to
suffer indignities and wrongs done to us without putting our parents to
shame, and we must be respectful of them even if their minds become deranged
in connection with their age. Of course, we are not to curse them or strike
them.
</font></p><hr width=150px style="margin-left: 10px">
