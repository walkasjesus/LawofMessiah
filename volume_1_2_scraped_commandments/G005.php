</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
We are commanded by <u>Deuteronomy 6:16</u> not to put God to the test. What
this means is that we must believe God in that whatever He says He will do,
and not intentionally act in such a way as to force His hand and provoke Him
to prove it. The bottom line is that we must trust and not doubt God's
warnings and promises. In <u>Matthew 4:5-7</u>, we read how Yeshua Himself
demonstrated the principle by refusing to jump from the Temple in order to
prove His identity by forcing His Father to save Him supernaturally.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
There are two instances in Scripture where God seems to be saying the
opposite. One of them is in <u>Judges 6:36-40</u>, where Gid'on asks God for
supernatural signs of His Word. But Gid'on was not testing God; he believed
God. On the contrary, he was doubting the accuracy with which he was hearing
God, and was testing himself. The other instance is in <u>Malachi 3:10</u>,
in which God says, in part: "<i>Bring the whole tenth into the storehouse, so
that there will be food in my house, and put me to the test ...</i>" This is
not a contradiction of the <u>Deuteronomy 6:16</u> commandment either,
because here God is not asking doubters or unbelievers to put Him to test Him
in order to prove His truthfulness. He is lovingly beseeching Israelite
believers to follow His instructions so that they can have and enjoy His
promised blessings.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides does not command against testing the promises and warnings
contained in Scripture, but rather against those voiced by God's prophets.
HaChinuch interprets <u>Deuteronomy 6:16</u> somewhat differently by saying
that we should not test true prophets excessively. Meir does not have a
<i>mitzvah</i> on this subject at all.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
