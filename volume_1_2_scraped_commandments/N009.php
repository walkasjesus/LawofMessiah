<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
"<i>Do unto others as you would have them do unto you</i>" is the well-known
"golden rule", but it does not appear in the Bible in that form. What is in
the Bible is <u>Mark 12:30-31</u>, which states:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>'... and you are to love ADONAI your God with all your heart, with all your
soul, with all your understanding and with all your strength.' The second is
this: 'You are to love your neighbor as yourself.' There is no other mitzvah
greater than these.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Applying that principle, Scripture teaches that we have a duty to help
others, even to try to save the life of a person who is attacked. What is
unclear is whether the duty applies even if it puts the protector in danger
and, because Scripture is silent on the matter, I will not speculate.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The parable of the "good Samaritan" is the classic Scripture that lauds
assisting a person who has been attacked, but it is noteworthy that at no
time was the "good Samaritan" in danger since he gave his help after the
attack on the injured man had occurred. Nevertheless, he helped the stranger
in need by spending his own time and money.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Interestingly, some U.S. States have adopted what they call the "Good
Samaritan Law" which does not require that a bystander help a person who is
in distress, but states that if he does and causes injury in the process, he
cannot be sued for his attempt.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The <i>mitzvah</i> that Maimonides and HaChinuch glean from Scripture is that
we must seek to save the life of one who is attacked even if it requires
killing the attacker. In reaching this conclusion they rely on
<u>Deuteronomy 25:11-12</u> in a very strange way, which is to construe that
the man who is attacking the other is not trying to kill him, but that the
wife who comes to her husband's aid (by grabbing the attacker's private
parts) is trying to kill the attacker, and thereby becomes a lethal attacker
herself. That is why Scripture commands that we must show no pity on her and
cut off her hand. Meir did not address the subject.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
