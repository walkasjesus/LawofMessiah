<b><i>We are to assemble to read and to hear the Torah read during the
Festival of Sukkot in the Sabbatical Year.</i></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This precept is derived from His Word (blessed be He):
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Key Scripture</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Deuteronomy 31:10-13</u>
(Maimonides RP16, Chinuch C612)
<br>
<i>Moshe gave them these orders: "At the end of every seven years, during the
festival of Sukkot in the year of sh&#039;mittah, when all Isra&#039;el have
come to appear in the presence of ADONAI at the place he will choose, you
are to read this Torah before all Isra&#039;el, so that they can hear it.
Assemble the people - the men, the women, the little ones and the foreigners
you have in your towns - so that they can hear, learn, fear ADONAI your God
and take care to obey all the words of this Torah; and so that their
children, who have not known, can hear and learn to fear ADONAI your God,
for as long as you live in the land you are crossing the Yarden to
possess."</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>John 7:1-3</u>
<br>
<i>After this, Yeshua traveled around in the Galil, intentionally avoiding
Y&#039;hudah because the Judeans were out to kill him. But the festival of
Sukkot in Y&#039;hudah was near; so his brothers said to him, "Leave here
and go into Y&#039;hudah, so that your talmidim can see the miracles you
do</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
We assume that Moses gave this order in compliance with God's directive, so
it should be considered <i>Torah</i>. The commandment involves assembling,
reading aloud (teaching), hearing, and learning. All of these are good and
necessary to do at times other than <i>Sukkot</i> and other than in the
Sabbatical Year, but the commandment specifies this particular time, so it
should be complied with. Since the designated place of assembly was where the
Ark of God was, it is especially important to conduct the reading in
<i>Yerushalayim</i>, at the place where the Temple last stood. Jews
everywhere should be encouraged to journey there to hear, read, and otherwise
participate but, for those who cannot, similar assemblies and readings can be
conducted in synagogues elsewhere and at distant parts of the world.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Complying with this <i>Mitzvah</i> today is perhaps more symbolic and
ceremonial than actually needed to learn the commandments of God because,
unlike in the time of Moses, synagogues the world over read through the
entire <i>Torah</i> once a year every year, and printed Bibles are generally
available to all. That notwithstanding, it is a general principle that
blessing follows from literally obeying <i>Torah</i> with a heart to please
God and wherever possible. Because this <i>Mitzvah</i> is so closely related
to Jewish identity and calling, I consider it to be optional and unnecessary
for non-<i>k'rov Yisrael</i> Gentiles.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Meir does not include this <i>Mitzvah</i> in his compilation, but it is
treated by Maimonides and HaChinuch as a requirement for all Jews - including
women, even though the commandment appears to be
time-dependant.<sup><small><a name=fr1 href=#fn1>1</a></small></sup>
HaChinuch states that the reason this assembly is so important is that
possessing the <i>Torah</i> is what distinguishes Israel from the other
nations.
