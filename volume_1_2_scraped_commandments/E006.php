<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
What is common to <i>tzitzit</i>, <i>tefillin</i>, & <i>mezuzot</i>, is that
all three are visible reminders of the Word of God, and teaching and
remembering God's Word through their use appears to be a Jewish covenant
responsibility. The question that must be answered, however, is whether
<i>tefillin</i> (phylacteries) and <i>tzitzit</i> (fringes) must be
physically worn (and in what circumstances), and <i>mezuzot</i> physically
affixed to our doorposts and gates, or whether the Scriptural commandments
regarding them are intended to be metaphoric. Orthodox Judaism has concluded
that they are all meant to be physically applied, while the Conservative and
Reform Jewish community has concluded that the requirement of <i>mezuzot</i>
is physical, and the others are symbolic or metaphoric. Practical as this
latter interpretation seems to be, it is not likely correct because the
commandments regarding <i>tefillin</i> and <i>mezuzot</i> are contained in
the very same verses of Scripture, and they should both, therefore, be
understood to be either one way or the other. My conclusion is that the
<i>tefillin</i> and <i>mezuzot</i> are meant to be metaphoric but the
requirement to wear fringes with a cord of blue is meant to be physical.
Putting God's words on our foreheads and tying them to our arms most likely
means that God should always be in our thoughts and in what we do, and
writing them on our door-frames most likely means that our homes should be
dedicated to all that is godly. No harm is done, however, if one elects to
apply them all physically, or to physically wear <i>tzitzit</i> and display
<i>mezuzot</i> but decline to wear <i>tefillin</i>.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This is one of those calls that (short of God revealing it prophetically)
cannot be proven, but I offer several considerations that lean to the
literal. First, hanging a <i>m'zuzah</i> on the doorpost of a house has the
actual effect of reminding those who enter of God's commandments and that
only holy thoughts and deeds are welcome within. Second, because displaying
<i>m'zuzot</i> is entirely a Jewish custom, it has the additional benefit of
announcing to all who enter that sons and daughters of the Covenant live
there, thus promoting God's primary purpose for Israel, which is to be a
nation of priests and a light to the Gentiles.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
I cannot help but think of another occasion in the Scriptures where the
children of Israel were commanded to put something on the doorposts of their
homes - the blood of the <i>Pesachi</i> - the Passover lamb sacrifice as
described in <u>Exodus 12:7, 13</u>:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>They are to take some of the blood and smear it on the two sides and top
of the door-frame at the entrance of the house in which they eat it ... The
blood will serve you as a sign marking the houses where you are; when I see
the blood, I will pass over you - when I strike the land of Egypt, the death
blow will not strike you.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Although one can see how God's instruction to the Israelites could have been
interpreted metaphorically (as in the case of the <u>Deuteronomy 6 and 11</u>
<i>mitzvah</i>), several things were accomplished by their complying
literally. First, it served to identify the houses' occupants as believers
in God and trusters in His Word. Second, it served to identify the houses'
occupants as probably being Israelites. Third, treating God's commandment
metaphorically and failing to physically apply the blood to the doorposts
would not have been sufficient and would have resulted in death of the
firstborn for that house.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
As I have already said, in Orthodox Jewish communities all men wear
<i>tzitziyot</i>, and there is discussion about whether they are also
appropriate for women; Orthodox women normally do not wear them. In
communities that have not adopted policies regarding <i>tzitziyot</i>, each
individual must make his own decision about whether to wear them as described
in Scripture, or substitute some other article of clothing or accessory that
will accomplish the same thing - reminding anyone seeing it of the
commandments of God.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
What fringes are is self-evident, so our decisions (other than whether to
wear them at all) are what to make them of, whether and how to knot them,
what the color and origin of the dye for the blue thread should be, and to
what articles of clothing they should be attached. Since the only
requirement in Scripture is that they be placed on the corners of garments,
unless one's community defines the particulars of acceptable
<i>tzitziyot</i>, the wearer can use his own judgment on all of these. The
orthodox Jewish community has adopted detailed rules for the construction of
<i>tzitziyot</i>, which their male members attach to a special four-cornered
garment called a <i>tallit katan</i>, and wear under their shirts or vests.
<i>Tzitziyot</i> are also worn on the traditional rectangular prayer shawl,
the <i>tallit gadol</i>. Both the <i>tallit katan</i> and the <i>tallit
gadol</i> are often intentionally lacking the prescribed blue
thread.<sup><small><a name=fr1 href=#fn1>1</a></small></sup>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
It is important to not to get so involved in the details of how to make
<i>tzitziyot</i>, that we forget what they are supposed to remind us of,
which are the commandments of God. We are not only to be reminded, but to be
made continually aware of God's commandments as we proceed through each day.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
I believe that the commandment to wear <i>tzitziyot</i> is applicable for
both Jewish men and Jewish women, notwithstanding the prevailing modern
practice of their only being worn by men. The principle of wearing
<i>tzitziyot</i> is also applicable to Gentiles, but there is greater liberty
for them and, in fact, it may be preferable that Gentiles (other than
<i>K'rovei Yisrael</i>) not wear fringes because the wearing of fringes today
has become almost completely associated with being a Jew. If there is a
concern, some kind of substitution to accomplish the same purpose is easily
adaptable.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
It is interesting to note that not all references to <i>tefillin</i> are for
the purpose of remembering God's Word broadly. <u>Exodus 13:9</u> connects
the <i>tefillin</i> to remembering how God brought the Jewish people out of
Egypt.
</font></p><hr width=150px style="margin-left: 10px">
<p align=left style="margin-left: 30px; margin-right: 30px">
<font size=2>
<a name=fn1 href=#fr1>1.</a>
The practice among some to not include the blue thread
has its origin in the scarcity of the <i>chilazon</i> - a sea creature from
which the blue dye was originally extracted.
</font>
</p>
<hr width=150px align=left style="margin-left: 10px">
<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Daniel C. Juster</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Whether or not a physical literal application is intended for all times is
debatable even if one holds to the literal application as originally
intended. Or was this a culturally fitting way of remembering? So for
example, wearing the <i>tzizit</i> on a four cornered garment fits the
culture of the time when such garments were commonly worn. They are not
today, so perhap the general principle to take steps to remember the covenant
and its commandments is sufficient.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
It is probably a good thing when Messianic Jews do seek ways to apply this
<i>mitzvah</i> literally - wearing a <i>tallit gadol</i> in worship services
and wearing fringes daily, because they show covenant loyalty. The same can,
of course, be said of putting on <i>tefillin</i> for prayer. It is
important, however, to remember that we cannot prove that these ways of
remembering are required for all times.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides, Meir, and HaChinuch adopt the Orthodox view that <i>tzitzit</i>,
<i>tefillin</i>, &amp; <i>mezuzot</i> are meant to be applied physically, and
they address each one (including the <i>tefillin</i> of the arm and head) as
a separate <i>mitzvah</i>. They also construe the particulars of each (i.e.
their shapes, appearances, contents, and how they are applied) according to
that which is written in the <i>Talmud</i>.
