<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This <i>Mitzvah</i> is recited by Jews the world over several times a day in
a portion of the <i>Torah</i> known as the "<i>Sh'ma</i>" (<u>Deuteronomy
6:4-9</u>). It is so foundational to Jewish faith, that when Yeshua was
asked in <u>Matthew 22:36</u>, "<i>Rabbi, which of the mitzvot in the Torah
is the most important?</i>" he replied by quoting <u>Deuteronomy 6:5</u>:
"<i>'You are to love ADONAI your God with all your heart and with all your
soul and with all your strength'</i>" (see also <u>Mark 12:30</u> and <u>Luke
10:27</u>).
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Loving God is a covenant commitment to Him that is proven though our
obedience (<u>John 14:21-24</u>) and our willingness and sincere desire to
put His agenda before our own (<u>Matthew 6:33, 16:24-25</u>; <u>Luke
9:23</u>). That notwithstanding, love (including love of God) has an
emotional component to it, a passion, that must be guided by that covenant
commitment; the two go together. Also, perceiving God's nature, character,
majesty, and faithfulness, causes us to recognize His love of us, and
stimulates our love of Him in return (<u>1 John 4:19</u>).
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Loving God is also connected to our loving one another in two prominent
Scriptures. <u>1 John 4:20-21</u> makes it clear that, despite any claim we
may make to the contrary, we cannot love God if we do not also love our
brother. The other Scripture is a continuation of Yeshua's statement that
loving God is the most important commandment; in <u>Matthew 22:39</u> he also
said: "<i>And a second is similar to it, 'You are to love your neighbor as
yourself.'</i>" (See also, <u>Mark 12:31</u> and <u>Luke 10:27</u>).
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides analogizes loving God with meditating upon and contemplating His
<i>mitzvot</i>; he says that by so doing, we get to know Him and receive joy.
Also, because the words of the <i>Sh'ma</i> speak of teaching God's
<i>mitzvot</i>, Maimonides associates the commandment to love God with our
obligation to teach the people of the world that they must have faith in Him
and serve Him.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Meir agrees with Maimonides, and adds the component of having affection for
God. He refers to Abraham's relationship with God as an example of there
being "love" between them, and he quotes <u>Genesis 12:5</u> as an example of
Abraham's love through obedience, and <u>Isaiah 41:8</u> as an example of God
considering Abraham to be His friend (see reference to this in <u>James
2:23</u>).
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
HaChinuch adds an interesting perspective by stating that a man cannot fully
obey God's Commandments unless he loves Him. This is the reverse of (but not
inconsistent with) the Scriptures cited above that infer that one way to love
God is by obeying His Commandments.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
HaChinuch also cites God's relationship with Abraham, and that our love of
God should cause us to encourage others to serve and worship Him as well.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
