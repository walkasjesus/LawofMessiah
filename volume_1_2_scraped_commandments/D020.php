<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
There is no explicit biblical commandment to determine the occurrence of the
New Moon (<i>Rosh Chodesh</i>), but the numerous references to the beginning
of months and to the New Year makes it a logical imperative to do so.
<u>Colossians 2:16</u> is the only mention of the New Moon in the Apostolic
Writings, but there are several references in the New Testament to months and
years.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The difficulty of determining the appearance of a New Moon is that there is
no appearance of the moon at all. On the night of a New Moon, one sees no
moon - similar to nights that are clouded over. During the time of Yeshua,
the New Moon was declared by the <i>San Hedrin</i> upon its receiving the
testimony of two individuals that they observed it. How can one see a moon
that one cannot see? The testimony received was that the witnesses saw the
last sliver of moon showing from the night previous.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Using modern astronomical methods of determining the New Moon is more precise
and is acceptable in most of Judaism today. Explaining these methods is,
however, beyond the scope of this <i>Mitzvah</i>.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
