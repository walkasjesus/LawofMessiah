<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Scripture tells us that God is our Heavenly Father, that we are His created
children, and that He wants us to have a loving relationship with Him. At the
same time, we are not to approach Him with the same kind of comfortable
familiarity as we would a peer, but with awesome respect, having full
knowledge of His power.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This is easier to understand for those of us who have (or had) godly and
loving parents here on earth, because we have experienced, in the natural,
what good fathers and mothers are like, and we are able to transfer our
understanding to our relationship with God. It is more difficult (and even
counter-intuitive) for those of us who have not had good parental
experiences, so we must glean our understanding from the words of Scripture,
and from what others tell us and teach us.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The attributes of God disclosed in Scripture are many, and often seem
contradictory. God is compassionate, yet He executes judgment. God loves,
yet He punishes. So when we read in <u>Deuteronomy 6:5</u> "<i>... you are
to love ADONAI your God with all your heart ...</i>", and then read in
Scriptures like <u>Deuteronomy 6:13</u> "<i>... You are to fear ADONAI your
God ...</i>", we may not know what to think.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The problem is partially that our English language Bibles tend to use the
word "fear" to translate different underlying Hebrew and Greek words used in
different kinds of situations. So, for example, the derivative of
<img src=images/word-yarei-1.gif align=middle> (<i>yarei</i>) that is
applied to God in <u>Deuteronomy 6:13</u> means "reverend fear" - the kind
that holds God in awe, deference, and honor. Yes, the word can also mean to
be afraid of God but, when it is used that way, it is meant as a warning to
sinners who ought to be afraid.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
It is a different kind of fear that is referred to in <u>2 Timothy 1:7</u>,
which the NKJ version translates:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>For God has not given us a spirit of fear, but of power and of love and
of a sound mind.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Here, the Greek word for "fear" δειλία
<!--
<img src="IFGreek.php?font=bold&height=24&ps=20&text=delta epsilon iota lambda iota-acute alpha">
-->
(<i>deiliah</i>), which has a different connotation than
<img src=images/word-yarei-1.gif align-middle> (<i>yarei</i>) in that it
means a lack of courage, cowardice, or timidity. Other Greek words such as
<!--
<img src="IFGreek.php?font=bold&height=24&ps=20&text=phi omicron-acute beta omicron sigma">
--->
φόβoς (<i>phabas</i>) and φoβέω
<!--
<img src="IFGreek.php?font=bold&height=24&ps=20&text=phi omicron beta epsilon-acute omega">
-->
(<i>phabei-oh</i>) in <u>2 Corinthians 5:11</u> and <u>1 Peter 2:17</u>
respectively, have connotations similar to
<img src=images/word-yarei-1.gif align=middle> (<i>yarei</i>).
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
In addition to the lexical evidence that fearing God (for an obedient
believer) should not mean being afraid of Him, we also have textual evidence
for the same premise. A Scripture that strongly implies this is <u>Proverbs
22:4</u>; it informs us that "fear of God" ("fear" being derived from
<img src=images/word-yarei-1.gif align=middle>) is a reward that results
from our being humble. In other words, it is a good thing - not something
to be dreaded. Another Scripture that implies something similar is
<u>Ephesians 5:21</u> that tells us to "<i>submit to one another in fear of
the Messiah</i>" ("fear" here is φόβoς).
<!--
<img src="IFGreek.php?font=bold&height=24&ps=20&text=phi omicron-acute beta omicron sigma">).
-->
There
is no other place in the New
Covenant Scriptures that suggests that we should be afraid of Messiah Yeshua
and, in fact, everything we read about Him suggests the opposite. Therefore
"fear of the Messiah" (and therefore φόβoς
<!--
<img src="IFGreek.php?font=bold&height=24&ps=20&text=phi omicron-acute beta omicron sigma">
-->
in this context) could not mean being
afraid and must, instead, be referring to a character virtue.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides does not say that we are to fear God, but rather that we are to
believe in the fear of God, and be expectant of his punishment at all times.
He then launches into a discussion of blaspheming, and pronouncing the Name
of God in vain as it is related to fearing Him.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Meir states that we must fear God, and that the path to fear and love are the
same. He also refers to "reverend fear" that requires that we not pronounce
God's Holy Name in vain or without purpose.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
HaChinuch also uses the term "reverend fear", and warns us to fear God's
punishment if we sin. Like Maimonides (and to a lesser extent Meir),
HaChinuch spends a considerable proportion of his writing on our not
blaspheming or speaking the Lord's Name in vain. The connection that both he
and Maimonides make to fearing God is that, doing either, results in our
losing the kind of reverend fear of God that God requires.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides and HaChinuch emphasize an expectation and fear of God's
punishment, whereas this <i>Mitzvah</i> emphasizes a reverend and loving fear
of God, with the hope and expectation of our not sinning. Maimonides also
speaks of not speaking God's Name in vain.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Although all three commentators state that we are not to speak God's Name in
vain, none of them make reference to the third of the Ten Commandments,
<u>Exodus</u>
<u>20:7</u><sup><small><a name=fr3 href=#fn3>3</a></small></sup>.
Also, even though it is a reality that if one sins, punishment is likely to
follow, I do not believe it is the intended meaning of the Commandment to
fear God. Perhaps the difference in my approach has to do with my belief
that we are exclusively in and subject to the New Covenant. The New Covenant
acknowledges the possibility that a believer in God and Messiah will sin, but
it does not assume it. This is unambiguously taught in <u>1 John 2:1</u>:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>My children, I am writing you these things so that you won't sin. But if
anyone does sin, we have Yeshua the Messiah, the Tzaddik, who pleads our
cause with the Father.</i>
