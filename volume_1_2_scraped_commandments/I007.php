of ghosts or spirits, or a necromancer. For whoever does these things is
detestable to ADONAI, and because of these abominations ADONAI your God is
driving them out ahead of you. You must be wholehearted with ADONAI your
God. For these nations, which you are about to dispossess, listen to
soothsayers and diviners; but you, ADONAI your God does not allow you to do
this.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
