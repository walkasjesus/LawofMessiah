<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The Scriptures are clear that we are not to listen to those who would lead us
toward idolatry. Although they refer to physical idols and false gods,
<u>Matthew 6:24</u> and <u>Colossians 3:5</u> consider anything to be a "god"
or an idol that competes with the one true God by becoming man's master, or
that we put ahead of the one true God. Such idols can be money, sexual
immorality, impurity, lust, evil desires, property, power, security, greed,
and others. Consequently, anyone who listens to those who lead toward such
things, violates this <i>Mitzvah</i>.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This <i>Mitzvah</i> incorporates two of the classical commentators'
<i>mitzvot</i> - to not listen to one who prophesies in the name of an idol,
and to not make one's home in the land of Egypt where one can be influenced
by Egypt's heresies and idolatrous customs.
