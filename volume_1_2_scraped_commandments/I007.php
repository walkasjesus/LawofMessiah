<b><u>Key Scriptures</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Exodus 14:10-14</u>
(Maimonides RN46; Chinuch C500)
<br>
<i>As Pharaoh approached, the people of Isra&#039;el looked up and saw the
Egyptians right there, coming after them. In great fear the people of
Isra&#039;el cried out to ADONAI and said to Moshe, "Was it because there
weren&#039;t enough graves in Egypt that you brought us out to die in the
desert? Why have you done this to us, bringing us out of Egypt? Didn&#039;t
we tell you in Egypt to let us alone, we&#039;ll just go on being slaves for
the Egyptians? It would be better for us to be the Egyptians&#039; slaves
than to die in the desert!" Moshe answered the people, "Stop being so
fearful! Remain steady, and you will see how ADONAI is going to save you. He
will do it today - today you have seen the Egyptians, but you will never see
them again! ADONAI will do battle for you. Just calm yourselves down!"</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Deuteronomy 13:2-4(1-3)</u>
(Maimonides RN28; Meir MN22; Chinuch C456)
<br>
<i>If a prophet or someone who gets messages while dreaming arises among you
and he gives you a sign or wonder, and the sign or wonder comes about as he
predicted when he said, &#039;Let&#039;s follow other gods, which you have
not known; and let us serve them,&#039; you are not to listen to what that
prophet or dreamer says. For ADONAI your God is testing you, in order to
find out whether you really do love ADONAI your God with all your heart and
being.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Deuteronomy 13:7-12(6-11)</u>
<br>
<i>If your brother the son of your mother, or your son, or your daughter, or
your wife whom you love, or your friend who means as much to you as
yourself, secretly tries to entice you to go and serve other gods, which you
haven&#039;t known, neither you nor your ancestors - gods of the peoples
surrounding you, whether near or far away from you, anywhere in the world -
you are not to consent, and you are not to listen to him; and you must not
pity him or spare him; and you may not conceal him. Rather, you must kill
him! Your own hand must be the first one on him in putting him to death, and
afterwards the hands of all the people. You are to stone him to death;
because he has tried to draw you away from ADONAI your God, who brought you
out of the land of Egypt, out of a life of slavery. Then all Isra&#039;el
will hear about it and be afraid, so that they will stop doing such
wickedness as this among themselves.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Deuteronomy 17:14-16</u>
(Maimonides RN46; Meir MN192; Chinuch C500)
<br>
<i>When you have entered the land ADONAI your God is giving you, have taken
possession of it and are living there, you may say, &#039;I want to have a
king over me, like all the other nations around me.&#039; In that event,
you must appoint as king the one whom ADONAI your God will choose. He must
be one of your kinsmen, this king you appoint over you- you are forbidden to
appoint a foreigner over you who is not your kinsman. However, he is not to
acquire many horses for himself or have the people return to Egypt to obtain
more horses, inasmuch as ADONAI told you never to go back that way again.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Deuteronomy 18:9-14</u>
<br>
<i>When you enter the land ADONAI your God is giving you, you are not to learn
