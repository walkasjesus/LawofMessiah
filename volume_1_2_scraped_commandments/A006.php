reference <u>Exodus 20:7</u> in any of their <i>mitzvot</i>, they do warn
against speaking God&#039;s Name in vain in RP4, MP4, and C432 respectively
(See <i>Mitzvah</i> #<a href=G006.php>G6</a> of this compilation).
</font>
