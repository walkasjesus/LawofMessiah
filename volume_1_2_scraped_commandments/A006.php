<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Exodus 20:7</u> is best known for it being the third of the "Ten
Commandments", rendered in the King James Version as:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>Thou shalt not take the name of the LORD thy God in vain; for the LORD
will not hold him guiltless that taketh his name in vain.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This translation and others like it, causes many to think that the
Commandment is only about improperly invoking God's name in the course of
communicating verbally (e.g. cussing). That would mean not using the word
"God", or one of his proper names, or "Yeshua", or "<i>Ruach HaKodesh</i>" in
profane, disrespectful, or trivial expressions, either orally or in
writing.<sup><small><a name=fr1 href=#fn1>1</a></small></sup>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Doing so is indeed an obvious prohibition, but one less frequently talked
about is profaning God's Name by conducting our lives sinfully while, at the
same time, professing to be a believer. Two common sayings (not in the
Bible) apply here; "It is not so much what we say as what we do", and
"Actions speak louder than words." It is the responsibility of all those who
profess belief in God (and Yeshua) to represent God properly by conducting
their lives in a holy manner. This responsibility falls especially on Jews
(even Jews who do not profess belief in God) because the Jews are widely
considered to be "God's chosen people", and are therefore seen as
representing God in all that they say and do. Sinful conduct by a Jew or a
Gentile believer in God brings discredit, shame, and disgrace to His Name.
Virtuous and holy conduct, on the other hand, brings Him honor and glory.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Another and more obvious way of violating this <i>Mitzvah</i> is by
prophesying in God's Name falsely. The most common way of prophesying
falsely is by declaring that God said something that He did not in fact say.
A false prophecy can result from an innocent mistake or from an intentional
deception, and it is not always preceded by a stylized announcement such as
"Thus says the Lord." A prophecy is a serious declaration because anyone who
hears it and believes that it originates with God is duty-bound to act on it
or to conform his or her life to it. So, if an alleged prophecy is false
(i.e. did not originate with God), it is capable of causing considerable
damage, including to the reputation of prophetic words in general. That is
why we must test all prophecies against Scripture, and exercise spiritual
discernment through prayer.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
So serious was false prophecy considered under the Mosaic Covenant and Mosaic
Law, that prophets who prophesied falsely were put to death (<u>Deuteronomy
18:20</u>). This "zero tolerance" standard was demanded because, in those
days, only certain persons called "prophets" were given the <i>Ruach
HaKodesh</i>, so all of Israel was dependent upon them for their
communication with God. That is not the case today in the New Covenant,
however, because today the <i>Ruach HaKodesh</i> is available to all
believers in Yeshua, and therefore, to one extent or another, all believers
are able to hear God and prophesy.
</font></p><hr width=150px style="margin-left: 10px">
<p align=left style="margin-left: 30px; margin-right: 30px">
<font size=2>
<a name=fn1 href=#fr1>1.</a>
Orthodox Jewish tradition seeks to respect God&#039;s
Name by making substitutions when writing or speaking of Him. Examples are
substituting "G-d" for "God" in writing, or using substitute words (e.g.
<i>Hashem</i>) or manufactured words (e.g. <i>Adoshem</i> and
<i>Elokeynu</i>) in the course of ordinary speech.
</font>
</p>
<hr width=150px align=left style="margin-left: 10px">
<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary by Daniel C. Juster</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
I would say in addition, that the most foundational meaning of the third of
the "Ten Commandments" is to not swear an oath in God's Name and then fail to
perform it (hence "in vain"). However, all Jewish and Christian commentators
also see the broader meanings as brought out in their commentaries and in
this <i>Mitzvah</i>.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides, Meir, and HaChinuch do not cite or refer to the third of the "Ten
Commandments" (<u>Exodus 20:7</u>), nor do they write about improperly using
God's name
lightly.<sup><small><a name=fr2 href=#fn2>2</a></small></sup>
They do, however, cite <u>Leviticus 22:32</u> that warns against desecrating
or profaning the Divine Name which, they say, is a very serious sin -
especially if committed in public. HaChinuch echoes Maimonides on this, and
Maimonides goes further to give some specific examples, through which it is
clear that he agrees with the premise of this <i>Mitzvah</i>, that swearing
by God's Name falsely or engaging in sinful conduct, even without mentioning
God's Name, violates <u>Leviticus 22:32</u>. He says that this is especially
so if it is committed by a person of known or presumed piety.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Meir refers to the sin of conspicuously profaning or desecrating the Name of
God (including when one is coerced to change his religion) as <i>chillul</i>
<i>HaShem</i><sup><small><a name=fr3 href=#fn3>3</a></small></sup>,
and says that a man must even surrender to death rather than commit this sin.
He says that even the power of repenting on <i>Yom Kippur</i> is not enough
to atone for <i>chillul Hashem</i>, and therefore the sin remains with the
offender for his entire
life.<sup><small><a name=fr4 href=#fn4>4</a></small></sup>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Prophesying falsely is another way that God's name can be used wrongfully.
Maimonides, Meir, and HaChinuch each wrote <i>mitzvot</i> (RN27, MN175, and
C517, respectively) codifying <u>Deuteronomy 18:20</u>, that says it is an
offense punishable by death for a prophet to prophesy falsely. Maimonides
and HaChinuch point out that the offense is not dependent upon whether the
prophet's statement is true, but whether God actually told the prophet to
speak it. So, for example, if God gave a word of truth to a first prophet, a
second prophet to which the word was not given would be in violation were he
to invoke God's Name in speaking the same word. All the classical
commentators agree that the false prophet should, according to the Mosaic
Law, be put to death by strangulation.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides and HaChinuch (but not Meir) wrote related <i>mitzvot</i> (RN29
and C519, respectively) that seek to interpret <u>Deuteronomy 18:22</u>.
Maimonides states that we are forbidden to take pity on a false prophet or,
out of fear, neglect to put him to death. HaChinuch says the same, and adds
that the fear spoken of in the Scripture is fear of our being punished for
conducting the execution of a prophet. That notwithstanding, <u>verse 22</u>
alludes neither to executing false prophets, nor to our being punished for
it; it merely says that we have nothing to fear from the false prophet.
</font></p><hr width=150px style="margin-left: 10px">
<p align=left style="margin-left: 30px; margin-right: 30px">
<font size=2>
<a name=fn2 href=#fr2>2.</a>
