<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The <i>Ru'ach Hakodesh</i> is so often identified with the New Covenant, that
the Spirit's ministry going back to the creation is often overlooked. It is
absolutely essential that we not oppose the <i>Ru'ach Hakodesh</i> in any
manner because He is our relational and communication link to the Father and
to Yeshua. Theologians debate the meaning of blaspheming the Holy Spirit, the
one and only act that Scripture tells us is unforgiveable. Whatever it is, it
cannot be easy to do, and if we are the least concerned about it, that alone
is strong evidence that the Holy Spirit has not left us.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The benefit of the New Covenant over the Mosaic Covenant (other than Yeshua's
sacrifice for our sins) is largely the ministry of the Holy Spirit and His
willingness to dwell within us. In <u>1 Corinthians 6:19</u> the
<i>Shaliach</i> Paul asks:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>... don't you know that your body is a temple for the Ruach HaKodesh who
lives inside you, whom you received from God?</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
It is therefore absolutely essential that we do nothing to grieve or oppose
the Holy Spirit in any manner. The Holy Spirit not only speaks to us
individually, but He is also able to speak to us for others - sometimes in a
language that we understand, and sometimes in a language that we do not
understand unless we or someone else is able to interpret. We call the latter
kind of language "tongues", and the <i>Shaliach</i> Paul is emphatic that we
must not forbid prophesying in tongues (albeit it must be done in a right
order, and with interpretation).
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Can we sin to a degree that God withdraws His Holy Spirit from us? The two
examples provided above (<u>1 Samuel 16:14</u> and <u>Psalms 51:11</u>)
occurred prior to the New Covenant; David was worried about losing the Holy
Spirit, and Saul actually did. In the New Covenant, we have <u>Matthew
12:30-32</u> and <u>Mark 3:28-29</u> that tell us that blaspheming the Holy
Spirit is unforgiveable and results in an eternal sin. Scripture does not say
that that involves loss of the Holy Spirit, but it certainly seems as though
it does.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides, Meir, and HaChinuch did not write about coming against the
<i>Ru'ach Hakodesh</i> or losing the <i>Ru'ach Hakodesh</i>; it is no doubt
due to their not believing that the Apostolic Writings are Scripture, nor
believing that we are in (and subject to) the New Covenant.
