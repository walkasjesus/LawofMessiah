as a latrine. You must include a trowel with your equipment, and when you
relieve yourself, you are to dig a hole first and afterwards cover your
excrement. For ADONAI your God moves about in your camp to rescue you and to
hand over your enemies to you. Therefore your camp must be a holy place.
[ADONAI] should not see anything indecent among you, or he will turn away
from you.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
