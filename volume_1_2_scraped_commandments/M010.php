</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Deuteronomy 23:10(9)-15(14)</u>
(Maimonides RP192-RP193; Chinuch C566-C567)
<br>
<i>When you are in camp, at war with your enemies, you are to guard yourself
