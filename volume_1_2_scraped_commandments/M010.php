<u>Deuteronomy 20:1-12</u>
(Maimonides RP190-RP191; Chinuch C525-C527)
<br>
<i>When you go out to fight your enemies and see horses, chariots and a force
larger than yours, you are not to be afraid of them; because ADONAI your
God, who brought you up from the land of Egypt, is with you. When you are
about to go into battle, the cohen is to come forward and address the
people. He should tell them, &#039;Listen, Isra&#039;el! You are about to
do battle against your enemies. Don&#039;t be fainthearted or afraid;
don&#039;t be alarmed or frightened by them; because ADONAI your God is
going with you to fight on your behalf against your enemies and give you
victory.&#039; Then the officials will speak to the soldiers. They are to
say, &#039;Is there a man here who has built a new house, but hasn&#039;t
dedicated it yet? He should go back home now; otherwise he may die fighting,
and another man will dedicate it. &#039;Is there a man here who has planted
a vineyard, but hasn&#039;t yet made use of its fruit? He should go back
home; otherwise he may die fighting, and another man will use it. &#039;Is
there a man here who is engaged to a woman, but hasn&#039;t married her yet?
He should go back home; otherwise he may die fighting, and another man will
marry her.&#039; The officials will then add to what they have said to the
soldiers: &#039;Is there a man here who is afraid and fainthearted? He
should go back home; otherwise his fear may demoralize his comrades as
well.&#039; When the officials have finished speaking with the soldiers,
commanders are to be appointed to lead the army. When you advance on a town
to attack it, first offer it terms for peace. If it accepts the terms for
peace and opens its gates to you, then all the people there are to be put to
forced labor and work for you. However, if they refuse to make peace with
you but prefer to make war against you, you are to put it under siege.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Deuteronomy 20:13-20</u>
(Maimonides RN57; Meir MN191; Chinuch C529)
<br>
<i>When ADONAI your God hands it over to you, you are to put every male to the
sword. However, you are to take as booty for yourself the women, the little
ones, the livestock, and everything in the city - all its spoil. Yes, you
will feed on your enemies&#039; spoil, which ADONAI your God has given you.
This is what you are to do to all the towns which are at a great distance
from you, which are not the towns of these nations. As for the towns of
these peoples, which ADONAI your God is giving you as your inheritance, you
are not to allow anything that breathes to live. Rather you must destroy
them completely - the Hitti, the Emori, the Kena&#039;ani, the P&#039;rizi,
the Hivi and the Y&#039;vusi - as ADONAI your God has ordered you; so that
they won&#039;t teach you to follow their abominable practices, which they
do for their gods, thus causing you to sin against ADONAI your God. When, in
making war against a town in order to capture it, you lay siege to it for a
long time, you are not to destroy its trees, cutting them down with an axe.
You can eat their fruit, so don&#039;t cut them down. After all, are the
trees in the field human beings, so that you have to besiege them too?
However, if you know that certain trees provide no food, you may destroy
them and cut them down, in order to build siege-works against the town
making war with you, until it falls.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
