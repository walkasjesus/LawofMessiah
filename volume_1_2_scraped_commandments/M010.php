<font size=4 face="Times New Roman, Times, serif">
<b><i>We are to consider the biblical principles of war and apply them where
possible.</i></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This precept is derived from His Word (blessed be He):
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Key Scriptures</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Numbers 1:2-3</u>
<br>
<i>Take a census of the entire assembly of the people of Isra&#039;el, by clans
and families. Record the names of all the men twenty years old and over who
are subject to military service in Isra&#039;el. You and Aharon are to
enumerate them company by company.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Numbers 1:44-50</u>
<br>
<i>Moshe, Aharon and the twelve leaders of Isra&#039;el, each from a clan,
counted the people of Isra&#039;el by their clans, those twenty years old
and over, eligible for military service in Isra&#039;el; and the grand total
came to 603,550. But those who were L&#039;vi&#039;im, according to the clan
of their fathers, were not counted in this census; because ADONAI had told
Moshe, "Do not include the clan of Levi when you take the census of the
people of Isra&#039;el. Instead, give the L&#039;vi&#039;im charge over the
tabernacle of the testimony, its equipment and everything else connected
with it. They are to carry the tabernacle and all its equipment, serve in it
and set up their camp around it."</i>
