<font size=4 face="Times New Roman, Times, serif">
<u>Numbers 6:1-21</u>
(Maimonides RN202-209; Chinuch C368-373, C375-376)
<br>
<i>ADONAI said to Moshe, "Tell the people of Isra&#039;el, &#039;When either a
man or a woman makes a special kind of vow, the vow of a nazir, consecrating
himself to ADONAI; he is to abstain from wine and other intoxicating liquor,
he is not to drink vinegar from either source, he is not to drink grape
juice, and he is not to eat grapes or raisins. As long as he remains a nazir
he is to eat nothing derived from the grapevine, not even the grape-skins or
the seeds. Throughout the period of his vow as a nazir, he is not to shave
his head. Until the end of the time for which he has consecrated himself to
ADONAI he is to be holy: he is to let the hair on his head grow long.
Throughout the period for which he has consecrated himself to ADONAI, he is
not to approach a corpse. He is not to make himself unclean for his father,
mother, brother or sister when they die, since his consecration to God is on
his head. Throughout the time of his being a nazir he is holy for ADONAI.
If someone next to him dies very suddenly, so that he defiles his
consecrated head, then he is to shave his head on the day of his
purification; he is to shave it on the seventh day. On the eighth day he is
to bring two doves or two young pigeons to the cohen at the entrance to the
tent of meeting. The cohen is to prepare one as a sin offering and the
other as a burnt offering and thus make atonement for him, inasmuch as he
sinned because of the dead person. That same day he is to re-consecrate his
head; he is to consecrate to ADONAI the full period of his being a nazir by
bringing a male lamb in its first year as a guilt offering. The previous
days will not be counted, because his consecration became defiled. This is
the law for the nazir when his period of consecration is over: he is to be
brought to the entrance of the tent of meeting, where he will present his
offering to ADONAI - one male lamb in its first year without defect as a
burnt offering, one female lamb in its first year without defect as a sin
offering, one ram without defect as peace offerings, a basket of matzah,
loaves made of fine flour mixed with olive oil, unleavened wafers spread
with olive oil, their grain offering and their drink offerings. The cohen
is to bring them before ADONAI, offer his sin offering, his burnt offering,
and his ram as a sacrifice of peace offerings to ADONAI, with the basket of
matzah. The cohen will also offer the grain offering and drink offering that
go with the peace offering. The nazir will shave his consecrated head at
the entrance to the tent of meeting, take the hair removed from his
consecrated head and put it on the fire under the sacrifice of peace
offerings. When the ram has been boiled, the cohen is to take its shoulder,
one loaf of matzah from the basket and one unleavened wafer, and place them
in the hands of the nazir, after he has shaved his consecrated head. The
cohen is to wave them as a wave offering before ADONAI; this is set aside
for the cohen, along with the breast for waving and the raised-up thigh.
Following that, the nazir may drink wine. This is the law for the nazir who
makes a vow and for his offering to ADONAI for his being a nazir - in
addition to anything more for which he has sufficient means. In keeping with
whatever vow he makes, he must do it according to the law for the
nazir.&#039;</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Numbers 30:2-16(1-15)</u>
(Maimonides RP94, RP95, RN157; Meir MP39, MP40, MN184;
C406, C407, C575)
<br>
<i>Then Moshe spoke to the heads of the tribes of the people of Isra&#039;el.
He said, "Here is what ADONAI has ordered: when a man makes a vow to ADONAI
or formally obligates himself by swearing an oath, he is not to break his
word but is to do everything he said he would do. When a woman makes a vow
to ADONAI, formally obligating herself, while she is a minor living in her
father&#039;s house; then, if her father has heard what she vowed or
obligated herself to do and holds his peace, then all her vows remain
binding - every obligation she has bound herself to will stand. But if on
the day her father hears it, he expresses his disapproval, then none of her
vows or obligations she has bound herself to will stand; and ADONAI will
forgive her, because her father expressed his disapproval. If, having made
vows or rashly committed herself to an obligation, she gets married; and her
husband hears but holds his peace with her on the day he learns of it, then
her vows and obligations she has bound herself to will stand. But if her
husband expresses his disapproval on the day he hears it, he will void the
vow which is on her and the obligation to which she has bound herself; and
ADONAI will forgive her. The vow of a widow, however, or of a divorcee,
including everything to which she has obligated herself, will stand against
her. If a woman vowed in her husband&#039;s house or obligated herself with
an oath; and her husband heard it but held his peace with her and did not
express disapproval, then all her vows and obligations will stand. But if
her husband makes them null and void on the day he hears them, then whatever
she said, vows or binding obligation, will not stand; her husband has voided
them; and ADONAI will forgive her. Her husband may let every vow and every
binding obligation stand, or he may void it. But if her husband entirely
holds his peace with her day after day, then he confirms all her vows and
obligations; he must let them stand, because he held his peace with her on
the day he heard them. If he makes them null and void after he has heard
them, then he will bear the consequent guilt."</i>
