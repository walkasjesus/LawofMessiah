<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
"<i>From the day after the day of rest - that is, from the day you bring the
sheaf for waving</i>" is the day we refer to as <i>Yom HaBikkurim</i> - Day
of Firstfruits (<u>Leviticus 23:10</u>). It is unique among the "designated
times" listed in <u>Leviticus 23</u>, in that it is the only one that is not
a Sabbath. The occurrence of the day we call <i>Shavuot</i> is determined by
counting from <i>Yom HaBikkurim</i>, and is unique among the Annual Sabbaths
in being the only one to which Scripture does not assign a particular date on
the lunar calendar; in that regard, it is similar to the weekly Sabbath. The
fifty-day count from "the day after the day of rest" (or Sabbath) in
<u>Leviticus 23:15-16</u> is known in Jewish practice as "counting the
<i>omer</i>", and which one of several possible Sabbaths is "day zero" of the
count, is a subject of ongoing debate. To add complication, <u>Numbers
28:26</u> calls <i>Shavu'ot</i> "<i>Yom HaBikkurim</i>" as well, the reason
being that there were two harvests involved.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Jewish orthodoxy has adopted the Pharisaic view that the count should start
from <i>Nisan</i> 16, the first day of the Feast of Unleavened Bread, thus
causing <i>Shavuot</i> to always fall on the 6<sup><small>th</small></sup>
day of <i>Sivan</i>. By contrast, many in Messianic Judaism have adopted the
Sadducean view which is that the count should start on the day after the
Seventh-Day Sabbath that falls in the midst of the Feast of Unleavened Bread.
This causes the count to begin on the first day of the week (a Sunday), on
<i>Yom HaBikkurim</i>, making this day the probable day of Yeshua's
resurrection. A result of this is that <i>Shavuot</i> falls on different
days of the lunar calendar in adjacent years, a consequence that seems to
have been intended in the <u>Leviticus 23</u> account.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
When requiring that there be an animal sacrifice to accompany the grain
offering of <i>Shavuot</i>, Scripture assumes the existence of a Tabernacle
(or Temple) and an operating Levitical priesthood. Since we have neither
today, counting to <i>Shavu'ot</i> and waiving two loaves of bread in the
synagogue are correct adaptations of the Scripture, and are obligatory for
both Jews and <i>K'rov Yisrael</i> Gentiles. Gentiles of the Nations should
also keep track of the count because knowing when <i>Shavuot</i> occurs is
important for connecting them to the Jewish people, and for commemorating the
Holy Spirit's appearance in Jerusalem on the <i>Shavuot</i> following
Yeshua's resurrection (<u>Acts 2:1-21, 10:45</u>).
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides analogizes the individual obligation to count the <i>omer</i> to
the <i>San Hedrin&#039;s</i> obligation to count the Years of Jubilee "year
by year and Sabbatical cycle by Sabbatical cycle." He also gives much
attention to justifying why counting fifty days and seven Sabbaths is one
<i>mitzvah</i> and not two, but makes no mention of when the count should
begin. Meir, in contrast, does not even mention that counting both days and
Sabbaths comprises one commandment, but specifically states that we are to
start to count the night of the sixteenth of <i>Nisan</i>, and that we are to
count while standing and accompany the count with a benediction. HaChinuch
states that we must count forty-nine days from the presentation of the sheaf
offering, which is the sixteenth of <i>Nisan</i>, and we must utter the day
count and the week count separately. He agrees that we are to do the count
while standing and with an accompanying benediction. Despite the different
way that HaChinuch expresses the number of days counted, all three
commentators endorse the Pharisaic way of counting - not the Sadducean way.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
