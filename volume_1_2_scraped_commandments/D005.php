<u>Leviticus 23:9-21</u>
(Maimonides RP46, RP161; Meir MP26; Chinuch C306-307)
<br>
<i>ADONAI said to Moshe, "Tell the people of Isra&#039;el, &#039;After you
enter the land I am giving you and harvest its ripe crops, you are to bring
a sheaf of the firstfruits of your harvest to the cohen. He is to wave the
sheaf before ADONAI, so that you will be accepted; the cohen is to wave it
on the day after the Shabbat. On the day that you wave the sheaf, you are to
offer a male lamb without defect, in its first year, as a burnt offering for
ADONAI. Its grain offering is to be one gallon of fine flour mixed with
olive oil, an offering made by fire to ADONAI as a fragrant aroma; its drink
offering is to be of wine, one quart. You are not to eat bread, dried grain
or fresh grain until the day you bring the offering for your God; this is a
permanent regulation through all your generations, no matter where you live.
From the day after the day of rest - that is, from the day you bring the
sheaf for waving - you are to count seven full weeks, until the day after
the seventh week; you are to count fifty days; and then you are to present a
new grain offering to ADONAI. You must bring bread from your homes for
waving - two loaves made with one gallon of fine flour, baked with leaven -
as firstfruits for ADONAI. Along with the bread, present seven lambs
without defect one year old, one young bull and two rams; these will be a
burnt offering for ADONAI, with their grain and drink offerings, an offering
made by fire as a fragrant aroma for ADONAI. Offer one male goat as a sin
offering and two male lambs one year old as a sacrifice of peace offerings.
The cohen will wave them with the bread of the firstfruits as a wave
offering before ADONAI, with the two lambs; these will be holy for ADONAI
for the cohen. On the same day, you are to call a holy convocation; do not
do any kind of ordinary work; this is a permanent regulation through all
your generations, no matter where you live."&#039;</i>
