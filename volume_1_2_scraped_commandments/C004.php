<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><i>We are not to deal harshly when lending or while collecting
debts.</i></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This precept is derived from His Word (blessed is He):
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Key Scriptures</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Exodus 22:24(25)</u>
(Maimonides RN234; Meir MN52; Chinuch C67)
<br>
<i>If you loan money to one of my people who is poor, you are not to deal with
him as would a creditor; and you are not to charge him interest.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Exodus 22:25-26(26-27)</u>
(Maimonides RP199, RN240; Meir MN61; Chinuch C586, C587)
<br>
<i>If you take your neighbor&#039;s coat as collateral, you are to restore it
to him by sundown, because it is his only garment - he needs it to wrap his
body; what else does he have in which to sleep? Moreover, if he cries out to
me, I will listen; because I am compassionate.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Deuteronomy 15:3</u>
(Maimonides RP142; Chinuch C476)
<br>
<i>You may demand that a foreigner repay his debt, but you are to release your
claim on whatever your brother owes you.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Deuteronomy 15:9</u>
(Maimonides RN231; Meir MN56, MP62; Chinuch C480)
<br>
<i>Guard yourself against allowing your heart to entertain the mean-spirited
thought that because the seventh year, the year of sh&#039;mittah is at
hand, you would be stingy toward your needy brother and not give him
anything; for then he may cry out to Adonai against you, and it will be your
sin.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Deuteronomy 23:21(20)</u>
(Maimonides RP198; Chinuch C573)
<br>
<i>To an outsider you may lend at interest, but to your brother you are not to
lend at interest, so that ADONAI your God will prosper you in everything you
set out to do in the land you are entering in order to take possession of
it.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Deuteronomy 24:6</u>
(Maimonides RN242; Meir MN58; Chinuch C583)
<br>
<i>No one may take a mill or even an upper millstone as collateral for a loan,
because that would be taking as collateral the debtor&#039;s very means of
sustenance.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Deuteronomy 24:10-13</u>
(Maimonides RP199, RN239, RN240; Meir MN59, MP63; Chinuch
C585-C587)
<br>
<i>When you make any kind of loan to your neighbor, you are not to enter his
house to take his collateral. You must stand outside, and the borrower will
bring the collateral outside to you. If he is poor, you are not to go to bed
with what he gave as collateral in your possession; rather, you must restore
the pledged item at sunset; then he will go to sleep wearing his garment and
bless you. This will be an upright deed of yours before ADONAI your God.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Deuteronomy 24:17</u>
(Maimonides RN241; Meir MN60; Chinuch C591)
<br>
<i>You are not to deprive the foreigner or the orphan of the justice which is
his due, and you are not to take a widow&#039;s clothing as collateral for a
loan.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The first thing to notice is that one should not consider himself a creditor
when one lends money to a poor brother (<u>Exodus 24(25)</u>). Scripture
establishes a different standard when the loan is to a brother Jew and
especially if it is for compassionate reasons. This is clear in the
Scriptures that deny interest to the lender and limit his collection options.
Notice also that the lender is to treat the borrower so as to preserve both
his safety and dignity, as we see in <u>Deuteronomy 24:10-13</u> which
prohibits the lender from entering the borrower's house to remove collateral,
and requires that the lender return needed collateral to him at night. Notice
also that importance is given to maintaining the borrower's good relationship
with the lender and his having gratitude for the lender's compassion in
having concern for his welfare.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The lender has no recourse of collection against a brother for a debt which
the brother is unable to pay, but such is not the case with a loan made to a
foreigner. In making a loan to a foreigner, interest may be charged and
collection means employed if the loan is defaulted upon. Nevertheless,
compassionate justice is due everyone, including the foreigner, so the
overall <i>torah</i> of this <i>Mitzvah</i> is that we are not to deal
harshly or unreasonably when lending money, or collecting debts from anyone.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
As with other <i>mitzvot</i>, that date back to the Mosaic period, the
foreigner or stranger was the Gentile who was not part of the camp of Israel,
and was most likely an idol-worshipper. With the New Covenant, Gentile
believers in Yeshua become brothers and members of the commonwealth (national
life) of Israel through grafting into the olive tree (<u>Romans 11:17</u>;
<u>Ephesians 2:12</u>), so this <i>mitzvah</i> and others like it should
apply to them as though they are Jews.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
It is fairly easy to apply this <i>Mitzvah</i> when a loan is made by and
individual to an individual. However, in this day and age, many loans are
not personal but rather corporate, and in such case the <i>Mitzvah</i> is
difficult to apply. Consider, for example, the plight of a Jewish bank
officer who facilitates his bank's loan to another Jew and, when the loan is
defaulted upon, called to foreclose on the borrower's house in order to
collect the loan. Since the bank officer does not own the loan and is duty
bound to represent his employer's interest, it is my opinion that he can
participate in the foreclosure after making every effort to help the borrower
solve his financial difficulty.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Interestingly, both Maimonides and HaChinuch, in their respective
<i>mitzvot</i>, go beyond what <u>Deuteronomy 15:3</u> and <u<23:21(20)</u>
says. They construe these verses as positive commandments requiring that
interest be charged to idol-worshippers and that the collection of their
debts be pursued rigorously even if means of pressure are required. This is
in contrast to the mercy and compassion that is must be shown when dealing
similarly with Jews, the logic, expressed by HaChinuch being, that we should
not provide idolaters with advantages lest we promote their ways and be drawn
into them. Maimonides actually says that is in order to harm them.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Meir does not deal with <u>Deuteronomy 15:3</u> at all, and I do not believe
that following Maimonides' and HaChinuch's emphasis of harshness toward
unbelievers or even idolaters is in keeping with the New Covenant's
commandments that we attempt to lead them to Messiah and win their souls for
God.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
