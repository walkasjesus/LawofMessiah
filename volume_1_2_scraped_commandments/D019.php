<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
We are to count seven Sabbaths of years (forty-nine years) from each Year of
Jubilee to the next. When the Year of Jubilee arrives, we are not to
cultivate our land or harvest its crops in the usual way, and we are to
return it to its prior owner, the one who sold it to us. When land is sold,
it must not be sold in perpetuity, and a right of redemption by the seller
must be an inherent part of the sale. In a very real sense, therefore, the
sale of land is not of the land itself, but of the value of the crops that
the land is likely to produce until the next Jubilee.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The Year of Jubilee, similar to the <i>Sh'mittah</i>, can be viewed as a time
of faith and benevolence because the crops that grow of their own accord
(without cultivation) during the year is available for all to glean,
including those who are poor. The Jubilee can also be viewed as a
<i>mitzvah</i> of social and economic equalization, because Israelites that
become wealthy through land that they have occupied, have to give the source
of their wealth up in the Year of Jubilee.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Like the <i>Sh'mittah</i>, I believe that the primary reason for God giving
the Jubilee Year to the Israelites was to hone their faith and cause them to
have to trust Him for their provision. A question that naturally comes to
mind, of course, is how resting our land and returning it to its prior owner
applies to us today. I am of the opinion that it can only apply literally as
in the past, where today's circumstances are similar to those under which the
ancient Israelites lived. So far as I am aware it is never the case since
the assumption of Scripture regarding the Jubilee is that (1) it is meant to
apply only in the Land of Israel, and (2) it is meant to apply by the entire
population so that the burden of returning land that one has bought does not
unjustly fall on just a few individuals who are willing to comply. Still, all
Scripture is inspired by God and is profitable (<u>2 Timothy 3:16-17</u>), so
we ought to consider how to use the inspiration when the Year of Jubilee
arrives. I propose that we consider it a time to reflect on our faith by
considering what our willingness would be to rest our land (should we own
land in geographic Israel) and give it up in the Jubilee year as a
demonstration of our trust and reliance on God.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary by Daniel C. Juster</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
In modern societies that are not primarily agricultural, the Jubilee (also
the <i>Sh'mittah</i>) is difficult to apply. However, there are underlying
principles that have been noted by many Bible scholars. First, similar to
the <i>Sh'mittah</i>, the command shows the importance of renewing and
preserving the land so that it is not worn out. Secondly, the command shows
that God does not want the ownership of land to create a permanent class of
landed gentry. The Jubilee promotes equal economic and social opportunity
that is also the goal of our most enlightened and compassionate governmental
systems.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The Jubilee restoration of land does not mean socialism since there could be
disparities of wealth by other means (e.g. in gold, silver, herds, etc.).
However, there is a redistribution of wealth in Jubilee was well, and it
therefore equalizes opportunity. I think the principle here is not to impose
taxes for on-going welfare, but to use taxes (especially inheritance taxes)
to provide education and training so that those without financial means have
economic opportunity. Perhaps we could call such taxes "equal opportunity
taxes".
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Meir has not constructed any <i>mitzvot</i> pertaining to the Jubilee.
Maimonides and HaChinuch have, but they do not take into consideration
today's changed circumstances as compared to those that existed during the
time of Moses. Their <i>mitzvot</i> therefore require literal obedience to
the Scriptures in a way that is difficult with regard to resting the land,
and nearly impossible regarding its return - even in <i>Eretz Yisrael</i>.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Similar to their <i>Sh'mittah mitzvot</i>, Maimonides and HaChinuch do not
connect the Year of Jubilee to man's need to trust God for provision.
