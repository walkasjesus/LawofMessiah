<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><i>We are not to leave our homes with the intention of doing work on the
Sabbath.</i></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This precept is derived from His Word (blessed be He):
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Key Scriptures</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Exodus 16:29</u>
(Maimonides RN321; Meir MN7; Chinuch C24)
<br>
<i>Look, ADONAI has given you the Shabbat. This is why he is providing bread
for two days on the sixth day. Each of you, stay where you are; no one is to
leave his place on the seventh day.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Hebrews 4:10-11</u>
<br>
<i>For the one who has entered God&#039;s rest has also rested from his own
works, as God did from his. Therefore, let us do our best to enter that
rest; so that no one will fall short because of the same kind of
disobedience.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The commandment in <u>Exodus 16:29</u> that is reflected in this
<i>Mitzvah</i> was given to the Israelites after they tried to gather manna
on the Sabbath despite God having provided a double portion for them the day
before. In today's context, it calls for us to trust that God will provide
sufficiently for us six days of the week, so that we should not even
contemplate leaving our homes in order to work on the Sabbath. In my opinion,
this is not a requirement that we stay in our house or limit the distance
that we may travel, but rather it is a requirement that we examine why we are
leaving our house, lest it be to go to work.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
In addition to other considerations, it is important to remember that God
first established the seventh day as holy prior to the Mosaic Covenant, and
at a time when there was, as of yet, no Israel (<u>Genesis 2:2-3</u>). Later,
speaking through the prophet Isaiah, God said that non-Jews who keep His
Sabbaths will receive blessings for doing so (<u>Isaiah 56:1-7</u>). Further
confirmation for this is found in <u>Hebrews 4:1-11</u> which compares
Sabbath rest with spiritual rest that comes from faith in Messiah. This is
not to say that keeping the seventh day Sabbath is required for Gentile
believers in this age. We again note the clearly stated passages in
<u>Romans 14</u>, <u>Galatians 5</u>, <u>Colossians 2</u>, and others, that
relieve Gentiles of Sabbath responsibility.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides interprets his <i>mitzvah</i> so as to prohibit travel on the
Sabbath, further than from one's home to across an Israelite's campsite in
the desert. Meir's concept in his <i>mitzvah</i> is similar, but he
describes the maximum permissible travel as the distance across a small town.
HaChinuch says the Sabbath limit is three parasangs away from the city, and
he explains the prohibition against traveling further by his understanding
that, after God created the world, He rested in the vicinity of His creation;
HaChinuch's opinion is that we should, therefore, rest in our place as well.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
