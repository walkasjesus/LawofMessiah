<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
"Following" God, "imitating" Him, and "walking" in His ways are different
ways of saying the same thing - learning God's ways and adopting them as our
own. We see from the above Scriptures that this principle traverses both the
Mosaic Covenant and the New Covenant, and that adopting the ways of Yeshua
(who is more visible to us than the Father) assures us of adopting the ways
of our Father in heaven as well. Yeshua Himself modeled this for us as we
read His words in <u>John 5:19</u>:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>Therefore, Yeshua said this to them: "Yes, indeed! I tell you that the Son
cannot do anything on his own, but only what he sees the Father doing;
whatever the Father does, the Son does too."</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
There is a common misunderstanding that the Scriptures quoted in this
<i>Mitzvah</i> should help dispel; the misunderstanding is that only through
the indwelling of the <i>Ru'ach HaKodesh</i> brought by the New Covenant, can
men succeed in obeying God. It is wrong because God never holds us
accountable for that which we cannot do, and Scripture teaches that, at Mt.
Sinai, God gave us instructions for conduct that He expected of us. On the
other hand, to think that man on his own can accomplish God's will is wrong
also, so what of the Spirit was available to man before the coming of the New
Covenant? Since man in his own strength cannot fully accomplish God's will,
it must be that the Holy Spirit was available at Sinai and before, but that
the level of the Spirit's availability was limited and was greatly increased
with the coming of the New Covenant. <u>Jeremiah 31:32-33(33-34)</u>
partially explains this:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>"For this is the covenant I will make with the house of Isra'el after
those days," says ADONAI: I will put my Torah within them and write it on
their hearts; I will be their God, and they will be my people. No longer
will any of them teach his fellow community member or his brother, 'Know
ADONAI'; for all will know me, from the least of them to the greatest;
because I will forgive their wickednesses and remember their sins no
more."</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Jeremiah prophesies both the Messiah and the <i>Ru'ach HaKodesh</i> - the
Messiah to provide a just means for God to forgive our sins, and the
<i>Ru'ach</i> to implant God's very own nature within us in order to bring
Him closer and assist us to walk in His ways. <u>Ezekiel 36:24-27</u> also
prophesies a New Covenant in which God would enhance our obedience through
the Holy Spirit placed within us, yet God assured us in <u>Deuteronomy
30:11-14</u>, that we were able to obey Him in pre-Yeshua times as well:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>For this mitzvah which I am giving you today is not too hard for you, it
is not beyond your reach. It isn&#039;t in the sky, so that you need to ask,
&#039;Who will go up into the sky for us, bring it to us and make us hear it,
so that we can obey it?&#039; Likewise, it isn&#039;t beyond the sea, so
that you need to ask, &#039;Who will cross the sea for us, bring it to us and
make us hear it, so that we can obey it?&#039; On the contrary, the word is
very close to you - in your mouth, even in your heart; therefore, you can do
it!</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Meir and Maimonides agree with each other - that we are to model ourselves
after God and expect to become like Him in nature. HaChinuch agrees, and
references <u>Micah 7:18</u>:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>Who is a God like you, pardoning the sin and overlooking the crimes of
the remnant of his heritage? He does not retain his anger forever, because
he delights in grace.</i>
