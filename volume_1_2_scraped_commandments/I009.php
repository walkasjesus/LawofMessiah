<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The Scriptures quoted herein prohibit the ancient Israelites and later New
Covenant believers from adopting the customs and practices of heathens.
Typical of these practices are eating blood, fortune-telling, practicing
divination, rounding one's hair, cutting the edge of one's beard, tattooing
or cutting one's flesh, wearing heathen-style of clothing, wearing garments
of the opposite sex, worshiping idols, practicing debauchery, becoming drunk,
and participating in wild parties and orgies.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Of special interest are <u>Deuteronomy 12:2-4</u> and <u>Deuteronomy
12:29-31</u> that enumerate ways in which heathens served their gods, and
forbid Israelites from serving the one true God in similar ways. These two
Scriptures have been the source of much debate as to whether customs and
practices that were once used in idolatrous ways but are no longer used in
those ways can be redeemed. Can a church not have a steeple because ancient
heathen temples most probably had pointed roofs? Can we not light candles on
<i>Shabbat</i> because the ancient heathen used candles and torches as part
of their idolatrous worship? My opinion is that some such things can be used
and some cannot, and we need to consult the Holy Spirit to know which are
which.<sup><small><a name=fr1 href=#fn1>1</a></small></sup>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Other Scriptures that are related to this <i>Mitzvah</i> are those that
prohibit wearing clothing made of wool and linen, and clothing of the
opposite sex. I see them as related. <u>Deuteronomy 22:9-11</u> prohibits our
sowing two different kinds of seeds, plowing with two kinds of animals, and
wear clothing made of two kinds of thread (wool and Linen). <u>Leviticus
19:19</u> prohibits our mating two kinds of animals, sowing two kinds of
grain, and wearing clothing made of two kinds of
<img src=images/word-shatneiz-1.gif align=middle height=25>
(<i>shatneiz</i>), which means "stuff". <u>Deuteronomy 22:5</u> prohibits
our wearing clothing of the opposite sex, which presumably means mixing two
kinds of clothing - those of our own sex, and those of the opposite sex. The
theme that is common to all of these is mixing things that do not naturally
go together, and reminds one of <u>2 Corinthians 6:14</u> that states:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>Do not yoke yourselves together in a team with unbelievers. For how can
righteousness and lawlessness be partners? What fellowship does light have
with darkness?</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Mixing things that do not belong together was (and still is) a perverted
heathen practice (for example, mixing temple worship with prostitution), and
we are prohibited from adopting heathen practices as our own.
</font></p><hr width=150px style="margin-left: 10px">
<p align=left style="margin-left: 30px; margin-right: 30px">
<font size=2>
<a name=fn1 href=#fr1>1.</a>
In view of this prohibition, one might consider the
inappropriateness of Christians adopting heathen practices such as
displaying images of deceased persons, erecting Christmas trees, and
coloring Easter eggs.
</font>
</p>
<hr width=150px align=left style="margin-left: 10px">
<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides, Meir, and HaChinuch address each Scriptural prohibition
separately; they do not connect them in a common theme. For example,
Maimonides' RN30 deals with adopting the habits and customs of unbelievers
generally, RN43 deals with shaving our temples, RN44 with shaving our beards,
RN41 with receiving tattoos, etc.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
According to Meir, examples of heathen customs that we must not follow are
distinctive heathen dress, growing locks of hair, shaving the sides of the
head, and leaving a crest of hair in the middle of the head (Mohawk hair
style). Curiously, Meir exempts from such restrictions, those who are close
to government rulers and, for whatever reason, must emulate their appearance.
Maimonides likewise prohibits our wearing heathen dress, emulating heathens
in their appearance, and participating in their social gatherings.
