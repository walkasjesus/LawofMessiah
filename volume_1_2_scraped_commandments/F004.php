<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><i>We are forbidden to afflict our wife by withholding food, clothing, or
marital rights.</i></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This precept is derived from His Word (blessed be He):
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Key Scriptures</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Exodus 21:7-11</u>
(Maimonides RN262; Meir MN42; Chinuch C46)
<br>
<i>If a man sells his daughter as a slave, she is not to go free like the
men-slaves. If her master married her but decides she no longer pleases
him, then he is to allow her to be redeemed. He is not allowed to sell her
to a foreign people, because he has treated her unfairly. If he has her
marry his son, then he is to treat her like a daughter. If he marries
another wife, he is not to reduce her food, clothing or marital rights. If
he fails to provide her with these three things, she is to be given her
freedom without having to pay anything.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Ephesians 5:25-33</u>
<br>
<i>As for husbands, love your wives, just as the Messiah loved the Messianic
Community, indeed, gave himself up on its behalf, in order to set it apart
for God, making it clean through immersion in the mikveh, so to speak, in
order to present the Messianic Community to himself as a bride to be proud
of, without a spot, wrinkle or any such thing, but holy and without defect.
This is how husbands ought to love their wives - like their own bodies; for
the man who loves his wife is loving himself. Why, no one ever hated his
own flesh! On the contrary, he feeds it well and takes care of it, just as
the Messiah does the Messianic Community, because we are parts of his Body.
Therefore a man will leave his father and mother and remain with his wife,
and the two will become one. There is profound truth hidden here, which I
say concerns the Messiah and the Messianic Community. However, the text
also applies to each of you individually: let each man love his wife as he
does himself, and see that the wife respects her husband.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>1 Timothy 5:8</u>
<br>
<i>Moreover, anyone who does not provide for his own people, especially for
his family, has disowned the faith and is worse than an unbeliever.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>1 Peter 3:7</u>
<br>
<i>You husbands, likewise, conduct your married lives with understanding.
Although your wife may be weaker physically, you should respect her as a
fellow-heir of the gift of Life. If you don&#039;t, your prayers will be
blocked.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
It is clear from the totality of the Scriptures (and especially from the New
Covenant Scriptures), that the overarching commandment is for men to love
their wives. The particulars of this <i>Mitzvah</i> in singling out food,
clothing, and marital (conjugal sexual) rights is forced upon us because it
is treated that way by the classical commentators, and we felt that we should
respond accordingly. Many Scriptures address men but are clearly applicable
to both men and women. We understand that wives are to love their husbands
but the responsibility of this <i>Mitzvah</i> is directed specifically to
husbands.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides, Meir, and HaChinuch use <u>Exodus 21:9-10</u> to support their
<i>mitzvot</i> that we must not withhold food, clothing, or marital rights
from our wives. The context of the Scripture that they use to support their
<i>mitzvot</i> is that of a wife who was previously a slave, marries her
master, and the master subsequently marries for a second time when his first
wife no longer pleases him.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
