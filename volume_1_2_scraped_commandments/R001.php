<font size=4 face="Times New Roman, Times, serif">
<u><b>Classical Commentators</b></u>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The only <i>mitzvot</i> that touch on listening to God's voice are
Maimonides' RP172, and HaChinuch's corresponding <i>mitzvah</i> C516. They
interpret <u>Deuteronomy 18:15-19</u> as our needing to listen to and obey
all of God's prophets even if the prophets should instruct us to temporarily
do the opposite of what appears to be a commandment in Scripture. I cannot
agree with that interpretation; I believe that <u>Deuteronomy 18:15-19</u>
refers to only one prophet - Yeshua the Messiah - who had yet to come at the
time the Commandment was given. And whereas Yeshua interpreted and applied
the Mosaic commandments, he never nullified them. Meir does not include the
subject of this <i>Mitzvah</i> in his compilation.
