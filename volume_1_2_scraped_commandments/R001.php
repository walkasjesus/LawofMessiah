<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This <i>Mitzvah</i> is related to the <i>Mitzvah</i> on "Obeying
<i>Torah</i>". The emphasis here is somewhat different however, in that this
<i>Mitzvah</i> has to do with listening to the voice of the Holy Spirit and
allowing Him to guide us on how God's Commandments should be applied in our
particular circumstances.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
What it means to obey God's commandments (those written down by Moses) is
clear, but what is not so clear is what listening to God's voice means.
Rabbinical Judaism would say that it is referring to an "Oral <i>Torah</i>"
- words allegedly spoken to Moses that were not written down, but rather
conveyed verbally (as an oral tradition) from generation-to-generation. I do
not believe that, nor do I believe that listening to God's voice is a mere
metaphor for understanding what the written words of <i>Torah</i> say, nor
does it mean hearing the words of <i>Torah</i> read aloud. Scripture is clear
that, in the New Covenant, we are able to hear God speak to us (usually in a
still small voice within us) by means of the Holy Spirit. At the time of
Elijah, the New Covenant had not yet come in His fullness, yet Elijah was
able to hear that still small voice of God because he was gifted with the
Holy Spirit; consider <u>1 Kings 19:11-12</u>:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>He [the angel] said, 'Go outside, and stand on the mountain before
ADONAI'; and right then and there, ADONAI went past. A mighty blast of wind
tore the mountains apart and broke the rocks in pieces before ADONAI, but
ADONAI was not in the wind. After the wind came an earthquake, but ADONAI was
not in the earthquake. After the earthquake, fire broke out; but ADONAI was
not in the fire. And after the fire came a quiet, subdued voice.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The Israelites of old heard and were guided by God's voice in primarily three
ways - audibly from Mount Sinai (rarely) (e.g. <u>Deuteronomy 4:32-33</u>),
from angels (e.g. <u>Exodus 23:20-22</u>) or from prophets (e.g. <u>Numbers
12:6</u>; <u>Deuteronomy 18:17-18</u>) who were specially gifted to hear the
voice of the Holy Spirit, and repeat it to the inquirer. God's Voice can be
heard today at least as clearly as it was in the past, because today we have
the great gift of the New Covenant that was prophesied in <u>Jeremiah
31:30-33(31-34)</u>, whereby the <i>Torah</i> is written on our hearts by the
Holy Spirit who dwells within each and every one of us who will receive Him.
Today, each of us we can hear God's voice in the same way as (in fact, more
clearly than) the prophets of old heard Him; as <u>Jeremiah</u> prophesied,
we no longer have to inquire of our neighbor or brother, for we all can know
and hear Him ourselves. It is clear to me that God's voice is and always has
been the voice of the Holy Spirit. Indeed, God speaks through His written
commandments, but it is the Holy Spirit that gives the commandments voice for
application in our individual lives and
circumstances.<sup><small><a name=fr1 href=#fn1>1</a></small></sup>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Expecting to hear God's Voice with a mind to obey it is very important if we
are to have an intimate and loving relationship with God (<u>John 14:15,
21</u>) - so much so that <u>Romans 8:14</u> says of it:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>All who are led by God's Spirit are God's sons.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Because men's hearing of God's voice can be faulty, we are admonished to by
<u>1 Thessalonians 5:21</u>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>test everything [and] hold onto what is good</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
It is the approach used by the men of Berea who "<i>welcomed Paul's
message</i>", but engaged in
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>checking the Tanakh every day to see if the things Sha'ul was saying were
true</i> (<u>Acts
17:11</u>).<sup><small><a name=fr2 href=#fn2>2</a></small></sup>
</font></p><hr width=150px style="margin-left: 10px">
<p align=left style="margin-left: 30px; margin-right: 30px">
<font size=2>
<a name=fn1 href=#fr1>1.</a>
English translations of <u>Deuteronomy 28:1-2
and 18</u> generally use the word "obey" when referring to
<img src=images/word-mitzvot-1.gif align=middle height=25> (God&#039;s
commandments), and "listen to" when referring to
<img src=images/word-kol-1.gif align=middle height=25> (the sound of
God&#039;s voice). That notwithstanding, the Hebrew word used for both
"obey" and "listen to" in these Scriptures is the same - a derivation of
the word <img src=images/word-shema-1.gif align=middle height=25>
(<i>sh&#039;ma</i>), and so the two functions of listening and obeying
cannot be separated. On the other hand, derivations of Hebrew words
<img src=images/word-mitzvot-1.gif align=middle height=25> (commandments)
and <img src=images/word-kol-1.gif align=middle height=25> (sound of voice)
are both used in the <u>Deuteronomy</u> Scriptures and, since these are not
the same, God&#039;s written Word and God&#039;s voice must be thought of
and dealt with differently.
