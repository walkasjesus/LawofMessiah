<font size=4 face="Times New Roman, Times, serif">
Scriptures of the Mosaic Law pertaining to family matters (typical of the
above Scripture) are not enforceable in the New Covenant because they
infringe on, and conflict with, the laws of the secular governments under
which we live. Still, there is teaching value in each of them. So, for
example, the <u>Deuteronomy 22:13-19</u> Scripture instructs us (1) to uphold
the value of premarital virginity; (2) to value the permanence of marriage
and not seek divorce for unsubstantial reasons such as dislike; (3) to not
bring false charges against our spouse; and (4) to not impugn our spouse's
reputation. We might also weigh the issue of marriage as required in the
Bible for believers who become sexually involved. It is a difficult issue
that requires more reflection and discussion. Would it be only with a virgin
woman as in the Bible? At the date of this writing, eight states and the
District of Columbia recognize common-law marriage, which is a legally
recognized state of marriage (despite their being no marriage license or
ceremony) by couples who cohabit openly with the intention of being married.
Intent is the key. Even in common-law marriage states, sexual cohabitation
without the intent of being considered married does not create a marriage and
is simply fornication.
