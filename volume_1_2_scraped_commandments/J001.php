respect, pay him respect; if you owe someone honor, pay him honor. Don't owe
anyone anything - except to love one another; for whoever loves his fellow
human being has fulfilled Torah. For the commandments, "Don't commit
adultery", "Don't murder", "Don't steal", "Don't covet", and any others are
summed up in this one rule: "Love your neighbor as yourself." Love does not
do harm to a neighbor; therefore love is the fullness of Torah.</i>
