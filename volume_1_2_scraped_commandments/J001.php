</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<a name=RefK>
<b><u>Scriptures that Pertain to Ritual Purity</u></b>
</a>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Leviticus 15:19-24</u>
<br>
<i>If a woman has a discharge, and the discharge from her body is blood, she
will be in her state of niddah for seven days. Whoever touches her will be
unclean until evening. Everything she lies on or sits on in her state of
niddah will be unclean. Whoever touches her bed is to wash his clothes and
bathe himself in water; he will be unclean until evening. Whoever touches
anything she sits on is to wash his clothes and bathe himself in water; he
will be unclean until evening. Whether he is on the bed or on something she
sits on, when he touches it, he will be unclean until evening. If a man goes
to bed with her, and her menstrual flow touches him, he will be unclean seven
days; and every bed he lies on will be unclean.</i>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Numbers 5:2-4</u>
<br>
<i>'Order the people of Isra'el to expel from the camp everyone with
tzara'at, everyone with a discharge and whoever is unclean because of
touching a corpse. Both male and female you must expel; put them outside the
camp; so that they won't defile their camp, where I live among you.' The
people of Isra'el did this and put them outside the camp - the people of
Isra'el did what ADONAI had said to Moshe.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
At the time of Moses, the commandments that pertained to ritual purity had to
do with being "clean" or "unclean" - not in the sense of being sanitary, but
for the purposes of being able to touch or be in the proximity of holy
objects or places. The cleanness of Human beings was the assumed norm, but
there were conditions and things that could render persons unclean and unable
to serve in the Tabernacle (later the Temple) and sometimes even unable to be
within the camp of Israel. Touching dead bodies, possessing weeping sores
(and other contagious conditions), giving birth, and a woman's menses are
some of the conditions.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Scholars disagree on the expectation of everyone living in ritual purity. For
example, a farmer is always touching dead animals and animal excrements. In
our opinion, being unclean for such reasons was expected and was not a
problem unless the unclean person was planning to go up to the Temple while
in that state.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Note also, that violating some of the laws of purity (e.g. disregarding the
holiness of blood and having sex during a woman's period) are beyond matters
of ritual purity, and required being cut off from one's people. Such
standards (without the penalty of being cut off) should be observed today.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
In the New Covenant, being unclean always has to do with being in sin. For
example, <u>Matthew 5:23-24</u>:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>So if you are offering your gift at the Temple altar and you remember
there that your brother has something against you [i.e. you sinned against
your brother], leave your gift where it is by the altar, and go, make peace
with your brother. Then come back and offer your gift.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The assumption here is that you who are about to offer your gift at God's
altar, have committed sin against your brother, and need to repent to him in
order to be ritually clean. A similar requirement that one must be repentant
in order to be ritually clean is found in <u>1 Corinthians 11:26-29</u>;
referring to the <i>Shulchan Adonai</i> (Table of the Lord), we read:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>For as often as you eat this bread and drink the cup, you proclaim the
death of the Lord, until he comes. Therefore, whoever eats the Lord's bread
or drinks the Lord's cup in an unworthy manner will be guilty of desecrating
the body and blood of the Lord! So let a person examine himself first, and
then he may eat of the bread and drink from the cup; for a person who eats
and drinks without recognizing the body eats and drinks judgment upon
himself.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<a href=#List>Scripture Catagories</a>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<a name=RefL>
<b><u>Scriptures that Pertain to Health & Sanitation</u></b>
</a>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Leviticus 13:32-34</u>
<br>
<i>On the seventh day the cohen is to examine the sore, and if he sees that
the crusted area hasn't spread, that it has no yellow hair in it, and that
the crusted area is not deeper than the skin around it; then the person is to
be shaved, except for the crusted area itself, and the cohen is to isolate
him for seven more days. On the seventh day the cohen is to examine the
crusted area; and if he sees that the crusted area has not spread on the skin
and does not appear to be deeper than the skin around it, then the cohen is
to declare him clean; he is to wash his clothes and be clean.</i>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Deuteronomy 23:13-14(12-13)</u>
<br>
<i>Also you are to have an area outside the camp to use as a latrine. You
must include a trowel with your equipment, and when you relieve yourself, you
are to dig a hole first and afterwards cover your excrement.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Health-related Scriptures such as those exemplified above are
self-explanatory. In both the <i>Tanakh</i> and the New Testament, physical
disease is associated with men's sinful condition; in fact, in the Mosaic
Covenant, some of the functions of the Levitical <i>cohanim</i> were similar
to those of physicians and public health officers. Good hygiene and
reasonable care to keep disease from spreading should be (and often is) an
obvious application of the Mosaic Law today. This is the way that Jewish
communities were preserved in times of plague in numbers that greatly
exceeded their Gentile counterparts.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Supernatural healing appears in the <i>Tanakh</i> as well as in the New
Testament - for example in <u>Deuteronomy 7:15</u>, where God refers to
Himself as the Israelites' healer (see also, <u>Exodus 15:26</u>).
Nevertheless, it is in the New Covenant Scriptures that we find supernatural
healing emphasized to its fullest, and with the power and authority to summon
God's healing delegated to men:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Matthew 10:1</u>
<br>
<i>Yeshua called his twelve talmidim and gave them authority to drive out
unclean spirits and to heal every kind of disease and weakness.</i>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Matthew 10:7-8</u>
<br>
<i>As you go, proclaim, 'The Kingdom of Heaven is near,' heal the sick,
raise the dead, cleanse those afflicted with tzara'at, expel demons. You have
received without paying, so give without asking payment.</i>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>James 5:14-16</u>
<br>
<i>Is someone among you ill? He should call for the elders of the
congregation. They will pray for him and rub olive oil on him in the name of
the Lord. The prayer offered with trust will heal the one who is ill - the
Lord will restore his health; and if he has committed sins, he will be
forgiven. Therefore, openly acknowledge your sins to one another, and pray
for each other, so that you may be healed. The prayer of a righteous person
is powerful and effective.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<a href=#List>Scripture Catagories</a>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<a name=RefM>
<b><u>Scriptures that Pertain to Slavery & Indenture</u></b>
</a>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
(a) <u>Slavery Resulting from Debt</u>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Exodus 22:2b(3b)</u>
<br>
<i>A thief must make restitution; so if he has nothing, he himself is to be
sold to make good the loss from the theft.</i>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>2 Kings 4:1</u>
<br>
<i>The wife of one of the guild prophets complained to Elisha. 'Your servant
my husband died,' she said, 'and you know that he feared ADONAI. Now a
creditor has come to take my two children as his slaves.'</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
(b) <u>Slaves Bought, Sold, & Bequeathed</u>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Exodus 21:7-8</u>
<br>
<i>If a man sells his daughter as a slave, she is not to go free like the
men-slaves. If her master married her but decides she no longer pleases him,
then he is to allow her to be redeemed. He is not allowed to sell her to a
foreign people, because he has treated her unfairly.</i>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Leviticus 25:44-46</u>
<br>
<i>Concerning the men and women you may have as slaves: you are to buy men-
and women-slaves from the nations surrounding you. You may also buy the
children of foreigners living with you and members of their families born in
your land; you may own these. You may also bequeath them to your children to
own; from these groups you may take your slaves forever. But as far as your
brothers the people of Isra'el are concerned, you are not to treat each other
harshly.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
(c) <u>Captives of War Enslaved</u>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Deuteronomy 20:10-11</u>
<br>
<i>When you advance on a town to attack it, first offer it terms for peace.
If it accepts the terms for peace and opens its gates to you, then all the
people there are to be put to forced labor and work for you.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
(d) <u>Voluntary Slavery & Indenture</u>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Exodus 21:5-6</u>
<br>
<i>Nevertheless, if the slave declares, 'I love my master, my wife and my
children, so I don't want to go free,' then his master is to bring him before
God; and there at the door or doorpost, his master is to pierce his ear with
an awl; and the man will be his slave for life.</i>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Leviticus 25:47-48</u>
<br>
<i>If a foreigner living with you has grown rich, and a member of your
people has become poor and sells himself to this foreigner living with you or
to a member of the foreigner's family, he may be redeemed after he has been
sold. One of his brothers may redeem him</i>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Deuteronomy 15:12-17</u>
<br>
<i>If your kinsman, a Hebrew man or woman, is sold to you, he is to serve
you for six years; but in the seventh year, you are to set him free.
Moreover, when you set him free, don't let him leave empty-handed; but supply
him generously from your flock, threshing-floor and winepress; from what
ADONAI your God has blessed you with, you are to give to him. Remember that
you were a slave in the land of Egypt, and ADONAI your God redeemed you; that
is why I am giving you this order today. But if he says to you, 'I don't
want to leave you,' because he loves you and your household, and because his
life with you is a good one; then take an awl, and pierce his ear through,
right into the door; and he will be your slave forever. Do the same with your
female slave.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The Mosaic Law not only permitted slavery - it regulated it. Slavery in
Scripture is not, however, the same as typically occurs in some secular
societies today, where certain people, for no fault of their own, are
kidnaped and enslaved or sold as slaves for personal gain:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Exodus 21:16</u>
<br>
<i>Whoever kidnaps someone must be put to death, regardless of whether he
has already sold him or the person is found still in his possession.</i>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Deuteronomy 24:7</u>
<br>
<i>If a man kidnaps any of his brothers, fellow members of the community of
Isra'el, and makes him his slave or sells him, that kidnapper must die; in
this way you will put an end to such wickedness among you.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
An Israelite or non-Israelite might become a slave for a number of reasons.
In the examples above, slavery that was procured by kidnaping was illegal and
was punishable by death, but slavery was also a means by which a person could
pay off a personal debt that he could not pay otherwise (<u>2 Kings 4:1</u>
supra). Slavery was also imposed on idolaters whose land was conquered by
Israel at God's command (<u>Deuteronomy 20:10-11</u> supra). Also, an
Israelite who burglarized and could not afford to pay back double could be
sold into slavery, and the value of his labor used to pay his debt (<u>Exodus
22:2b(3b)</u> supra). Israelite slaves would work for six years and be freed
automatically on the <i>Sh'mitah</i>, at which time his debt would be
forgiven (<u>Exodus 21:2</u>; <u>Deuteronomy 15:1-3</u>
supra);<sup><small><a name=fr1 href=#fn1>1</a></small></sup>
Slavery was, therefore, an ancient means of debt-collection that had an
escape clause for Israelites. Indentured servanthood was a kind of slavery
that was voluntarily entered into for a season, and there was another kind of
voluntary slavery that was permanent (see <u>Exodus 21:5-6</u> supra).
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Unlike the Scriptures of the Mosaic Covenant, those of the New Covenant do
not seek to regulate slavery. They do, however, recognize its existence, and
instruct those who are subject to it, how to cope, while bringing glory to
God by maintaining a servant's heart.
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Colossians 3:22-25</u>
<br>
<i>Slaves, obey your human masters in everything, not serving only when they
are watching you, to win their favor, but single-heartedly, fearing the Lord.
Whatever work you do, put yourself into it, as those who are serving not
merely other people, but the Lord. Remember that as your reward, you will
receive the inheritance from the Lord. You are slaving for the Lord, for the
Messiah. Don't worry - whoever is doing wrong will be paid in kind for his
wrong, and there is no favoritism shown.</i>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Ephesians 5:5-9</u>
<br>
<i>Slaves, obey your human masters with the same fear, trembling and
single-heartedness with which you obey the Messiah. Don't obey just to win
their favor, serving only when they are watching you; but serve as slaves of
the Messiah, doing what God wants with all your heart. Work willingly as
slaves, as people do who are serving not merely human beings but the Lord.
Remember that whoever does good work, whether he be a slave or a free man,
will be rewarded by the Lord. And masters, treat your slaves the same way.
Don't threaten them. Remember that in heaven both you and they have the same
Master, and he has no favorites.</i>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>1 Timothy 6:1</u>
<br>
<i>Those who are under the yoke of slavery should regard their masters as
worthy of full respect, so that the name of God and the teaching will not be
brought into disrepute.</i>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Titus 2:9-10</u>
<br>
<i>Tell slaves to submit to their masters in everything, to give satisfaction
without talking back or pilfering. On the contrary, they should demonstrate
complete faithfulness always, so that in every way they will make the
teaching about God our Deliverer more attractive.</i>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>1 Peter 2:18-19</u>
<br>
<i>Household servants, submit yourselves to your masters, showing them full
respect - and not only those who are kind and considerate, but also those who
are harsh. For it is a grace when someone, because he is mindful of God,
bears up under the pain of undeserved punishment.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Paul's letter to <u>Philemon</u> urges the New Covenant's position that one
who is a slave is not less of a person before God. In this epistle Paul (who
is in prison) appeals to Philemon to receive back his runaway slave Onesimus
as a brother in the Lord and an equal, because he had received Messiah and
been "born again". Also, <u>1 Corinthians 7:21-23</u> teaches:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>Were you a slave when you were called? Well, don't let it bother you;
although if you can gain your freedom, take advantage of the opportunity.
For a person who was a slave when he was called is the Lord's freedman;
likewise, someone who was a free man when he was called is a slave of the
Messiah. You were bought at a price, so do not become slaves of other human
beings.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The thrust of the New Covenant whereby the slave is considered equal in value
as his master who is to treat him as a brother, eventually led to the
perception among believers in Yeshua, that slavery was incompatible with
there being a brotherhood of believers. Thus slavery fell into disuse in the
believing communities of the first century. Today, slavery is outlawed in
the western countries, but the Scriptures about slavery are, nevertheless,
still useful in teaching us how to selflessly serve God, how to give godly
service to others, and how to compassionately treat and respect those who
give service to us.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
