For us, the main value of the various punishments we read about in the
Scriptures is that they give us an understanding of the seriousness with
which God considers certain kinds of wrongful conduct. In today's world,
capital punishment of a rebellious son would not be allowed. Yet, God
considers rebellion and stubbornness so serious a sin that we read in <u>1
Samuel 15:23</u>:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
