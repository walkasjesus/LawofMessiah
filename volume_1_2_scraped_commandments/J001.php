<font size=4 face="Times New Roman, Times, serif">
<a name=RefA>
<b><u>Scriptures that Require the Holy Tabernacle, Temple, or Other Place
that God Chooses</u></b>
</a>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Exodus 25:8</u>
<br>
<i>They are to make me a sanctuary, so that I may live among them.</i>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Deuteronomy 12:21</u>
<br>
<i>If the place which ADONAI your God chooses to place his name is too far
away from you; then you are to slaughter animals from your cattle or sheep,
which ADONAI has given you; and eat on your own property, as much as you
want.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The Holy Tabernacle, the Temple, and other special places designated by God
were not only places in which to conduct sacrifices, but also where the glory
of God dwelt on earth, and where men (who were authorized) could encounter
God intimately and powerfully. <u>Chapters 40 to 48 of Ezekiel</u> prophesy
the detailed rebuilding of a Temple that is mysterious as to its identity,
function, and when it is to happen (or has happened). Some scholars believe
that the passage is a commandment for the Jewish people to rebuild God's
Temple. Most significant, however, is that the place where God chooses to
place his name is a foreshadowing of the New Covenant's visitation of the
Holy Spirit who has come to live within all men that are willing to receive
Him:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>1 Corinthians 6:19a</u>
<br>
<i>Or don't you know that your body is a temple for the Ruach HaKodesh who
lives inside you, whom you received from God?</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Also, <u>1 Corinthians 3:16</u> shows that the corporate body of Messiah is
also a Temple of the Spirit today:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>Don't you know that you people are God's temple and that God's Spirit
lives in you?</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This is anticipated by <u>John 4:23-24</u> where worship in spirit and in
truth transcends a particular temple location:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>But the time is coming - indeed, it's here now - when the true
worshippers will worship the Father spiritually and truly, for these are the
kind of people the Father wants worshipping him. God is spirit; and
worshippers must worship him spiritually and truly.</i>
