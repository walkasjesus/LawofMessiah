Much that has been said regarding the Mosaic criminal law is the same for the
Mosaic civil law in that they are both promulgated by, and enforced pursuant
to, the ancient government of Israel that no longer exists. By the term
"civil law", we do not mean law promulgated by civil authorities since that
is true of criminal law as well. The difference between criminal and civil
law is that offenses of civil law are against individuals, whereas violations
of criminal law are deemed to be against society as a whole. We normally
associate violations of criminal law with punishment, and violations of civil
law with restitution, and damages having to be paid to individuals who have
been harmed. Sometimes the same wrongful act violates both a criminal and a
civil statute, such as in the case of an unjustified physical assault. An
assault against an individual is a breach of the peace against society as a
whole, but it also causes injury to the person who is assaulted;
consequently, both punishment and monetary damages to the injured party is
commonly required. As has been said regarding the Mosaic criminal law, the
way that we of the New Covenant should view it, is that it gives us God's
view of the seriousness of certain kinds of wrongful behaviors, and the kinds
of restitution that should be required for transgressing. <u>Romans
13:7-10</u> expresses the New Covenant concept of civil law best; it states:
