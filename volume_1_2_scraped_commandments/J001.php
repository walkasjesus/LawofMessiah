city (<u>Isaiah 52:1</u>) and the mountain in Jerusalem is a holy mountain
(<u>Isaiah 27:13</u>); but where else? We no longer have the Holy Place of
the Tabernacle and Temple; is the sanctuary of a modern synagogue holy by
analogy? We no longer have the Holy Ark; is a synagogue's "<i>Aron
Hakodesh</i>" holy by analogy? The characteristic of anything holy is that
it is set apart to God, or for a godly purpose. The original holy places and
things we read about in Scripture were made holy by God, but God commanded
us:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Leviticus 11:45</u>
<br>
<i>For I am the LORD who brings you up out of the land of Egypt, to be your
God. You shall therefore be holy, for I am holy.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
With that understanding we, as holy people, can designate places and things
as holy, so yes, the synagogue's sanctuary is holy and so is its <i>Aron
Hokodesh</i>.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
