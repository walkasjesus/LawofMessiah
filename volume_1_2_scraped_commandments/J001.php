<font size=4 face="Times New Roman, Times, serif">
<a name=RefJ>
<b><u>Scriptures that Pertain to Consecrations, Dedications, &
Redemptions</u></b>
</a>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Exodus 13:13</u>
<br>
<i>very firstborn from a donkey, you are to redeem with a lamb; but if you
choose not to redeem it, you must break its neck. But from people, you are to
redeem every firstborn son.</i>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Exodus 29:37</u>
<br>
<i>Seven days you will make atonement on the altar and consecrate it; thus
the altar will be especially holy, and whatever touches the altar will become
holy.</i>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Numbers 7:11</u>
<br>
<i>and ADONAI said to Moshe, 'They are to present their offerings to
dedicate the altar, each leader on his own day.'</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Consecrations, dedications, and redemptions of various kinds are performed
today, but the persons, objects, and places dedicated, etc. are typically not
the same as in the past. The rabbinical ceremony of <i>pidyon ha-ben</i>
(buying back one's firstborn son from a <i>cohen</i>) is an adaptation of the
<u>Exodus 13:13</u> commandment to redeem one's firstborn son from serving in
the Tabernacle / Temple, and an example of a New Covenant dedication and
consecration is found in <u>Romans 12:1</u>:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>I exhort you, therefore, brothers, in view of God's mercies, to offer
yourselves as a sacrifice, living and set apart for God. This will please
him; it is the logical "Temple worship" for you.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
