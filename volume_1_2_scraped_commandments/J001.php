must be established by the testimony of two or three witnesses.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
And also, in <u>1 Timothy 5:19</u> we read:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>Never listen to any accusation against a leader unless it is supported
by two or three witnesses.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Today as in the days of Moses, giving false testimony is considered a serious
crime, but a difference from during the time of Moses is that today, maximum
punishments for crimes are prescribed by statute and are not individually
tailored to each crime (e.g. an eye for eye,
etc.<sup><small><a name=fr3 href=#fn3>3</a></small></sup>.
Today, judgments for restitution in civil cases are generally established
according to the extent of injury to the victim, and restitution is usually
in money, and not in causing injury to the perpetrator as was often the case
during the days of Moses.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
In general, Scripture discourages adjudication and, instead, encourages the
avoidance of disputes:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Proverbs 17:14</u>
<br>
<i>Starting a fight is like letting water through [a dike] - better stop the
quarrel before it gets worse.</i>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Proverbs 20:3</u>
<br>
<i>Avoiding quarrels brings a person honor; for any fool can explode in
anger.</i>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Proverbs 25:7b-8</u>
<br>
<i>What your eyes have seen, don't rush to present in a dispute. For what
will you do later on, if your neighbor puts you to shame?</i>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Matthew 5:40</u>
<br>
<i>If someone wants to sue you for your shirt, let him have your coat as
well!</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
If an offense has already occurred and a dispute cannot be avoided, Scripture
recommends settling out of court rather than litigating:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Matthew 5:25</u>
<br>
<i>If someone sues you, come to terms with him quickly, while you and he are
on the way to court; or he may hand you over to the judge, and the judge to
the officer of the court, and you may be thrown in jail!</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
In the New Covenant, we are instructed to not bring complaints against
brother believers to the public courts, but rather to bring them to <i>betei
den</i> of the Messianic community:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>1 Corinthians 6:1-7</u>
<br>
<i>How dare one of you with a complaint against another go to court before
pagan judges and not before God's people? Don't you know that God's people
are going to judge the universe? If you are going to judge the universe, are
you incompetent to judge these minor matters? Don't you know that we will
judge angels, not to mention affairs of everyday life? So if you require
judgments about matters of everyday life, why do you put them in front of men
who have no standing in the Messianic Community? I say, shame on you! Can it
be that there isn't one person among you wise enough to be able to settle a
dispute between brothers? Instead, a brother brings a lawsuit against
another brother, and that before unbelievers! Actually, if you are bringing
awsuits against each other, it is already a defeat for you. Why not rather be
wronged? Why not rather be cheated?</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
When we do bring complaints against brother believers in the New Covenant,
there is a prescribed way of going about it.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
1. <u>Deciding Whether to Overlook an Offense</u>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Even when we are authorized to seek redress for a sin committed against us,
we need not do so.
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Proverbs 19:11</u>
<br>
<i>People with good sense are slow to anger, and it is their glory to
overlook an offense.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Some offenses are easily overlooked - especially those that are
unintentional, have done no great harm, and are unlikely to be repeated.
Although it may seem that overlooking an offense is always the loving thing
to do, it is not necessarily so. One's decision should always be based upon
what is best for the offender, and what is best for others against whom the
offender may sin if he is not made accountable. If, however, the offense is
overlooked, it must not be brought up again unless the offense is repeated.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
2. <u>Bringing Correction to an Offending Brother</u>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
If our decision is to not overlook the offense, we must confront the
offending brother privately:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Matthew 18:15</u>
<br>
<i>Moreover, if your brother commits a sin against you, go and show him his
fault - but privately, just between the two of you. If he listens to you,
you have won back your brother.</i>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Galatians 6:1-2</u>
<br>
<i>Brothers, suppose someone is caught doing something wrong. You who have
the Spirit should set him right, but in a spirit of humility, keeping an eye
on yourselves so that you won't be tempted too. Bear one another's burdens -
in this way you will be fulfilling the Torah's true meaning, which the
Messiah upholds.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
There are three things in the foregoing Scriptures which stand out. First,
when we go to our brother to tell him his fault, our attitude must be pure
and our demeanor proper. The operative expression in the <u>Galatians</u>
Scripture is "a spirit of gentleness".
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Second, we are to go to our brother alone. That means we are not to share our
complaint with others before first giving our brother the opportunity to
repent and make things right. This principle assumes two things: (1) The
parties are relatively equal in their ability to deal with one another, and
(2) There is no impropriety in the parties meeting privately. An example of
inequality would be a child having to confront an adult; an example of
improper privacy would be a male and female meeting alone to confront one
another concerning sexual sin. In these and similar cases, the Scriptures
should be interpreted broadly enough to permit chaperoning and for allowing
the weaker of the two adversaries to be accompanied by a suitable protector
but not an advocate or spokesperson.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Third, our purpose for confronting our brother must be to restore him to
righteousness, and our hope must be for reconciliation.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
3. <u>Returning with Witnesses</u>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
If the offending brother agrees with our complaint and repents, the matter
is, of course, concluded, and we forgive him. If he does not agree or
refuses to meet privately, we must then elect whether to pursue the matter
further, or to belatedly overlook his sin (<u>Proverbs 19:11</u>) while being
content that we have complied with <u>Galatians 6:1-2</u>. Although
overlooking the sin at this point is possible, its appropriateness is
unlikely. If there remains un-reconciliation, we are obligated to go to our
brother again, and this time, bring one or two witnesses:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Matthew 18:16</u>
<br>
<i>If he doesn't listen, take one or two others with you so that every
accusation can be supported by the testimony of two or three witnesses.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
These need not be witnesses to the original offense complained of (although
they may be), but rather to this second meeting with our brother.<sup><small><a name=fr4 href=#fn4>4</a></small></sup>
Our usual concept of witnesses is that they are silent observers. In this
case, however, the verse of Scripture that follows (<u>Matthew 18:17</u>),
infers that these witnesses determine which of the parties to the dispute is
wrong, and urges that person to listen to reason and repent. These verses of
Scripture are constructed with the assumption that the brother receiving the
visit is the one who is in the wrong but, in our interpretation, we allow for
the possibility that, after hearing the dialog between the brothers, the
witnesses conclude that the brother bringing the complaint is the one who is
in the wrong.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
As before, if the parties come to agreement during the visit or the offending
brother repents, the matter is concluded. If however, the attempt at
reconciliation is unsuccessful, the matter must not be allowed to fester,
and must therefore be brought for adjudication to the <i>ekklesia</i>.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
4. <u>Bringing the Dispute to the <i>Ekklesia</i></u>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Matthew 18:17</u>
<br>
<i>If he refuses to hear them, tell the congregation; and if he refuses to
listen even to the congregation, treat him as you would a pagan or a
tax-collector.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The word "congregation" in the CJB (translated "church" in the NKJ) is
<img src=images/N005.gif align=top> (<i>ekklesia</i>), in the Greek text,
which means "a gathering", "an assembly (for worship)", "a deliberative
council". According to <u>Matthew 18:17</u>, if the complainant and the
witnesses are unsuccessful in convincing the offender to repent, the matter
is to be brought to the <i>ekklesia</i> for adjudication.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Since the entire <i>ekklesia</i> (the body of believers or even the
membership of a single congregation) is too large a group to assemble in
order to hear and judge disputes, it is the prevailing view that a <i>bet
din</i> (ecclesiastical court) of assembled elders acts for the
<i>ekklesia</i> and rules on the complaint brought before it. If, after
announcing its judgment, the person ruled to be in the wrong does not repent,
we are to treat him as we would "<i>a pagan or a tax-collector</i>"
(<u>Matthew 18:17</u>). This is sometimes referred to as a decree of
"disfellowship" or "excommunication".
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
It is a common misunderstanding that when a brother is excommunicated
pursuant to <u>Matthew 18:17</u>, he must necessarily be ejected from the
congregation. On the contrary, after a judgment of excommunication, the
brother must be treated as an unbeliever in every way. Therefore, although
he must be refused holy communion (<i>Shulchan Adonai</i>) with the brethren,
unless he is also a violator of <u>1 Corinthians 5:9-13</u> (walking in
immorality while calling himself a believer) or <u>Titus 3:10-11</u>
(divisive or otherwise harmful to the body), he should be encouraged to
attend congregational services and other events where he is likely to hear
the Word of God and be encouraged to repent.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
One interesting consequence of a <u>Matthew 18:17</u> excommunication, is
that the complainant is released from the constraint of <u>1 Corinthians
6:1-7</u>, and is free to sue the unrepentant "former" brother in a secular
court. This has special ramifications for persons seeking to divorce an
excommunicated spouse.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Another common misunderstanding is that a decree of excommunication applies
only to the excommunicating congregation or religious denomination.
Biblically, that is not so. Judgments arising from <u>Matthew 18</u>
proceedings apply across the entire body of believers and, so long as correct
biblical doctrine is applied and due process is afforded, congregations of
all stripes are biblically required to recognize the judgment. What is more,
God Himself recognizes and honors the judgment, for we read in <u>Matthew
18:18</u>:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>Yes! I tell you people that whatever you prohibit on earth will be
prohibited in heaven, and whatever you permit on earth will be permitted in
heaven.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
We note that sins without repentance that would lead to capital punishment
in the Mosaic order generally require disfellowship in the New Covenant
order.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
5. <u>Restoring a Brother to Fellowship</u>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
A <u>Matthew 18</u> judgment of "disfellowship" is reversible in the same way
as the status of being an unbeliever is reversible. What is required is that
the sanctioned brother repent of his former sin, comply with all orders of
the convicting <i>bet din</i>, and receive Yeshua again as his Lord and
savior. Ideally, the same <i>bet din</i> that ruled previously is convened to
judge the repentance and, if the repentance is deemed genuine, the tribunal
should set aside its prior judgment, and publish a decree of restoration.
This restoration which is bound on earth, is also bound in heaven (<u>Matthew
18:18</u>).
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
6. <u>When the Offender is Us!</u>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Matthew 5:23-24</u>
<br>
<i>So if you are offering your gift at the Temple altar and you remember
there that your brother has something against you, leave your gift where it
is by the altar, and go, make peace with your brother. Then come back and
offer your gift.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This is the reciprocal of the situation dealt with by <u>Matthew 18</u>. If
we know or suspect that we have sinned against a brother, we are not to wait
until the brother comes to us - we are to go to him. A noted author, Jay
Adams, has written that, because of <u>Matthew 5:23-24</u> and <u>Matthew
18:15</u>, the offender and the offended should be meeting in the street
between their respective homes, each on his way to seek reconciliation with
the other.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<a href=#List>Scripture Catagories</a>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<a name=RefR>
<b><u>Scriptures that Pertain to Idols & Idolatry</u></b>
</a>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Exodus 20:3-6</u>
<br>
<i>You are to have no other gods before me. You are not to make for
yourselves a carved image or any kind of representation of anything in heaven
above, on the earth beneath or in the water below the shoreline. You are not
to bow down to them or serve them; for I, ADONAI your God, am a jealous God,
punishing the children for the sins of the parents to the third and fourth
generation of those who hate me, but displaying grace to the thousandth
generation of those who love me and obey my mitzvot.</i>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Exodus 22:19(20)</u>
<br>
<i>Anyone who sacrifices to any god other than ADONAI alone is to be
completely destroyed.</i>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Deuteronomy 7:1-5</u>
<br>
<i>ADONAI your God is going to bring you into the land you will enter in
order to take possession of it, and he will expel many nations ahead of you -
the Hitti, Girgashi, Emori, Kena'ani, P'rizi, Hivi and Y'vusi, seven nations
bigger and stronger than you. When he does this, when ADONAI your God hands
them over ahead of you, and you defeat them, you are to destroy them
completely! Do not make any covenant with them. Show them no mercy. Don't
intermarry with them - don't give your daughter to his son, and don't take
his daughter for your son. For he will turn your children away from
following me in order to serve other gods. If this happens, the anger of
ADONAI will flare up against you, and he will quickly destroy you. No, treat
them this way: break down their altars, smash their standing-stones to
pieces, cut down their sacred poles and burn up their carved images
completely.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
We see from the examples cited above that idolatry (worshiping gods other
than the Lord our God) is punishable by death under Mosaic Law, and all
objects of idolatrous worship are to be torn down and destroyed. Upon the
New Covenant's arrival, pagan worship of physical idols and foreign gods was
still rampant among the Romans, Greeks and others, and so a significant
number of New Testament Scriptures speak against it; for example:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Acts 15:28-29</u>
<br>
<i>For it seemed good to the Ruach HaKodesh and to us not to lay any heavier
burden on you than the following requirements: to abstain from what has been
sacrificed to idols, from blood, from things strangled, and from fornication.
If you keep yourselves from these, you will be doing the right thing.
Shalom!</i>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Acts 17:29</u>
<br>
<i>So, since we are children of God, we shouldn't suppose that God's essence
resembles gold, silver or stone shaped by human technique and imagination.</i>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>1 Corinthians 10:7</u>
<br>
<i>Don't be idolaters, as some of them were - as the Tanakh puts it, 'The
people sat down to eat and drink, then got up to indulge in revelry.'</i>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>1 Corinthians 10:14</u>
<br>
<i>Therefore, my dear friends, run from idolatry!</i>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>1 Corinthians 12:2</u>
<br>
<i>You know that when you were pagans, no matter how you felt you were being
led, you were being led astray to idols, which can't speak at all.</i>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>1 John 5:21</u>
<br>
<i>Children, guard yourselves against false gods!</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Also, the Mosaic Law that required death for idolaters and destruction of
their idols is carried forward into the New Covenant - not that we execute
idolaters any longer, but that God dispatches them by denying them eternal
life:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>1 Corinthians 10:9-10</u>
<br>
<i>Don't you know that unrighteous people will have no share in the Kingdom
of God? Don't delude yourselves - people who engage in sex before marriage,
who worship idols, who engage in sex after marriage with someone other than
their spouse, who engage in active or passive homosexuality, who steal, who
are greedy, who get drunk, who assail people with contemptuous language, who
rob - none of them will share in the Kingdom of God.</i>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Revelation 21:8</u>
<br>
<i>But as for the cowardly, the untrustworthy, the vile, the murderers, the
sexually immoral, those involved with the occult and with drugs,
idol-worshippers, and all liars - their destiny is the lake burning with
fire and sulfur, the second death.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Apart from the New Testament's warnings against worshiping foreign gods and
physical idols, the New Covenant expands the meaning of idolatry to giving
ourselves to satanic temptations and sinful temptations of the flesh: Of
course, explicitly worshipping idols is grounds for disfellowshipping in the
New Covenant.
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>1 Corinthians 10:13-14</u>
<br>
<i>No temptation has seized you beyond what people normally experience, and
God can be trusted not to allow you to be tempted beyond what you can bear.
On the contrary, along with the temptation he will also provide the way out,
so that you will be able to endure. Therefore, my dear friends, run from
idolatry!</i>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>1 Corinthians 10:19-20</u>
<br>
<i>So, what am I saying? That food sacrificed to idols has any significance
in itself? or that an idol has significance in itself? No, what I am saying
is that the things which pagans sacrifice, they sacrifice not to God but to
demons; and I don't want you to become sharers of the demons!</i>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Galatians 4:7-9</u>
<br>
<i>So through God you are no longer a slave but a son, and if you are a son
you are also an heir. In the past, when you did not know God, you served as
slaves beings which in reality are non-gods. But now you do know God, and,
more than that, you are known by God. So how is it that you turn back again
to those weak and miserable elemental spirits? Do you want to enslave
yourselves to them once more?</i>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Colossians 3:5</u>
<br>
<i>Therefore, put to death the earthly parts of your nature - sexual
immorality, impurity, lust, evil desires and greed (which is a form of
idolatry)</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>James 4:7</u> commands us:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
