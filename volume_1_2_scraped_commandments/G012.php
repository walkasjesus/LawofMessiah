</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Leviticus 23:7</u>
(Maimonides RP159; Meir MP25; Chinuch C297)
<br>
<i>On the fifteenth day of the same month is the festival of matzah; for seven
