<font size=4 face="Times New Roman, Times, serif">
<b><i>We are to have faith in God &amp; trust Him for all things - for our
protection, for those we love, for our worldly needs, and for our very
lives.</i></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
