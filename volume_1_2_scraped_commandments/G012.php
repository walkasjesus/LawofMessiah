your sins. It is a Shabbat of complete rest for you, and you are to deny
yourselves. "This is a permanent regulation."</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Leviticus 23:3</u>
(Maimonides RP154)
<br>
<i>Work is to be done on six days; but the seventh day is a Shabbat of complete
