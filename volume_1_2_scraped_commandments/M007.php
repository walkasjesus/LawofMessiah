<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This <i>Mitzvah</i> addresses our governmental, societal, and religious
institutions (e.g. our national and local governments, our congregations, our
families, etc.), regarding their contributions to the financial equity for us
that God intends. Financial equity is not the same thing as financial
equality; equity is fairness in opportunity, and God addresses this most
clearly in <u>Deuteronomy 8:18</u> when He says to the Israelites:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>No, you are to remember ADONAI your God, because it is he who is giving
you the power to get wealth, in order to confirm his covenant, which he swore
to your ancestors, as is happening even today.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The "power to get wealth" spoken of in the Scripture is financial
opportunity, but Scripture is clear that not all of us will achieve wealth,
for Yeshua said in <u>Matthew 26:11a</u>:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>The poor you will always have with you ...</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Exodus 21:2-6</u>, <u>Exodus 23:10-12</u>, <u>Leviticus 25:2-34</u>, and
<u>Deuteronomy 15:1-18</u>, contain commandments that redistributed monetary
wealth and restored land to its original Israelite owners every seven years
(the <i>Sh'mitah</i> - the Sabbatical year) and every fifty years (the
Jubilee year - also a <i>Sh'mitah</i>) respectively. They also command
forgiving debts and releasing slaves on <i>Sh'mitah</i>, and the
<i>Sh'mitah</i> is a year of rest (analogous to the weekly Sabbath) when the
Israelites, along with their land and animals, were required to cease from
work. It was a year when all (including those who were poor) could eat from
what grew of its own accord, that was the result of the prior six years of
cultivation. Food could be gathered but not methodically harvested; the
latter was considered working the land, whereas the former was not.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
These Scriptures, especially those about the <i>Sh'mitah</i> and the year of
Jubilee (<i>Yovel</i>), difficult as they are with which to comply in today's
society, remind us that it is God (and not we) who owns the land and other
forms of worldly wealth and, if we acquire them, we are required to
periodically return them so that other individuals and other generations may
have similar opportunity. This is especially emphasized by the requirement
that all land sales must be with a right of redemption by either the one who
sells it or his kin.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Some argue against the foregoing by pointing out that the mandated redemption
or return is not back to God, but rather back to persons and families that
were previously given the land or, in the case of forgiveness of loans, to
individual borrowers who had no historic right to retain what they borrowed.
What causes all of this to make sense is adding to it the biblical mandate of
stewardship. God ultimately owns everything, and we are mere trustees of
what He owns and entrusts to us for a time. Our property, both real and
personal, is temporary and intended for our own use, and also for the use of
others as we are charged with generosity, and compassion for the needs of our
neighbors. Whether we are good stewards of God's wealth depends on how
wisely and how charitably we use His wealth - how much we use for ourselves
as compared to how much of it we use to bless others.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
While this <i>Mitzvah</i> has ramifications for our personal tithing and
<i>tzadakah</i> (charitable giving), its purpose is mainly to mandate that we
employ its principle of economic equity in how we manage our various
institutions over which we have influence. The relevant Scriptures of the
<i>Torah</i> to which I have referred cannot be literally applied today
because we are subject to the laws of the secular governments under which we
live that almost never make allowance for them. That notwithstanding, God
wants us to incorporate the <i>Torah</i> principle of financial equity in the
institutions that we control, and wherever else we are able.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Daniel Juster</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Although the Bible allows for gaining wealth and allows for inequality of
results in increase, it seems to also try to limit vast disparities of wealth
and poverty. Note the text of <u>Deuteronomy 17:16-17</u>, that refers to the
appointed king of Israel:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>However, he is not to acquire many horses for himself or have the people
return to Egypt to obtain more horses, inasmuch as ADONAI told you never to
go back that way again. Likewise, he is not to acquire many wives for
himself, so that his heart will not turn away; and he is not to acquire
excessive quantities of silver and gold.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Now if this applies to the king, it must certainly apply to all others. We
are not given a detailed law of its application except that the land upon
which we live is to be returned. In ancient Israel, most of people's wealth
(not all) was in the Land. Oddly, one could profit from the sale of land,
and the resulting profit (money) would not have to be returned. Also, the
Bible seems to look to some level of wisdom in financial proportionality, but
does not give a rule for percentages or amounts.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This <i>Mitzvah</i>, in the general way it is stated, is not addressed by
Maimonides, Meir, or HaChinuch. The probable reason is that their
<i>mitzvot</i> are drawn solely from the Scriptures of the Mosaic period when
economic equity was intrinsically built into Israel's societal and
governmental institutions by God, so they felt that constructing a separate
<i>mitzvah</i> commanding it was not needed.
