<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><i>We are to test everything that is spoken in God's name.</i></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This precept is derived from His Word (blessed be He):
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Key Scriptures</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Deuteronomy 13:2-4(1-3)</u>
(Maimonides RN28; Meir MN22; Chinuch C456)
<br>
<i>If a prophet or someone who gets messages while dreaming arises among you
and he gives you a sign or wonder, and the sign or wonder comes about as he
predicted when he said, &#039;Let&#039;s follow other gods, which you have
not known; and let us serve them,&#039; you are not to listen to what that
prophet or dreamer says. For ADONAI your God is testing you, in order to
find out whether you really do love ADONAI your God with all your heart and
being.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Deuteronomy 18:21-22</u>
(Maimonides RN29; Chinuch C519)
<br>
<i>You may be wondering, &#039;How are we to know if a word has not been spoken
by ADONAI?&#039; When a prophet speaks in the name of ADONAI, and the
prediction does not come true - that is, the word is not fulfilled - then
ADONAI did not speak that word. The prophet who said it spoke
presumptuously; you have nothing to fear from him.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Acts 17:11</u>
<br>
<i>Now the people here were of nobler character than the ones in Thessalonica;
they eagerly welcomed the message, checking the Tanakh every day to see if
the things Sha&#039;ul was saying were true.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>1 Thessalonians 5:19-22</u>
<br>
<i>Don&#039;t quench the Spirit, don&#039;t despise inspired messages. But do
test everything - hold onto what is good, but keep away from every form of
evil.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>1 John 4:1-3</u>
<br>
<i>Dear friends, don&#039;t trust every spirit. On the contrary, test the
spirits to see whether they are from God; because many false prophets have
gone out into the world. Here is how you recognize the Spirit of God: every
spirit which acknowledges that Yeshua the Messiah came as a human being is
from God, and every spirit which does not acknowledge Yeshua is not from God
- in fact, this is the spirit of the Anti-Messiah. You have heard that he is
coming. Well, he&#039;s here now, in the world already!</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Supportive Scriptures</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>1 Corinthians 14:29-33</u>
<br>
<i>Let two or three prophets speak, while the others weigh what is said.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>1 Corinthians 14:37-38</u>
<br>
<i>If anyone thinks he is a prophet or is endowed with the Spirit, let him
