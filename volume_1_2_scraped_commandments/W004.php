someone doesn&#039;t recognize this, then let him remain unrecognized.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Scripture admonishes us to "<i>test everything</i>" (<u>1 Thessalonians
5:21</u>) because there are false prophets in the world who would mislead us
intentionally, and also because well-meaning men sometimes mishear the voice
of God. It was the approach used by the men of Berea who "<i>welcomed Paul's
message</i>", but engaged in "<i>checking the Tanakh every day to see if the
things Sha'ul was saying were true</i>" (<u>Acts 17:11</u>).
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This <i>Mitzvah</i> is not included among the 613 of Maimonides, Meir, or
HaChinuch. However, #RN28, #MN22, and #C456 (based upon <u>Deuteronomy
13:4(3)</u>), instructs us to not listen to anyone who prophesies in the name
of an idol.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
