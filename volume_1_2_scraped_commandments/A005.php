</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
A vow is a promise uttered with deliberate solemnity. An oath is an
utterance added to a vow that invokes an authority capable of enforcing the
vow, and punishing the oath-maker if the vow is breached. When a person adds
an oath to his vow, he is giving an assurance that is in addition to his
personal honor. He is essentially saying that if he reneges on his promise,
he invites the invoked authority to enforce his compliance and/or mete out
punishment.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
It is clear from many Scriptures in the <i>Tanakh</i> (e.g. <u>Deuteronomy
10:20</u>) that God endorses the swearing of oaths, but in His Name alone.
That makes perfect sense since if a vow is serious enough to require the
added assurance of an oath, no authority other than God can be counted upon
to enforce it (<u>Deuteronomy 6:13-15</u>). Also, when we swear an oath by
God's Name we invoke His Honor; therefore, if we swear falsely we profane His
Name (<u>Leviticus 19:12</u>).
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The <i>Tanakh</i> contains many examples of oaths sworn in ancient times,
and even God has been known to swear an oath on occasion (e.g. <u>Hebrews
6:13-17</u>). In more recent times, oaths are most commonly sworn ancillary
to giving legal testimony in a court of law. When oaths are sworn outside of
a court proceeding they are called "notarizations", and are generally for
attesting to the accuracy of a legal document or for affirming the
authenticity of a signature.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
There are some who refuse to swear oaths based on their interpretation of
<u>Matthew 5:34</u> and <u>James 5:12</u> that contain the words "<i>But I
tell you not to swear at all ...</i>" and "<i>Above all, brothers, stop
swearing oaths ...</i>" respectively. Their explanation for how this can be
in light of God's teaching and the many examples in the <i>Tanakh</i>, is
that Yeshua changed the rules for the New Covenant in order to raise the
standard of truth-telling to a higher level. That is not correct for, if one
looks at the Matthew and James "statements" that were taken out of context,
when each one's entire verse and adjacent verses are returned to them, one
sees that there is no contradiction at all. In fact, <u>Matthew 5:33-37</u>
affirms the importance and legitimacy of oaths made in God's Name by warning
that we should not swear by "heaven", by "earth", by "Jerusalem", by one's
"head" or, by extension, by anything else that is not "God". Similarly,
<u>James 5:12</u> admonishes the reader to stop swearing oaths by "heaven",
by "earth", or by "any other formula". These verses of Scripture are
apparently intended to stop a common practice of invoking meaningless
authorities for their effect in enhancing the oath-taker's appearance that
he is telling the truth (when he would never promise the same thing if he
was swearing in God's Name). Instead, a man should be able to put forth his
reputation that his "yes" and his "no" (i.e. whatever he says) can be relied
upon without reinforcement.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The Scriptures prohibit using God's Name lightly (<u>Exodus 20:7</u>), but
do not, in any way, prohibit swearing oaths in God's Name in appropriate
situations. <u>Matthew 23:16-24</u> explains this especially well, and
infers a warning to the oath-taker that he deludes himself if he thinks that
his oath is not binding just because it does not invoke God's Name directly.
Yeshua scolds the teachers and Pharisees for using "oath formulas" that
create the appearance of enforceability, while their secret intent is to not
honor their vows. It is intentional casuistry, and they will be held to
their word by God nevertheless.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary of Daniel C. Juster</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
I do think that, in his "Sermon on the Mount", Yeshua preaches a higher
standard for truth-telling and for verbalizing oaths than the one commonly
practiced in his day; his exhortation is to return us to the standard that
was always implied in the <i>Torah</i>. Yeshua is saying that our
truthfulness as believers and our commitment to doing what we say we will do
should have such integrity, that oaths become unnecessary among us. This
would limit our oath-taking to public law contexts where it is required for
testifying in court and for entering into certain contracts. As
Yeshua-believers, we should become known for our trustworthiness even beyond
the New Covenant community.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The corresponding <i>mitzvot</i> of Maimonides and HaChinuch are completely
consistent with this <i>Mitzvah</i>, and emphasize that we must not swear an
oath by anything that is created. Maimonides adds to this by saying that if
we swear an oath by any created object in the belief that it has, in itself,
sufficient truth to support the oath, we have put that created object on an
equal footing with the Creator, which is sin. There is no <i>mitzvah</i>
written on the subject by Meir.
