<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The requirement that God's people give to the poor (<i>tz'dakah</i>) is found
throughout the Bible. At the time of Moses, Israel was an agrarian society,
so a major way of providing for the poor was by leaving a portion of each
field and vineyard un-harvested and by not picking up grapes or grain that
fell to the ground. By the time of Yeshua, Israel was still agricultural but
was already moving toward using money for <i>tz'dakah</i>. Today, most of the
world's population does not live in a farm environment, and wages paid in
money has largely replaced being paid in produce and goods. Nevertheless,
the principle of the Mosaic commandments to set aside a part of our increase
for the poor is still God's will.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The first Corinthian Scripture cited above makes mention of setting aside
money for the Apostle Sha'ul to bring to God's people in Jerusalem. In
addition to it being required by <i>Torah</i>, giving <i>tz'dakah</i> is a
time-honored practice in Jewish homes where a <i>tz'dakah</i> box for spare
coins is often in plain view. It is my opinion that <i>tz'dakah</i> should
always be in addition to other giving that is required of us such as our
tithes and other offerings, and it should be noted that, according to
<i>Torah</i>, part of the tithe must also be set aside for the poor.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
