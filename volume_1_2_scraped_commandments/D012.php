<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The Hebrew of <u>Leviticus 23:23:24</u> says that the first day of the
seventh month (<i>Tishrei</i> on the lunar calendar) is a remembrance of
blowing (<i>Zich'ron T'ruah</i>), and <u>Numbers 29:1</u> says it is a day of
blowing (<i>Yom T'ruah</i>); the CJB translation adds the word
"<i>shofar</i>" (a trumpet made from an animal horn) because that was
what was customarily blown in Moses' time to draw the community's attention,
to summon to assembly, and to sound alarms. That is why the first day of
<i>Tishrei</i> is sometimes referred to as the "Day of Trumpets" (<i>Yom
HaShofar</i>), and it is a warning to prepare for the Day of Atonement
(<i>Yom Kippur</i>) that is about to come. It is generally understood that
not everyone in the community need blow a <i>shofar</i>; it is sufficient
that the community cause the <i>shofar</i> to be blown in public for all to
hear.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
In the Jewish world, <i>Yom T'ruah</i> is most commonly known as
<i>Rosh HaShanah</i> (Head of the Year), and considered the "Day of Judgment"
(<i>Yom HaDin</i>) when the "Books of Life" are opened, and we are called to
begin an examination of our lives, with the purpose of repenting of our sins
before the "Books" are again closed and sealed, ten days later, on the "Day
of Atonement" (<i>Yom Kippur</i>). The day is also said, in <i>Talmud</i>,
to be the anniversary of the creation of the world.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Scripture does not indicate the reason for <i>Yom T'ruah</i>, but its
placement 10 days prior to <i>Yom Kippur</i> strongly suggests that it is
intended to begin a count-down of days for personal introspection leading to
<i>Yom Kippur</i>. This theme is reflected in <u><i>Mishna Yoma 1</i></u>
and its related <i>Talmudic Gemara</i>, that explain how the High Priest
(<i>Kohen HaGadol</i>) was sequestered for purification seven days prior to
<i>Yom Kippur</i>; that process would have begun three days from when the
<i>shofar</i> sounded on <i>Yom T'ruah</i>.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This time of introspection from <i>Yom T'ruah</i> to <i>Yom Kippur</i> (known
as the "Days of Awe" or <i>Yamim Nora'iym</i>) should be seen as meaningful
to all Jews and all followers of Yeshua because, according to <u>Exodus
19:6</u>, Israel is a kingdom of priests (albeit not all of Israel are
Levitical priests) and, according to <u>1 Peter 2:9</u> and <u>Revelation
5:10</u>, all New Covenant believers have been made priests as well.
Priestly duties require time spent for personal cleansing, so what better use
is there of the ten day period from when the trumpets sound to <i>Yom
Kippur</i>? That notwithstanding, literal compliance with this
<i>Mitzvah</i>, i.e., to blow the <i>shofar</i>, is mandatory only for Jews
and <i>K'rov Yisrael</i> Gentiles. It is optional for Gentiles generally.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
It is an interesting side-note that the Babylonian <i>Talmud</i> (<u><i>b.
Rosh HaShanah 29b</i></u>) prohibits the <i>shofar</i> from being blown when
<i>Rosh HaShanah</i> falls on a weekly Shabbat. The reason given for this
prohibition is to protect the <i>Shabbat</i> from being violated by someone
who might inadvertently carry his shofar beyond the prescribed distance. My
position is that the <i>Talmudic</i> prohibition is incorrect because it is
in direct contradiction of several Scriptures that require that the shofar be
blown on <i>Yom T'ruah</i> (which, by the way, is itself a <i>Shabbat</i>).
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
We have said much about blowing the <i>shofar</i> on <i>Yom T'ruah</i>, but
Scripture commands that the <i>shofar</i> be blown at other times as well,
such as on the other appointed days, on <i>Rosh Chodesh</i>, on days of
rejoicing, and as an alarm to war. In this last-mentioned time of blowing we
should not limit our understanding of war to just the physical, but also to
times of spiritual warfare, especially when declared by our communities'
leaders. We are also to blow the <i>shofar</i> on days of rejoicing, and the
contrast of war and rejoicing is something to consider as we ponder complying
with <u>1 Thessalonians 5:16-18</u>:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>Always be joyful. Pray regularly. In everything give thanks, for this is
what God wants from you who are united with the Messiah Yeshua.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary by Daniel C. Juster</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
I believe that when the New Covenant Scriptures speak of a heavenly
<i>shofar</i> being sounded upon Yeshua's return, upon the dead being raised,
and upon His disciples (then alive) being transformed and caught up with Him
in the air, they are referring to <i>Yom T'ruah</i> (<u>1 Thessalonians
4:16-17</u>; <u>1 Corinthians 15:51-52</u>). This can also be connected to
an eschatological time of universal repentance, including in Israel
(<u>Zechariah 12:10</u>), that leads to a world-wide "Day of Atonement". If
so, then the days between <i>Yom T'ruah</i> and </i>Yom Kippur</i> have
teaching significance for all followers of Yeshua, and enhanced
responsibility for the Jewish People, and for <i>K'rov Yisrael</i> Gentiles,
who are charged with fulfilling the commandment to blow and hear the sound of
the <i>shofar</i>.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides, Meir, and HaChinuch all refer to <i>Yom T'ruah</i> as <i>Rosh
HaShanah</i> and state their respective <i>mitzvot</i> as our having to hear
the </i>shofar</i> being blown. This no doubt reflects their view (as it is
also mine) that blowing the <i>shofar</i> is the responsibility of the
community, but going to hear them being blown is the responsibility of each
individual.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Of the three, HaChinuch is the most expansive in his discussion of <i>Rosh
HaShanah</i>. He explains that the <i>Mishnah</i> in </i>Talmud</i> tractate
<u><i>b. Rosh HaShanah 2a</i></u> considers the 1<sup><small>st</small></sup>
of <i>Tishrei</i> to be the beginning of the year (for numbering the years),
and that the blowing (or sounding) was determined, by the sages, to be of the
<i>shofar</i> as distinguished from some other musical instrument. He also
speaks of "<i>t'ruah</i>" as a broken sound, which he relates to man's need
of repentance from sin.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
HaChinuch states that any hollow animal horn may be blown on <i>Rosh
HaShanah</i>, whereas Meir states that only the horn of a sheep is
acceptable. They both state that it is obligatory to hear nine sounds of the
<i>shofar</i>, consisting of <i>t'kiah</i> (a single unwavering sound),
<i>t'ruah</i> (a series of short broken sounds), and <i>sh'varim</i> (several
short unwavering sounds). They do not refer to a <i>t'kiah g'dolah</i>.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
