<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides', Meir's, and HaChinuch's positive mitzvot RP9, MP5, and C296,
respectively, all draw, from <u>Leviticus 22:32</u>, the commandment that we
are to sanctify God (or God's Name). Their negative <i>mitzvot</i> RN63,
MN155, and C295, respectively, draw from the same Scripture, the commandment
that we are not to profane (or desecrate) God's Name. All three commentators
say, of their positive <i>mitzvot</i>, that we are to proclaim God to the
world and never deny Him - even at the risk of injury or death. We are also
to refrain from committing sin (especially sins connected to idolatry), since
sin dishonors God and, by inference, denies His holiness.
