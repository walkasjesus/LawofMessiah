<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This <i>Mitzvah</i> re-formulates two of the traditional <i>taryag
mitzvot</i> into a <i>mitzvah</i> having three components. The traditional
<i>mitzvot</i> direct us to (1) sanctify God's Name, and (2) not profane
God's Name. The components of this re-formulated <i>mitzvah</i> direct us to
(1) treat God as holy, (2) proclaim God's holiness, and (3) not profane God's
Name. There is logic to linking them in this way. God proclaims His own
holiness, and requires that we acknowledge it by being obedient to these
three directives. God's holiness encompasses His entire nature that includes
His integrity, purity, trustworthiness, goodness, righteousness, justice,
compassion, love, and all His other attributes.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
There are other ways (in addition to these three) of acknowledging God's
holiness, but the key to them all is holding God holy in our hearts and
thoughts and reminding ourselves of it regularly. That is the purpose of the
third benediction of the <i>Amidah</i> and of the <i>Kedushah</i> - prayers
that are a part of most synagogue services.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The third benediction of the <i>Amidah</i> states:<sup><small><a name=fr1 href=#fn1>1</a></small></sup>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
You are holy and Your Name is holy, and holy ones praise You every day,
forever. Blessed are You, Hashem, the Holy God.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
And the <i>Kedushah</i> (quoting <u>Isaiah 6:3</u>) states:<sup><small><a name=fr2 href=#fn2>2</a></small></sup>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
We shall sanctify Your Name in this world, just as they sanctify It in heaven
above, as it is written by Your prophet, "And one [angel] will call to
another and say: 'Holy, holy, holy is Hashem, Master of Legions, the whole
world is filled with His glory.'
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
And it ends with<sup><small><a name=fr3 href=#fn3>3</a></small></sup>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
From generation to generation we shall relate Your greatness and for infinite
eternities we shall proclaim Your holiness. Your praise, our God, shall not
leave our mouth forever and ever, for You O God, are a great and holy King.
Blessed are You Hashem, the holy God.
