cohen gadol as has been described. And he does sit at the right hand of
HaG&#039;dulah in heaven. There he serves in the Holy Place, that is, in
the true Tent of Meeting, the one erected not by human beings but by ADONAI.
For every cohen gadol is appointed to offer both gifts and sacrifices; so
this cohen gadol too has to have something he can offer. Now if he were on
earth, he wouldn&#039;t be a cohen at all, since there already are cohanim
offering the gifts required by the Torah. But what they are serving is only
a copy and shadow of the heavenly original; for when Moshe was about to
erect the Tent, God warned him, &#039;See to it that you make everything
according to the pattern you were shown on the mountain.&#039;</i>
(see also <u>Hebrews 3:1-2, 4:14-15, 5:4-6, 6:19-20, 9:6-12, 24-26,
10:19-23</u>)
