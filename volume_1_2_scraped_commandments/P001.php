covenant of which Yeshua has become guarantor. Moreover, the present
cohanim are many in number, because they are prevented by death from
continuing in office. But because he lives forever, his position as cohen
does not pass on to someone else; and consequently, he is totally able to
deliver those who approach God through him; since he is alive forever and
thus forever able to intercede on their behalf. This is the kind of cohen
gadol that meets our need - holy, without evil, without stain, set apart
from sinners and raised higher than the heavens; one who does not have the
daily necessity, like the other cohanim g&#039;dolim, of offering up
sacrifices first for their own sins and only then for those of the people;
because he offered one sacrifice, once and for all, by offering up himself.
