term, &#039;new&#039;, he has made the first covenant &#039;old&#039;; and
something being made old, something in the process of aging, is on its way
to vanishing altogether.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This <i>Mitzvah</i> is formulated from Scriptures distributed throughout the
Bible that describe, in detail, how God made Israel (i.e. the Jewish People)
into a "Kingdom of Priests", and later created a New Covenant priesthood with
followers of Yeshua joined to Israel in a "Commonwealth" that conists of
both Jews and Gentiles.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Israel's calling to be a Kingdom of Priests was imparted to it by God
(through Abraham and later through Moses), so all Jews (regardless of their
belief in Messiah) are members of a priestly people. That notwithstanding,
in the New Covenant, only those Jews who believe in Yeshua, i.e. who are
"born of the Spirit" and consequently reside as branches on the <u>Romans
11:16ff</u> olive tree (a tree that belongs to the Jews), can function in
their full power as New Covenant priests; Jews who do not believe in Yeshua
have priestly potential and a continued degree of priestly effectiveness, but
they cannot serve as true New Covenant priests until they come to faith in
Yeshua.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
When Gentiles come to faith, they become grafted into the aforesaid olive
tree and join their Jewish brethren there as co-priests. Although they (the
Gentiles) do not become Jews by virtue of their having been grafted in, the
priesthood they become joined to (sometimes referred to as the "Priesthood of
Believers") is fundamentally a Jewish priesthood. Yet, the Bible makes it
clear that the Jews in this Priesthood retain their Jewish identities and
Jewish covenantal responsibilities.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The origin of all of this goes back to <u>Genesis 12:1-3</u>, where God gave
Avram a covenant whereby through him "<i>all the families of the earth
would be blessed.</i>" Avram's name was later changed to Avraham (Abraham)
and, over several <u>Genesis</u> chapters, we read that God promised that he
would become "<i>the father of many nations</i>", that his wife Sarai (whose
name was changed to Sarah) would bear him a son, and that, through Abraham's
descendants, "<i>all the nations of the earth would be blessed.</i>"
(<u>Genesis 17:3-7, 15-21, 22:10-18, 26:1-5, & 28:10-14</u>).
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The Priestly Covenant given to Abraham was transmitted through Isaac and
later Jacob (<u>Genesis 17:3-7, 26:3-4, 27:29, 28:12-14, & 35:10-12</u>), and
was declared by God (at Mt. Sinai) to the entire Nation of Israel by His
words:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>... you will be a kingdom of cohanim [priests] for me, a nation set
apart</i> (Exodus 19:3-6)
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The Tribe of Levi was set apart from the rest of Israel to serve God in the
Tabernacle (later the Temple), and Aaron and his sons were further set apart
to serve as priests. This became known as the Levitical Priesthood in the
"Order of Aaron" because Aaron was its first High Priest. It was the
functioning priesthood for its time, and the shadow of a heavenly priesthood
(later to be revealed) in which Yeshua the Messiah would be High Priest in
the Order of <i>Malki-Tzedek</i> (<u>Hebrews 3:1-2, 4:14-15, 5:4-6, 6:19-20,
7:17-8:13, 9:6-12, 24-26, & 10:19-23</u>). Levites and Levitical
<i>cohanim</i> (known descendants of Levi and Aaron) continue to have
functions and responsibilities in the synagogues and Jewish Communities of
today. These include performing special duties in the <i>Torah</i> service,
pronouncing blessings over the synagogue assembly, officiating at <i>Pidyon
HaBenim</i>, and several others.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
From the time of Abraham to the present time, all the covenants that God made
were with the Jewish people, and the priesthoods related to them were
Jewish-based priesthoods. The Law given at Mt. Sinai that created and
defined the Levitical Priesthood did not allow Gentiles and non-Levitical
Jews to perform priestly services in the Temple, but the New Covenant opened
the way for both to become priests by joining themselves to the <u>Romans
11</u> "Jewish" olive tree through faith in the "Jewish" Messiah Yeshua.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Just as the Levitical priests had duties related to the Temple's animal
sacrifices, New Covenant priests have duties related to the sacrificed "lamb
of God" Yeshua. In <u>Matthew 28:16-20</u> and parallel Scriptures, we read
that Yeshua directed His disciples (all Jews) to
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>go and make people from all nations into talmidim (disciples), immersing
them into the reality of the Father, the Son and the Ruach HaKodesh, and
teaching them to obey everything [they had been commanded].</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This commandment to preach the "Good News" of Yeshua to the utmost ends of
the earth (commonly referred to as the "Great Commission") is the main
priestly duty of the "Yeshua Priesthood of Believers". It is the New
Covenant counterpart of Temple service but, unlike Temple service, it is open
to (in fact it is required of) all disciples of Yeshua, whether they be Jews
or Gentiles.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Finally, according to <u>Romans 1:16, 2:9-11</u>, "Great Commission" ministry
must be:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>... to the Jew first and then to the Gentile.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This is not because God prefers Jews over Gentiles, but because Jews were
made by God to be His foundational kingdom of priests (<u>Exodus 19:3-6</u>)
who must become spiritually capable to where they are able to support the
"Priesthood of Believers" and bring God's blessings to the Gentile nations of
the world (<u>Genesis 22:16-18</u>). This is the same principle of priestly
preparation that we read about in <u>Leviticus 16:3-6</u>, where Aaron the
High Priest was required to perform a sacrifice for himself and his family
before he could act as a priest for others on <i>Yom Kippur</i>.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
None of the classical commentators (Maimonides, Meir, nor HaChinuch) include,
among their <i>mitzvot</i>, any obligation for Levites to perform priestly
duties outside of the Temple, except those connected to health and blessing
the Jewish People.
