<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
In the Scriptures that underlie this <i>Mitzvah</i>, "bread" represents
ordinary food, and it also represents God's instructions to us (His words)
that are delivered through the Scriptures (the Word of God) and through
Messiah Yeshua who is said by Scripture to be God's Word and the "bread of
life". Although couched in these symbolisms the plain meaning of this
<i>Mitzvah</i> is that we are not to live only by that which sustains us
physically, but also that which sustains us spiritually and brings us closer
to God.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
