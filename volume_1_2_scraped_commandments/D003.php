<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Scripture speaks of keeping the Sabbath Day holy by abstaining from work,
resting physically, and assembling for a holy convocation. Beyond those, it
is well to remember that keeping the Sabbath Day holy means treating it as
special, and setting it apart for God. That does not mean that we cannot
engage in some things that are ordinary or recreational, but we need to use
good judgment and maintain a Sabbath consciousness.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
One way of making the Sabbath special is to keep track of when it begins and
when it ends. Jewish practice ushers in the Sabbath on Friday evening
(before sundown) with the lighting of candles, <i>Kiddush</i>, prayer in
either the home or synagogue, and enjoying a festive meal. There are
traditionally three daytime services (<i>Shachrit</i>, <i>Musaf</i>, and
<i>Minchah</i>) held on <i>Shabbat</i>, and the <i>Shabbat</i> is ended with
<i>Havdalah</i> at sundown, after which begins the first day of the next
work-week (albeit after sundown) with the evening service of <i>ma'ariv</i>.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Within the wider Jewish community (and especially within the Orthodox
community) there are other ways of making the Sabbath special as well. From
the original <i>Torah</i> command to not light fires, Orthodox tradition has
extended the uniqueness of the day to not turning on or off electric
appliances including lights, and not driving cars (spark plugs make fire).
In the same way, <i>Torah</i> is extended to not carrying things on one's
person outside of one's home (not even a bible or <i>siddur</i> to the
synagogue), and not spending money (if one does not carry it, one cannot
spend it). In some communities, exceptions are made for those in the
military and life-saving services.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
We who recognize the New Covenant have an additional depth of appreciation
for the Sabbath's holiness because of what <u>Hebrews 4:4-11</u> says about
it:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>For there is a place where it is said, concerning the seventh day, "And
God rested on the seventh day from all his works." And once more, our
present text says, "They will not enter my rest." Therefore, since it still
remains for some to enter it, and those who received the Good News earlier
did not enter, he again fixes a certain day, "Today," saying through David,
so long afterwards, in the text already given, "Today, if you hear God&#039;s
voice, don&#039;t harden your hearts." For if Y&#039;hoshua had given them
rest, God would not have spoken later of another "day." So there remains a
Shabbat-keeping for God&#039;s people. For the one who has entered
God&#039;s rest has also rested from his own works, as God did from his.
Therefore, let us do our best to enter that rest; so that no one will fall
short because of the same kind of disobedience.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides states that the meaning of <u>Exodus 20:8</u> is that we are to
recite words proclaiming the greatness of the Sabbath day - the
<i>Kiddush</i> at the beginning, and the <i>Havdalah</i> at the end. Meir
agrees with Maimonides about hallowing the Sabbath with those particular
words, and adds that we must recite the <i>Kiddush</i> prayer over either
wine or bread. He also cites <u>Isaiah 58:13</u> in calling the Sabbath a
delight, and states that we must honor God by washing our hands and face with
warm water, dressing in clean clothing, and eating a minimum of three meals.
HaChinuch is in agreement about hallowing the Sabbath with words. He states
that the <i>Kiddush</i> should be said over wine, but that the sages have
allowed it to be said over bread if a man delights in bread more than wine.
That notwithstanding, HaChinuch maintains that <i>Havdalah</i> must be
conducted over wine.
