<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Righteousness is the quality of being morally right or justifiable. A
righteous man always seeks to do what is right but, if he errs in innocence,
he remains righteous because his character in desiring to do right is
unchanged. Similarly but opposite, a man of unrighteous character can stumble
into doing things that are right from time to time but, if his character is
unchanged, he remains unrighteous.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This <i>Mitzvah</i> anticipates times when doing the right thing seem risky
due to possible adverse consequences that we foresee and fear, or at least
would prefer to avoid. At such times of decision we are tempted to disregard
what we know is right and, instead, do what seems safest. The foregoing
Scriptures call us instead to faith, and to three simple steps: (1) Determine
what's right; (2) Do it; (3) Trust God to take care of the consequences.
These steps are exemplified by Esther's heroism in <u>Esther 4:7-16</u>, and
are the natural follow-through from <u>2 Corinthians 5:7</u> that reminds us:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>for we live by trust, not by what we see.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Or, as the New King James Version translates the Greek:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>We walk by faith, not by sight.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The Bible, beginning to end, describes the righteous character of God, and
exhorts us to aspire to be like him - righteous. There are so many
Scriptures supporting this <i>Mitzvah</i> that I have elected to save space
by printing out the text of those from the <i>Torah</i> and the Gospel books,
and only citing the others by chapter and verse.
