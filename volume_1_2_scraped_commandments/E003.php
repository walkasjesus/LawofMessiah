<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This <i>Mitzvah</i> is formulated by joining together two commandments
(directed to the Israelites) that were priesthood-related - the second
reversing the first. Subsequent to God having saved the firstborn sons of
the Israelites when He slew the firstborn males (both sons and cattle) of the
Egyptians (<u>Exodus 11:4-6</u>), He declared also, that all firstborn of the
womb belonged to Him (<u>Exodus 13:1-2</u>, <u>34:19</u>; <u>Numbers
3:13</u>). We understand that to mean that each Israelite family was to
sanctify its firstborn son (i.e., dedicate him to service in the Tabernacle
and later the Temple). After Moses rebuked the Israelites for worshiping the
Golden Calf and only the Levites stepped forward to repent (<u>Exodus
32:25-29</u>), God took the priesthood away from the Israelites as a whole,
and gave it to the Levites (<u>Numbers 1:49-53</u>, <u>3:12</u>), appointing
the sons of Aaron to serve as <i>cohanimi</i> in place of the firstborn sons
of the Israelites (<u>Numbers 3:10</u>). He did this, not by revoking His
previous commandment to sanctify each Israelite firstborn son, but by
commanding that each Israelite family redeem their firstborn son from
priestly service by paying a Levitical cohen five <i>shekalim</i> of money
(<u>Numbers 3:40-51</u>, <u>18:15-16</u>).
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Because underlying this <i>Mitzvah</i> are commandments with which Levites
can no longer comply, I could have easily omitted it as being obsolete. I
chose not to do so, however, because (1) the ceremony of <i>Pidyon HaBen</i>
(redemption of the firstborn son) is performed among many Jewish people
today, and (2) Yeshua's consecration (and possibly His simultaneous <i>Pidyon
HaBen</i>) is recorded in <u>Luke 2:22-32</u>.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
There is no biblically specified ceremony for sanctifying the firstborn son
today, although circumcision may be thought of as a sanctification ceremony
for all male Jewish children, with the firstborn of them assumed to be
specially sanctified. Today, when the firstborn son of a Jewish family (not
a <i>cohen</i> or a Levite) is 30 days old, he is taken to a Levitical
<i>cohen</i> who receives the monetary equivalent of five <i>shekalim</i>
from the father, and then proceeds to bless the child with the <i>Birkat
Cohanim</i> (the Aaronic Benediction). Since Temple <i>shekalim</i> are no
longer in use, their equivalent value in silver is substituted - either
special coins made for the purpose or, in the U.S., five pre-1936 silver
dollars (26.73 grams of 90% silver).
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
It is obvious that this <i>Mitzvah</i> is based upon the assumption of a
functioning Temple priesthood that does not exist today. Nevertheless, I
believe it is important for Messianic Jews to comply ceremonially in the
universal Jewish way for three reasons: (1) without it, there is no closure
regarding the priestly status of firstborn male Israelites beyond their
general priestly identity as Jews; (2) without it, there is ambiguity
regarding the continuing priestly status of today's descendants of Aaron
(i.e. Levitical <i>cohanim</i>); and (3) there is value in our being in
solidarity with the rest of the Jewish world on this. I do not, however,
believe that failing to comply with this <i>Mitzvah</i> in this New Covenant
age is sinful.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The ceremony of <i>Pidyon HaBen</i> only has meaning when performed within
non-Levitical Jewish families since historically, the Levites were not
redeemed and did, therefore, serve in the Temple. Although the ceremony is
traditionally the responsibility of a firstborn son's Jewish father, in
Messianic Judaism it may also be performed by his Jewish mother or by himself
when he comes of age. The <i>Pidyon HaBen</i> ceremony has no historic or
spiritual meaning at all for Gentiles.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Meir states that the <i>Pidyon HaBen</i> is to be performed after thirty days
by Jewish families who are neither <i>cohanim</i> nor Levites and, if a
firstborn son has no father, he must redeem himself after he comes of age;
HaChinuch says this also of a son who has a father, but the father does not
redeem him. Maimonides and HaChinuch agree with Meir on all matters, and
specifically state that the <i>Pidyon HaBen</i> is to be performed by the
father - not by the mother.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Meir quotes Scripture that speaks of our need to sanctify the firstborn of
<i>kosher</i> animals as well, of which we are to say: "This is hereby holy."
He says that if we do not consecrate a firstborn <i>kosher</i> animal it is,
nevertheless, holy. He also says that because there is no Temple, the
firstborn animals must be left to graze until they develop a defect, and must
then be given to a <i>cohen</i>. Both Maimonides and HaChinuch state that
the consecration of cattle is to occur only in the Land of Israel, whereas
Meir is silent on the matter.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
