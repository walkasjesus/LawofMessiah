<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The history of God choosing certain men to set apart as His priests, holy
unto Himself, begins with God's promise to Abraham that through him, all the
Nations of the earth will be blessed" (<u>Genesis 22:18</u>). We see this
continuing in the Mosaic Covenant where Israel is made by God to be a
"kingdom of priests (<i>cohanim</i>), a nation set apart" (<u>Exodus
19:5-6</u>), and Aaron and his sons are installed as priests (<u>Exodus
28:1-3</u>), also "set apart" (<u>Leviticus 21:8</u>). Then, later, in the
New Covenant, Yeshua is made a High Priest (<i>Cohen Gadol</i>) in the order
of Malki-Tzedek (<u>Hebrews 5:4-6</u>), and all disciples of Yeshua are made
<i>cohanim</i> under Him (<i>2 Peter 2:9-10</i>).
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Notwithstanding the change in the covenant (<u>Hebrews 8:13</u>) and our loss
of the Jerusalem Temple, the Judaism that survives today retains a few
functions for those believed to be descended (patrilinearly) from the early
Levitical
<i>cohanim</i>.<sup><small><a name=fr1 href=#fn1>1</a></small></sup>
Among these functions are making <i>aliyot</i> to the <i>bima</i> (going up
to read from the <i>Torah</i> during services), the privilege of redeeming
firstborn Jewish sons (the ceremony of <i>Pidyon HaBen</i>), and imparting
the <i>Birkat Cohanim</i> (Priestly Blessing or Aaronic Benediction) over
synagogue congregants. In Messianic Jewish congregations, the <i>Birkat
Cohanim</i> is generally ministered by the presiding leader of a service in
recognition of the New Covenant's expansion of priestly holiness to all
believers.
</font></p><hr width=150px style="margin-left: 10px">
<p align=left style="margin-left: 30px; margin-right: 30px">
<font size=2>
<a name=fn1 href=#fr1>1.</a>
Historically, <i>cohanim</i> were only men;
today, however, there is a movement within Reform and Reconstructionist
Judaism to extend the status of <i>cohanim</i> to women; this is not
generally accepted within Messianic Judaism.
</font>
</p>
<hr width=150px align=left style="margin-left: 10px">
<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides, Meir, and HaChinuch are unified in stating that a <i>cohen</i>
should be treated with honor befitting his inherited identity. He should be
made to be first in all matters of holiness, the first to be called to
<i>Torah</i>, the first to say the blessing at meals, and the first to be
served. This deference to the <i>cohen's</i> holiness should be made
regardless of the <i>cohen's</i> willingness to accept it.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
