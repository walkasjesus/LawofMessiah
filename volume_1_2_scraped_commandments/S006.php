<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The words "prostitutes" and "whore" are both found in the Bible, and are
similar. A "whore" is a sexually loose woman, and a "prostitute" is a whore
who sells her sexual favors for a price. Used in this way they are nouns,
but both words can also be used as verbs. When they are used as verbs (e.g.
to "whore" or to "prositute" one's self) they are often not referring to
sexual activity but rather to giving or selling oneself against
society&#039;s acceptable norms or God's will (e.g. prostititing onself to
the heathen gods).
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Whereas classically, the <i>Mitzvah</i> prohibiting prostitution alludes only
to a man paying a woman to have sexual intercourse, this <i>Mitzvah</i>
includes the wider prohibition mentioed above, which is most often
exemplified by Israel selling herself through its sinful conduct. That is
why the statement of this <i>Mitzvah</i> is "We are not to participate in
prostitution <u>of any kind or in any way</u>."
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Prostitution, by definition, has to have a price of sale, so what might men's
(or women's) price or gain be for prostituting themselves? It varies from
time to time, but it has typically been power, safety, economic advantage, or
social acceptance.
