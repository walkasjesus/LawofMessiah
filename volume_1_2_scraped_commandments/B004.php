<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Charity, in the Jewish world, is known as <i>tz'dakah</i>. The first thing
to notice about the Scriptures that underlie this commandment is that they do
not limit <i>tz'dakah</i> to giving for, as you can see in <u>Deuteronomy
15:8</u> and <u>Leviticus 25:36</u>, lending to the poor without charging
interest is considered a work of <i>tz'dakah</i> as well. <u>Leviticus
25:35</u> begins by referring to "a member of your people", and charges us
with a duty to help our brother Israelite so that he (and presumably his
family) can continue to live among us, meaning whatever style of living is
par in our community. A charitable burden such as this cannot be complied
with by the average person or family, so it is reasonable that the Scripture
intends a considerable part of it to be fulfilled communally. Also,
although the term "your people" meant fellow Israelites, for us the
commandment should be broadened to mean fellow Jews (be we Jewish), and
fellow brothers in Messiah, be we Jews or not.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The heart of the commandment is that God wants us to take responsibility for
our impoverished brother's welfare and, in order to be able to do so, our
communities must establish some level of financial safety net for its
members. If we do not, brothers on whom hard times fall will have to move
away from the community, and the inevitable result is fractured
relationships, which is not God's intent. The possibility of emergency and
sometimes long-term needs developing within any congregational community
suggests the wisdom of the community establishing a benevolence fund;
administering such a fund, as well as coordinating other resources (both
congregational and personal), is one of the important functions of a
deaconate (<i>shamashim</i>).
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Besides the corporate measures I spoke of, the Scriptures I mentioned suggest
that we have personal responsibility as well. For that reason, we ought to
develop a plan for giving charity that includes setting aside funds, and
opening our homes with which <i>HaShem</i> has blessed us, to provide
emergency sheltering when needed. The degree to which we must funds
and resources from our own families in order to give to others is not clear
from the Scriptures, so we must pray for the Holy Spirit to give us guidance
before (and certainly when) the need to give occurs.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Another thing to notice in the Scriptures is that <u>Leviticus 25:35</u>
implies that we are also to give <i>tz'dakah</i> to those who we do not count
as brothers, for it says "<i>... you are to assist him as you would a
foreigner or a temporary resident.</i>" The way this is put is interesting
because it suggests that we would have no question or hesitation in helping
the foreigner, whereas we need a commandment to motivate us to help those
closer to us. Could it be that we sometimes have less compassion for our
family and peers than we do for strangers?
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Our obligation to a give to others in need is carried over to the New
Covenant Scriptures with such strength that it might as well have been
presented as a commandment there as well. <u>1 John 3:17</u> equates
withholding <i>tz'dakah</i> from our brother with our not loving God, and
<u>James 2:14-17</u> tells us that our charitable acts are an indication of
the level of our faith. <u>Luke 6:38</u> is interesting in its application
of sowing and reaping, but I prefer to not think in those terms because I do
not want to taint any charitable act on my part with even a fleeting thought
of self-interest.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
In addition to it being required by <i>Torah</i>, giving <i>tz'dakah</i> is a
time-honored practice in Jewish homes where a <i>tz'dakah</i> box for spare
coins is often in plain view. It is further my opinion that giving
<i>tz'dakah</i> should always be over and above other giving that is required
of us, including our tithes and other offerings.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides warns us against giving <i>tz'dakah</i> as a way of benefiting
ourselves and suggests that, where possible, it ought to be given
anonymously. Meir has a great deal to say about <i>tz'dakah</i> that is very
motivating. For starters, he makes the statement: "We are duty-bound to be
more careful about the <i>mitzvah</i> of charity than about all the other
positive commandments"<sup><small><a name=fr1 href=#fn1>1</a></small></sup>.
He goes on to say that "charity is a distinguishing characteristic of the
descendants of Abraham"<sup><small><a name=fr2 href=#fn2>2</a></small></sup>, and that the "faith of truth"
and charity are inseparably linked. Maimonides construes the commandment as
our being responsible to give <i>tz'dakah</i> to humanity broadly, while Meir
is more explicit in applying it to Jews. HaChinuch tells us that one of the
values of giving <i>tz'dakah</i> anonymously us to not embarrass the
recipient, and suggests that if a needy person is reluctant to receive it as
a gift, he might, nevertheless, receive it as a loan. He also opines that
even a poor man is obligated to give <i>tz'dakah</i>, and reminds us that
even a normally wealthy man can fall on hard times financially due to illness
and other causes, and may need temporary help, possibly in the way of a loan.
