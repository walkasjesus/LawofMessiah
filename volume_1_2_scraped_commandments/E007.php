<u>Hebrews 7:1-11</u>
<br>
<i>This Malki-Tzedek, king of Shalem, a cohen of God Ha&#039;Elyon, met Avraham
on his way back from the slaughter of the kings and blessed him; also
Avraham gave him a tenth of everything. Now first of all, by translation of
his name, he is "king of righteousness"; and then he is also king of Shalem,
which means "king of peace." There is no record of his father, mother,
ancestry, birth or death; rather, like the Son of God, he continues as a
cohen for all time. Just think how great he was! Even the Patriarch Avraham
gave him a tenth of the choicest spoils. Now the descendants of Levi who
became cohanim have a commandment in the Torah to take a tenth of the income
of the people, that is, from their own brothers, despite the fact that they
too are descended from Avraham. But Malki-Tzedek, even though he was not
descended from Levi, took a tenth from Avraham. Also, he blessed Avraham,
the man who received God&#039;s promises; and it is beyond all dispute that
the one who blesses has higher status than the one who receives the
blessing. Moreover, in the case of the cohanim, the tenth is received by
men who die; while in the case of Malki-Tzedek, it is received by someone
who is testified to be still alive. One might go even further and say that
Levi, who himself receives tenths, paid a tenth through Avraham; inasmuch as
he was still in his ancestor Avraham&#039;s body when Malki-Tzedek met him.
Therefore, if it had been possible to reach the goal through the system of
cohanim derived from Levi (since in connection with it, the people were
given the Torah), what need would there have been for another, different
kind of cohen, the one spoken of as to be compared with Malki-Tzedek and not
to be compared with Aharon?</i>
