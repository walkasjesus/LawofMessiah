<font size=4 face="Times New Roman, Times, serif">
<u>Deuteronomy 14:22-29</u>
(Maimonides RP128, RP130; Meir ML14-ML15; Chinuch
C473-C474)
<br>
<i>Every year you must take one tenth of everything your seed produces in the
field, and eat it in the presence of ADONAI your God. In the place where he
chooses to have his name live you will eat the tenth of your grain, new wine
and olive oil, and the firstborn of your cattle and sheep, so that you will
learn to fear ADONAI your God always. But if the distance is too great for
you, so that you are unable to transport it, because the place where ADONAI
chooses to put his name is too far away from you; then, when ADONAI your God
prospers you, you are to convert it into money, take the money with you, go
to the place which ADONAI your God will choose, and exchange the money for
anything you want - cattle, sheep, wine, other intoxicating liquor, or
anything you please - and you are to eat there in the presence of ADONAI
your God, and enjoy yourselves, you and your household. But don&#039;t
neglect the Levi staying with you, because he has no share or inheritance
like yours. At the end of every three years you are to take all the tenths
of your produce from that year and store it in your towns. Then the Levi,
because he has no share or inheritance like yours, along with the foreigner,
the orphan and the widow living in your towns, will come, eat and be
satisfied - so that ADONAI your God will bless you in everything your hands
produce.</i>
