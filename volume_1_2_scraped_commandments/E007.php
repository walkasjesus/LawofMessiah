<u>Deuteronomy 15:7-11</u>
<br>
<i>If someone among you is needy, one of your brothers, in any of your towns in
your land which ADONAI your God is giving you, you are not to harden your
heart or shut your hand from giving to your needy brother. No, you must
open your hand to him and lend him enough to meet his need and enable him to
obtain what he wants. Guard yourself against allowing your heart to
entertain the mean-spirited thought that because the seventh year, the year
of sh&#039;mittah is at hand, you would be stingy toward your needy brother
and not give him anything; for then he may cry out to ADONAI against you,
and it will be your sin. Rather, you must give to him; and you are not to
be grudging when you give to him. If you do this, ADONAI your God will bless
you in all your work, in everything you undertake - for there will always be
poor people in the land. That is why I am giving you this order, &#039;You
must open your hand to your poor and needy brother in your land.&#039;</i>
