<font size=4 face="Times New Roman, Times, serif">
<u>Exodus 30:13-14</u>
(Maimonides RP171; Chinuch C105)
<br>
<i>Everyone subject to the census is to pay as an offering to ADONAI half a
shekel [one-fifth of an ounce of silver] - by the standard of the sanctuary
shekel (a shekel equals twenty gerahs). Everyone over twenty years of age
who is subject to the census is to give this offering to ADONAI</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Leviticus 27:30-33</u>
(Maimonides RP78, RP128, RN109; Chinuch C360-C361, C473)
<br>
<i>All the tenth given from the land, whether from planted seed or fruit from
trees, belongs to ADONAI; it is holy to ADONAI. If someone wants to redeem
any of his tenth, he must add to it one-fifth. All the tenth from the herd
or the flock, whatever passes under the shepherd&#039;s crook, the tenth one
will be holy to ADONAI. The owner is not to inquire whether the animal is
good or bad, and he cannot exchange it; if he does exchange it, both it and
the one he substituted for it will be holy; it cannot be redeemed.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Numbers 18:20-32</u>
(Maimonides RP127, RP129; Meir ML12-ML13; Chinuch
C395-C396)
<br>
<i>ADONAI said to Aharon, "You are not to have any inheritance or portion in
their land; I am your portion and inheritance among the people of
Isra&#039;el. To the descendants of Levi I have given the entire tenth of
the produce collected in Isra&#039;el. It is their inheritance in payment
for the service they render in the tent of meeting. From now on, the people
of Isra&#039;el are not to approach the tent of meeting, so that they will
not bear the consequences of their sin and die. Only the L&#039;vi&#039;im
are to perform the service in the tent of meeting, and they will be
responsible for whatever they do wrong. This is to be a permanent regulation
through all your generations. They are to have no inheritance among the
people of Isra&#039;el, because I have given to the L&#039;vi&#039;im as
their inheritance the tenths of the produce which the people of Isra&#039;el
set aside as a gift for ADONAI. This is why I have said to them that they
are to have no inheritance among the people of Isra&#039;el." ADONAI said
to Moshe, Tell the L&#039;vi&#039;im, &#039;When you take from the people of
Isra&#039;el the tenth of the produce which I have given you from them as
your inheritance, you are to set aside from it a gift for ADONAI, one tenth
of the tenth. The gift you set aside will be accounted to you as if it were
grain from the threshing-floor and grape juice from the wine vat. In this
way you will set aside a gift for ADONAI from all your tenths that you
receive from the people of Isra&#039;el, and from these tenths you are to
give to Aharon the cohen the gift set aside for ADONAI. From everything
given to you, you are to set aside all that is due ADONAI, the best part of
it, its holy portion.&#039; Therefore you are to tell them, &#039;When you
set aside from it its best part, it will be accounted to the
L&#039;vi&#039;im as if it were grain from the threshing-floor and grape
juice from the wine vat. You may eat it anywhere, you and your households;
because it is your payment in return for your service in the tent of
meeting. Moreover, because you will have set aside from it its best parts,
you will not be committing any sin because of it; for you are not to profane
the holy things of the people of Isra&#039;el, or you will die.&#039;</i>
