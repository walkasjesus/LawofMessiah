<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Remarkably, none of the classical commentators include obeying God's
Commandments as one of their 613 <i>Mitzvot</i>. One might speculate that
the reason for the omission is that the very existence of God's
<i>Mitzvot</i> makes our need to obey them self-evident, but then why did God
command our obedience so many times? The only <i>mitzvot</i> that touch on
obeying God are Maimonides' <i>mitzvah</i> #RP172, and HaChinuch's
corresponding <i>mitzvah</i> #C516. In those <i>mitzvot</i>, they interpret
<u>Deuteronomy 18:15-19</u> as our needing to listen to and obey all of God's
prophets even if they instruct us to temporarily do the opposite of a
commandment of Scripture. Of course I do not agree with that interpretation,
and instead believe that the Scripture prophetically refers to only one
prophet - Yeshua - who had yet to come at the time that the Commandment was
given, but who has since come. Meir does not have a corresponding
<i>mitzvah</i> to #RP172 and #C516.
