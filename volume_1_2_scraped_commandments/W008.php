<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Christian theologians have often misinterpreted (and sometimes mistranslated)
the <i>Shaliach</i> Paul's writings to be saying that the <i>mitzvot</i>
(commandments of the <i>Torah</i>) - the laws - the statutes, ordinances, and
judgments given at Mt. Sinai - were rendered uselessly obsolete by Yeshua.
Those who hold that view teach that God's law has been replaced by His grace,
so that if we love God and one another, none of the specifics of the "Mosaic
Law" matter
anymore.<sup><small><a name=fr1 href=#fn1>1</a></small></sup>
Such views are in direct contradiction of Yeshua&#039;s words in <u>Matthew
5:19</u>, which says:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>So whoever disobeys the least of these mitzvot and teaches others to do so
will be called the least in the Kingdom of Heaven. But whoever obeys them and
so teaches will be called great in the Kingdom of Heaven.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Also problematic, is the mistranslation (of <u>verse 17</u> of many Bible
versions) of the Greek word
<img src=images/word-pleyrosai-1.gif align=top height=25>, that is commonly
but incorrectly rendered "fulfill":
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>Do not think that I came to destroy the Law or the Prophets. I did not
come to destroy but to fulfill.</i> (NKJ)
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The word in this context that should actually be translated "fill" (the CJB
translates it "complete"), and these numerous mistranslations have fueled
antinomians to teach that no part of God's Law given at Mount Sinai is to be
obeyed today because Yeshua fulfilled it in
Himself.<sup><small><a name=fr2 href=#fn2>2</a></small></sup>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
There is another Scripture whose misunderstanding has caused no little amount
of controversy, and that is <u>Matthew 19:17</u>, in which Yeshua tells a
man:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>But if you want to obtain eternal life, observe the mitzvot.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Yeshua seems to be saying that salvation comes from a legalistic observance
of the law, whereas we know from <u>Galatians 2:16</u> (as well as other
Scriptures) that:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>... a person is not declared righteous by God on the ground of his
legalistic observance of Torah commands, but through the Messiah Yeshua's
trusting faithfulness.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The answer to this seeming contradiction is, of course, that <u>Deuteronomy
18:15-19</u> of the Mosaic <i>Torah</i> prophesies the coming of Messiah
Yeshua, and commands us to listen to Him and obey Him, as he will be speaking
in God's Name. So, to obey the <i>Torah</i>, is also to receive and obey the
<i>mitzvot</i> of Yeshua.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
