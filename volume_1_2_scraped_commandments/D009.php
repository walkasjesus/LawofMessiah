<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
I believe the two reasons given in <u>Numbers 9:10</u> for delaying Passover
observance are not the only two intended, but are rather meant to suggest
others as well that are equally valid. For example, if a Jewish man or woman
cannot observe Passover on the 14<sup><small>th</small></sup> of <i>Nisan</i>
due to illness, he or she must earnestly seek to do so a month later on the
14<sup><small>th</small></sup> of <i>Iyar</i>.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
I consider this <i>mitzvah</i> mandatory for individual Jews and for <i>K'rov
Yisrael</i> Gentiles who find themselves in the position of not being able to
fulfill their Passover observance obligation on the
14<sup><small>th</small></sup> of <i>Nisan</i>. Other Gentiles, however,
should they undertake this <i>Mitzvah</i> would, in my opinion be wrongly
stating that meticulous Passover observance is an obligation as to them.
For that reason, I consider literal compliance of this <i>Mitzvah</i> to be
unauthorized and not recommended for Gentiles who are not <i>K'rovei
Yisrael</i>.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
