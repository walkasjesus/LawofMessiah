<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Adultery is sexual intimacy between a married person and a person who is not
his or her spouse. The Scriptures on adultery in the <i>Tanakh</i> refer
only to physical intimacy, but it is broader than that in the New Covenant,
and even includes lusting after someone of the opposite sex, and divorcing
without biblical grounds (<u>Matthew 5:27-28</u>).
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Adultery is also mentioned in the New Covenant Scriptures as (arguably) the
only reason that one may divorce a spouse (Some include abandonment and cite
<u>1 Corinthians 7:15</u>). The Scriptures usually quoted to support this
are <u>Matthew 5:32</u> and <u>Matthew 19:9</u>, which speak of fornication
and sexual immorality; fornication during marriage is adultery.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Adultery is one of the foundational prohibitions in the Bible because it
negates the marital covenant of two becoming one (<u>Genesis 2:24</u>;
<u>Matthew 19:4-5</u>; <u>Mark 10:7-8</u>; and <u>Ephesians 5:31</u>). Not
often discussed, however, is <u>verse 32</u> that follows <u>Ephesians
5:31</u>:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>There is profound truth hidden here, which I say concerns the Messiah and
the Messianic Community.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Some translations call it a profound mystery, so what might the mystery be?
I believe that it refers to a husband-wife relationship that exists between
Messiah and the Messianic Community,<sup><small><a name=fr1 href=#fn1>1</a></small></sup>
a relationship within which adultery can be committed in the same way as
with married couples.
</font></p><hr width=150px style="margin-left: 10px">
<p align=left style="margin-left: 30px; margin-right: 30px">
<font size=2>
<a name=fn1 href=#fr1>1.</a>
Variously called the
"Commonwealth of Israel", the "Body of Believers", and the "Church".
