<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Different corrective considerations apply according to whether a brother's
sin is against another brother (private offense) or against the community
broadly (public offense). Also, that which applies to a brother does not
necessarily apply to unbelieving neighbors because unbelievers have not
committed themselves to the principles and disciplines of Scripture (<u>1
Corinthians 5:12-13</u>).
<p align=center>
<b>A. RESPONDING TO THE PRIVATE OFFENSE COMMITTED BY A BROTHER</b>
</p>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b>OVERLOOKING A SIN</b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Even when we are authorized to seek redress for a sin committed against us,
we need not do so, for <u>Proverbs 19:11</u> states:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>People with good sense are slow to anger, and it is their glory to
overlook an offense.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
And <u>Ecclesiastes 7:21-22</u> states:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>Also, don't take seriously every word spoken, such as when you hear your
servant speaking badly of you; because often, as you yourself know, you have
spoken badly of others.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This is in keeping with God's desire that we take upon ourselves His holy
nature, for He Himself has been known to overlook sin (<u>Acts
17:29-30</u>).<sup><small><a name=fr1 href=#fn1>1</a></small></sup>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Some private offenses are easily overlooked - especially those that are
unintentional, have done no great harm, and are unlikely to be repeated.
Although it may seem that overlooking an offense is always the loving thing
to do, it is not necessarily so. One's decision should always be based upon
what is best for the offender, and what is best for others against whom the
offender may sin in the future if he is not made accountable. If the offense
is overlooked, it should not be brought up again unless the offense is
repeated. Also, overlooking a sin does not assure that the parties will
remain in, or return to, reconciliation.
</font></p><hr width=150px style="margin-left: 10px">
<p align=left style="margin-left: 30px; margin-right: 30px">
<font size=2>
<a name=fn1 href=#fr1>1.</a>
Consider also, how Moses pleaded with God to
overlook the sins of Israel (<u>Deuteronomy 9:27</u>).
</font>
</p>
<hr width=150px align=left style="margin-left: 10px">
<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b>GOING TO OUR OFFENDING BROTHER</b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Much is said about rebuking and correcting in both the <i>Tanach</i> and the
New Covenant Scriptures. <u>Leviticus 19:17-18</u> is generally cited by the
classical Jewish commentators as the Scripture that commands this, but there
are many others that apply as well, as one can see from the large number of
Scriptures cited in this <i>Mitzvah</i>. Another key Scripture from the
<i>Tanach</i> is <u>Proverbs 27:5</u>:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>Better open rebuke than hidden love.</i> (See also, <u>Proverbs 9:8-9</u>)
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
If our decision is to not overlook an offense, we must confront the offending
brother with his sin:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Matthew 18:15</u>:&nbsp;&nbsp;
<i>Moreover, if your brother commits a sin against you, go and show him his
fault - but privately, just between the two of you. If he listens to you,
you have won back your brother.</i>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Galatians 6:1-2</u>:&nbsp;&nbsp;
<i>Brothers, suppose someone is caught doing something wrong. You who have
the Spirit should set him right, but in a spirit of humility, keeping an eye
on yourselves so that you won't be tempted too. Bear one another's burdens -
in this way you will be fulfilling the Torah's true meaning, which the
Messiah upholds.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
There are three things in the foregoing Scriptures that stand out. First, we
are to go to our brother alone. That means we are not to share our complaint
with others before first giving our brother the opportunity to repent and
make things right. This principle assumes two things: (1) The parties are
relatively equal in their ability to deal with one another, and (2) There is
no impropriety in the parties meeting privately. An example of inequality
would be a child having to confront an adult; an example of improper privacy
would be a male and female meeting alone to confront one another concerning
sexual sin. In these and similar cases, the Scriptures should be interpreted
broadly enough to permit chaperoning and for allowing the weaker of the two
adversaries to be accompanied by a suitable protector.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Second, when we go to our brother to tell him his fault, our attitude must be
pure, and our demeanor proper. The operative expression in the
<u>Galatians</u> Scripture is "<i>a spirit of humility</i>", and our motive
for going must be to reconcile and not to extract a confession of fault.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Third, our purpose for confronting our brother must be to restore him to
righteousness, and our hope must be for reconciliation. Although in cases
involving damage or loss we may seek restitution as part of the
reconciliation process, we are not to sue our brother in a secular court
(<u>1 Corinthians 6:1-7</u>).
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b>RETURNING WITH WITNESSES</b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
If the offending brother agrees with our complaint and repents, the matter
is, of course, concluded, and we forgive him. If he does not agree or refuses
to meet privately, we must then elect whether to pursue the matter further,
or to belatedly overlook his sin (<u>Proverbs 19:11</u>). Although
overlooking the sin at this juncture is possible, its appropriateness has to
be questioned. If there remains un-reconciliation, we should go to our
brother again and, this time, bring one or two witnesses:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Matthew 18:16</u>:&nbsp;&nbsp;
<i>If he doesn't listen, take one or two others with you so that every
accusation can be supported by the testimony of two or three witnesses.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
These need not be witnesses to the original offense complained of (although
they may be), but rather to our second meeting with our brother.<sup><small><a name=fr2 href=#fn2>2</a></small></sup>
Our usual concept of witnesses is that they are silent observers. In this
case, however, <u>Matthew 18:17</u>, authorizes the witnesses to determine
which of the parties to the dispute is wrong, and to urge that person to
listen to reason and repent:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Matthew 18:17a</u>:&nbsp;&nbsp;
<i>If he refuses to hear them ...</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
As before, if the parties come to agreement or the offending brother repents,
the matter is concluded. If however, the attempt at reconciliation is
unsuccessful, the matter must be brought to the <i>ekklesia</i> (assembly)
for adjudication (see <u>Matthew 18:17c</u> below).
</font></p><hr width=150px style="margin-left: 10px">
<p align=left style="margin-left: 30px; margin-right: 30px">
<font size=2>
<a name=fn2 href=#fr2>2.</a>
Although not addressed in Scripture, fairness probably allows the
offending brother to invite his own witnesses to the meeting as well.
</font>
</p>
