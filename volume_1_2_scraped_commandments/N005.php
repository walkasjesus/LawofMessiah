<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b>BRINGING THE DISPUTE TO A <i>BET DIN</i> OF ELDERS</b>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Matthew 18:17b</u>:&nbsp;&nbsp;
<i>... tell the congregation</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The word "congregation" in the CJB translation ("church" in some
translations) is <img src=images/N005.gif align=top> (<i>ekklesia</i>) in the
Greek text, which means "a gathering", "an assembly" (for worship), or "a
deliberative council". According to <u>Matthew 18:17b</u>, if the
complainant and the witnesses are unsuccessful in convincing the offender to
repent, the matter is to be brought to the <i>ekklesia</i> for adjudication.
Since the entire <i>ekklesia</i> (the body of believers) is too large a group
to assemble for hearing and judging cases, it is the prevailing view that a
court (<i>bet din</i>) of assembled elders (reminiscent of <u>Deuteronomy
17:8-13</u>) acts on behalf of the <i>ekklesia</i>.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b>JUDGMENT OF THE ELDERS</b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
After the elders of a <i>bet din</i>render their judgment, the party ruled
against is required to repent and comply with any orders issued, including
orders of restitution. If he refuses to do so, he commits a new offense -
that of disobeying the lawful orders of the tribunal. This new offense is
public in nature because the disobedience is against the public
authority.<sup><small><a name=fr3 href=#fn3>3</a></small></sup> When this occurs, the judging elders are required to inform
the body of believers of the respondent's disobedience, and order that he
henceforth be treated as one who is no longer a believer:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Matthew 18:17c</u>:&nbsp;&nbsp;
<i>... and if he refuses to listen even to the congregation, treat him as
you would a pagan or a tax-collector.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This is sometimes referred to as a decree of "disfellowship" or
"excommunication".
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
It is a common misunderstanding that when a brother is excommunicated
pursuant to <u>Matthew 18</u>, he must necessarily be ejected from the
congregation. On the contrary, after a judgment of excommunication, unless
he is also disruptive, the "former" brother should be treated as an
unbeliever in every way and we do not eject unbelievers. Instead, unless he
is also a violator of <u>1 Corinthians 5:9-13</u> (walking in immorality
while calling himself a believer) or <u>Titus 3:10-11</u> (being divisive or
otherwise harmful to the body), he should be encouraged to attend
congregational services and other events where he is likely to hear the Word
of God and be encouraged to repent. One important consequence of a
<u>Matthew 18</u> excommunication is that the complainant is released from
the constraint of <u>1 Corinthians 6:1-7</u> and is free to sue the
unrepentant respondent in a public court.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Another common misunderstanding is that a decree of excommunication applies
only to the excommunicating congregation or denomination. Biblically, that
is not so. Judgments arising from <u>Matthew 18</u> proceedings apply across
the entire body of believers and, so long as correct biblical doctrine is
applied and due process is afforded, all Yeshua-believing congregations
should recognize and comply with judgments rendered by reputable <i>batei
din</i>. What is especially ominous, is that God Himself recognizes
<i>batei din</i> judgments (<u>Matthew 18:18-20</u>).
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Unfortunately, the current state of the body of believers is such that a
valid excommunication by one congregation's or church's ecclesiastic court is
often ignored by other congregations, the result of which is that
excommunicated persons are often received into a believing community without
being required to repent. The body of believer needs to correct this.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
It is important to appreciate that a brother who is accused of sin cannot be
forced to attend or to cooperate with any of the <u>Matthew 18</u> meetings.
However, if he refuses to meet or to attend, he does so at his peril, because
his refusal elevates the controversy to its next level without anyone being
able to hear his defense or point of view. If he refuses to attend or
cooperate at the last (<i>bet din</i>) stage, evidence against him will
likely be presented in his absence, with predictable results.
</font></p><hr width=150px style="margin-left: 10px">
<p align=left style="margin-left: 30px; margin-right: 30px">
<font size=2>
<a name=fn3 href=#fr3>3.</a>
See section below: "Responding to a Public
Offense."
</font>
</p>
<hr width=150px align=left style="margin-left: 10px">
<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b>RESTORING A BROTHER TO FELLOWSHIP</b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
A judgment of "disfellowship" is reversible in the same way as the status of
being an unbeliever is reversible. What is required is that the sanctioned
brother repent of his former sin, comply with all orders of the convicting
<i>bet din</i>, and receive Yeshua again as his Lord and Savior. Ideally, the
same <i>bet din</i> that ruled previously is convened to judge the repentance
and, if it is deemed genuine, the tribunal sets aside its prior judgment and
publishes a decree of restoration. This restoration, which is declared on
earth, is also recognized in heaven:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Matthew 18:18-20:</u>&nbsp;&nbsp;
<i>Yes! I tell you people that whatever you prohibit on earth will be
prohibited in heaven, and whatever you permit on earth will be permitted in
heaven. To repeat, I tell you that if two of you here on earth agree about
anything people ask, it will be for them from my Father in heaven. For
wherever two or three are assembled in my name, I am there with them.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b>THE "JUDGE-ME-NOT" DEFENSE</b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Believers who seek to avoid correction often quote <u>Matthew 7:1</u>:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>Don't judge, so that you won't be judged.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The full context of this Scripture is, however, rarely quoted; it continues
with <u>verses 2-5</u>:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>For the way you judge others is how you will be judged - the measure with
which you measure out will be used to measure to you. Why do you see the
splinter in your brother's eye but not notice the log in your own eye? How
can you say to your brother, 'Let me take the splinter out of your eye,' when
you have the log in your own eye? You hypocrite! First, take the log out of
your own eye; then you will see clearly, so that you can remove the splinter
from your brother's eye!</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
So, the Scripture does not prohibit judging one's brother; it merely exhorts
us to repent of our own sins first. There is, in addition, another
Scripture, <u>1 Corinthians 5:12</u>, that specifically authorizes brothers
to judge each other; it reads:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>For what business is it of mine to judge outsiders? Isn't it those who are
part of the community that you should be judging?</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b>WHEN THE OFFENDER IS US!</b>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Matthew 5:23-24</u>:&nbsp;&nbsp;
<i>So if you are offering your gift at the Temple altar and you remember
there that your brother has something against you, leave your gift where it
is by the altar, and go, make peace with your brother. Then come back and
offer your gift.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
If we know or suspect that we have sinned against a brother, we are not to
wait until the brother comes to us - we are to go to him. Noted author Jay
Adams has been known to say that ideally, the offender and the offended
should meet in the street between their respective homes, each on his way to
seek reconciliation with the other.
<p align=center>
<b>B. RESPONDING TO A PUBLIC OFFENSE COMMITTED BY A BROTHER</b>
</p>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
A public offense is a sin committed by an individual against the community
broadly, i.e., against the local congregation, or believers at large. This
kind of offense is the biblical analogy of a criminal offense in secular law.
Public offenses pose a danger to the community, so the primary responsibility
of the community's leaders is to protect the flock by ministering correction
to the offender. The goal here is different than in the case of private
offenses, where promoting reconcil­iation through the <u>Matthew 18</u>
process is the primary purpose. The following verses of Scripture reveal how
the <i>shaliach</i> Paul dealt with one such public offense:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>1 Corinthians 5:1-2</u>:&nbsp;&nbsp;
<i>It is actually being reported that there is sexual sin among you, and it
is sexual sin of a kind that is condemned even by pagans - a man is living
with his stepmother! And you stay proud? Shouldn't you rather have felt some
sadness that would have led you to remove from your company the man who has
done this thing?</i>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>1 Corinthians 5:5</u>:&nbsp;&nbsp;
<i>... hand over such a person to the Adversary for his old nature to be
destroyed, so that his spirit may be saved in the Day of the Lord.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b>PROSECUTING PUBLIC OFFENSES</b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
An important difference between public and private offenses is that, in the
case of public offenses, it is the community's guardians - its elders - who
are responsible for protecting the community by correcting wrongs, and
administering justice (<u>Hebrews 13:17</u>). That notwithstanding, an
individual congregant is often the first person to become aware that a public
offense has or may have been committed. Although, according to <u>Galatians
6:1-2</u> he may confront the perpetrator for the purpose of ministering
correction and urging his brother to repent, he is not authorized to act for
the community in either judging the matter or deciding what remedial action
should be taken. Therefore, in all but trivial cases, the
congregant-citizen's duty is to report the suspected offense, or the evidence
thereof, to the community's elders<sup><small><a name=fr4 href=#fn4>4</a></small></sup>.<sup><small><a name=fr5 href=#fn5>5</a></small></sup>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
In my opinion, the procedure of choice for prosecuting public offenses
should, where possible, be a modification of the <u>Matthew 18</u> process
previously discussed. In the case of public offenses, however, the
complainant is not an individual; rather, it is the community, so the ones to
confront are one or more elders representing the community, analogous to
<u>Matthew 18:15</u>. If the initial confrontation does not produce an
adequate result, the next step is to meet again, this time taking one or two
additional witnesses (who should also be elders, analogous to <u>Matthew
18:16</u>). If, after this meeting, the elder delegation believes that the
accused has committed sin and the matter is not resolved, then, just as in
the case of a private offense, a formal <i>bet din</i> of the <i>ekklesia</i>
is convened. This conclave consists of the elders who were the witnesses,
preferably joined by other elders, analogous to <u>Matthew 18:17</u>. Then,
after hearing, if the <i>bet din</i> rules against the accused, it is to take
whatever measures it deems appropriate for enabling justice and protecting
the community.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
It is also my opinion, when an individual has reason to believe that a
brother in the faith has committed a significant trespass against the
criminal law of society, a prudent and covenantal first step is to consult
elders of the body for their wisdom in how to proceed. Nevertheless, the
individual may inform the secular governing authorities directly but, if he
does, his reason for doing so should be that it is required for public
safety, and to comply with <u>Romans 13:1-2</u>. <u>1 Corinthians 6:1-7</u>
does not apply in this case, because that Scripture only prohibits brothers
suing brothers for redress of <i>private</i> grievances; this would not be a
private grievance.
</font></p><hr width=150px style="margin-left: 10px">
<p align=left style="margin-left: 30px; margin-right: 30px">
<font size=2>
<a name=fn4 href=#fr4>4.</a>
A gentle covenantal approach
to doing this is to urge the offender to go to the elders himself and
confess his transgression. If he is unwilling to do so, he should be
invited to be present when the informant discloses to the
elders.
</font>
</p>
<p align=left style="margin-left: 30px; margin-right: 30px">
<font size=2>
<a name=fn5 href=#fr5>5.</a>
According to <u>Deuteronomy 19:15</u>, one may not
bring an accusation, except by the testimony of two or three witnesses.
This does not preclude one from informing the elders that an offense may
have been committed; it does, however, define the level of proof needed for
verdict of "guilty".
</font>
</p>
<hr width=150px align=left style="margin-left: 10px">
<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b>PUNISHING FOR PUBLIC OFFENSES</b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The only proper response for having committed a public offense is to repent
and obey all remedial orders issued by a properly constituted <i>bet din</i>.
Although there are a myriad of possible public offenses, there are two
categories of them which, if not repented of, result in separation from the
body of believers; the two categories are:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
(a) sins that can lead to excommunication
<br>
(b) sins that can lead to being removed from fellowship.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The first of these categories (a) has already been discussed: It is refusing
to repent for a sin after being ordered to do so by a <i>bet din</i> in the
aftermath of a judicial proceeding. As previously explained, excommunication
is a judgment by a court of elders that a person, once considered to be a
brother in the faith, is no longer so. It is important to mention here that
excommunication does not result from the underlying sin itself, but from the
disobedience of refusing to repent after being ordered to do so.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The second of these categories (b) consists of five behaviors for which a
person must be banned from fellowship with believers:
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<ol>
<li> A believer, who is walking in unrepentant immorality (<u>1 Corinthians
5:9-13</u>).
<li> A believer, who is in gross doctrinal error, and not in accord with
the Scripture and with Apostolic doctrine (<u>2 John 1:9-11</u>,
<u>Galatians 1:8-9</u>).
<li> A believer who is disorderly in his lifestyle (<u>2 Thessalonians
3:6-15</u>):
<li> A believer who is divisive and has been warned twice (<u>Titus
3:10-11</u>; <u>Romans 16:17</u>).
<li> An unbeliever who is rebellious and scoffs at the Word of God
(<u>Proverbs 22:10</u>).
</ol>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
In the first three cases, the danger to the body comes from the offender
holding himself out, or being considered to be, part of the community - a
believer, yet being in significant sin, professing heretical doctrines, or
walking in a disorderly manner. While these traits are understandable in
unbelievers, a believer (or professing believer) with such characteristics
brings discredit to the body. Such behaviors can also present a stumbling
block for new members of a believing community who are not yet fully
discipled, and whose discernment is not yet fully developed.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The fourth and fifth cases on the list apply to persons who sow discord,
whether or not they are believers, and it makes no difference that the
rebelliousness or divisiveness is unconscious or unintended. They are both
dangerous and contagious in a community of believers, and those who practice
them and do not stop must be excluded.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
In the first four cases, repentance restores the offending brother to
fellowship, and in the fifth case, the unbeliever to again being welcome.
Restoration is not automatic, however, for whether or not an offender's
repentance is genuine is a matter to be judged by the same <i>bet din</i>
that judged and disciplined him originally.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b>ACCUSATIONS AGAINST ELDERS</b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Some contend that Scripture establishes a more stringent standard for
bringing an accusation against an elder and, for their authority, they refer
to <u>1 Timothy 5:19-20</u>:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>Never listen to any accusation against a leader unless it is supported by
two or three witnesses. Rebuke before the whole assembly those leaders who
continue sinning, as a warning to the others.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Actually, the foregoing Scripture establishes the same standard for bringing
an accusation against an elder as does <u>Deuteronomy 19:15</u> for bringing
an accusation against anyone else. A plausible explanation for why there
exists a special Scripture for elders, is that the verses which precede <u>1
Timothy 5:19</u> speak of the awesome responsibility which leaders have to
their flock. The flock is therefore exhorted to obey their leaders so as to
assist them in performing their functions with joy. Because leaders are
highly visible and their actions sometimes controversial, they are especially
vulnerable to accusations and criticisms in the form of whispers or gossip.
These are damaging to an elder's reputation and ability to lead, and so the
above verses of Scripture are a reminder to us that we must apply to our
elders the same high standard that we apply when we bring public accusations
against others.
<p align=center>
<b>C. REBUKING AND CORRECTING A BROTHER EVEN WHEN THERE IS NO OFFENSE
COMMITTED AGAINST US</b>
</p>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Leviticus 19:17</u>:&nbsp;&nbsp;
<i>Do not hate your brother in your heart, but rebuke your neighbor
frankly, so that you won't carry sin because of him.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This Scripture infers that we have a responsibility to rebuke and correct our
neighbor brother who is in sin, even when the sin is not directed against us.
It could be for our brother's sake alone, and the Scripture is clear that, if
we do not rebuke and correcthim, we will be in sin as well (see also,
<u>Galatians 6:1-2</u>; <u>James 5:16b</u>).
<p align=center>
<b>D. BRINGING CORRECTION TO PERSONS OUTSIDE OF THE BELIEVING COMMUNITY</b>
</p>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
In disputes with non-brethren, the believer is required to act biblically,
while no such constraint can be expected from, or enforced upon, his
opponent. Some may see this as a disadvantage for the believer, but it is
actually a strength, because God's peace and wisdom result from submitting
one's self to the Word of God. To begin with, the believer is not to
exacerbate conflict by his own improper conduct. Rather, he is to live in
peace with all persons as much as it is possible (<u>Romans 12:18</u>
<u>Proverbs 16:7</u>) and, once conflict has arisen, he is not to respond in
the flesh, but out of love (<u>Romans 12:19-21</u>).
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
As with conflicts between brethren, the exercise of personal forgiveness is
mandatory:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Mark 11:25</u>:&nbsp;&nbsp;
<i>And when you stand praying, if you have anything against anyone, forgive
him; so that your Father in heaven may also forgive your offenses.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Also, there are occasions when an offense may or should be overlooked, as
stated in <u>Proverbs 19:11</u>. If the offense is not overlooked, employing
the first step in the <u>Matthew 18</u> process (going to him privately)
should be considered, even though it is not required in disputes with
unbelievers, because Scripture teaches the advisability of settling disputes
quickly (<u>Matthew 5:25</u>). If going to your opponent does not solve the
problem, suing in the public courts may be an option.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Matthew 5:37-41</u> is often misunderstood as commanding believers to be
pacifistic toward those who would hurt them, enslave them, or take their
property. To understand this better, let us focus on <u>verse 39a</u>:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>But I tell you not to stand up against someone who does you wrong.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The word "resist" in the Greek, "<i>anth-is'-tay-mee</i>" can refer to
passive resistance, but one of its recognized meanings is "to set one's self
against" (Strong's Greek Lexicon, word 436) - a decidedly aggressive posture.
Which approach to employ when <i>anthistaymee</i> is used, can be deduced
from the next sentence:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>On the contrary, if someone hits you on the right cheek, let him hit you
on the left cheek too!</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
According to Craig S. Keener,<sup><small><a name=fr6 href=#fn6>6</a></small></sup> "<i>The blow on the
right cheek was the most grievous insult possible in the ancient world
...</i>" (e.g. <u>1 Kings 22:24</u>). What the Scripture is actually saying
(Keener opines), is that we should not retaliate against an evil person. For
example, if the person tries to provoke us by insulting us, rather than
meeting his challenge with aggression of our own, we should accept the insult
and even a second insult (present our other cheek). This does not mean that
we should not protect ourselves from harm either physically or through legal
means when appropriate.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The next statement in the Scripture, <u>Matthew 5:40</u>, says:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>If someone wants to sue you for your shirt, let him have your coat as
well.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
I agree with Keener that this is meant as hyperbole, and is addressing our
attitude regarding ownership. It teaches that where someone is suing us
unjustly, we should prefer to bear the injustice, and even give him more than
he wants, rather than become a defendant in a law suit. The statement
"<i>And if a soldier forces you to carry his pack for one mile, carry it for
two!</i>" is also a teaching about our attitude regarding ownership - in this
case, however, ownership of time and freedom. It is a reference to the
ancient Roman soldier's right to impress a person into serving him (e.g.
<u>Mark 15:21</u>). The Scripture teaches that we should prefer to bear the
injustice of servitude, and even give more than required as ones who, in
obedience to God, are called to love our enemies.
