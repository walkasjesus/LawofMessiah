<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This <i>Mitzvah</i> contains elements that are not dealt with in the
<i>mitzvot</i> of Maimonides, Meir, and HaChinuch. The only <i>mitzvot</i>
in their respective compilations that touch on this <i>Mitzvah</i> (other
than those that impose specific penalties for violations of law and deal with
idolators and false prophets) are Maimonides' RP205, Meir's MP72, and
HaChinuch's C239.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimondes, Meir, and HaChinuch express their <i>mitzvot</i> as our being
responsible for rebuking sinners without reference to whether or not the sin
is directed against us. Maimonides includes those who are contemplating but
have not yet committed sin, whereas Meir and HaChinuch do not touch on that.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Another difference in the commentators is that HaChinuch states that one is
prohibited from correcting a sinner if he believes that his correction will
not be heeded. For his reason, he cites <u><i>TB Yebamoth 65b</i></u>, and
expresses concern that the righteous admonisher will be disgraced by the
sinner ignoring his rebuke. Also, HaChinuch states his <i>mitzvah</i> C239
in the context of an Israelite rebuking another Israelite. Maimonides and
Meir speak of rebuking sinners more generally.
