<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
At first impression, <u>Matthew 5:48</u> (<i>Therefore, be perfect, just as
your Father in heaven is perfect</i>) and <u>Leviticus 11:45</u>
(<i>Therefore you are to be holy, because I am holy</i>) seem highly
presumptuous and even blasphemous. As God's creation, we are so far below
His holiness and perfection - how can we even think of being holy and perfect
as He is? Yet, we are commanded to be holy and perfect, so somehow it is
possible even though it seems impossible in light of our spiritual
imperfections.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The answer is suggested in <u>Leviticus 22:32</u>: "<i>I am ADONAI, who makes
you holy ...</i>" In other words, we cannot aspire to holiness and
perfection in our own power; we have to receive it from God Himself, and
realize that for us it is a never-ending goal rather than one that we will
achieve during our lifetime. In the Mosaic Covenant, the Israelites pursued
holiness through repentance and the prescribed Tabernacle (later Temple)
sacrifices. In the New Covenant, we are made holy by being one with Yeshua,
"the Holy One of Israel". Only through his blood sacrifice can we be made
holy and aspire toward perfection, for we read in <u>1 John 1:9</u>:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>If we acknowledge our sins, then, since he [Yeshua] is trustworthy and
just, he will forgive them and purify us from all wrongdoing.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary by Dr. Daniel C. Juster</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This <i>Mitzvah</i> refers to a heart orientation and not literal perfection
in this life.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
