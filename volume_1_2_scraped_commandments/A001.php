<font size=4 face="Times New Roman, Times, serif">
<b><u>Supportive Scriptures</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Exodus 23:32-33</u>
<br>
<i>You are not to make a covenant with them or with their gods. They are not to
live in your land; otherwise they will make you sin against me by ensnaring
you to serve their gods.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Matthew 6:24</u>
<br>
<i>No one can be slave to two masters; for he will either hate the first and
love the second, or scorn the second and be loyal to the first. You
can&#039;t be a slave to both God and money.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The Scriptures supporting this <i>mitzvah</i> appear to have three dissimilar
themes: agriculture, marriage, and idolatry. What they all have in common,
however, are sets of things that work in opposition to each other, causing a
nullification of positive value that one of them alone might otherwise have
had.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
First, the agricultural examples: (1) that we not yoke an ox and a donkey
together to a plow, (2) that we not wear garments with wool and linen woven
together, and (3) that we not sow two kinds of seeds in fields and in
vineyards. The traditional explanation for not working two kinds of animals
together is that it is cruel to the animals since the stronger of the two
will drag the weaker one, and the weaker of the two will impede the stronger
one. An additional observation offered here is that the work that either of
the animals could do alone is impeded by their having to pull against each
other.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides' explanation (echoed by HaChinuch) for not wearing wool and linen
woven together is that such a fabric was worn by the heathen priests in
Egypt. From a different point of view and the one I offer here is that wool
and linen shrink different amounts after they are wet, so if they are woven
together, they pull against each other when they dry and weaken the fabric.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
HaChinuch's explanation for not sowing two kinds of seed is that it is an
extension of the prohibition against mating mixed species. From a different
point of view and the one I offer here is that different species of plants
absorb nutrients from the soil in different proportions, so when they are
planted close together they compete for the nutrients to the detriment of
both.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Consider the marriage examples next: <u>2 Corinthians 6:14-17</u> explains it
best (although marriage is not mentioned in that Scripture) when it
states:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>What does a believer have in common with an unbeliever? What agreement
can there be between the temple of God and idols?</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Without agreement on spiritual things between husband and wife, the husband
cannot lead the wife, they pull in different directions, and destruction
follows, sometimes with the believer compromising his or her faith.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Finally, consider the idolatry examples: Israel was prohibited from making
covenants with heathen nations and intermarrying with their sons and
daughters for the same reason as was stated between believers and
unbelievers. God predicted that if that happened, Israelites would be lured
away from God and into idol worship, as He said in <u>Deuteronomy 7:4</u>:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>For he will turn your children away from following me in order to serve
other gods.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Whereas, the classical commentators tend to interpret the Scriptures of this
<i>mitzvah</i> according to the plain meaning of their words and separate
from each other, I see them as connected by their common thread of forces
pulling against each other. Clearly, the weight of this <i>Mitzvah</i>'s
application is that, if we belong to God, we must not yoke ourselves with
anything or anyone who is not of God. Marriage is the most obvious example,
followed perhaps by other partnerships like business partnerships. People
who do not believe similarly about God hold different values, and take
different ethical and moral positions on things. This either causes the
believer to compromise, or it breaks up the partnership. As <u>Matthew
6:24</u> states:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>No one can be slave to two masters.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
If <u>2 Corinthians 6:14-17</u> were the only subject of this <i>Mitzvah</i>
I could have given it the more narrow title "Being Unequally Yoked with
Unbelievers". I did not, because but even in a partnership between two
believers, the partnership is unwise if the partners see things very
differently and respond accordingly. In this, the Scriptures have very
far-reaching wisdom.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
None of Maimonides&#039;, Meir&#039;s, or HaChinuch&#039;s <i>mitzvot</i>
oppose unequal yoking in general. Rather, their <i>mitzvot</i> speak to
specific prohibitions against combining dissimilar items combinations;
Maimonides&#039; <i>mitzvot</i> prohibiting such combinations are as follows:
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
RN42: Not wearing cloth made from both wool and linen.
<br>
RN52: Not marrying an heretic.
<br>
RN158: A priest not marrying a harlot.
<br>
RN159: A priest not marrying a profaned woman.
<br>
RN215: Not sowing two kinds of seeds.
<br>
RN216: Not sowing grain or vegetables among grape vines.
<br>
RN218: Not working a field with two different kinds of animals.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
