<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><i>We are not to engage in sexual intimacy with a person and also with a
close member of that person's family.</i></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This precept is derived from His Word (blessed be He):
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Key Scriptures</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Leviticus 18:17-18</u>
(Maimonides RN337-RN339; Meir MN121-MN123; Chinuch
C203-C205)
<br>
<i>You are not to have sexual relations with both a woman and her daughter, nor
are you to have sexual relations with her son&#039;s daughter or her
daughter&#039;s daughter; they are close relatives of hers, and it would be
shameful. You are not to take a woman to be a rival with her sister and
have sexual relations with her while her sister is still alive.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Galatians 5:19</u>
<br>
<i>And it is perfectly evident what the old nature does. It expresses itself in
sexual immorality, impurity and indecency</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Supportive Scriptures</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Matthew 15:19-20</u>
<br>
<i>For out of the heart come forth wicked thoughts, murder, adultery and other
kinds of sexual immorality, theft, lies, slanders ... These are what really
make a person unclean, but eating without doing n&#039;tilat-yadayim does
not make a person unclean.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Romans 1:26-28</u>
<br>
<i>For this reason God gave them up to vile passions. For even their women
exchanged the natural use for what is against nature. Likewise also the
men, leaving the natural use of the woman, burned in their lust for one
another, men with men committing what is shameful, and receiving in
themselves the penalty of their error which was due. And even as they did
not like to retain God in their knowledge, God gave them over to a debased
mind, to do those things which are not fitting</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Romans 13:13-14</u>
<br>
<i>Let us live properly, as people do in the daytime - not partying and getting
drunk, not engaging in sexual immorality and other excesses, not
quarrelling and being jealous. Instead, clothe yourselves with the Lord
Yeshua the Messiah; and don&#039;t waste your time thinking about how to
provide for the sinful desires of your old nature.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>1 Corinthians 5:1-5</u>
<br>
<i>It is actually being reported that there is sexual sin among you, and it is
sexual sin of a kind that is condemned even by pagans - a man is living with
his stepmother! And you stay proud? Shouldn&#039;t you rather have felt
some sadness that would have led you to remove from your company the man who
has done this thing? For I myself, even though I am absent physically, am
with you spiritually; and I have already judged the man who has done this as
if I were present. In the name of the Lord Yeshua, when you are assembled,
with me present spiritually and the power of our Lord Yeshua among us, hand
over such a person to the Adversary for his old nature to be destroyed, so
that his spirit may be saved in the Day of the Lord.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>1 Corinthians 5:9-13</u>
<br>
<i>In my earlier letter I wrote you not to associate with people who engage in
sexual immorality. I didn&#039;t mean the sexually immoral people outside
your community, or the greedy, or the thieves or the idol-worshippers - for
then you would have to leave the world altogether! No, what I wrote you was
not to associate with anyone who is supposedly a brother but who also
engages in sexual immorality, is greedy, worships idols, is abusive, gets
drunk or steals. With such a person you shouldn&#039;t even eat! For what
business is it of mine to judge outsiders? Isn&#039;t it those who are part
of the community that you should be judging? God will judge those who are
outside. Just expel the evildoer from among yourselves.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>1 Corinthians 6:13</u>
<br>
<i>"Food is meant for the stomach and the stomach for food"? Maybe, but God
will put an end to both of them. Anyhow, the body is not meant for sexual
immorality but for the Lord, and the Lord is for the body.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>1 Corinthians 6:18-19</u>
<br>
<i>Run from sexual immorality! Every other sin a person commits is outside the
body, but the fornicator sins against his own body. Or don&#039;t you know
that your body is a temple for the Ruach HaKodesh who lives inside you, whom
you received from God? The fact is, you don&#039;t belong to yourselves</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>1 Corinthians 10:8</u>
<br>
<i>And let us not engage in sexual immorality, as some of them did, with the
consequence that 23,000 died in a single day.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Ephesians 5:3</u>
<br>
<i>Among you there should not even be mentioned sexual immorality, or any kind
of impurity, or greed; these are utterly inappropriate for God&#039;s holy
people.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Colossians 3:5</u>
<br>
<i>Therefore, put to death the earthly parts of your nature- sexual immorality,
impurity, lust, evil desires and greed (which is a form of idolatry)</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>1 Thessalonians 4:3-5</u>
<br>
<i>What God wants is that you be holy, that you keep away from sexual
immorality, that each of you know how to manage his sexual impulses in a
holy and honorable manner, without giving in to lustful desires, like the
pagans who don&#039;t know God.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The Scriptures underlying this <i>Mitzvah</i> list only certain female family
members, whereas this <i>Mitzvah</i> itself is written so as to include
individuals of both sexes and their close family members.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Classical Commentators:</u>
<br>
Maimonides, Meir, and HaChinuch wrote their <i>mitzvot</i> to prohibit having
sexual relations with the persons listed in <u>Leviticus 18:17</u>. A woman
and her sister while the sister is still alive (listed in <u>Leviticus
18:18</u>) is included in this <i>Mitzvah</i> but is not dealt with by the
three classical commentators.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
