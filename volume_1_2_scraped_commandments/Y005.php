<br>
<i>Fearing human beings is a snare; but he who trusts in ADONAI will be raised
high [above danger].</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Mark 8:38</u>
<br>
<i>For if someone is ashamed of me and of what I say in this adulterous and
sinful generation, the Son of Man also will be ashamed of him when he comes
in his Father&#039;s glory with the holy angels.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>2 Peter 2:1</u>
<br>
<i>But among the people there were also false prophets, just as there will be
false teachers among you. Under false pretenses they will introduce
destructive heresies, even denying the Master who bought them, and thus
bring on themselves swift destruction.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>1 John 2:22-23</u>
<br>
<i>Who is a liar at all, if not the person who denies that Yeshua is the
Messiah? Such a person is an anti-Messiah- he is denying the Father and the
Son. Everyone who denies the Son is also without the Father, but the person
who acknowledges the Son has the Father as well.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Fear of men's disapproval is a primary motive when believers deny Yeshua, and
when unbelievers refuse to consider him. Those feared may be family members,
employers, co-workers, friends, or even fictitious figments of the
imagination. Scripture is clear that whether one is a believer or an
unbeliever, denying the true and full identity of Yeshua (Messiah, son of
God, fullness of the deity, etc.) is
sin,<sup><small><a name=fr1 href=#fn1>1</a></small></sup>
and can have disastrous consequences.
</font></p><hr width=150px style="margin-left: 10px">
