<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><i>We are not to deny believing in Yeshua for fear of man.</i></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This precept is derived from His Word (blessed be He):
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Key Scriptures</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>*Matthew 10:28-33</u>
<br>
<i>Do not fear those who kill the body but are powerless to kill the soul.
Rather, fear him who can destroy both soul and body in Gei-Hinnom.
Aren&#039;t sparrows sold for next to nothing, two for an assarion? Yet not
one of them will fall to the ground without your Father&#039;s consent. As
for you, every hair on your head has been counted. So do not be afraid, you
are worth more than many sparrows. Whoever acknowledges me in the presence
of others I will also acknowledge in the presence of my Father in heaven.
But whoever disowns [denies] me before others I will disown [deny] before my
Father in heaven.</i>
((see also, <u>Luke 12:4-9</u>))
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>2 Timothy 1:7-8</u>
<br>
<i>For God gave us a Spirit who produces not timidity, but power, love and
self-discipline. So don&#039;t be ashamed of bearing testimony to our Lord
or to me, his prisoner. On the contrary, accept your share in suffering
disgrace for the sake of the Good News. God will give you the strength for
it ...</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Supportive Scriptures</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
