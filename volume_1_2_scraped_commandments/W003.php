<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Prophecy involves hearing God's voice and, if that which God says is meant
for persons other than the hearer, repeating it for those others to hear. In
the time of Moses, prophecy was limited to only certain people (prophets)
upon whom God's Spirit rested and indwelt, and their function in speaking
what God said was vitally important because the average Israelite was unable
to hear God's voice on his or her own. These prophets were held to a very
high level of accountability - they were even to be put to death if they
prophesied falsely.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Prophecy in the New Covenant is an extension of the Mosaic, but with the huge
step up of everyone being able to receive the Holy Spirit and hear the voice
of God for themselves (<u>Romans 12:6</u>; <u>1 Corinthians 12:8-10, 14:1,
22-33</u>). The Holy Spirit's universal availability (along with the
salvation brought by Yeshua) is what characterizes the New Covenant (see <u>1
Corinthians 14:1-40</u>) as being both new and "better" (<u>Hebrews
8:6-7</u>). Sometimes what God says to us is meant for us alone, and
sometimes it is meant for others. And just as with the prophets of old, when
we hear words that are meant for others, are obligated to speak them out.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
In the New Covenant (as in the Mosaic), the existence of false prophesy and
false prophets remains a problem and, although we no longer execute those who
speak falsely, we are instructed to test everything that we hear so as not to
be misled (<u>1 Thessalonians 5:20-21</u>; <u>1 John 4:1</u>). The principal
way of testing what we hear is comparing it to Scripture and what we know
about God from Scripture.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Perhaps analogous to the "school" (company) of prophets mentioned in <u>1
Samuel</u>, <u>2 Kings</u> and elsewhere in Scripture, the entire body of
believers (now able to prophesy) along with prophets of the fivefold
ministries (see <u>Ephesians 4:11-12</u>) can be viewed as the New Covenant's
expansion of the prophets of old that came together in order to hear God,
affirm God's words that were heard, and weed out prophecy that was false.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary by Daniel C. Juster</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
I would add that prophecy in the New Testament seems to not require the
strictness and penalty of the Mosaic period. We find that no penalty is
enjoined in the New Covenant for inaccuracy in prophetic words, since the
community should be able to test these. In addition, we see words such as
those of Agabus whose word was partially correct, but not correct in its
detail. Nevertheless, Agabus was known as a credible prophet in the book of
<u>Acts</u>.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
There is a level of prophecy and prophetic accuracy that can be experienced
by every believer, but there are three levels of the prophetic gifting:
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<ol>
<li> All believers who are open to the Holy Spirit can prophecy on a basic
level.
<br>
<li> Some believers are imbued with a special gift of prophecy; these tend
to prophesy more frequently than others and with greater accuracy.
<br>
<li> Some with the prophetic gift have been proven, over time, to prophesy
with exceptional quality and accuracy, and these we call prophets (see
<u>1 Corinthians 14</u> and <u>Ephesians 4:11</u>).
</ol>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides, Meir, and HaChinuch prohibit prophesying falsely, and refer to
the <i>Talmud's</i> penalty of strangulation for doing so. Maimonides and
HaChinuch (but not Meir) also wrote a <i>mitzvah</i> prohibiting us from
having pity for a false prophet and declining to put him to death for his sin.
