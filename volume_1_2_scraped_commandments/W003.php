prophet or dreamer says. For ADONAI your God is testing you, in order to
find out whether you really do love ADONAI your God with all your heart and
being. You are to follow ADONAI your God, fear him, obey his mitzvot,
listen to what he says, serve him and cling to him; and that prophet or
dreamer is to be put to death; because he urged rebellion against ADONAI
your God, who brought you out of the land of Egypt and redeemed you from a
life of slavery; in order to seduce you away from the path ADONAI your God
ordered you to follow. This is how you are to rid your community of this
