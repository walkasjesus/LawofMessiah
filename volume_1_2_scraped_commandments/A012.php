<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The common understanding of scholars is that Yeshua's exhortation to
Nicodemus in <u>John 3:7</u> that he "must be born again from above" was a
reference to his first birth being from the waters of natural childbirth.
There is, however, another way to understand this, and that is that man's
first birth was also from above - from the waters of creation (<u>Genesis
1:1-2</u>) - and that man's original death due to Adam's disobedience
required that he be "born again from above" in order for him to have eternal
life, which was God's original intention.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
One might argue that being "born again" (while it is God's desire for us all)
is not a true <i>mitzvah</i> because the power to accomplish it is God's and
not ours. True, the supernatural power to cause a man to be born from above
is God's alone, but God gave us the key to unleash His power by receiving His
son Yeshua - our Messiah - as we read in <u>John 3:14-16</u>:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>Just as Moshe lifted up the serpent in the desert, so must the Son of Man
be lifted up; so that everyone who trusts in him may have eternal life. For
God so loved the world that he gave his only and unique Son, so that
everyone who trusts in him may have eternal life, instead of being utterly
destroyed.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Trusting [NKJ: Believing in] Yeshua as our Messiah (savior) means repenting
of our former unbelief and accepting his sacrificial death as ours, and our
means of receiving God's forgiveness.
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>Now every cohen stands every day doing his service, offering over and over
the same sacrifices, which can never take away sins. But this one, after he
had offered for all time a single sacrifice for sins, sat down at the right
hand of God, from then on to wait until his enemies be made a footstool for
his feet. For by a single offering he has brought to the goal for all time
those who are being set apart for God and made holy.</i> (<u>Hebrews
10:11-14</u>)
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
As a Messianic Jewish believer in Yeshua I am sometimes confronted with the
following question or a variation of it:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
"I consider myself a good person. All the things the Bible teaches I try to
do, and all the virtues the Bible teaches I try to have. Are you telling me
that just because I do not believe that Yeshua is the Messiah I am destined
for Hell?"
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
My answer to the question is always the same:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
"Your standing with God and your salvation are not for me to judge, nor is it
for me to tell God whom he can save and whom He cannot. I can only tell you
that <u>Acts 4:12</u> of the New Covenant Scriptures teaches that "<i>There
is salvation in no one else</i>" [meaning no one other than Yeshua], and that
"there is no other name under heaven given to mankind by whom we must be
saved!</i>" Notice that the Scripture I quoted is God's promise that if you
repent of your sins and receive Yeshua as your Messiah, YOU WILL BE SAVED! If
you want to take your chances and negotiate with God for some other way to
heaven, that's up to you. But If you want to be sure of your salvation, I
would advise you to do it the way that God guaranteed."
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
