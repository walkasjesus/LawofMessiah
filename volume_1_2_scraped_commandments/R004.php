<font size=4 face="Times New Roman, Times, serif">
<b><i>We are test every spirit to see if it is from God.</i></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This precept is derived from His Word (blessed be He):
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Key Scripture</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>*1 John 4:1-3, 6</u>
<br>
<i>Dear friends, don&#039;t trust every spirit. On the contrary, test the
spirits to see whether they are from God; because many false prophets have
gone out into the world. Here is how you recognize the Spirit of God: every
spirit which acknowledges that Yeshua the Messiah came as a human being is
from God, and every spirit which does not acknowledge Yeshua is not from
God - in fact, this is the spirit of the Anti-Messiah. You have heard that
he is coming. Well, he&#039;s here now, in the world already! ... We are
from God. Whoever knows God listens to us; whoever is not from God
doesn&#039;t listen to us. This is how we distinguish the Spirit of truth
from the spirit of error.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
