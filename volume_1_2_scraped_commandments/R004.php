<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The assumption of Scripture is that professions, teachings, and exhortations
of men are made in response to spirit influences that are either from God or
from the adversary. If a spirit bringing a teaching is God's Spirit, what he
says will be true; if not, it will be false. We must therefore test the
spirit of everything that is proffered to us so that we are not misled; <u>1
John 4:2-3</u> tells us how:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>Here is how you recognize the Spirit of God: every spirit which
acknowledges that Yeshua the Messiah came as a human being is from God, and
every spirit which does not acknowledge Yeshua is not from God - in fact,
this is the spirit of the Anti-Messiah.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
