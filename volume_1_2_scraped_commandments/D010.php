<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The Scriptures cited herein are unambiguous that God commanded the Israelites
to eat <i>matzah</i> on each of the seven days of the Feast of unleavened
bread that begins on the 15<sup><small>th</small></sup> day of <i>Nisan</i>.
The rationale for it appears to be to remember the Israelites' hasty
departure from Egypt by eating bread similar to the kind they carried with
them - bread that did not have time to rise during their travel. This
translates to being a mandate today for Jews and <i>K'rov Yisrael</i>
Gentiles, but not for other Gentiles, although they may do so (if they are so
moved) in identification with the Jewish people.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides and HaChinuch state that we must eat <i>matzah</i> on the evening
of the 15<sup><small>th</small></sup> of <i>Nisan</i>, and Maimonides states
that doing so after that is optional. On this latter point, Meir and
HaChinuch are silent, and they offer no <i>mitzvah</i> to support eating
<i>matzah</i> during the Feast of Unleavened Bread. <u><i>Pesachim</i>
120a</u> in the <i>Talmud</i> appears to be the source of the commentators'
apparent belief that there is no commandment to eat <i>matzah</i> after
<i>Pesach</i>. Perhaps they understand the Scriptures to be saying that if
one chooses to eat bread from the 15<sup><small>th</small></sup> to the
21<sup><small>sti</small></sup> of <i>Nisan</i>, then the bread must be
unleavened. I do not know their rationale, but I am of opinion that
Scripture commands eating <i>matzah</i> on each of the seven days of
Unleavened Bread as a daily reminder of God's deliverance of the Israelites
from Egypt.
