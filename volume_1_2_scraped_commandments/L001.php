<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The Scriptures quoted above clearly show that the Land of Israel was given to
the Children of Israel (the Jewish people) as a main component of God's
covenant with Abraham. Those that say otherwise (principally Islamic Arabs
and their supporters) are contradicting both the Holy Scriptures and the
writings of their revered "prophet" Mohammed.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Because God gave the Land of Israel to the Jews, it is sinful to deny it to
them and a <i>mitzvah</i> to support them in the Land. There are many ways
to give that support. Some Jews are called to make <i>Aliyah</i> (emigrate)
to Israel and become part of Israeli Jewish society, while others remain
among the nations (the diaspora) and contribute by donating and raising money
for Israel, by visiting Israel to work on <i>kibbutzim</i>, by participating
in humanitarian causes that benefit Israel and its citizens, and by praying
in accordance with <i>Psalm 122:6-9</i>:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>Pray for shalom in Yerushalayim; may those who love you prosper.
<br>
May shalom be within your ramparts, prosperity in your palaces.
<br>
For the sake of my family and friends, I say, "Shalom be within you!"
<br>
For the sake of the house of ADONAI our God, I will seek your
well-being.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
There is a caveat associated with this <i>Mitzvah</i>, and that is that we
must distinguish between supporting Israel's right to exist and thrive on the
one hand, and supporting the politics of its secular government on the other.
Because God's hand is on Israel it is often right in its political decisions,
but not always because it is not yet fully submitted to God and Messiah. In
faith, that will come soon, but meanwhile we must seek divine discernment for
how to pray and how to support.
