</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b>A. <u>A Wise Man is Superior to a Prophet</u></b>
<br>
<u><i>Moses Ibn Ezra</i> (al-Andalus, c. 1055-1138), <i>Book of Discussion
and Conversation (Kitab al-muhadara wa-l-mudhakara)</i>, 19b-20a.</u>
<br>
<i>The sage is greater than the prophet - because the prophet transmits the
communication (risala) ... or prophecy (nubuwa) ... as revealed to him,
whereas the sage ... extrapolates laws (yafra&#039;u) [from Scripture] in
accordance with what the Law allows him to extrapolate (tafri&#039;), and
he utilizes his own mental capacity, and draws conclusions (yuntiju) from
his intellectual premises. [In relation to the prophet] he has the
distinction (fadl) of creative ability (al-ibda&#039;).</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Similarly:
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u><i>Mishneh Torah</i> (Rambam) <i>Shab. 5:1</i></u>
<br>
<i>R. Abdimi from Haifa said: "Since the day when the Temple was destroyed,
prophecy has been taken from the prophets and given to the wise. Is then a
wise man not also a prophet?" What he meant was this: Although it has been
taken from the prophets, it has not been taken from the wise. Amemar said:
A wise man is even superior to a prophet.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The contradiction is in lauding human wisdom over the gift of hearing the
voice of God.
