</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Traditions are good (and even necessary) when they are consistent with, and
supportive of, the Word of God. Regrettably, traditions have been misused by
the Jewish people and others throughout the ages, which is why we read of
Yeshua scolding the Pharisees and Torah teachers that were criticizing him
and his disciples for violating inconsequential traditions, while they
themselves were breaking major Commandments of God through their traditions
(<u>Mark 7:7-9</u>).
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Some traditions are explicitly commanded in Scripture, such as <u>Leviticus
23:40</u>:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>On the first day [of Sukkot] you are to take choice fruit, palm fronds,
thick branches and river-willows, and celebrate in the presence of ADONAI
your God for seven days.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
You will notice, however, that the Scripture does not say what we are to do
with these items, so it was left to our community leaders or community
consensus, and the Jewish tradition that developed was to wave them in a
prescribed way. One can assume, therefore, that a Jew who does not take up
the "four species" in some manner on <i>Sukkot</i> violates the commandment
of God, but if he merely departs from the rabbinical way of doing it, he only
violates rabbinical tradition.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
I say "rabbinical tradition", but it is also "rabbinical law" because the
sages of Judaism have come to believe that they are authorized by God to
interpret <i>Torah</i> and to make law for the Jewish community. Law for the
Jewish Community (statutory or <i>stare decisis</i>)<sup><small><a name=fr1 href=#fn1>1</a></small></sup> is known as <i>halachah</i>, and its basis is
in <u>Deuteronomy 17:8-11</u>:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>If a case comes before you at your city gate which is too difficult for
you to judge, concerning bloodshed, civil suit, personal injury or any other
controversial issue; you are to get up, go to the place which ADONAI your God
will choose, and appear before the cohanim, who are L'vi'im, and the judge in
office at the time. Seek their opinion, and they will render a verdict for
you. You will then act according to what they have told you there in that
place which ADONAI will choose; you are to take care to act according to all
their instructions. In accordance with the Torah they teach you, you are to
carry out the judgment they render, not turning aside to the right or the
left from the verdict they declare to you.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Authority to interpret statutory law is not an unusual or strictly Jewish
concept; it is assumed by all judicial bodies, both ecclesiastical and
secular. The process of statutory interpretation, when done correctly, seeks
to determine a statute's legislative intent - that is, its meaning and
application that its framer(s) had in mind. All New Covenant believers are
able to (and expected to) interpret God's statutes (commandments) through the
prophetic insight that has been given to us by the Holy Spirit. The problem
arises when we seek to interpret in our own logic and our own wisdom, and not
according to what the Holy Spirit reveals to us when we consult him.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Regrettably, the way Rabbinical Judaism has chosen to interpret and apply
God's commandments is without the help of the Holy Spirit, and by man's own
power and authority. The justification for this is a story that is recorded
in the Babylonian Talmud, <u>Baba Metzia 59b</u>, where, in the midst of a
dispute between rabbis, a heavenly voice was allegedly heard to cry out:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
'Why do you dispute with R. Eliezer, seeing that in all matters the
halachah agrees with him!' But R. Joshua arose and exclaimed: '<b>It is not
in heaven.</b>' What did he mean by this? - Said R. Jeremiah: That the Torah
had already been given at Mount Sinai; we pay no attention to a Heavenly
Voice, because You have long since written in the Torah at Mount Sinai,
'After the majority must one incline.'
<small>(<u>Soncino Talmud</u>)</small>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The statement "<b>It is not in heaven</b>" (<i>lo ba-shamayim hi</i>) also
appears in <u>Deuteronomy 30:12</u>, where it is used in a different context:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<b>It is not in heaven</b>, that you should say, 'Who will ascend into
heaven for us and bring it to us, that we may hear it and do it?'
<small>(NKJ)</small>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
In the Babylonian Talmud, "<b>It is not in heaven</b>" is used to justify not
seeking the voice of God or even paying attention to it for <i>Torah</i>
interpretation and application, on the theory that God gave the <i>Torah</i>
to mankind on Mount Sinai, and that it is now exclusively man's possession,
and exclusively man's to interpret and apply.<sup><small><a name=fr2 href=#fn2>2</a></small></sup>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
And interpret the <i>Torah</i> man did - by what has come to be known as
"building a fence around the <i>Torah</i>." It is achieved by enacting a
<i>gezeirah</i> - a law to prevent a person from accidentally violating a
<i>mitzvah d'oraita</i> - a commandment of <i>Torah</i>.<sup><small><a name=fr3 href=#fn3>3</a></small></sup> The concept of building a fence around the <i>Torah</i> for
safety was inspired by <u>Deuteronomy 22:8</u>:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>When you build a new house, you must build a low wall around your roof;
otherwise someone may fall from it, and you will be responsible for his
death.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
In order to be effective, a <i>gezeirah</i> must be more restrictive than the
<i>Torah</i> commandment it seeks to protect. Quoting from a paper by Daniel
C. Juster:<sup><small><a name=fr4 href=#fn4>4</a></small></sup>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
The most common examples of protecting the Torah are the laws of kashrut
(Jewish dietary laws), especially the laws concerning milk and meat. The
Torah (<u>Exodus 23:19</u>, &amp; <u>34:26</u> and <u>Deuteronomy 14:21</u>)
exhorts us to not boil a kid (a baby goat) in its mother's milk. This
command is in the context of pagan Canaanite practice. It is also an obvious
humanitarian deference to animal life. To protect this law, the rabbis
decided that eating milk and meat together, even if not from the same animal,
should be avoided. Once this was accepted, they determined that we needed to
have hours of separation between meat meals and milk meals so that milk and
meat will not be cooked together in our digestive system. Once this was
accepted, we were required to have separate dishes for milk and meat since
there is a possibility that particles of meat or milk may be left on the
plate and get mixed and eaten. In the case of Kashrut, a new fence is made
for each new rabbinical law!
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
On its surface, it appears that building a fence around the <i>Torah</i> is a
sensible thing to do, since if one does not violate the fence, one cannot
violate the <i>Torah</i> which is what is sought to be protected.<sup><small><a name=fr5 href=#fn5>5</a></small></sup> Ironically, in its apparent virtue, it does not achieve
that which it seeks to achieve. When we seek to keep <i>Torah</i>, we are
not merely keeping rules - we are connecting with God who is the rule-maker
