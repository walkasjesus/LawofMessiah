<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><i>We are not to have close physical contact & sexual intimacy with
family members.</i></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This precept is derived from His Word (blessed be He):
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Key Scriptures</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Leviticus 18:3</u>
(Maimonides RN353; Chinuch C188)
<br>
<i>You are not to engage in the activities found in the land of Egypt, where
you used to live; and you are not to engage in the activities found in the
land of Kena&#039;an, where I am bringing you; nor are you to live by their
laws.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Leviticus 18:6-18</u>
(Maimonides RN330-RN336, RN340-RN345, RN353; Meir MN110,
MN112-MN113, MN115, MN119-MN120, MN125-MN131; Chinuch
C188, C190-C198, C200-C202, C206)
<br>
<i>None of you is to approach anyone who is a close relative in order to have
sexual relations; I am ADONAI. You are not to have sexual relations with
your father, and you are not to have sexual relations with your mother. She
is your mother - do not have sexual relations with her. You are not to have
sexual relations with your father&#039;s wife; that is your father&#039;s
prerogative. You are not to have sexual relations with your sister, the
daughter of your father or the daughter of your mother, whether born at home
or elsewhere. Do not have sexual relations with them. You are not to have
sexual relations with your son&#039;s daughter or with your daughter&#039;s
daughter. Do not have sexual relations with them, because their sexual
disgrace will be your own. You are not to have sexual relations with your
father&#039;s wife&#039;s daughter, born to your father, because she is your
sister; do not have sexual relations with her. You are not to have sexual
relations with your father&#039;s sister, because she is your father&#039;s
close relative. You are not to have sexual relations with your
mother&#039;s sister, because she is your mother&#039;s close relative. You
are not to disgrace your father&#039;s brother by having sexual relations
with his wife, because she is your aunt. You are not to have sexual
relations with your daughter-in-law; because she is your son&#039;s wife. Do
not have sexual relations with her. You are not to have sexual relations
with your brother&#039;s wife, because this is your brother&#039;s
prerogative. You are not to have sexual relations with both a woman and her
daughter, nor are you to have sexual relations with her son&#039;s daughter
or her daughter&#039;s daughter; they are close relatives of hers, and it
would be shameful. You are not to take a woman to be a rival with her
sister and have sexual relations with her while her sister is still alive.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Leviticus 20:11-12</u>
<br>
<i>The man who goes to bed with his father&#039;s wife has disgraced his father
sexually, and both of them must be put to death; their blood is on them. If
a man goes to bed with his daughter-in-law, both of them must be put to
death; they have committed a perversion, and their blood is on them.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Leviticus 20:14</u>
(Maimonides RN336; Chinuch C195)
<br>
<i>If a man marries a woman and her mother, it is depravity; they are to be put
to death by fire, both he and they, so that there will not be depravity
among you.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Leviticus 20:17</u>
<br>
<i>If a man takes his sister, his father&#039;s daughter or his mother&#039;s
daughter, and has sexual relations with her, and she consents, it is a
shameful thing; they are to be cut off publicly - he has had sexual
relations with his sister, and he will bear the consequences of their
wrongdoing.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Leviticus 20:19-21</u>
<br>
<i>You are not to have sexual relations with your mother&#039;s sister or your
father&#039;s sister; a person who does this has had sexual relations with
his close relative; they will bear the consequences of their wrongdoing. If
a man goes to bed with his uncle&#039;s wife, he has disgraced his uncle
sexually; they will bear the consequences of their sin and die childless.
If a man takes his brother&#039;s wife, it is uncleanness; he has disgraced
his brother sexually; they will be childless.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Deuteronomy 23:1(22:30)</u>
<br>
<i>A man is not to take his father&#039;s wife, thus violating his
father&#039;s rights.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>1 Corinthians 5:1-5</u>
<br>
<i>It is actually being reported that there is sexual sin among you, and it is
sexual sin of a kind that is condemned even by pagans - a man is living with
his stepmother! And you stay proud? Shouldn&#039;t you rather have felt
some sadness that would have led you to remove from your company the man who
has done this thing? For I myself, even though I am absent physically, am
with you spiritually; and I have already judged the man who has done this as
if I were present. In the name of the Lord Yeshua, when you are assembled,
with me present spiritually and the power of our Lord Yeshua among us, hand
over such a person to the Adversary for his old nature to be destroyed, so
that his spirit may be saved in the Day of the Lord.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Supportive Scriptures</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Matthew 15:19-20</u>
<br>
<i>For out of the heart come forth wicked thoughts, murder, adultery and other
kinds of sexual immorality, theft, lies, slanders ... These are what really
make a person unclean, but eating without doing n&#039;tilat-yadayim does
not make a person unclean.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Romans 1:26-28</u>
<br>
<i>For this reason God gave them up to vile passions. For even their women
exchanged the natural use for what is against nature. Likewise also the
men, leaving the natural use of the woman, burned in their lust for one
another, men with men committing what is shameful, and receiving in
themselves the penalty of their error which was due. And even as they did
not like to retain God in their knowledge, God gave them over to a debased
mind, to do those things which are not fitting</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Romans 13:13-14</u>
<br>
<i>Let us live properly, as people do in the daytime - not partying and getting
drunk, not engaging in sexual immorality and other excesses, not
quarrelling and being jealous. Instead, clothe yourselves with the Lord
Yeshua the Messiah; and don&#039;t waste your time thinking about how to
provide for the sinful desires of your old nature.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>1 Corinthians 5:9-13</u>
<br>
<i>In my earlier letter I wrote you not to associate with people who engage in
sexual immorality. I didn&#039;t mean the sexually immoral people outside
your community, or the greedy, or the thieves or the idol-worshippers - for
then you would have to leave the world altogether! No, what I wrote you was
not to associate with anyone who is supposedly a brother but who also
engages in sexual immorality, is greedy, worships idols, is abusive, gets
drunk or steals. With such a person you shouldn&#039;t even eat! For what
business is it of mine to judge outsiders? Isn&#039;t it those who are part
of the community that you should be judging? God will judge those who are
outside. Just expel the evildoer from among yourselves.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>1 Corinthians 6:13</u>
<br>
<i>"Food is meant for the stomach and the stomach for food"? Maybe, but God
will put an end to both of them. Anyhow, the body is not meant for sexual
immorality but for the Lord, and the Lord is for the body.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>1 Corinthians 6:18-19</u>
<br>
<i>Run from sexual immorality! Every other sin a person commits is outside the
body, but the fornicator sins against his own body. Or don&#039;t you know
that your body is a temple for the Ruach HaKodesh who lives inside you, whom
you received from God? The fact is, you don&#039;t belong to yourselves</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>1 Corinthians 10:8</u>
<br>
<i>And let us not engage in sexual immorality, as some of them did, with the
consequence that 23,000 died in a single day.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Galatians 5:19</u>
<br>
<i>And it is perfectly evident what the old nature does. It expresses itself in
sexual immorality, impurity and indecency</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Ephesians 5:3</u>
<br>
<i>Among you there should not even be mentioned sexual immorality, or any kind
of impurity, or greed; these are utterly inappropriate for God&#039;s holy
people.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Colossians 3:5</u>
<br>
<i>Therefore, put to death the earthly parts of your nature- sexual immorality,
impurity, lust, evil desires and greed (which is a form of idolatry)</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>1 Thessalonians 4:3-5</u>
<br>
<i>What God wants is that you be holy, that you keep away from sexual
immorality, that each of you know how to manage his sexual impulses in a
holy and honorable manner, without giving in to lustful desires, like the
pagans who don&#039;t know God.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This <i>Mitzvah</i> commands against having sexual relations with defined
members of one's family - even members by marriage (i.e. in-laws). Since the
list includes both married and umarried family members, this <i>Miztvah</i>
may also be considered as defining a class of perons whom we cannot marry.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
