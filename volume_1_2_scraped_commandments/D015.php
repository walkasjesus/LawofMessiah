<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The <u>Leviticus</u> Scripture referenced above commands that all citizens of
Israel are to live in <i>sukkot</i> for the seven days of the Feast (of
<i>Sukkot</i>), and it gives the reason - so that each generation of Israel
will remember God's provision of shelter for us when He brought us out of
Egypt. Although the expression "citizen of Isra'el" might be construed to
mean only those Jews who live in the Land, the expression that follows it -
"the people of Isra'el" - broadens its meaning to include all Jewish People,
wherever in the world they may live. I believe it is also applicable to
Gentiles who permanently live within the Jewish community (<i>K'rovei
Yisrael</i>) because those whom God brought out of Egypt were a mixed
multitude that consisted of both Jews and Gentiles (<u>Exodus 12:38</u>).
That may be part of the explanation for why worshiping God on <i>Sukkot</i>
is required of all the Gentile nations (<u>Zechariah 14:16-19</u>). Living
in a <i>sukkah</i> during the Feast is not a requirement for Gentiles who are
not <i>K'rov Yisrael</i>, but they may do so if they deem it meaningful.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The matter of how often one must physically occupy a <i>sukkah</i> during the
seven days of the Feast, and what functions one must perform there in order
that it be counted as "dwelling" is a matter of interpretation, especially in
the Diaspora where climate and other considerations have to be considered. A
minimum standard that is common among Diaspora Jews is to eat at least one
meal each day in a <i>sukkah</i>.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>John 7</u> references a <i>Sukkot</i> Feast that Yeshua attended, but
there is no description of the <i>sukkah</i> in which He must have lived
during the seven days. Nevertheless, the theme of <i>Sukkot</i> (which is
living under God's provision and protection) is very much a New Covenant
theme, with Yeshua, <i>Ru'ach HaKodesh</i> and the <i>B'rit Chadashah</i>
Scriptures being the main New Covenant provisions.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides says relatively little in his <i>mitzvah</i> - merely that we are
to live in a <i>sukkah</i> throughout the seven days of the Feast. Meir is
much more detailed, adding that the <i>sukkah</i> must have a roof of
branches, and that we must eat, drink, and otherwise live in it both day and
night to the extent that one must not eat even one regular meal outside of
it. HaChinuch expounds on the biblical reason for living in a <i>sukkah</i>,
speaks a great deal about its structure, and is much more liberal than Meir
in how much "living" one must do in it. He states, for example, that on the
first night of <i>Sukkot</i> one must eat at least an olive's amount of
bread in a <i>sukkah</i> and, after that, it is voluntary. HaChinuch also
focuses on bread by stating that meals other than "regular meals of bread"
may be eaten outside of a <i>sukkah</i>, but he adds that the early sages
would not eat any meal outside of a <i>sukkah</i> during the Feast.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
