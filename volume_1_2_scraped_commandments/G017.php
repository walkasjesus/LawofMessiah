<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The Scriptures that support this <i>Mitzvah</i> speak for themselves and, for
the most part, require little embellishment. I will, however, comment on one
of them that is often misunderstood. In <u>1 Thessalonians 5:17</u>
(according to many translations) the Apostle Paul exhorts us to:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>Pray without ceasing.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
If looked at the Scripture wrongly, it seems impossible to do and still
conduct the other affairs of life. Therefore, I believe that Paul is
referring, not to formal prayer where we stop everything and speak with God,
but rather to the continuous dialogue that believers are able to have with
God as we walk with Him (and He with us) throughout the day.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
