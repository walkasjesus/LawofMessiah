<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The Hebrew word for "our God" in <u>Deuteronomy 6:4</u> is "<i>Eloheynu</i>",
which is the first person plural possessive of "<i>Elohim</i>" - a word
which, despite its plurality, all <u>Genesis 1:1</u> translators agree means
"God". In addition, the English word "one" in <u>Deuteronomy 6:4</u> is the
Hebrew word "<i>echad</i>", which is a composite (and not an absolute) unity.
So, while our God is plural in His nature, He is one God and not many Gods
combined into one. This is a great mystery for which we have no natural
example to aid our understanding.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Wherever it occurs in Scripture, the Hebrew word "<i>Elohim</i>" (along with
its derivatives), although plural, is universally translated as "God", and
the Hebrew word "<i>echad</i>", a composite unity, is most often translated
"one".
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides, Meir, and HaChinuch express this <i>mitzvah</i> as "knowing that
God is a unity." They emphasize the singleness of the unity by adding that
God has no partner or associate, and are in agreement that believing in the
unity of God is a core belief of the Jewish faith. None of the commentators
acknowledge or discuss the composite nature of "<i>echad</i>", or the plural
nature of "<i>Elohim</i>".
