</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The commandment "<i>You are not to boil a young animal in its mother's
milk.</i>" forms the basis for a number of <i>halachic</i> rules that include
not mixing milk with meat, not eating foods that contain both milk and meat,
and abstaining from eating milk and meat at the same meal or within some
hours of each other. This illustrates the rabbinical principle of "building
a fence around the Law" in which rules are promulgated that are more
stringent than those of the <i>Torah</i>, ostensibly to keep one from
violating the commandment itself.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The problem with this particular fence is (1) no one accidently cooks a young
animal in its own mother's milk, and (2) keeping the rules of <i>kashrut</i>
regarding milk and meat have so overtaken the attention of Orthodox Judaism,
that the underlying moral message in the Scripture has been all but lost.
What is that moral message? It most probably is "Don't do cruel things to
God's creatures as the heathen do."
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
I say "most probably" because we have no absolute proof that boiling young
animals in their mother's milk was a heathen practice. Nevertheless, it was
certainly not a Jewish practice, so whose practice would it otherwise be? We
know that heathens sacrificed children to their gods by fire (<u>Leviticus
18:21</u>), and how very similar it is to kill a mother's child (albeit an
animal mother) by boiling it in her own milk - milk of her body that was
meant to nurture it, not kill it. It is as though the mother herself had
killed her child!
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
I believe the commandment "You are not to boil a young animal in its mother's
milk" is an idiom that was very well understood in Moses' day as meaning:
"Don't do cruel things to God's creatures as the heathen do." The reason I
believe it is an idiom meant to be interpreted broadly, is because it appears
three times in Scripture, and all three times it is appended (almost as an
afterthought that doesn't quite fit) to commandments associated with our
treatment of animals. In <u>Exodus 23:17-19</u> and <u>Exodus 34:23-26</u>,
the commandment follows instructions involving blood sacrifices, and in
<u>Deuteronomy 14:21</u>, it follows an extensive exposition regarding our
use of living creatures for food. It is also meaningful that Abraham saw no
problem in serving his guests a meal prepared with milk and meat (<u>Genesis
18:7-8</u>), and his guests, who were (at the very least) God's emissaries,
ate it.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
What does this mean for Messianic Jewish practice? First, and quite
obviously, we must not boil a young animal in its mother's milk. Second and
most important though, we must treat all of God's creatures with humane
kindness - especially those that we domestically raise for slaughter; this
<i>Mitzvah</i> stands for that above all.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Whether or to what degree we comply with Orthodox <i>halachah</i> regarding
the mixing of milk and meat is entirely a matter of choice and not of
commandment, but there are some situations where compliance is appropriate
and even needed. One that comes to mind is where we live within, or are part
of, a Jewish community that keeps rabbinically <i>kosher</i>. Another is
where hospitality requires it because a guest whom we wish to accommodate
keeps some level of <i>kashrut</i>, and will be constrained in what he can
eat at our table if we serve meat and dairy at the same meal. Still a third
is where (while recognizing our liberty to do otherwise) keeping
<i>kosher</i> helps us in maintaining our identity as Jews.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Although the practice of not mixing milk and meat is most commonly associated
with Orthodox and Conservative Judaism, this <i>Mitzvah</i>, with its
emphasis on morality, is as applicable to Gentiles as it is to Jews. The
only thing that does not apply to a Gentiles is keeping <i>halachically
kosher</i> as a help in maintaining Jewish identity (since there is none).
The reasoning might, however, apply to <i>K'rovei Yisrael</i> Gentiles
who find that keeping <i>halachically kosher</i> is helpful in maintaining
their <i>K'rov</i> identity within the Jewish community.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides, Meir, and HaChinuch are almost identical in how they express
their two <i>mitzvot</i> - the one that prohibits cooking meat in milk, and
the other that prohibits eating meat cooked in milk. Their <i>mitzvot</i> do
not specify any specific animal or its age. Since the Scriptures they rely
on prohibit the cooking (boiling) but not the eating, they need a rationale
for their <i>mitzvot</i> that prohibit the eating. The rationale they offer
is that eating meat cooked in milk derives an improper benefit from the
prohibited cooking. Meir states that we must not benefit from cooking meat
in milk if both the meat and the milk come from <i>kosher</i> animals, but if
one or the other is from a non-<i>kosher</i> animal, we may benefit (but, of
course, not eat). He also states that we may cook fish or locusts in milk
and eat the resulting product. Maimonides speculates that the reason for the
Scriptures prohibiting boiling a kid in its mother's milk has to do with
idolatrous practices (<u>Guide to the Perplexed 111:48</u>); HaChinuch
disagrees, but offers no alternative.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
