<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><i>We are to return anything we find which was lost by another. If he is
not near us or we do not know who he is, we are to keep it safe for him until
he claims it.</i></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This precept is derived from His Word (blessed be He):
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Key Scriptures</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Exodus 23:4</u>
(Maimonides RP204; Chinuch C538)
<br>
<i>If you come upon your enemy&#039;s ox or donkey straying, you must return it
to him.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Leviticus 5:21-24(6:2-5)</u>
<br>
<i>If someone sins and acts perversely against ADONAI by dealing falsely with
his neighbor in regard to a deposit or security entrusted to him, by
stealing from him, by extorting him, or by dealing falsely in regard to a
lost object he has found, or by swearing to a lie- if a person commits any
of these sins, then, if he sinned and is guilty, he is to restore whatever
it was he stole or obtained by extortion, or whatever was deposited with
him, or the lost object which he found, or anything about which he has sworn
falsely. He is to restore it in full plus an additional one-fifth; he must
return it to the person who owns it, on the day when he presents his guilt
offering. He is to bring as his guilt offering to ADONAI a ram without
defect from the flock, or its equivalent according to your appraisal, to the
cohen; it is a guilt offering. Thus the cohen will make atonement for him
before ADONAI, and he will be forgiven in regard to whatever it was he did
that made him guilty.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Deuteronomy 22:1</u>
(Maimonides RP204; Meir MP69, MN182; Chinuch C538)
<br>
<i>You are not to watch your brother&#039;s ox or sheep straying and behave as
if you hadnt seen it; you must bring them back to your brother.'</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Deuteronomy 22:2</u>
<br>
<i>If your brother is not close by, or you don&#039;t know who the owner is,
you are to bring it home to your house; and it will remain with you until
your brother asks for it; then you are to give it back to him.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Deuteronomy 22:3</u>
(Maimonides RN269; Meir MN182; Chinuch C539)
<br>
<i>You are to do the same with his donkey, his coat or anything else of your
brother&#039;s that he loses. If you find something he lost, you must not
ignore it.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>2 Corinthians 8:20-21</u>
<br>
<i>Our aim in this is to show that our conduct in dealing with these
substantial sums is above reproach; for we take pains to do what is right
not only in the sight of God but also in the sight of other people.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The <u>Deuteronomy</u> verses that support this <i>Mitzvah</i> speak of an
item lost by a brother. Nevertheless, <u>Exodus 23:4</u> and general
principles of morality strongly suggest that it must apply to a non-brother
as well - even an enemy - for otherwise, the finder would be unjustly
enriched at the expense of the loser. It is therefore logical that if the
finder knows the loser and does not return the article, his omission amounts
to constructive theft.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Deuteronomy 22:2</u> contains a departure from many secular laws today
that permit a finder of lost property to take ownership of it if it is not
claimed within a specified period of time. Scripture forbids a person from
ever claiming title to a lost item, even if the owner is unknown or at a
great distance so that return would be impractical. The finder must keep the
item safe, and take reasonable steps to discover its owner. It is significant
that some state governments today also prohibit the finder of property from
keeping it, by providing that certain kinds of unclaimed property escheats to
the state if the owner does not come forth to claim it within a prescribed
period of time.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
There are no New Covenant Scriptures that speak directly of finding and
returning lost property, but <u>Matthew 7:12</u> and <u>Luke 6:31</u> should
nevertheless apply.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Meir and Maimonides differ in their opinion as to whether our obligation
related to lost property applies to non-brothers (i.e. Gentiles); Meir
restricts his <i>mitzvot</i> to Jews who find the property of other Jews,
while Maimonides makes no such distinction. Neither Maimonides, nor Meir, nor
Hachinuch include <u>Deuteronomy 22:2</u> as a reference source for their
respective <i>mitzvot</i>. HaChinuch says that we must return property
belonging to a brother Jew, but he also quotes Sages who say that found
property that has no identifying features need not be returned. Examples of
such property are: scattered fruit, scattered money, small bundles of grain
in a public area, round packets of pressed figs, strings of fish, pieces of
meat, and similar items. The rationale for this is that when one loses these
kinds of items, the owner gives up the hope of retrieving them, and thereby
loses his right of ownership.
