<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides' <i>mitzvah</i> RP175 and Chinuch's C78 are worthy of special
mention because of how Maimonides and HaChinuch translate <u>Exodus 23:2</u>.
Both of their <i>mitzvot</i> follow <i>TB <u>Hullin
112</u></i><sup><small><a name=fr2 href=#fn2>2</a></small></sup>
in declaring that the majority should rule in a legal case when there is a
dispute among judges. Not contrary but different, the Complete Jewish Bible
(in basic agreement with Christian translations) states:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>Do not follow the crowd when it does what is wrong; and don't allow the
popular view to sway you into offering testimony for any cause if the effect
will be to pervert justice.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
That notwithstanding, the following English translation of the
<i>Tanakh</i><sup><small><a name=fr3 href=#fn3>3</a></small></sup>
supports Maimonides and HaChinuch by translating <u>Exodus 23:2</u>:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>Thou shalt not follow a multitude to do evil; neither shalt thou speak in
a cause to decline after many to wrest judgment.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Clearly, this second translation is quite opposite in that its phrase "to
decline after many to wrest judgment" supports Maimonides' and HaChinuch's
<i>mitzvot</i> RP175 and C78 respectively, whereas the CJB translation does
not.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
It is important to note that even when a <i>mitzvah</i> of Mosaic vintage
appears to have application today, the application may have to be adjusted.
An example would be the <i>mitzvah</i> to not convict a person on the
testimony of only one witness. The caution is still observed, but today's
forensic technologies (e.g. ballistic and DNA testing, photographs, voice
recordings, etc.) allow very great certainty in connecting suspects to crimes
without their being even a single eyewitness.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
A word is needed regarding Maimonides' departure from Meir and HaChinuch
concerning the meaning of <u>Deuteronomy 17:11</u>. Meir and HaChinuch
interpret "<i>the place which ADONAI your God will choose</i>" as the
<i>Sanhedrin</i>. Maimonides, on the other hand, in his RN312, interprets the
Scripture to mean that we are not to disagree with the authorized conveyors
of tradition.
