<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The Scriptures that teach against murder appear to be of two kinds - the
majority of them speak only of killing the physical body, and a few of them
hint at a non-physical kind of murder - hostility in one's heart to one's
brother.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Of all the Scriptures on murder, <u>Exodus 20:13</u> is the most well-known
because it is among the "Ten Commandments". Murder (the intentional
unrighteous killing of a human being) is universally perceived to be wrong,
even by those who do not believe in God; it is what secularists may sometimes
call "natural law". God Himself does not kill with impunity either. He
values those whom He has created in His image and, when men do otherwise to
their fellow men with intention, God deems it evil as though they are seeking
to destroy God Himself. We see this clearly in <u>Genesis 9:6</u>, which
associates murder with destroying God's image; it reads:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>Whoever sheds human blood, by a human being will his own blood be shed;
for God made human beings in his image.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Perhaps the same rationale explains <u>1 John 3:15</u> that extends the sin
of murder to hating one's brother, for it says:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>Everyone who hates his brother is a murderer, and you know that no
murderer has eternal life in him.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Seeking to kill a man who is created in God's image is akin to coming against
God Himself, and is quite the opposite of receiving Him as Lord and Messiah
by which we are saved. In a similar way, <u>Matthew 5:22</u> states:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>But I tell you that anyone who nurses anger against his brother will be
subject to judgment; that whoever calls his brother, 'You good-for-nothing!'
will be brought before the Sanhedrin; that whoever says, 'Fool!' incurs the
penalty of burning in the fire of Gei-Hinnom!</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Murder, probably because of its permanently destructive nature, is presented
as the quintessential opposite of loving our neighbor for, as <u>Romans
13:10</u> states:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>Love does not do harm to a neighbor; therefore love is the fullness of
Torah.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
In the Bible, the penalty for murder is always death. That notwithstanding,
the biblical law (similar to modern secular law) recognizes a category of
justifiable killing and also unjustifiable killing that is a lesser crime
than murder (terms used today are "murder in the second degree",
"manslaughter", "negligent homicide", etc.). Murder is ordinarily
distinguished from these lesser offenses by whether there was intention,
deliberation, premeditation, provocation, and malice. Examples of
justifiable killing are self-defense and killing in battle. Culpability in
the case of accidental killing has to do with whether or not negligence
contributed to the death.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Not precisely analogous but interestingly, whereas modern systems of justice
typically employ imprisonment as a penalty for these lesser categories of
homicide, the biblical law provides cities of refuge to which a man who has
killed another (excluding intentional murder) might flee to avoid being
seized upon and killed by the deceased's vengeful relatives.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Daniel C. Juster</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The Bible knows nothing of our modern justice system where so very many
crimes are punishable by jail time for the offender and often no restitution
in any real way for the victim. The <i>Torah</i>, instead, bases its justice
system on two categories. The first of them is capital crimes for which the
perpetrator is put to death. The second is all other crimes. For the latter,
there are penalties that both restore the offender and make restitution to
the offended. The community, under its judges (elders), oversees the
restitution process.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
We may argue that capital punishment should be carried out in cases of
capital crimes committed in modern society, or we can argue that, due to the
difficulty of doing so and due to the many examples of false convictions
(although capital punishment is legitimate in principle and goes back to
Noah), life imprisonment is more appropriate and potentially more just.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Of course, biblical laws need to be updated for application in modern
society, but their principles still hold. Really, the prison system for
non-capital crimes is counter-productive compared to the biblical
prescriptions. In the Bible, one working off one's debt through indentured
slavery is merely a means for making restitution. In our age, the analogy
would be a judge attaching an employees' wages so that the garnished person
is in a partially parallel situation to the indentured slave whose slavery
lasts until the debt is paid off. Other crimes, like sexual intercourse
before marriage, are also dealt with by restitution, where the male subject
has to pay the bride price to the parents, and marry the woman if the father
so desires. I am aware that these remarks are gratuitously general, and
depart from the subject of this <i>Mitzvah</i> which is murder.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Even in the case of homicide, modern societies should consider replacing at
least a part of their justice systems that utilizes jail time, with methods
of procuring restitution for those injured (the families of the one killed in
the case of homicide). Where a person simply cannot abide by community
oversight and restitution or because he is criminally dangerous, then perhaps
jail is the only alternative, but it should be the last one considered.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides' <i>mitzvah</i> commands against murder (physical killing),
references <i><u>Sanhedrin 9</u></i> and <i><u>Makkoth 2</u></i>, and
prescribes death by beheading.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Meir prescribes beheading as well, but only if the murder is by the
perpetrator's own hand. If he only caused the death, he is not punishable by
a <i>bet din</i> but only by "Heaven's hands". Ironically, this may even be
the case if one pays another to commit murder. Meir also opines that if one
kills even a single Jewish person, it is as though he slew the entire world.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
HaChinuch begins with citing <u>Genesis 1:28</u>:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>God blessed them: God said to them, 'Be fruitful, multiply, fill the earth
and subdue it. Rule over the fish in the sea, the birds in the air and every
living creature that crawls on the earth.'</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
He sees murder as violating God's plan that human beings multiply and settle
the world. He also cites <u>Proverbs 11:10</u> and <u>28:17</u> as
justification for executing murderers. As does Maimonides, HaChinuch
references <u><i>Sanhedrin 9</i></u> and <u><i>Makkoth 2</i></u>. None of
the commentators refer to <u>Genesis 9:6</u> or suggest a connection between
murder and God's image.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
