to death; rather, he must be put to death. Likewise, you are not to accept
for someone who has fled to his city of refuge a ransom that would allow him
to return to his land before the death of the <i>cohen</i>. In this way you
will not defile the land in which you are living. For blood defiles the
land, and in this land no atonement can be made for the blood shed in it
except the blood of him who shed it. No, you are not to defile the land in
which you live and in which I live; for I, ADONAI, live among the people of
Isra&#039;el.</i>
