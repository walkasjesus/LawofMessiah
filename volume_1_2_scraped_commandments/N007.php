<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This <i>Mitzvah</i> is derived from <u>Deuteronomy 22:8</u>, which teaches
that one must build protective railings around the roofs of our houses, and
from the more general commandment, <u>Leviticus 19:18</u>, which commands
that we show love to our neighbor. Our concern for the safety of our fellow
man is a reflection of how God cares for and protects us.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The <u>Deuteronomy</u> Scripture was commanded at a time when the roofs of
houses were flat living areas, and it was intended to keep our families, our
neighbors, and ourselves safe from falling off. The general principle of this
<i>Mitzvah</i> that is derived from Scripture is that we should take
responsibility for the safety of ourselves and others by removing dangerous
conditions from areas that are under our control. While most roofs today are
not flat living spaces, some still are, and there are other conditions of
danger that we must not allow to exist such as unfenced or uncovered swimming
pools, the harboring of unchained vicious animals, dangerous implements and
substances left accessible to children, etc.; there are too many to list them
all.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides' positive <i>mitzvah</i> RP184 requires that we remove conditions
of danger from our homes, and his negative <i>mitzvah</i> RN298 commands that
we not cause or allow dangerous conditions to exist, or leave obstacles that
could cause injury in either public or private places. Meir's positive
<i>mitzvah</i> MP75 requires that we erect a fence around our roofs and
remove all obstacles in our house upon which a person can trip and fall, and
his negative <i>mitzvah</i> MN190 is related to it in that it prohibits
allowing any dangerous condition in our houses to persist. HaChinuch's two
<i>mitzvot</i> run parallel to those of Meir, so Maimonides is the only one
of the three who extends responsibility for safety to public areas.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
