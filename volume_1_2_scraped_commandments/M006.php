<font size=4 face="Times New Roman, Times, serif">
<b><i>We are not to steal or commit robbery.</i></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This precept is derived from His Word (blessed be He):
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Key Scriptures</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Exodus 20:13(15)</u>
(Meir MN33; Chinuch C36)
<br>
<i>Do not murder. Do not commit adultery. Do not steal. Do not give false
evidence against your neighbor.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Exodus 21:16</u>
(Maimonides RN243; Chinuch C36)
<br>
<i>Whoever kidnaps someone must be put to death, regardless of whether he has
already sold him or the person is found still in his possession.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Exodus 21:37-22:3 (22:1-4)</u>
(Maimonides RP239; Chinuch C54)
<br>
<i>If someone steals an ox or a sheep and slaughters or sells it, he is to pay
five oxen for an ox and four sheep for a sheep. If a thief caught in the
act of breaking in is beaten to death, it is not murder; unless it happens
after sunrise, in which case it is murder. A thief must make restitution; so
if he has nothing, he himself is to be sold to make good the loss from the
theft. If what he stole is found alive in his possession, he is to pay
double, no matter whether it is an ox, a donkey or a sheep.</i>
