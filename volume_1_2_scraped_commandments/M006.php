<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides, Meir, and HaChinuch treat their respective <i>mitzvot</i>
similarly to one another. They command against stealing or robbing, and
discuss the amount of restitution that is required under various
circumstances. They do not, however, address theft and robbery in terms of
their inherent immorality, or suggest that there is a requirement that the
offender repent.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
There is an unusual application (by all three classical commentators) of the
<u>Exodus 20</u> Scriptures that prohibit stealing in that they do not
discuss them in connection with the stealing of property, but rather
regarding kidnapping - the stealing of people - and, in particular, Jewish
people. This is strange, not only because the immorality of kidnapping human
beings is self-evident, but also because <u>Exodus 21:16</u> and
<u>Deuteronomy 24:7</u> that specifically prohibit kidnapping are not dealt
with in any of the classical <i>mitzvah</i> compilations.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
