</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Stealing is taking from someone, that to which the taker has no right. One
who steals is called a thief. Robbery is similar in that there is an
unlawful taking, but it is more severe in that the taking is by force. One
who commits robbery is called a robber. The prohibition against stealing is
one of the Ten Commandments; stealing and committing robbery are dealt with
in separate Scriptures.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
One might ask why <u>Psalm 62:11(10)</u> calls extortion robbery, since there
is no apparent force employed. The answer is that the kind of force which
elevates stealing to robbery does not have to be physical force. Coercion
and threats are a form of force, in that they achieve their end by evoking
fear. It is similar with <u>Leviticus 19:13</u>, which calls delaying the
payment of wages not only theft, but robbery. That is because the employer,
in delaying payment to his worker, improperly exercises coercive power he has
over his worker - a form of force.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Stealing and robbery are not only technical sins, but are also moral ones in
that the perpetrator is willing to harm another individual. This intentional
causing of harm to another automatically violates <u>Leviticus 19:18</u>:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>... love your neighbor as yourself ...</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
as well as a plethora of other Scriptures commanding that we love (and
therefore not harm) others.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
If one is caught stealing, he must pay restitution and, under certain
circumstances, must pay several times the restitution. The higher goal,
however, is for the thief or robber to experience a heart-change and be moved
to repentance even if he is not caught. Zakkai the tax collector is an
example of such a man when, touched by the Spirit in <u>Luke 19:8</u>, blurts
out to Yeshua:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>Here, Lord, I am giving half of all I own to the poor; and if I have
cheated anyone, I will pay him back four times as much.</i> Yeshua responds
in <u>verse 9</u>: <i>Today salvation has come to this house, inasmuch as
this man too is a son of Avraham. For the Son of Man came to seek and save
what was lost.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This same need for heartfelt repentance is expressed in <u>Ephesians 4:28</u>
which instructs the thief to stop stealing and, instead, to earn honest money
that he can "share with those in need". A noted writer Jay E. Adams asks
rhetorically: "When is a thief not a thief? When he stops
stealing?"<sup><small><a name=fr1 href=#fn1>1</a></small></sup>
Then he answers his own question: "No, if the only thing a thief has done is
to stop stealing, he remains a thief who, at the moment, is not stealing."
Adams goes on to explain that it takes a repentant change of heart for a
thief to become an honest man; only then is he no longer a thief.
</font></p><hr width=150px style="margin-left: 10px">
<p align=left style="margin-left: 30px; margin-right: 30px">
