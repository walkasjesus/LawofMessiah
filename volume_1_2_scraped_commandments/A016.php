<p align=left>
<font size=6>
<b>A16. </b>
</font>
<font size=5>
<b>Seeking to Please Men over God.</b>
</font>
<font size=3 face="Times New Roman, Times, serif">
&nbsp;&nbsp;
<a href="comment.php?d=A016&h=A16"
onClick="return show_hide_box(this,400,280,'1px solid')">[Make a Comment]</a>
</font>
</p>
<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><i>We are not to seek men's approval over God's.</i></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This precept is derived from His Word (blessed be He):
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Key Scriptures</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Matthew 6:2-4</u>
<br>
<i>So, when you do tzedakah, don&#039;t announce it with trumpets to win
people&#039;s praise, like the hypocrites in the synagogues and on the
streets. Yes! I tell you, they have their reward already! But you, when you
do tzedakah, don&#039;t even let your left hand know what your right hand is
doing. Then your tzedakah will be in secret; and your Father, who sees what
you do in secret, will reward you.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Galatians 1:10</u>
<br>
<i>Now does that sound as if I were trying to win human approval? No! I want
God&#039;s approval! Or that I&#039;m trying to cater to people? If I were
still doing that, I would not be a servant of the Messiah.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Supportive Scriptures</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Luke 16:15</u>
<br>
<i>He said to them, "You people make yourselves look righteous to others, but
God knows your hearts; what people regard highly is an abomination before
God!"</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>John 12:42-43</u>
<br>
<i>Nevertheless, many of the leaders did trust in him; but because of the
P&#039;rushim they did not say so openly, out of fear of being banned from
the synagogue; for they loved praise from other people more than praise from
God.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Acts 5:29</u>
<br>
<i>Kefa and the other emissaries answered, "We must obey God, not men."</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Ephesians 6:5-8</u>
<br>
<i>Slaves, obey your human masters with the same fear, trembling and
single-heartedness with which you obey the Messiah. Don&#039;t obey just to
win their favor, serving only when they are watching you; but serve as
slaves of the Messiah, doing what God wants with all your heart. Work
willingly as slaves, as people do who are serving not merely human beings
but the Lord. Remember that whoever does good work, whether he be a slave
or a free man, will be rewarded by the Lord.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Colossians 3:22</u>
<br>
<i>Slaves, obey your human masters in everything, not serving only when they
are watching you, to win their favor, but single-heartedly, fearing the
Lord.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>1 Thessalonians 2:4-6</u>
<br>
<i>Instead, since God has tested us and found us fit to be entrusted with Good
News, this is how we speak: not to win favor with people but with God, who
tests our hearts. For, as you know, never did we employ flattering talk,
nor did we put on a false front to mask greed - God is witness. Nor did we
seek human praise- either from you or from others.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Jude 1:16</u>
<br>
<i>These people are grumblers and complainers, they follow their evil passions,
their mouths speak grandiosities, and they flatter others to gain
advantage.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The un-regenerated man seeks man's approval over God's. It is a hold-over
from the fall of mankind in the Garden of Eden. But <u>Ephesians 4:22-24</u>
instructs us:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>... so far as your former way of life is concerned, you must strip off
your old nature, because your old nature is thoroughly rotted by its
deceptive desires; and you must let your spirits and minds keep being
renewed, and clothe yourselves with the new nature created to be godly, which
expresses itself in the righteousness and holiness that flow from the
truth.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
It is not wrong to seek men's approval so long as we put God's approval
first:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>1 Peter 2:13-14</u>&nbsp;&nbsp;<i>For the sake of the Lord, submit
yourselves to every human authority - whether to the emperor as being
supreme, or to governors as being sent by him to punish wrongdoers and praise
those who do what is good.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This <i>Mitzvah</i> is not addressed by any of the Jewish classical
commentators.
