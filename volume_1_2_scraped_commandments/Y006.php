<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
For Yeshua to say that to follow him we must leave our homes and members of
our family seems unreasonably extreme until we understand what he means. It
is disclosed in <u>Luke 18:22</u> in which Yeshua tells the young leader:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>There is one thing you still lack. Sell whatever you have, distribute the
proceeds to the poor, and you will have riches in heaven. Then come, follow
me!</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Verse 23</u> that follows tells the story:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>But when the man heard this, he became very sad, because he was very
rich.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
For him giving up wealth was a requirement because he was enamored of his
holdings to the extent that he gave it precedence over following Yeshua. For
many of us, money may not be the hindrance; what each of us must give up or
walk away from will be according to our own idolatries and excessive
affections.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
What Yeshua is calling us to do, is to divest ourselves of everything and
everyone that we care about more than we care about him. Were Yeshua a mere
mortal, his insistence on this would be akin to human slavery and sinful. But
Yeshua is not a mere mortal; "<i>in him, bodily, lives the fullness of all
that God is</i>" (<u>Colossians 2:9</u>), and he is God's son.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Although pursuing eternal life (salvation) is not commanded, it is the clear
benefit and result of following Yeshua.
