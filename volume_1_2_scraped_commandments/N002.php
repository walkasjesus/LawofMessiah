<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
There is a children's rhyme: "Sticks and stones may break my bones but words
can never harm me." It is taught to children as a lesson that they should
not retaliate against name-calling, but the weight of Scripture teaches that
the rhyme is not true. Indeed, without interfering with its intended lesson,
the rhyme should rather state: "Sticks and stones may break my bones but
words can hurt me deeply." The greater lesson of the substitute rhyme and of
Scripture is that we must guard our tongue to ensure its use for good and
holiness, and not for harm.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
There is a rabbinical <i>halachah</i> against <i>lashon hara</i>, (negative
speech) - literally "evil or bad tongue" - that is inspired by <u>Leviticus
19:16</u> (see also, <u>Ephesians 4:29</u> and <u>James 4:11</u>). The
<i>halachah</i> prohibits speaking negatively about a person, regardless of
the truth of what is said, and it also prohibits listening to or believing
such negative statements. It is regrettable that a number of English
translations of <u>Leviticus 19:16</u> render the Hebrew noun
"<i>rachiyl</i>" as "slander" because the English word "slander" connotes
both negativity and untruthfulness. "<i>Rachiyl</i>", on the other hand,
connotes negativity, but not necessarily untruthfulness, so a better
translation of "<i>rachiyl</i>" is "negative report".
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Speaking negatively about a person invariably harms him in some way, and
bringing harm to a person (except when sanctioned legally) is always wrong.
It is egregious to God, and especially so if the <i>lashon hara</i> is
against a leader that God Himself has set in office. That which happened to
Miryam when she murmured against Moses is the quintessential example of how
serious a sin it is; we read about it in <u>Numbers 12:1-2</u> and
<u>6-10</u>:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>Miryam and Aharon began criticizing Moshe on account of the Ethiopian
woman he had married, for he had in fact married an Ethiopian woman. They
said, "Is it true that ADONAI has spoken only with Moshe? Hasn't he spoken
with us too?" ADONAI heard them.
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
He said, "Listen to what I say: when there is a prophet among you, I, ADONAI,
make myself known to him in a vision, I speak with him in a dream. But it
isn't that way with my servant Moshe. He is the only one who is faithful in
my entire household. With him I speak face to face and clearly, not in
riddles; he sees the image of ADONAI. So why weren't you afraid to criticize
my servant Moshe?" The anger of ADONAI flared up against them, and he left.
But when the cloud was removed from above the tent, Miryam had tzara'at,
[leprosy] as white as snow. Aharon looked at Miryam, and she was as white as
snow.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Miryam and Aaron speaking against Moses and the grumbling referred to in
<u>James 5:9</u> are similar.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Matthew 5</u> and <u>Matthew 18</u> are relevant to the subject of
wrongful speech because speech so often causes (or is the result of) human
conflicts. <u>Matthew 5</u> speaks to the offender, and <u>Matthew 18</u> to
the offended and, in both cases, the required initial action is to hold our
tongue, not speak negatively about our brother to others, and go to the
person with whom we have the conflict. It is only after there has been no
success at reconciliation that others may become involved, and then, only in
a controlled manner. The second and final attempts at reconciliation
<u>Matthew 18:16 & 17</u> are parts of an authorized judicial process that
begins with mediation, and culminates in adjudication. Whatever is spoken
before witnesses at these two "judicial" sessions is considered testimony,
and is therefore exempt from the restrictions of <i>lashon hara</i>.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Uttering a curse against a person or against God is speech that intends harm,
is wrongful, and is prohibited.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
A number of the classical <i>mitzvot</i> aspire to control how we speak so as
not to harm others, but the one closest to this <i>Mitzvah</i> is Maimonides'
negative <i>mitzvah</i> RN301 (corresponding to MN77 and C236), that is based
on <u>Leviticus 19:16</u>, and which forbids us from bearing tales among our
people. Maimonides' <i>mitzvah</i> RN301 also prohibits "defaming", which
some rabbis and commentators believe means defaming the reputation of a bride
(see RP219).
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Meir and HaChinuch have much more than Maimonides to say about <u>Leviticus
19:16</u> and its relationship to <i>lashon hara</i>. First of all, they
interpret the Scripture narrowly as meaning that a person must not tell
another person that a third person has spoken negatively about him. Meir
adds that gossiping with evil intent is criminally worse than merely
tale-bearing, and that it has the effect of killing three people - the
gossiper, the person who receives the gossip, and the person who is gossiped
about. Neither Meir nor HaChinuch make mention of defaming a bride.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Meir speaks of punishment for violating <u>Leviticus 19:16</u> that will come
from <i>HaShem</i>, but HaChinuch is more temporally severe, and speaks of it
being our religious duty to end the life of person who threatens to inform on
another, and under certain other circumstances as well. If such executions
were ever authorized under the Mosaic Law, they were most certainly part of
the Sinai Covenant's judicial processes. Under no circumstances are they
authorized in the New Covenant under which we live today.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
One may wonder why this <i>Mitzvah</i> lists <u>Leviticus 19:17</u>, a
Scripture that speaks of rebuking our neighbor, but that does mention anyone
being wronged or harmed. It is because Maimonides, Meir, and HaChinuch
interpret and quote that Scripture as the basis for their respective
<i>mitzvot</i> RN303, MN77 and C240, which admonish us to not shame our
neighbor by rebuking him in public.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Another wrongful use of speech, recognized by the commentators, is cursing.
Maimonides, Meir, and HaChinuch constructed <i>mitzvot</i> that prohibit the
cursing of parents, judges, and Israelites, and Maimonides and HaChinuch each
wrote a <i>mitzvah</i> that prohibits the cursing of a ruler. Strangely,
none of them mention cursing God, that is specifically prohibited by
<u>Exodus 22:27(28)</u>, which all three quote to support their
<i>mitzvot</i>.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Leviticus 19:33</u> is listed because it is referenced by Maimonides and
HaChinuch in their RN252 and C63 (respectively), both of which assume that
the wrong alluded to in the Scripture is wrongful speech.
