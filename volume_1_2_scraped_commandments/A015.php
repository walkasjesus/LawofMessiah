<b>A15. </b>
</font>
<font size=5>
<b>Doing Religious Things in Order to Be Seen.</b>
</font>
<font size=3 face="Times New Roman, Times, serif">
&nbsp;&nbsp;
<a href="comment.php?d=A015&h=A15"
onClick="return show_hide_box(this,400,280,'1px solid')">[Make a Comment]</a>
</font>
</p>
<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><i>We are not to do religious things in order to be seen.</i></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This precept is derived from His Word (blessed be He):
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Key Scriptures</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Matthew 6:1-4</u>
<br>
<i>Be careful not to parade your acts of tzedakah in front of people in order
to be seen by them! If you do, you have no reward from your Father in
heaven. So, when you do tzedakah, don&#039;t announce it with trumpets to
win people&#039;s praise, like the hypocrites in the synagogues and on the
streets. Yes! I tell you, they have their reward already! But you, when you
do tzedakah, don&#039;t even let your left hand know what your right hand is
doing. Then your tzedakah will be in secret; and your Father, who sees what
you do in secret, will reward you.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Matthew 6:5-6</u>
<br>
<i>When you pray, don&#039;t be like the hypocrites, who love to pray standing
in the synagogues and on street corners, so that people can see them. Yes! I
tell you, they have their reward already! But you, when you pray, go into
your room, close the door, and pray to your Father in secret. Your Father,
who sees what is done in secret, will reward you.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Matthew 6:16-18</u>
<br>
<i>Now when you fast, don&#039;t go around looking miserable, like the
hypocrites. They make sour faces so that people will know they are fasting.
Yes! I tell you, they have their reward already! But you, when you fast,
wash your face and groom yourself, so that no one will know you are fasting
- except your Father, who is with you in secret. Your Father, who sees what
is done in secret, will reward you.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Supportive Scriptures</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Matthew 23:5</u>
<br>
<i>Everything they [the Pharisees] do is done to be seen by others; for they
make their t&#039;fillin broad and their tzitziyot long,</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Mark 12:38-40</u>
<br>
<i>As he taught them, he said, "Watch out for the kind of Torah-teachers who
like to walk around in robes and be greeted deferentially in the
marketplaces, who like to have the best seats in the synagogues and take the
places of honor at banquets, who like to swallow up widows&#039; houses
while making a show of davvening at great length. Their punishment will be
all the worse!"</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Luke 20:45-47</u>
<br>
<i>Within the hearing of all the people, Yeshua said to his talmidim, "Watch
out for the kind of Torah-teachers that like to walk around in robes and be
greeted deferentially in the marketplaces, the kind that like to have the
best seats in the synagogues and the places of honor at banquets, the kind
that swallow up widows&#039; houses while making a show of davvening at
great length. Their punishment will be all the worse!"</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This <i>Mitzvah</i> exhorts us to not do religious things for the purpose of
being seen and receiving the adulation of others. It is the exact opposite
of <u>James 1:27</u> which states:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>Pure and undefiled religion before God and the Father is this: to visit
orphans and widows in their trouble, and to keep oneself unspotted from the
world.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This <i>Mitzvah</i> is not addressed by any of the Jewish classical
commentators.
