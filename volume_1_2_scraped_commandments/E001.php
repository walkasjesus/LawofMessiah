<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The Jewish People (previously called Israelites and defined as those
descended from Abraham, Isaac, and Jacob), are God's original and primary
priesthood to the Gentile nations. This priesthood was proclaimed during the
time of Moses, and its purpose was (and still is) to bring the Word of God
(indeed God Himself) to those in the world who do not know Him.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
By virtue of Yeshua's sacrifice and the New Covenant that he brought, all who
receive Yeshua as Lord and savior become affiliated through grafting into a
"Jewish" olive tree (<u>Romans 17:11</u>, etc.), not to lose their original
intrinsic identities as Jews or non-Jews, but to become members together in a
new (co-existing) priesthood that has come to be known as the "priesthood of
believers" (<u>1 Peter 2:4-5</u>). This new priesthood has functions and
responsibilities that overlap with those of the Jewish priesthood, but the
latter retains its historic place in preserving and disseminating the Word of
God, and now being the host priesthood to the priesthood of believers.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides, Meir, and HaChinuch do not address disseminating God's Word to
others directly. They imply it by emphasizing commandments (e.g.
<i>t'fillin</i>, <i>m'zuzot</i>, <i>tzitzit</i>) that are visible reminders.
