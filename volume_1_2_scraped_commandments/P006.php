<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The <u>Leviticus 11, 21, and 22</u> Scriptures quoted above are cited by
Maimonides and HaChinuch as cumulatively commanding the Levitical
<i>cohanim</i> of old to refrain from ministering in the Holy Sanctuary while
they are ceremonially unclean. I have expressed my view elsewhere in this
book, that ceremonial cleanness (ritual purity) in the Tabernacle and Holy
Temple of old are analogous to repentant holiness today when our New Covenant
Temples are not of brick and mortar, but are our physical bodies (<u>1
Corinthians 3:16-17</u>). Since Scripture teaches that we who are Yeshua's
disciples are priests under His New Covenant Priesthood (<u>1 Peter 2:4-5,
9-10</u>), how much more must the commandment to the Levitical <u>cohanim</u>
apply to us! We must therefore refrain from ministering to others while we
ourselves are spiritually unclean and in need of repentance. That is not an
invitation to "stop ministering" - it is a commandment to "repent before
ministering."
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This principle that a priest had to be spiritually clean before he could
minister in the Temple was operative even as far back as Moses. We find a
powerful example of this in <u>Leviticus 16:5-11, 15-19</u>, which commands
the <i>Cohen Gadol</i> (on <i>Yom Kippur</i>) to first sacrifice a bull for
himself and his family before he could conduct sacrifices in behalf of the
people of Israel. <u>Matthew 5:23-24</u> teaches that if we have sinned
against our brother (or he thinks we have), we are to make things right with
our brother (i.e. repent of our sin and reconcile with him) even before
presenting our offering to God. That of course speaks of a time when animal
sacrifices were still being offered in the Holy Temple, but the application
for today is clear: We should not think that we can have a reconciled
relationship with our Father in heaven without first repenting of our sins
and reconciling with our brother on earth.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Matthew 7:3-5</u> and <u>Luke 6:41-42</u> teach a related lesson using the
example of a splinter in the eye - something with which we are all familiar.
The smallest splinter in our eye is so disabling to our sight that it might
as well be an entire log. The splinter and log are symbolic of sin, and we
are instructed to correct ourselves and repent of our own sin (that blocks
our sight and judgment) before we are qualified and able to minister
correction and repentance to our brother.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides' <i>mitzvah</i> RN75 and HaChinuch's corresponding <i>mitzvah</i>
C278 prohibit <i>cohanim</i> from ministering in the Temple sanctuary while
they are ceremonially unclean; there is no Meir counterpart. Also,
Maimonides' <i>mitzvah</i> RN76 and HaChinuch's corresponding <i>mitzvah</i>
C265 prohibit <i>cohanim</i> who are <i>t'vul yom</i> (bathed but unclean
until sundown) from serving in the Temple sanctuary; there is no Meir
counterpart of this <i>mitzvah</i> either. I do not see how Maimonides and
HaChinuch derive their respective <i>mitzvot</i> from the Scriptures they
cite, but other Scriptures on the subject make it clear that they are
correct.
