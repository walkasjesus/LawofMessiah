<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
It is a repeated teaching of Scripture that cleanliness of a man's body is
analogized to cleanliness of his spirit. Scripture teaches that both are
important, but that cleanliness of spirit is weightier in that if a man's
spirit is not clean, no amount of washing with water can cleanse him. A man
becomes spiritually clean by submitting himself in obedience to God,
repenting of his sins, and receiving Messiah Yeshua as his personal savior.
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>Some of you used to do these things [premarital sex, idol worship,
adultery, homosexuality, theft, greed, drunkenness]. But you have cleansed
yourselves, you have been set apart for God, you have come to be counted
righteous through the power of the Lord Yeshua the Messiah and the Spirit of
our God.</i> (1 Corinthians 6:11)
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>If we acknowledge our sins, then, since he is trustworthy and just, he
will forgive them and purify us from all wrongdoing.</i> (1 John 1:9)
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
