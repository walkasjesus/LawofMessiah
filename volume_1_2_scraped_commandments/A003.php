<b>A PARABLE OF GOOD & BAD STEWARDSHIP</b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Luke 12:40-48</u>
<br>
<i>"You too, be ready! For the Son of Man will come when you are not expecting
him." Kefa said, "Sir, are you telling this parable for our benefit only or
for everyone&#039;s?" The Lord replied, "Nu, who is the faithful and
sensible manager whose master puts him in charge of the household staff to
give them their share of food at the proper time? It will go well with that
servant if he is found doing his job when his master comes. Yes, I tell you
he will put him in charge of all he owns. But if that servant says to
himself, &#039;My master is taking his time coming,&#039; and starts
bullying the men- and women-servants, and eating and drinking, getting
drunk, then his master will come on a day when the servant isn&#039;t
expecting him, at a time he doesn&#039;t know in advance; his master will
cut him in two and put him with the disloyal. Now the servant who knew what
his master wanted but didn&#039;t prepare or act according to his will, will
be whipped with many lashes; however, the one who did what deserves a
beating, but didn&#039;t know, will receive few lashes. From him who has
been given much, much will be demanded - from someone to whom people entrust
much, they ask still more."</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b>A MANAGER'S QUESTIONABLE STEWARDSHIP OF WORLDLY WEALTH AS EVIDENCE OF HIS
TRUSTWORTHINESS WITH HEAVENLY WEALTH</b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Luke 16:1-13</u>
<br>
<i>Speaking to the talmidim, Yeshua said: "There was a wealthy man who employed
a general manager. Charges were brought to him that his manager was
squandering his resources. So he summoned him and asked him, &#039;What is
this I hear about you? Turn in your accounts, for you can no longer be
manager.&#039; &#039;What am I to do?&#039; said the manager to himself.
&#039;My boss is firing me, I&#039;m not strong enough to dig ditches, and
I&#039;m ashamed to go begging. Aha! I know what I&#039;ll do - something
that will make people welcome me into their homes after I&#039;ve lost my
job here!&#039; So, after making appointments with each of his
employer&#039;s debtors, he said to the first, &#039;How much do you owe my
boss?&#039; Eight hundred gallons of olive oil,&#039; he replied.
&#039;Take your note back,&#039; he told him. &#039;Now, quickly! Sit down
and write one for four hundred!&#039; To the next he said, &#039;And you,
how much do you owe?&#039; &#039;A thousand bushels of wheat,&#039; he
replied. &#039;Take your note back and write one for eight hundred.&#039;
