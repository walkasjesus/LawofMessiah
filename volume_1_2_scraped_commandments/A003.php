<i>So stay alert, because you know neither the day nor the hour. For it will
be like a man about to leave home for a while, who entrusted his possessions
to his servants. To one he gave five talents [equivalent to a hundred
years&#039; wages]; to another, two talents; and to another, one talent - to
each according to his ability. Then he left. The one who had received five
talents immediately went out, invested it and earned another five.
Similarly, the one given two earned another two. But the one given one
talent went off, dug a hole in the ground and hid his master&#039;s money.
After a long time, the master of those servants returned to settle accounts
with them. The one who had received five talents came forward bringing the
other five and said, &#039;Sir, you gave me five talents; here, I have made
five more.&#039; His master said to him, &#039;Excellent! You are a good
and trustworthy servant. You have been faithful with a small amount, so I
will put you in charge of a large amount. Come and join in your
master&#039;s happiness!&#039; Also the one who had received two came
forward and said, &#039;Sir, you gave me two talents; here, I have made two
more.&#039; His master said to him, &#039;Excellent! you are a good and
trustworthy servant. You have been faithful with a small amount, so I will
put you in charge of a large amount. Come and join in your master&#039;s
happiness!&#039; Now the one who had received one talent came forward and
said, &#039;I knew you were a hard man. You harvest where you didn&#039;t
plant and gather where you didn&#039;t sow seed. I was afraid, so I went
and hid your talent in the ground. Here! Take what belongs to you!&#039;
&#039;You wicked, lazy servant!&#039; said his master, &#039;So you knew,
did you, that I harvest where I haven&#039;t planted? and that I gather
where I didn&#039;t sow seed? Then you should have deposited my money with
the bankers, so that when I returned, I would at least have gotten back
interest with my capital! Take the talent from him and give it to the one
who has ten. For everyone who has something will be given more, so that he
will have more than enough; but from anyone who has nothing, even what he
does have will be taken away. As for this worthless servant, throw him out
in the dark, where people will wail and grind their teeth!&#039;</i>
