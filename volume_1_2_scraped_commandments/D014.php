<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
There are many verses in Scripture (e.g. in <u>Psalms</u> and <u>Philippians
4:4</u>) that exhort us to rejoice in the Lord, and <u>1 Thessalonians
5:16</u> that exhorts us to rejoice at all times. That being said, the above
Scriptures single out three festivals (<i>Shavuot</i>, <i>Sukkot</i>, and
<i>Sh'mini Atzeret</i> - the eighth day assembly) for rejoicing - possibly
because they are festivals of harvest.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Most of the appointed times listed in <u>Leviticus 23</u>, while required to
be kept by Jews, are only optional for Gentiles who are not <i>K'rovei
Yisrael</i>. Sukkot is an exception however, because of <u>Zechariah
14:16</u>'s reference to "all the nations". That notwithstanding, its
description is of the "Age to Come", so its requirement for Gentile
participation in this age cannot be established. However, Gentiles
connecting to its meaning is important, however, so the season appears to be
a fitting time for Jewish and Gentile congregations to come together for
celebration and worship.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides, Meir, and HaChinuch rely on <u>Deuteronomy 16:14</u> (Maimonides
and HaChinuch rely additionally on <u>Deuteronomy 27:7</u>) to state that we
are to rejoice in (celebrate) all of God's feasts. They do not limit their
<i>mitzvot</i> of celebration to <i>Shavuot</i>, <i>Sukkot</i> and <i>Sh'mini
Atzeret</i>, although these are the only festivals in Scripture on which
rejoicing or celebrating is specifically commanded.
