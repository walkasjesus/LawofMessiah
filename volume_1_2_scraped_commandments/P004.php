<u>Hebrews 8:6-13</u>
<br>
<i>But now the work Yeshua has been given to do is far superior to theirs, just
as the covenant he mediates is better. For this covenant has been given as
Torah on the basis of better promises. Indeed, if the first covenant had
not given ground for faultfinding, there would have been no need for a
second one. For God does find fault with the people when he says,
"&#039;See! The days are coming,&#039; says ADONAI, &#039;when I will
establish over the house of Isra&#039;el and over the house of Y&#039;hudah
a new covenant. &#039;It will not be like the covenant which I made with
their fathers on the day when I took them by their hand and led them forth
out of the land of Egypt; because they, for their part, did not remain
faithful to my covenant; so I, for my part, stopped concerning myself with
them,&#039; says ADONAI. "&#039;For this is the covenant which I will make
with the house of Isra&#039;el after those days,&#039; says ADONAI: &#039;I
will put my Torah in their minds and write it on their hearts; I will be
their God, and they will be my people. &#039;None of them will teach his
fellow-citizen or his brother, saying, "Know ADONAI!" For all will know me,
from the least of them to the greatest, because I will be merciful toward
their wickednesses and remember their sins no more.&#039;" By using the
term, "new," he has made the first covenant "old"; and something being made
old, something in the process of aging, is on its way to vanishing
altogether.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
One of the most significant aspects of the New Covenant brought by Yeshua's
sacrificial death and resurrection is elevating to priesthood, those of us
that repent of our sins and receive him as Lord. In the Mosaic Covenant,
Israel was made by God to be a kingdom of priests, and later in the New
Covenant, he extended priesthood (not the same priesthood) to all believers
in Yeshua - Jews and Gentiles alike.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
There was another priesthood that was part of the Mosaic Covenant - the
Levitical Priesthood - in which Aaron was the High Priest. Only Aaron could
enter the Most Holy Place and then, only once a year on <i>Yom Kippur</i>.
Under the Levitical Priesthood, ordinary priests could not enter the Most
Holy Place, and were therefore limited in how close to God they could come.
Today's Priesthood of Believers is a superior priesthood in that through our
High Priest Yeshua, all believers in Yeshua can enter the Most Holy Place in
Heaven, and draw closer to God than the Levitical priests could. It is one
of the reasons that <u>Hebrews 8:6</u> can state:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>But now the work Yeshua has been given to do is far superior to theirs,
just as the covenant he mediates is better. For this covenant has been given
as Torah on the basis of better promises.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides and HaChinuch each wrote their <i>mitzvot</i> (RN68 and C184
respectively) as though the Holy Temple of God was still standing. They
state that the High Priest can only enter the Temple's sanctuary at a
prescribed time once a year. Meir did not write a <i>mitzvah</i> on the
subject.
