<font size=4 face="Times New Roman, Times, serif">
<b><u>Key Scriptures</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Deuteronomy 31:9</u>
(Maimonides RP18; Meir MP15; Chinuch C613)
<br>
<i>Therefore, write this song for yourselves, and teach it to the people of
Isra&#039;el. Have them learn it by heart, so that this song can be a
witness for me against the people of Isra&#039;el.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Revelation 15:1-3</u>
<br>
<i>Then I saw another sign in heaven, a great and wonderful one - seven angels
with the seven plagues that are the final ones; because with them, Gods
fury is finished. I saw what looked like a sea of glass mixed with fire.
Those defeating the beast</i>
(its image and the number of its name were
standing by the sea of glass)
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The "Song of Moses" to which this <i>Mitzvah</i> refers is recorded in
<u>Deuteronomy
32:1-43</u>.<sup><small><a name=fr1 href=#fn1>1</a></small></sup>
The reason given for the people of Israel to learn it was so that its
contents would be a witness against them when they turn toward other gods,
God hides His face from them, and they begin to blame God for not being among
them. Although <u>Deuteronomy 31:19</u> points toward a particular
prophesied event in the history of Israel, it has general application for any
time God's chosen people turn against Him.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Even though God's instruction to Moses was for the Israelites to write this
particular portion of Scripture, the classical commentators do not even
address the "song" and instead write their <i>mitzvah</i> as commanding all
Israelites to write for themselves, purchase, or at the very least possess, a
copy of the <i>Torah</i> in its entirety. Although they do not explain their
reasons for coming to this conclusion, perhaps they were influenced both by
the fact that this was the last commandment given before Moses' death, and
also by the words of <u>Deuteronomy 31:22-29</u>:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>So Moshe wrote this song that same day and taught it to the people of
Isra'el. ADONAI also commissioned Y'hoshua the son of Nun with these words:
"Be strong and full of courage; for you are to bring the people of Isra'el
into the land about which I swore to them; and I will be with you." Moshe
kept writing the words of this Torah in a book until he was done. When he
had finished, Moshe gave these orders to the L'vi'im who carried the ark
with the covenant of ADONAI: "Take this book of the Torah and put it next
to the ark with the covenant of ADONAI your God, so that it can be there to
witness against you. For I know how rebellious and stiffnecked you are!
Here, even while I am still alive with you today, you have rebelled against
ADONAI; so how much more will you do so after my death? Assemble for me all
the leaders of your tribes and your officials, so that I can say these
things in their hearing, calling heaven and earth to witness against them -
because I know that after my death you will become very corrupt and turn
aside from the way that I have ordered you, and that disaster will come
upon you in the acharit-hayamim, because you will do what ADONAI sees as
evil and provoke him by your deeds."</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
I believe that the commentators are correct in broadening their
interpretation of this portion of Scripture, but are wrong in not retaining
God's original instruction to write, teach, and learn the Song of Moses. The
substance and message of the "song" is such that it makes sense for
individual Israelites to learn it, whereas, Moses' instruction to place the
full <i>Torah</i> next to the Ark of the Covenant was directed solely to the
Levites who were ministers of the people. Consequently, I believe that the
words of <u>Deuteronomy 31:19</u> are meant to be about the "song" only,
while Moses' instruction to the Levites to preserve the <i>Torah</i> (words
which we assume God gave him) was a separate directive meant for the
community's leaders. Although it can easily be separated out as a commandment
standing on its own, I have decided to combine the two instructions into one
<i>mitzvah</i>, understanding that it has both an individual and a corporate
component. Finally, in contemplating Israel's responsibility to preserve the
Words of the <i>Torah</i>, I cannot help but recall the <i>shaliach</i>
Paul's words in <u>Romans 3:1-2</u>:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>Then what advantage has the Jew? What is the value of being circumcised?
Much in every way! In the first place, the Jews were entrusted with the
very words of God.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
In light of this <i>Mitzvah</i>, it is important to consider how we should
comply with its two parts. In my opinion, the first part - the instruction
to write, teach, and learn the Song of Moses should be complied with by doing
just that. Every Jew should write it out at least once in his or her life
and, while doing so, contemplate and discuss the implications of falling away
from God and into idolatry. Such an exercise and study may well be included
as part of a young person's <i>Bar/Bat Mitzvah</i> preparation. The second
part - being instrumental in preserving the words of the <i>Torah</i> can be
accomplished in many ways and over a lifetime. We can become <i>Torah</i>
scribes and scholars, write books, teach, purchase and distribute Bibles,
read <i>Torah</i> from the <i>bimah</i>, and so on. It is also worth noting
that the manner in which synagogues preserve their <i>Sefer Torah</i> scrolls
in an <i>aron hakodesh</i> is closely analogous to how Moses instructed the
Levites to place theirs beside the Ark of the Covenant.
</font></p><hr width=150px style="margin-left: 10px">
<p align=left style="margin-left: 30px; margin-right: 30px">
