<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u><b>Classical Commentators</b></u>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides, Meir, and HaChinuch interpret <u>Deuteronomy 31:19</u> similarly.
Maimonides states that every Jew is to have a <i>Sefer Torah</i> - a
<i>Torah</i> scroll, and he then goes on to say that the man who hand-writes
one himself is to be praised, and that the effect of doing so is as though he
has received it personally from Mount Sinai. Maimonides further opines that
if a man cannot write a <i>Torah</i> scroll, he should at least purchase one
or have one made for him. Meir agrees and states, in addition, that
purchasing <i>chumashim</i> also fulfills the commandment, as does correcting
even a single letter in a <i>Torah</i> scroll that contains an error.
