<font size=4 face="Times New Roman, Times, serif">
<b><i>We are to preserve and not destroy things that belong to God and that
are holy.</i></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This precept is derived from His Word (blessed be He):
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Key Scriptures</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Deuteronomy 12:2-4</u>
(Maimonides RN65; Meir MN157; Chinuch C437)
<br>
<i>You must destroy all the places where the nations you are dispossessing
served their gods, whether on high mountains, on hills, or under some leafy
tree. Break down their altars, smash their standing-stones to pieces, burn
up their sacred poles completely and cut down the carved images of their
gods. Exterminate their name from that place. But you are not to treat
ADONAI your God this way.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Deuteronomy 20:19-20</u>
(Maimonides RN57; Meir MN191; Chinuch C529)
<br>
<i>When, in making war against a town in order to capture it, you lay siege to
it for a long time, you are not to destroy its trees, cutting them down with
an axe. You can eat their fruit, so don&#039;t cut them down. After all, are
the trees in the field human beings, so that you have to besiege them too?
However, if you know that certain trees provide no food, you may destroy
them and cut them down, in order to build siege-works against the town
making war with you, until it falls.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>John 2:13-16</u>
<br>
<i>It was almost time for the festival of Pesach in Y&#039;hudah, so Yeshua
went up to Yerushalayim. In the Temple grounds he found those who were
selling cattle, sheep and pigeons, and others who were sitting at tables
exchanging money. He made a whip from cords and drove them all out of the
Temple grounds, the sheep and cattle as well. He knocked over the
money-changers&#039; tables, scattering their coins; and to the
pigeon-sellers he said, "Get these things out of here! How dare you turn my
Father&#039;s house into a market?"</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Romans 3:1-2</u>
<br>
<i>Then what advantage has the Jew? What is the value of being circumcised?
Much in every way! In the first place, the Jews were entrusted with the very
words of God.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>2 Timothy 1:14</u>
<br>
<i>Keep safe the great treasure that has been entrusted to you, with the help
of the Ruach HaKodesh, who lives in us.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Revelation 22:18-19</u>
<br>
<i>I warn everyone hearing the words of the prophecy in this book that if
anyone adds to them, God will add to him the plagues written in this book.
And if anyone takes anything away from the words in the book of this
prophecy, God will take away his share in the Tree of Life and the holy
city, as described in this book.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This <i>Mitzvah</i> brings together two Scriptures that, at first glance,
have little in common. <u>Deuteronomy 12:2-4</u> commands the Israelites to
destroy all the places and objects used for idol-worship that they find among
the heathen nations they are dispossessing; however, if in the process of
doing so they come across places of worship and things belonging to
<i>HaShem</i> - those they must not destroy. Eight chapters later,
<u>Deuteronomy 20:19-20</u> forbids the Israelites from destroying fruit
trees while laying siege to a city.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The connection between these seemingly unrelated Scriptures is the attribute
of "holiness" as applied to the trees. God indeed wants places where
idolatry is practiced and implements used there to be destroyed, but He does
not want His holy places and things - things that belong to Him or that He
provides as a blessing for men (like the fruit trees) to be destroyed in the
process. Even when they have fallen into the hands of an enemy - holy things
of God retain their holiness and must not be destroyed.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Ephesians 4:29</u> illustrates this <i>Mitzvah</i> in a less tangible
form:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>Let no harmful language come from your mouth, only good words that are
helpful in meeting the need, words that will benefit those who hear them.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Here the holy possession of God that we must not harm or destroy is our
neighbor. The Scripture warns us that our words can be harmful to those who
hear them, so we must speak only good and helpful words in order to not bring
destruction to our neighbor who is God's holy creation.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Jewish <i>halachah</i> and tradition have long recognized the principle of
preserving, and not destroying, things that belong to God and that are holy.
An example is that we do not "trash" or tear up old Bibles, <i>Torah</i>
scrolls or <i>siddurim</i>. Instead, there is a procedure whereby we
ceremonially bury them in a way that is analogous to the way we bury a
deceased
person.<sup><small><a name=fr1 href=#fn1>1</a></small></sup>.
I am also reminded of the way in which <u>Leviticus 19:23-24</u> commands our
holy respect for fruit-bearing trees. We do not eat their fruit during the
first three years of their planting; the fourth year fruit is considered
holy, and all of it must be used for praising God. It is only from the fifth
year on that we may eat the fruit with an expectation of receiving its
blessing year after year. Consider also, our "practical" respect for trees
in Israel where we seek to plant them and not destroy them (e.g. <i>Tu
B'shvat</i>). By contrast, the Israel Defense Forces came under severe
criticism for destroying olive trees in the process of clearing land for
Israeli settlements.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This <i>Mitzvah</i> is mandated for both Jews and <i>K'rovei Yisrael</i>, and
is highly recommended for believing Gentiles as well. The particulars of how
to accomplish it need prayer - especially in these modern times when text
that includes God's Name can be deleted from a computer or from cyberspace at
the touch of a button.
</font></p><hr width=150px style="margin-left: 10px">
