<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This "positive" <i>Mitzvah</i> on "directing our speech, thoughts and actions
to that which is good and holy" is a companion to a "negative" <i>Mitzvah</i>
that "we are not to wrong one another through our speech." While the
compilations of rabbinical commentators list many of the positive and
negative elements of this <i>Mitzvah</i>, the weight of Scripture (that we
should use every aspect of our being for good) suggests that this
<i>Mitzvah</i> be written to combine them.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This <i>Mitzvah</i> quotes a goodly number of Scriptures that reference how
we are to use (and not use) speech and actions, but I want to also address
what might seem (at first) to be opposite, which is holding back on speech,
or remaining silent. <u>Proverbs 18:21</u> says that speech has power, but
silence also has power, as we read in <u>Isaiah 53:7</u>, a prophecy of
Yeshua's use of silence in the final hours of his life:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>He was oppressed and He was afflicted, Yet He opened not His mouth; He
was led as a lamb to the slaughter, and as a sheep before its shearers is
silent, So He opened not His mouth.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Here are some other Scriptures that suggest times when there is wisdom and
virtue in being slow to speak or in remaining silent:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
