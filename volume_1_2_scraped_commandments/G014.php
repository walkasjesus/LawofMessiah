<b><u>Supportive Scriptures - God Saw That His Creation was Good.</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Genesis 1:1-5</u>
(Day 1 of Creation)
<br>
<i>In the beginning God created the heavens and the earth. The earth was
unformed and void, darkness was on the face of the deep, and the Spirit of
God hovered over the surface of the water. Then God said, "Let there be
light"; and there was light. <b>God saw that the light was good</b>, and
God divided the light from the darkness. God called the light Day, and the
darkness he called Night. So there was evening, and there was morning, one
day.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Genesis 1:6-8</u>
(Day 2 of Creation)
<br>
<i>God said, "Let there be a dome in the middle of the water; let it divide the
water from the water." God made the dome and divided the water under the
dome from the water above the dome; that is how it was, and God called the
dome Sky. So there was evening, and there was morning, a second day.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Genesis 1:9-13</u>
(Day 3 of Creation)
<br>
<i>God said, "Let the water under the sky be gathered together into one place,
and let dry land appear," and that is how it was. God called the dry land
Earth, the gathering together of the water he called Seas, and <b>God saw
that it was good</b>. God said, "Let the earth put forth grass,
seed-producing plants, and fruit trees, each yielding its own kind of
seed-bearing fruit, on the earth"; and that is how it was. The earth brought
forth grass, plants each yielding its own kind of seed, and trees each
producing its own kind of seed-bearing fruit; and <b>God saw that it was
good</b>. So there was evening, and there was morning, a third day.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Genesis 1:14-19</u>
(Day 4 of Creation)
<br>
<i>God said, "Let there be lights in the dome of the sky to divide the day from
the night; let them be for signs, seasons, days and years; and let them be
for lights in the dome of the sky to give light to the earth"; and that is
how it was. God made the two great lights - the larger light to rule the
day and the smaller light to rule the night - and the stars. God put them in
the dome of the sky to give light to the earth, to rule over the day and
over the night, and to divide the light from the darkness; and <b>God saw
that it was good</b>. So there was evening, and there was morning, a
fourth day.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Genesis 1:20-23</u>
(Day 5 of Creation)
<br>
<i>God said, "Let the water swarm with swarms of living creatures, and let
birds fly above the earth in the open dome of the sky." God created the
great sea creatures and every living thing that creeps, so that the water
swarmed with all kinds of them, and there was every kind of winged bird; and
<b>God saw that it was good</b>. Then God blessed them, saying, "Be
fruitful, multiply and fill the water of the seas, and let birds multiply on
the earth." So there was evening, and there was morning, a fifth day.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Genesis 1:24-31</u>
(Day 6 of Creation)
<br>
<i>God said, "Let the earth bring forth each kind of living creature - each
kind of livestock, crawling animal and wild beast"; and that is how it was.
God made each kind of wild beast, each kind of livestock and every kind of
animal that crawls along the ground; and <b>God saw that it was good</b>.
Then God said, "Let us make humankind in our image, in the likeness of
ourselves; and let them rule over the fish in the sea, the birds in the air,
the animals, and over all the earth, and over every crawling creature that
crawls on the earth." So God created humankind in his own image; in the
image of God he created him: male and female he created them. God blessed
them: God said to them, "Be fruitful, multiply, fill the earth and subdue
it. Rule over the fish in the sea, the birds in the air and every living
creature that crawls on the earth." Then God said, "Here! Throughout the
whole earth I am giving you as food every seed-bearing plant and every tree
with seed-bearing fruit. And to every wild animal, bird in the air and
creature crawling on the earth, in which there is a living soul, I am giving
as food every kind of green plant." And that is how it was. <b>God saw
everything that he had made, and indeed it was very good.</b> So there was
evening, and there was morning, a sixth day.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>1 Timothy 4:4-5</u>
<br>
<i>For <b>everything created by God is good</b>, and nothing received with
thanksgiving needs to be rejected, because the word of God and prayer make
it holy.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>James 1:17-18</u>
<br>
<i>Every good act of giving and <b>every perfect gift is from above</b>, coming
down from the Father who made the heavenly lights; with him there is neither
