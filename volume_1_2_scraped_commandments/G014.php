<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><i>We are to know that God exists and Who God is by observing His
creation.</i></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This precept is derived from His Word (blessed be He):
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Key Scriptures</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Romans 1:18-32</u>
<br>
<i>What is revealed is God&#039;s anger from heaven against all the godlessness
and wickedness of <b>people who in their wickedness keep suppressing the
truth; because what is known about God is plain to them, since God has made
it plain to them. For ever since the creation of the universe his invisible
qualities - both his eternal power and his divine nature - have been clearly
seen, because they can be understood from what he has made. Therefore, they
have no excuse;</b> because, although they know who God is, they do not
glorify him as God or thank him. On the contrary, they have become futile in
their thinking; and their undiscerning hearts have become darkened.
Claiming to be wise, they have become fools! In fact, they have exchanged
the glory of the immortal God for mere images, like a mortal human being, or
like birds, animals or reptiles! This is why God has given them up to the
vileness of their hearts&#039; lusts, to the shameful misuse of each
other&#039;s bodies. They have exchanged the truth of God for falsehood, by
worshipping and serving created things, rather than the Creator - praised be
he forever. Amen. This is why God has given them up to degrading passions;
so that their women exchange natural sexual relations for unnatural; and
likewise the men, giving up natural relations with the opposite sex, burn
with passion for one another, men committing shameful acts with other men
and receiving in their own persons the penalty appropriate to their
perversion. In other words, since they have not considered God worth
knowing, God has given them up to worthless ways of thinking; so that they
do improper things. They are filled with every kind of wickedness, evil,
greed and vice; stuffed with jealousy, murder, quarrelling, dishonesty and
ill-will; they are gossips, slanderers, haters of God; they are insolent,
arrogant and boastful; they plan evil schemes; they disobey their parents;
they are brainless, faithless, heartless and ruthless. They know well enough
God&#039;s righteous decree that people who do such things deserve to die;
yet not only do they keep doing them, but they applaud others who do the
same.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
