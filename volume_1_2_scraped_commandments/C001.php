<font size=4 face="Times New Roman, Times, serif">
<b><i>We are to deal fairly and honestly in business.</i></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This precept is derived from His Word (blessed is He):
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Key Scriptures</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Exodus 22:20(21)</u>
(Maimonides RN253; Meir MN50; Chinuch C64)
<br>
<i>You must neither wrong nor oppress a foreigner living among you, for you
yourselves were foreigners in the land of Egypt.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Leviticus 5:21-24(6:2-5)</u>
(Maimonides RN248, 249; Chinuch C225, C226)
<br>
<i>If someone sins and acts perversely against ADONAI by dealing falsely with
his neighbor in regard to a deposit or security entrusted to him, by
stealing from him, by extorting him, or by dealing falsely in regard to a
lost object he has found, or by swearing to a lie- if a person commits any
of these sins, then, if he sinned and is guilty, he is to restore whatever
it was he stole or obtained by extortion, or whatever was deposited with
him, or the lost object which he found, or anything about which he has sworn
falsely. He is to restore it in full plus an additional one-fifth; he must
return it to the person who owns it, on the day when he presents his guilt
offering.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Leviticus 19:11-12</u>
(Maimonides RN248, 249; Meir MN30, 36; Chinuch C225, 226)
<br>
<i>Do not steal from, defraud or lie to each other. Do not swear by my name
falsely, which would be profaning the name of your God; I am ADONAI.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Leviticus 19:13</u>
(Maimonides RN247; Meir MN37; Chinuch C228)
<br>
<i>Do not oppress or rob your neighbor; specifically, you are not to keep back
the wages of a hired worker all night until morning.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Leviticus 19:35</u>
(Maimonides RN271; Meir MN83; Chinuch C258)
<br>
<i>Don&#039;t be dishonest when measuring length, weight or capacity.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Leviticus 19:36</u>
(Maimonides RP208; Chinuch C259)
<br>
<i>Rather, use an honest balance-scale, honest weights, an honest bushel
dry-measure and an honest gallon liquid-measure; I am ADONAI your God, who
brought you out of the land of Egypt.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Leviticus 25:14</u>
(Maimonides RP245, RN250; Meir MN47, MP67; Chinuch C336,
