</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This <i>Mitzvah</i> consolidates a number of God's <i>mitzvot</i> into the
single principle that we are to deal fairly and honestly when transacting
business with our neighbors; it applies equally to the buyer and to the
seller of both goods and services. The buyer must not defraud a seller by
purchasing an item which he knows (or suspects) is under-priced in error.
Likewise, a seller must not sell an item whose condition and value do not
justify its price.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Error in price is only one of several kinds of errors which can be made. It
is possible that either the buyer or the seller could err in the identity,
condition, or usability of an item. If an error is made and discovered within
a reasonable time by either party, it should be called to the other party's
attention, and an adjustment should be negotiated to meet Scripture's
requirement of fairness.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
In an ethical sale, nothing is concealed, there is full disclosure, and both
parties act willingly and without coercion. This is in partial contrast with
the British and American Common-law which does not countenance fraud, but
embraces the principle of <i>Caveat Emptor</i> - "Let the buyer beware!"
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Intentionally using inaccurate weights and measures in a transaction of
buying or selling is, of course, dishonest and sinful. However, it can also
be accidental, so Scripture prohibits even possessing weights and measures
that are inaccurate. To make the point graphic, Maimonides states that one
cannot possess an inaccurate measuring vessel, even if it is only used to
collect urine.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides draws on the Oral Tradition to say that one-sixth of the fair
price is the amount that an item can be overcharged or undercharged without
either side having recourse. It is noteworthy that Maimonides'
<i>mitzvah</i> RN253 commands that we are not to wrong a proselyte (convert
to Judaism) in matters of business. He derives this from <u>Exodus
22:20(21)</u>, which refers neither to conversion nor business. Meir and
HaChinuch follow Maimonides' reasoning, but broaden their respective
statements to include all matters involving monetary value.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
