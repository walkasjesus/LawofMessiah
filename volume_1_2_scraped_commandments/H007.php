<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><i>We are not to castrate a male human being other than for medical
necessity.</i></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This precept is derived from His Word (blessed be He):
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Key Scripture</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Leviticus 22:24</u>
(Maimonides RN361; Meir MN143; Chinuch C291)
<br>
<i>An animal with bruised, crushed, torn or cut genitals you are not to offer
to ADONAI. You are not to do these things in your land.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Supportive Scriptures</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Leviticus 21:17-23</u>
<br>
<i>Tell Aharon, &#039;None of your descendants who has a defect may approach to
offer the bread of his God. No one with a defect may approach - no one
blind, lame, with a mutilated face or a limb too long, a broken foot or a
broken arm, a hunched back, stunted growth, a cataract in his eye, festering
or running sores, or damaged testicles - no one descended from Aharon the
cohen who has such a defect may approach to present the offerings for ADONAI
made by fire; he has a defect and is not to approach to offer the bread of
his God. He may eat the bread of his God, both the especially holy and the
holy; only he is not to go in to the curtain or approach the altar, because
he has a defect - so that he will not profane my holy places, because I am
ADONAI, who makes them holy.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Deuteronomy 23:2</u>
<br>
<i>A man with crushed or damaged private parts may not enter the assembly of
ADONAI.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Castration is removing or doing irreparable damage to a male's testicles.
There is no specific commandment in Scripture prohibiting the castration of
human beings or animals, but we surmise it with regard to men from the
Scriptures cited that impute holiness-connected disabilities to castrated
men and beasts. Castrated men were prohibited from entering the assembly of
the Lord and could not serve as priests, and castrated animals could not be
used for sacrifice in the Temple.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
So far as I am aware, men-slaves are no longer castrated to produce eunuchs
for guarding harams or young boys castrated to preserve their soprano,
mezzo-soprano or contralto voices. Nevertheless, our society does neuter
male animals to change their behavior, their size, or their tenderness when
used for food. For example, steer meat comes from a castrated bull, a capon
"chicken" is a castrated rooster, and an ox is a steer that has been bred for
work. Castrating animals or fowls does not appear to prohibit their being
considered kosher for food.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Castration of men is occasionally done for medical reasons (e.g. cancer), and
our various societies have, on occasion, castrated especially sex offenders
as part of a punitive process.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides, Meir, and HaChinuch state that <u>Leviticus 22:24</u> (despite it
not saying so) prohibits castrating a male of any species whatsoever. Also,
Maimonides and HaChinuch (not Meir) reference <u>Leviticus 2:11</u> and
<u>Leviticus 6:10(17)</u>, neither of which seem to have relevance to the subject of castration.
