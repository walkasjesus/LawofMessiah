<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><i>(a) The gleanings and part of every harvest &amp; vintage must be left
for the poor &amp; disadvantaged.</i></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><i>(b)The gleanings and part of all life-sustaining commodities and
services should be reserved for the poor &amp; disadvantaged.</i></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This precept is derived from His Word (blessed be He):
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Key Scriptures</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Leviticus 19:9-10</u>
(Maimonides RP120-RP121, RP123-RP124, RN210-RN213; Meir
ML1-ML4, ML7-ML9; Chinuch C216-C223)
<br>
<i>When you harvest the ripe crops produced in your land, don&#039;t harvest
all the way to corners of your field, and don&#039;t gather the ears of
grain left by the harvesters. Likewise, don&#039;t gather the grapes left on
the vine or fallen on the ground after harvest; leave them for the poor and
the foreigner; I am ADONAI your God.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Leviticus 23:22</u>
(Maimonides RP121; Meir ML6-ML7)
<br>
<i>When you harvest the ripe crops produced in your land, don&#039;t harvest
all the way to the corners of your field, and don&#039;t gather the ears of
grain left by the harvesters; leave them for the poor and the foreigner; I
am ADONAI your God.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Deuteronomy 24:19-22</u>
(Maimonides RP122, RN212, RN214; Meir ML5, ML8, ML10;
Chinuch C221, C592-C593)
<br>
<i>When harvesting the grain in your field, if you forgot a sheaf of grain
there, you are not to go back and get it; it will remain there for the
foreigner, the orphan and the widow, so that ADONAI your God will bless you
in all the work you do. When you beat your olive tree, you are not to go
back over the branches again; the olives that are left will be for the
foreigner, the orphan and the widow. When you gather the grapes from your
vineyard, you are not to return and pick grapes a second time; what is left
will be for the foreigner, the orphan and the widow. Remember that you were
a slave in the land of Egypt. That is why I am ordering you to do this.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>1 Timothy 6:17-19</u>
<br>
<i>As for those who do have riches in this present world, charge them not to be
proud and not to let their hopes rest on the uncertainties of riches but to
rest their hopes on God, who richly provides us with all things for our
enjoyment. Charge them to do good, to be rich in good deeds, to be generous
and ready to share. In this way they will treasure up for themselves a good
foundation for the future, so that they may lay hold of the real life.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
In ancient Israel, the life-sustaining commodities were foods grown in the
soil, typically grains and grapes. God's commandments to leave the gleanings
and reserve a part of the fields and vineyards for the poor and the foreigner
were part of His larger plan to provide for those who could not otherwise
live (e.g. <u>Ruth 2:2</u>). It is instructive that no such commandments
were given regarding herds, flocks, manufactured goods, and artisans'
services other than the left-over of certain animal offerings to be given to
the Levites. My conclusion, therefore, is that these commandments were not
intended to impute broad moral responsibility to the owners of all industries
and commodities; their purpose was solely <i>tz'dakah</i>.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Although the Scriptures heretofore presented have diminished applicability in
today's industrial and technical world, they must be complied with literally
in agricultural environments where they can be applied. There are also wider
applications of the Scriptures that should be considered by those of us who
produce or provide life-sustaining (or even life-enhancing) products and
services of a non-agricultural nature, and are moved to comply with the
spirit of what God sought to achieve in ancient Israel. In such cases, part
of our production (be they goods or services) can be identified and set apart
as <i>tz'dakah</i> (over and above our other requirements to give
<i>tz'dakah</i>), and the items themselves or profits therefrom can be given
to the poor and
disadvantaged.<sup><small><a name=fr1 href=#fn1>1</a></small></sup>
Setting aside food and clothing in food banks and humanitarian aid centers is
very much in keeping with this concept, and a goodly number of synagogues and
churches participate in this kind of charitable giving.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Another opportunity for applying this <i>Mitzvah</i> occurs when we
inadvertently produce defective but usable products. When that happens,
instead of discarding them or selling them at a discount for our own gain, we
could donate the irregular items or the proceeds of their sale to the poor
and disadvantaged. Examples of such items are blemished fruits, irregularly
sewn garments, unevenly cut building materials, discolored fabrics, and
dented appliances.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The original agricultural commandments stand unchanged and are mandatory,
whereas this broader <i>mitzvah</i> is optional but recommended for prayerful
resolution.
</font></p><hr width=150px style="margin-left: 10px">
<p align=left style="margin-left: 30px; margin-right: 30px">
<font size=2>
