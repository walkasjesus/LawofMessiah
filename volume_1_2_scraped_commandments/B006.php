<b>B6. </b>
</font>
<font size=5>
<b>Gleanings and Part of Life-Sustaining Commodities Left for the Poor
&amp; Disadvantaged.</b>
</font>
<font size=3 face="Times New Roman, Times, serif">
&nbsp;&nbsp;
<a href="comment.php?d=B006&h=B6"
