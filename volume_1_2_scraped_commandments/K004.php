<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides, Meir, and HaChinuch all wrote <i>mitzvot</i> prohibiting the
eating of blood and quoted <u>Leviticus 7:26</u> in support.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Special Note</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Curiously, although <u>Leviticus 19:26</u> is similar to <u>Leviticus
7:26</u> in prohibiting the eating of blood, the commentators did not use it
for that purpose and instead, used it to support an entirely different
<i>mitzvah</i>. So in his RN195, for example, Maimonides interprets
<u>Leviticus 18:26</u> to mean that we must not eat or drink to excess and
cause the death of a rebellious son by judicial sentence. In MN106, Meir
speaks of blood as being the kind of food eaten by a stubborn and rebellious
son, that we should not eat a live animal, and that we should pray before we
eat. And in C248, HaChinuch speaks of not overeating, and uses many words in
trying to show what <u>Leviticus 19:26</u> has to do with having a rebellious
son.
