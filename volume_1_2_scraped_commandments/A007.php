<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
It is the general rule that we are to confess our sins, repent, and make
restitution. Confession is always to God but, according to <u>James
5:16</u>, we are also to confess our sins to one another. Confession alone is
not adequate, however, and we are commanded to repent as well. Repentance
involves regretting that we sinned, and soberly determining to not repeat the
sin. A common definition of repentance is "turning away from sin and turning
toward God."
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Not all sins are of the kind that the making of restitution is wise,
reasonable, or even possible. For that reason, we must rely on the
<i>Ru'ach Hakodesh</i> to know if, when, and how repentance should be made.
Regret and repentance, on the other hand, should always occur immediately
upon realizing that we have sinned.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Matthew 5:23-24</u> requires that when we sin against a brother we
are to go to him, confess, repent, and attempt to reconcile. Even if we do
not think we have sinned but our brother does, we should go to him and try
to reconcile. Although Scripture is not explicit on this point, it would
seem that we should do so even if the one we sinned against is not a brother,
but an unbeliever. When that is not possible or appropriate, Scripture still
requires that we confess our sins to God and to one another, meaning to
mature brothers and sisters in the faith who will hear us.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary by Daniel C. Juster</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Laws of restitution such as referred to in these passages were intended to
guide the civil courts in ancient Israel. As such, the idea of paying
damages beyond just restoring what was stolen has become part of most Western
law systems; the civil laws in the Bible greatly influenced the legal systems
of the west. On a personal level, the person who repents should restore what
can be restored and, beyond that, should restore even more as the biblical
texts suggest. This is so that the pain of the violation is felt by the one
who has sinned, and is mitigated somewhat by the one who has been sinned
against. The amounts of restitution specified in the Scriptures can guide
personal acts of restitution as well as those of the civil courts. When
personal restitution takes place, court actions can be avoided, keeping in
mind that <u>1 Corinthians 6:1-8</u> prohibits a believer from suing a fellow
believer in a secular court.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides, Meir, and HaChinuch state that we are to confess our sins and
repent, but they do not indicate to whom the confession should be given.
Maimonides specifically requires that the confession must be given orally.
The others do not specifically say that, but imply it through their use of
the word "say" (translated of course). All three commentators reference the
fast of repentance on <i>Yom Kippur</i>. Maimonides and HaChinuch reference
the commandment in <u>Leviticus 4:27-29</u> to make a sin offering after one
sins, while Meir is silent on the matter.
