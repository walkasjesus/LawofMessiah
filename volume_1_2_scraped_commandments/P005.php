<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><i>Priests are not to marry persons who are likely to compromise their
holiness.</i></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This precept is derived from His Word (blessed be He):
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Key Scriptures</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Leviticus 21:7</u>
(Maimonides RN158-160; Meir MN138-140; Chinuch C266-268)
<br>
<i>A cohen is not to marry a woman who is a prostitute, who has been profaned
or who has been divorced; because he is holy for his God.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Leviticus 21:8</u>
(Maimonides RP32; Meir MP50; Chinuch C269)
<br>
<i>Rather, you are to set him apart as holy, because he offers the bread of
your God; he is to be holy for you, because I, ADONAI, who makes you holy,
am holy.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>2 Corinthians 6:14-17</u>
<br>
<i>Do not yoke yourselves together in a team with unbelievers. For how can
righteousness and lawlessness be partners? What fellowship does light have
with darkness? What harmony can there be between the Messiah and
B&#039;liya&#039;al? What does a believer have in common with an unbeliever?
What agreement can there be between the temple of God and idols? For we are
the temple of the living God - as God said, "I will house myself in them,
and I will walk among you. I will be their God, and they will be my people."
Therefore ADONAI says, "Go out from their midst; separate yourselves;
don&#039;t even touch what is unclean. Then I myself will receive you."</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Supportive Scripture</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Matthew 6:24</u>
<br>
<i>No one can be slave to two masters; for he will either hate the first and
love the second, or scorn the second and be loyal to the first. You
can&#039;t be a slave to both God and money.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Leviticus 21:7</u> commands that a Levitical <i>cohen</i> is not to marry
a <i>zonah</i> (a harlot), a <i>chalalah</i> (a woman who has been
profaned)<sup><small><a name=fr1 href=#fn1>1</a></small></sup>, or a
<i>g'rushah</i> (divorcee). Its clear application was for when the Jerusalem
Temple was fully functioning, in order to dissuade <i>cohanim</i> from
marrying women who would compromise their ritual purity and disqualify them
from Temple service. This has application even today, by those who look
toward a future restored Temple (<u>Ezekiel 40-47</u>) that is accompanied by
a restored Levitical priesthood (e.g. Orthodox Judaism applies the Scripture
literally and without exception to men they believe to be
<i>cohanim</i>).<sup><small><a name=fr2 href=#fn2>2</a></small></sup>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Conservative Judaism has ruled differently. Regarding the marriage of a
<i>cohen</i> to a divorcee, in 1954, the "Committee on Jewish Law and
Standards" (CJLS) of the Conservative Jewish Movement adopted a 1952
responsum written by R. Ben Zion Bokser authorizing Rabbinical Assembly
rabbis to join <i>cohanim</i> and divorcees in marriage without interfering
with the <i>cohen's</i>
status.<sup><small><a name=fr3 href=#fn3>3</a></small></sup>
This was incorporated in R. Arnold M. Goodman's paper titled "<u>Solemnizing
the Marriage between a <i>Cohen</i> and Divorcee</u>", which was adopted by
the CJLS in 1996, and which contains the following paragraph:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
We also regard divorce differently than did our Biblical and Rabbinic
forbearers. We no longer perceive the divorced woman as being guilty of [an
unsavory act]. To exclude a Jewish woman who is divorced from marrying the
man with whom she is in love, affirms the negative status of the divorcee.
This is inconsistent with our view of divorce or of our assessment of the
character of a woman who happens to be g'rushah [divorced].
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Messianic Judaism's view of a priest marrying a divorcee is similar but more
complex, because we (unlike other branches of Judaism) contend that the New
Covenant has replaced (or is in the process of replacing) the Covenant that
created and gave function to the Levitical priesthood in the first place
(<u>Hebrews 8:13</u>). It is also more complex for us because we profess the
existence of a New Covenant priesthood that consists of all believers in
Messiah Yeshua, both men and women.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Most Messianic Jewish scholars and leaders interpret <u>Ezekiel 40-47</u> as
applying to a Temple that will someday be rebuilt and, in which, certain
sacrifices will resume. We do not believe that the sin-related sacrifices
will resume (since Yeshua has become our sacrifice (<u>Hebrews 9:11-14</u>;
<u>10:1-14</u>)), nor that the presiding priests in the rebuilt Temple will
necessarily be Levitical. Nevertheless, we tend to follow the Conservative
Jewish Movement's point of view, and their practice of allowing both men and
women descendants of Aaron to marry divorced persons (<i>g'rushot</i>)
without any penalty to their perceived priestly status. We make an exception,
however, in the case of a divorce that was biblically unauthorized, and the
divorcee, while claiming to be a believer in Yeshua, either caused it or
procured it. In that case, remarriage by the divorcee might be prohibited
regardless of the intended spouse's identity. Such matters should be decided
by a <i>bet din</i> of elders.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Regarding the other biblical prohibitions of <u>Leviticus 21:7</u> - a
<i>cohen</i> marrying a <i>zonah</i> and a <i>cohen</i> marrying a
<i>chalalah</i> - the Scripture is speaking to the Levitical <i>cohanim</i>
of the Mosaic Covenant, but it has application in the New Covenant as well.
In Temple times, marrying a <i>zonah</i> or <i>chalalah</i> would have
adversely affected a <i>cohen</i>'s ritual purity, and would have
disqualified him from serving in the Temple. The New Covenant extension of
this is that no priests - neither men nor women - neither Jews nor Gentiles -
neither those who are priests by virtue of their faith in Yeshua nor those
who are priests by virtue of their Levitical inheritance - no priests of God
(which means none of us) are to be yoked in marriage to persons who are
likely to compromise our holiness and therefore our effectiveness in serving
God.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Every covenant we are in yokes us to our covenant partners for better or
worse, and the covenant of marriage is no exception. Both through our own
marriages and through observing the marriages of others, we are keenly aware
of how profoundly our spouse can affect the holiness of our thoughts and
actions. The person we choose to marry becomes our life partner in whatever
priestly ministry God expects of us, and he or she will either help or
hinder. That is why, for both our own sakes and for God's, it is vital that
we not marry persons who are likely to compromise our holiness and resulting
ability to fulfill our priestly responsibilities.
