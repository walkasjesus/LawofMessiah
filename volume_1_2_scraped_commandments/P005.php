</font>
<font size=5>
<b>Priests Marrying Persons Likely to Compromise Their Holiness.</b>
</font>
<font size=3 face="Times New Roman, Times, serif">
&nbsp;&nbsp;
<a href="comment.php?d=P005&h=P5"
onClick="return show_hide_box(this,400,280,'1px solid')">[Make a Comment]</a>
