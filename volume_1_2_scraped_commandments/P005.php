<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Of the three commentators compared herein, Chinuch is the only one who is
expansive on the subject of how marriage is related to holiness. He states
that the underlying purpose of the three prohibitions in <u>Leviticus
21:7</u> is to preserve the <i>cohen</i>'s ritual purity for his priestly
service to God. HaChinuch and Meir point out an irony of the <i>mitzvah</i>
that prohibits marriage to a <i>zonah</i>. It is that a woman who copulates
with an animal is liable to death by stoning, but she is not a <i>zonah</i>
because her copulation was not with a man. By inference, therefore, a
<i>cohen</i> is not forbidden from marrying her - a strange result of logic.
HaChinuch and Meir define a <i>chalalah</i> as a woman who is the product of
a marriage that is prohibited to a <i>cohen</i>, or as a woman who has been
intimate with a <i>cohen</i> whom she was forbidden to marry. Neither
Maimonides, nor Meir, nor HaChinuch comment much about the prohibition
against marrying a <i>g'rushah</i>, nor do they make any exceptions for
unusual or contemporary circumstances that might be different from those of
antiquity.
