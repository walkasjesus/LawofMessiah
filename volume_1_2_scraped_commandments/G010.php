<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
In the Mosaic Covenant, an Israelite would bring his animal sacrifice to the
Temple (or the Tabernacle), and a Levitical <i>cohen</i> would assist him by
slaying it, placing it on an altar, and offering it up to God in his behalf.
The Israelite had to be ceremonially clean (i.e. ritually pure) to even enter
the Temple's sanctuary, but whether or not he was actually repentant (i.e.
the condition of his heart was right) when he offered his sacrifice was known
only to God. There is another Mosaic Covenant example of the need to
approach God only when repentant, and that is <u>Leviticus 16:11</u>'s
requirement that the High Priest sacrifice a bull for himself prior to
sacrificing a goat for the sins of Israel on <i>Yom Kippur</i>. We also
assume that the ten days between <i>Yom T'ruah</i> and <i>Yom Kippur</i> was
for the purpose of all Israelites examining themselves and repenting of their
sins (committed during the previous year) prior to their coming before the
Lord on <i>Yom Kippur</i>.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The various commandments requiring that the worshiper be ceremonially clean
in order to enter the Holy Temple were, no doubt, given in order to remind
him of God's holiness, and that the house in which God chose to put His Name
(i.e. where the <i>Sh'khinah</i> dwelt) must not be defiled. One thing is
certain however - the worshipper's sacrifice was only acceptable if his heart
was repentant before God (<u>Genesis 4:3-7</u>; <u>Isaiah 1:15-19,
59:1-2</u>; <u>Matthew 5:23-24</u>).
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The Temple that stood in Jerusalem was built of brick and mortar but, in this
era of the New Covenant, God's presence (The Holy Spirit) lives in Temples
made of flesh (the "bodies" of believers) and, therefore, in the "bodies" of
each and every one of us who will have Him (<u>1 Corinthians 3:16-17,
6:19-20</u>). That gives new meaning to the requirement that God's Temple be
undefiled. Today, the commandment has not to do with entering a physical
Temple while in an unclean state; rather, it is allowing the Temple of our
bodies to fall into a spiritually unclean state, thereby making it
uninhabitable by the Holy Spirit. Spiritual uncleanliness is caused by sin,
and it persists if there is no repentance.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Our need for spiritual cleanness and repentance is illustrated by several
Scriptures (listed above) that essentially state that God does not have
fellowship with, nor does He even hear, those who are unrepentant of their
sin (see <u>Proverbs 15:29</u>; <u>Isaiah 59:1-2</u>; <u>1 Corinthians
11:27-29</u>; and <u>James 5:16</u>).
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides and HaChinuch wrote <i>mitzvot</i> #RN77 and #C363 respectively,
which state that no person who is ceremonially unclean is to enter the
Sanctuary. Maimonides and HaChinuch do not offer <i>mitzvot</i> on spiritual
cleanness broadly, and Meir is silent on both subjects.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
