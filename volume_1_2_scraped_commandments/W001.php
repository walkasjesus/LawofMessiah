Teaching <i>Torah</i> and <i>Torah</i>-obedience is commanded (<u>Matthew
5:19</u>), and is expected of all of us. The <i>Mitzvah</i> as it applies to
our children is especially important, however, because teaching children is
the way our heritage of belief, obedience, and relationship with God is
passed from generation to generation. As <u>Proverbs 22:6</u> tells us:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>even when old, he will not swerve from it.</i>
