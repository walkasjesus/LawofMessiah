<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides, Meir and HaChinuch wrote their respective <i>mitzvot</i> in
fairly literal ways. Maimonides' #RP11 speaks of studying the <i>Torah</i>,
while Meir's #MP14 and HaChinuch's #C419 emphasize learning and teaching.
Maimonides' #RP10 specifies that we are to read the <i>Sh'ma</i> twice daily,
morning and evening, and Meir's #MP11 says similarly that we are to recite
it. HaChinuch's #C420 says something quite different; he says that we are
only required to recite one verse of the <i>Torah</i>, namely <u>Deuteronomy
6:4</u>. I am of the opinion that the Orthodox interpretation of reading or
reciting the <i>Sh'ma</i> morning and evening is not what the Scripture
intends, but the practice is a good one and a valid implementation so long as
the Scripture's higher and more far-reaching message is understood and
pursued.
