<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
While the Scriptures cited here speak to all of the <i>Mitzvah's</i>
elements, the two that are primary are <u>Deuteronomy 6:6-7</u>, which is
part of the <i>Sh'ma</i> (the Scripture-prayer that is recited by Orthodox
Jews several times a day), and <u>Jeremiah 31:30-33(31-34)</u> (re-affirmed
in <u>Hebrews 8:10-11</u>), the prophecy that God would give Israel a New
Covenant, the essence of which would be God's Word (<i>Torah</i>) embedded in
hearts and minds so that all could know Him intimately. That prophecy was
fulfilled by Yeshua who, according to <u>John 1:1-14</u>, is God's Word made
flesh, and we know from other Scriptures that, after coming to earth, Yeshua
offered Himself up as a sacrifice for our sin, was resurrected from the dead,
and later returned to the Father, sending back to us the <i>Ruach
Hakodesh</i> to reside within all who are willing to receive Him. The
prophecy of Jeremiah was thus fulfilled, and we who are living today and
believe, are its beneficiaries.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The Scriptures cited do not use the word "study", but do say "on His
<i>Torah</i> they meditate day and night", and "meditate on it all day".
Also, the Bereans are said to have engaged in "checking the <i>Tanakh</i>
every day", all of which are tantamount to studying God's Word, but even
exceed mere study because they connote repetition.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
According to <u>Jeremiah 31:32(33)</u> and <u>Hebrews 8:10</u>, God puts His
words within us but, just as with other blessings of God, He doesn't force
Himself on us; we have to invite Him. Therefore, we have a responsibility to
receive and internalize God's Word or we will derive no benefit from it.
Internalizing God's Word is the same as allowing the <i>Ruach HaKodesh</i> to
live within us, to counsel, comfort, and yes, even convict us of sin should
we stumble. Although Jeremiah spoke his prophecy to the Jewish people, God's
words
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>for all will know me, from the least of them to the greatest</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
did not mean just Jews, but Gentiles also (<u>Romans 2:14-15a</u>) who, by
confessing Yeshua, are grafted into an olive tree among Jewish branches
(<u>Romans 11:17</u>), thereby becoming members in the "<i>national life of
Isra&#039;el</i>"<sup><small><a name=fr1 href=#fn1>1</a></small></sup>
or "<i>commonwealth of
Israel</i>",<sup><small><a name=fr2 href=#fn2>2</a></small></sup>
(<u>Ephesians 2:12</u>) and, consequently, in the wider body of believers.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Deuteronomy 6:7</u> commands us to talk about God's words (meaning His
words in Scripture), and it mentions four occasions in particular, which I
interpret to mean on all occasions. Speaking God's words has the benefit of
learning them, remembering them, and teaching them even to those who hear
them spoken in passing.
