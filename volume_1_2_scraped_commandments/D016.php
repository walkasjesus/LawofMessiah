<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><i>We are to take up the four species during the Feast of Sukkot. </i></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This precept is derived from His Word (blessed be He):
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Key Scripture</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Leviticus 23:39-40</u>
(Maimonides RP169; Meir MP36; Chinuch C324)
<br>
<i>But on the fifteenth day of the seventh month, when you have gathered the
produce of the land, you are to observe the festival of ADONAI seven days;
the first day is to be a complete rest and the eighth day is to be a
complete rest. On the first day you are to take choice fruit, palm fronds,
thick branches and river-willows, and celebrate in the presence of ADONAI
your God for seven days.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>John 7:1-3</u>
<br>
<i>After this, Yeshua traveled around in the Galil, intentionally avoiding
Y&#039;hudah because the Judeans were out to kill him. But the festival of
Sukkot in Y&#039;hudah was near; so his brothers said to him, "Leave here
and go into Y&#039;hudah, so that your talmidim can see the miracles you
do</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The four species are (1) choice fruit, (2) palm branches, (3) thick branches,
and (4) willows of the brook. Traditionally, parts of each are bound
together into a bouquet (sometimes referred to as a <i>lulav</i>, but
literally, <i>lulav</i> only refers to a palm branch) and are preserved and
waived for each of the seven days of <i>Sukkot</i>. The command to take up
the four species clearly applies to the first day but, since the Scripture
states that we are to celebrate for seven days, we suppose that waving them
as part of the celebration is contemplated. Scripture does not specify a
particular select fruit, so the tradition developed to use an etrog (citron)
that has part of its fragile stem still attached. <i>Halachically</i>,
therefore, the etrog becomes unusable if the short nub of a stem becomes
detached.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
We do not know whether, during the time of Yeshua, binding the four species
together and waving all four was practiced at celebrations other than
<i>Sukkot</i>. The practice was probably specific to <i>Sukkot</i> because
we read in <u>John 12:12</u> (speaking of before Passover) that Yeshua was
met with the waving of palm branches only. The question remains, however,
"What is the significance of waving the four species at <i>Sukkot</i>?"
Scripture is silent on this, but its significance was probably agricultural
since <i>Sukkot</i> is a harvest celebration. The <i>Talmud</i> seeks to
answer this by suggesting that the four species represent God's creation, and
that waving them in all directions signifies that <i>HaShem</i> is Lord of
all.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
It is significant that an expression of worship meant for God (waving palms)
was used to recognize and welcome Yeshua, and it is also significant that
<u>John 12:15</u> states:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>Daughter of Tziyon, don't be afraid! Look! your King is coming, sitting on
a donkey's colt.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
What it means to "take up" the four species is open to interpretation, but
this <i>Mitzvah</i> is unquestionably applicable to Jews and, in my opinion,
to <i>K'rov Yisrael</i> Gentiles as well. Because we don't know exactly what
"taking up" means, the procedure should be done according to the established
practice or <i>halachah</i> of each community in which the worshiper finds
himself. I do not believe that taking up the four species is required of
other Gentiles as part of their worship of God during Sukkot, but they may do
so if they deem it to be meaningful.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides, Meir, and HaChinuch state their respective <i>mitzvot</i>
somewhat differently as follows: (1) Maimonides: Rejoice with a palm branch
for the seven days; (2) Meir: Lift up (or wave) one palm branch, one etrog,
three myrtle branches, and two willow branches on each of the seven days (but
not on Shabbat); (3) HaChinuch: Similar to Meir, but adds considerable
content from the "oral tradition", including the analogy of each species to a
man's organs. The etrog is said to be like the heart, the <i>lulav</i> (palm
branch) like the spine, the myrtle like the eyes, and the willow like the
lips.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
