Although
in Yeshua dwelt the fullness of God (<u>Colossians 1:19, 2:9)</u>, Yeshua
became a man like us (<u>Hebrews 2:14-17</u>) and, like us, received his
godly attributes (including grace and truth) from His Father in Heaven.
</font>
