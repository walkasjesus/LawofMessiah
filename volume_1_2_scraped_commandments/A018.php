<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
"Grace", commonly defined as unmerited favor, is both an attribute and a gift
that He imparts to us. But for what purpose and how are we to use it?
Daniel Juster answers the question this way:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>Empowered by the Holy Spirit, we are to use our gifts and talents (from
the Spirt) as instruments for the blessing and building up of fellow
believers in order to reach others for the Good news. In a large sense then,
grace is not only unmerited favor, but includes the idea of power to do
things. Hence the grace gifts are gifts of power in that the grace of God
changes us and gives us ability to live in a right manner and to bless
others.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
One might ask what Scripture means when it says that Yeshua and Stephen were
"full of grace and truth." The grace and truth that filled them was not
intrinsically their own, but rather that which was placed in them by the
Father to benefit others as the Holy Spirit directed.<sup><small><a name=fr1 href=#fn1>1</a></small></sup>
We are to follow their example by being good Stewards of God's grace and
other spiritual gifts imparted to us as were they.
</font></p><hr width=150px style="margin-left: 10px">
<p align=left style="margin-left: 30px; margin-right: 30px">
