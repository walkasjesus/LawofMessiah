</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Commentary</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This <i>Mitzvah</i> "Keeping Our Promises" is a generalization of several
more specific <i>mitzvot</i> promulgated by the Classical Commentators
Maimonides, Meir, and HaChinuch having to do with keeping oral commitments,
and delaying or revoking vows.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
In addition to the <i>Torah</i> Scriptures, our obligation to keep our
promises appears to also be the message of <u>Matthew 5:33-37</u> and
<u>James 5:12</u>, their general rule being:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>... let your 'Yes' be simply 'Yes' and your 'No' simply 'No'.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>James</u> advises against swearing oaths to emphasize one's truthfulness.
The expectation is that whatever New Covenant believers say ought to be so
trustworthy that, except for society's requirement of submission to judicial
authorities and its laws pertaining to certain legal documents, we should
avoid swearing oaths. It is part of the intensification of <i>Torah</i> in
the New Covenant.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The literal application of <u>Numbers 30:4-16(3-15)</u> was operative under
the Mosaic Covenant in ancient Israel when women had fewer legal rights than
men, and their fathers or husbands could intercede to nullify their vows made
to God and others. That may continue to be the secular law in various places
of the world today, and it is the prevailing secular law today that parents
can nullify contracts made by their minor children, and guardians can nullify
commitments made by those who are adjudicated legally incompetent. It should
not have to be said (but I will say it), that under the New Covenant (also
under today's Anglo-American law), adult women are no longer legally
subordinate to their fathers and husbands as they were in the past.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Daniel C. Juster</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
It must be said that vows affect communal and family relations. Because the
Bible gives a husband headship in the marriage and family, it is my view that
his authority with regard to vows, contracts and important promises still
remains for the Body of believers. Although the secular courts today will
not credit his authority in such situations, the courts of the Body of
Believers will often do so.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Classical Commentators</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Maimonides, Meir, and HaChinuch limit their mitzvot to promises made orally,
although one can infer that they encompass written contracts as well.
Maimonides recognizes exceptions to the general rule in the case of wives and
daughters who are under the authority of their husbands and fathers
respectively. Meir makes no exception for women in his <i>mitzvot</i>, but
cites the <i>Talmudic</i> rule that allows an "expert scholar" or three
"ordinary persons" to release a "man" from a vow that he has made and
subsequently regrets.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
HaChinuch cites the <i>Talmudic</i> rule but states that, according to
Maimonides, it should not be listed among the compilation of <i>mitzvot</i>.
He then goes on to expound the rule and describes four situations in which a
man should be released from his vow. Of the three commentators mentioned,
HaChinuch treats the subject far more in depth than the other two.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
