<font size=4 face="Times New Roman, Times, serif">
<b><i>We are to keep our promises.</i></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This precept is derived from His Word (blessed be He):
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><u>Key Scriptures</u></b>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Numbers 31:2-16(1-15)</u>
(Maimonides RP94, RP95, RN157; Meir MP39, MP40, MN184;
Chinuch C406, C407, C575)
<br>
<i>Then Moshe spoke to the heads of the tribes of the people of Isra&#039;el.
He said, "Here is what ADONAI has ordered: when a man makes a vow to ADONAI
or formally obligates himself by swearing an oath, he is not to break his
word but is to do everything he said he would do. When a woman makes a vow
to ADONAI, formally obligating herself, while she is a minor living in her
father&#039;s house; then, if her father has heard what she vowed or
obligated herself to do and holds his peace, then all her vows remain
binding - every obligation she has bound herself to will stand. But if on the
day her father hears it, he expresses his disapproval, then none of her vows
or obligations she has bound herself to will stand; and ADONAI will forgive
her, because her father expressed his disapproval. If, having made vows or
rashly committed herself to an obligation, she gets married; and her husband
hears but holds his peace with her on the day he learns of it, then her vows
and obligations she has bound herself to will stand. But if her husband
expresses his disapproval on the day he hears it, he will void the vow which
is on her and the obligation to which she has bound herself; and ADONAI will
forgive her. The vow of a widow, however, or of a divorcee, including
everything to which she has obligated herself, will stand against her. If a
woman vowed in her husband&#039;s house or obligated herself with an oath;
and her husband heard it but held his peace with her and did not express
disapproval, then all her vows and obligations will stand. But if her
husband makes them null and void on the day he hears them, then whatever she
said, vows or binding obligation, will not stand; her husband has voided
them; and ADONAI will forgive her. Her husband may let every vow and every
binding obligation stand, or he may void it. But if her husband entirely
holds his peace with her day after day, then he confirms all her vows and
obligations; he must let them stand, because he held his peace with her on
the day he heard them. If he makes them null and void after he has heard
them, then he will bear the consequent guilt."</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Deuteronomy 23:22-24(21-23)</u>
(Maimonides RP94; Meir MP39; Chinuch C575)
<br>
<i>When you make a vow to ADONAI your God, you are not to delay in fulfilling
it, for ADONAI your God will certainly demand it of you, and your failure to
do so will be your sin. If you choose not to make a vow at all, that will
not be a sin for you; but if a vow passes your lips, you must take care to
perform it according to what you voluntarily vowed to ADONAI your God, what
you promised in words spoken aloud.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Matthew 5:33-37</u>
<br>
<i>Again, you have heard that our fathers were told, &#039;Do not break your
oath&#039;, and &#039;Keep your vows to ADONAI&#039;. But I tell you not to
swear at all- not &#039;by heaven&#039;, because it is God&#039;s throne;
not &#039;by the earth&#039;, because it is his footstool; and not &#039;by
Yerushalayim&#039;, because it is the city of the Great King. And don&#039;t
swear by your head, because you can&#039;t make a single hair white or
black. Just let your &#039;Yes&#039; be a simple &#039;Yes&#039;, and your
&#039;No&#039; a simple &#039;No&#039;; anything more than this has its
origin in evil.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>James 5:12</u>
<br>
<i>Above all, brothers, stop swearing oaths - not "By heaven", not "By the
earth", and not by any other formula; rather, let your "Yes" be simply "Yes"
and your "No" simply "No", so that you wont fall under condemnation.'</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
