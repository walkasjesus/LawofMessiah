<font size=4 face="Times New Roman, Times, serif">
Our purpose in writing this book is to codify the <i>mitzvot</i> of Scripture
without the classical Jewish presumption and constraint that they must total
six hundred thirteen, and to discuss their meaning and application from a New
Covenant perspective.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
In modern times until recently, the New Covenant and the <i>Kit'vei B'rit
Chadashah</i> (New Testament) were considered solely Christian and, since the
First Century, God's Law as an approach to biblical living has been
considered exclusively Jewish and limited to the commandments given through
Moses. In addition to mainstream Judaism's having rejected Yeshua as
Messiah, a chasm of interpretation and application of Scripture developed
between Judaism and Christianity such that Judaism's approach to holiness has
come to be through obedience to law, while Christianity's approach has been
through faith with the assumption of God's grace apart from law.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The resurgence of Jewish belief in Messiah Yeshua in the Twentieth Century
has created the need for a fresh look at God's law applied with New Covenant
principles and the Holy Spirit's interpretation, and taking into account
historic Rabbinic interpretations as well. This book seeks to fulfill that
need by codifying and commenting on God's <i>mitzvot</i> wherever they may be
found in Scripture - in the <i>Torah</i>, the <i>Nev'im</i>, the
<i>Ketuvim</i>, the <i>Besorah</i> (Gospels) or the Apostolic Writings.
