original Apostles. Lineage is important in priesthoods - physical lineage in
the former, and a spiritual lineage of continuity by the laying on of hands
in the latter. The Church parallels the Tabernacle-Temple in offering daily
and special sacrifices upon an altar. However, the sacrifice offered is the
sacrifice of "Christ" in the Mass. Until recently, the priest turned from
the congregation for the elements to be transformed in the liturgy. This was
analogous to the High Priest entering the Most Holy Place on Yom Kippur. In
the Eastern Church, the priest actually goes behind the door and leaves the
presence of the congregation to enter the "Most Holy Place". The people eat
of the sacrifice (the bread); traditionally there was a portion only for the
priest (the wine - blood). This was before today's change whereby the people
are also offered the wine. There are many symbols reminiscent of the Hebrew
Scriptures. There are seven-branched lamps, priestly garments, priestly
hats, and an eternal light and incense. Stained glass windows include heroes
from ancient Israel as well as New Testament figures. The Roman Catholic
Church has created a "New Covenant Temple worship". The Sabbath day is also
special, but has been transferred to Sunday when the resurrection is also
celebrated. Whatever one might think of the totality of the Roman Catholic
orientation, it is clear that there is a large degree of continuity from
ancient times. Yet, continued Jewish life among Jewish believers was seen as
not entering into the reality of the New Covenant priesthood and Temple
realities. Catholic moral theology also developed a Canon Law tradition that
is parallel to Rabbinic thought. New rules are added and ways are developed
to get around the law in various cases (casuistry). It should also be noted
that the Christian calendar is in continuity in some ways, and in
discontinuity in other ways, with the biblical calendar. Passover and Easter
sometimes coincide, but solar dating makes this happen only periodically;
nevertheless, the fact that Easter occurs in the Spring is clearly rooted in
Passover. In the Eastern Church, which defended the Bible's lunar calendar,
the concordance of its feasts with the Jewish calendar is more obvious. Good
Friday before Easter and Passover season is the closest thing on the
Christian calendar to Yom Kippur, the Day of Atonement. Protestant tradition
largely maintained the same calendar as the Catholic tradition, so we can see
that the distinction between moral and Ceremonial Law is not followed through
with.
