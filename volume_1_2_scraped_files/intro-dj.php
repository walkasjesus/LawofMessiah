<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Generally, this consensus taught that the Church was the new Israel and had
replaced the old ethnic Israel (There were significant dissenters from this
viewpoint, especially among the Puritans, English Anglicans, and Baptists).
The Protestant Church divided the Law into two parts - the Moral Law and the
Ceremonial Law. The Moral Law continued in the New Covenant order, but the
Ceremonial Law was merely for the purpose of pointing forward to the Messiah,
and was therefore temporary. When fulfillment came, the Ceremonial Law was
made obsolete, and to continue to engage in the Ceremonial Law was understood
to be acting as though the Messiah (the law's fulfillment) had not come;
hence, practice of the Ceremonial Law was forbidden by the Catholic and
Orthodox churches.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b>The Classical Protestant View</b>
</font>
<font size=3 face="Times New Roman, Times, serif">
&nbsp;&nbsp;
<a href="comment.php?d=intro-dj&h=The Classical Protestant View"
onClick="return show_hide_box(this,400,3005,'1px solid')">[Make a Comment]</a>
