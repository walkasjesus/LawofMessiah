<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
In applying the <i>Torah</i>, we recognize a degree of discontinuity between
the Mosaic and New Covenants. Just how does the New Covenant differ from the
Mosaic? How is it a better covenant? It is not a contrast between one being
of law and the other of grace, one where God is harsh and the other where God
is forgiving and kind. These are sub-cultural myths. Nor is it that one is
for Israel and the other is for the Nations. Both are made with Israel, but
the New Covenant explicitly applies the promise of the Abrahamic Covenant to
bless all nations by commanding us to bring the message of the Gospel to all.
The following are the basic differences. We now have the power of
identification with Yeshua's crucifixion to put to death the deeds of the
flesh. In addition, all may now be indwelt and filled with the Holy Spirit.
Each individual is a Temple of the Holy Spirit. Furthermore, a Temple is
formed wherever a New Covenant congregation gathers together in faith.
Prophecy is given to all and the gifts of the Holy Spirit are poured out upon
us. Jew and Gentile are now one in the Messiah. The Law has been written
upon our hearts, and we are moved by the Spirit to obey God. As for the
Mosaic writings, its teachings and principles are to be applied to the New
Covenant Order. This is the teaching of Yeshua and Paul (<u>Matthew
5:17-18</u>; <u>2 Timothy 3:16-17</u>) and it is their example to quote from
Moses to settle issues.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
