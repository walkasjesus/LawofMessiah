<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The Anglican Church developed an amazing eclecticism of Catholic, Lutheran,
and Reform thought. Puritans were in and out of the Church of England
preceding, during, and after the period of Oliver Cromwell in the
17<sup><small>th</small></sup> Century. One can see the Ceremonial Law
applied as in the Catholic Church in the ceremony of the High Anglican. The
theology of the Eucharist which is given in both kinds (bread and wine) is
closer to Luther. The understanding of the general relationship of law to
grace is like unto Calvin, and justification by faith is accepted doctrine.
A married priesthood by apostolic succession is parallel to but different
than the Roman Catholic view. When it came to applying the Mosaic Law, the
Anglican orientation was set by Richard Hooker in his <u>Laws of
Ecclesiastical Polity</u>. Hooker argues against the Puritan position of
literally applying the Mosaic Law to order social and civil life (there was
no debate between Puritans and Anglicans). Hooker argues that the Mosaic
Law, as given, was for ancient Israel. We had to discern more universal
principles in the Law and apply them to new circumstances in our society.
Reason could as well discover principles for Church, civil and social life as
well as helping us apply the Scriptures to new situations. The Puritans
responded that the human mind is not given to second-guess the Law of God by
looking for principles in the manner of Hooker. One can see from this debate
that the question of continuity-discontinuity and the application of the Law
is no new issue. My view is that the Anglicans were too quick to dismiss
direct application because it offended their human sentiment. However, many
laws are based on obvious principle and are not required of us literally.
Shall we build a fence on our roof (<u>Deuteronomy 22:8</u>) when it is not
used for living space as in ancient flat-roof houses? The principle of
taking reasonable steps to protect human life from accident is clear.
