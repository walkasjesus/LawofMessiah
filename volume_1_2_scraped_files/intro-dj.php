<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The degree of lack of understanding God's call for justice (His righteous
order as defined in Scripture - not some false humanistic idea of equality)
is appalling. God's justice is hardly a topic in the contemporary Church,
whereas it ought to be seen as number one. The Gospel is commonly understood
as cheap grace (Bonhoeffer) that requires nothing of us. Rather, Scripture
puts forth two primary motives for man. The first is to love, glorify and
serve God. This is to capture our hearts totally. The second is to pursue
justice. We preach the Gospel because unless people are transformed and
receive eternal life, they cannot pursue justice motivated by love for
others. The Gospel is God's means of establishing justice. Micah summarized
it well in his great verse that answers the question, <font face="Arial,Helvetica,sans-serif" size=3>"<i>...
what does the LORD require of you but to do justice, and to love kindness,
and to walk humbly with your God?</i>"</font> (<u>Micah 6:8</u>, RSV) God has not
changed. All of the instruction of God is to be understood as the details of
how to live out love for God and our fellows. In the Bible, love and justice
are not opposed, but could be seen as a hyphenated word, "love-justice".
When the law is broken, justice is satisfied by blood sacrifice or by paying
the penalty. Love requires restitution to the one wronged; if the offender
is truly repentant, this is his desire. God's love-justice provides the
sacrifice of Yeshua so He can be both just and the justifier of those who are
in Yeshua. I should note that justice in the Bible is only equality before
the law and its requirement. It does not imply economic equality as today's
Marxist influenced social justice warriors. It is rather an order of
righteousness where each person can fulfill their God intended good destiny.
But there can be great disparities of wealth as long as there is good
provision for all. Those with much are to expand opportunity and provision
for all.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
In addition, love is the passionate identification with others that seeks
their good guided by law. As such love seeks the destiny fulfillment of the
other. We share much in destiny; to know and love God, to marry and have
children, to have adequate provision. But there is a distinct calling for
every person.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
I believe that the Messianic Jewish perspective is in basic accord with this
classical Christian understanding of the relationship of Law and Grace. It
is by grace that we are saved, but the saved individual is a Kingdom person
who glorifies God in obedience to the Law of God. The point at issue with
Messianic Jews is rather the easy distinction between the moral and
Ceremonial Law, which eliminates the place of ethnic Israel in the plan of
God, and eliminates the distinct practices of the Jewish people which define
their "irrevocable" call (<u>Romans 11:29</u>). Having looked at the general
consensus of Classical Protestantism, we now need to look more closely at the
New Testament.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b>Yeshua and the Torah</b>
</font>
<font size=3 face="Times New Roman, Times, serif">
&nbsp;&nbsp;
<a href="comment.php?d=intro-dj&h=Yeshua and the Torah"
onClick="return show_hide_box(this,400,3005,'1px solid')">[Make a Comment]</a>
</font>
</p>
<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The Gospel material implies a context for understanding the coming of the New
Covenant order. It is the order of the in-breaking Kingdom which is at hand
here. He that is least in the Kingdom of God has a better place than John
the Immerser (<u>Mark 1:14-15</u>; <u>Matthew 11:12</u>). The order of the
New Covenant Kingdom is seen in the acts and teaching of Yeshua and the
disciples. He heals the sick, casts out demons, and raises the dead. These
are manifestations of the Age to Come. Yet it is still a transitional time,
for the Kingdom will not come in fullness until His return. This is the
growing stage of the Kingdom where the Word of the Good News is preached as a
farmer scatters seed, and the Kingdom grows from a small beginning until it
is a large tree as in the parable of the mustard seed (<u>Matthew 13</u>).
Though partial, though "already but not yet" (George Ladd in <u>The Gospel of
the Kingdom</u>), the Kingdom (God's New Covenant order) has come and
manifests itself in many spheres when the people of the New Covenant are
submitted to the rule of God. We pray and work to extend this Kingdom and
disciple those from the nations to live by its precepts.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
In this context, Yeshua teaches that the written <i>Torah</i> has not been
abolished. Heaven and earth will pass away before "one jot or one tittle"
passes from the <i>Torah</i> (<u>Matthew 5:18</u>, NKJ). Yeshua has come to
fulfill the <i>Torah</i>. What does this mean? It is clear from His
teaching in <u>Matthew 5-7</u>, but is also reflected in all of the Gospel
material. Where the Law of Moses implied a right heart given to obedience to
the Law of God, Yeshua made the right heart orientation the key to His whole
exposition. From the heart comes evil. Not only must one not commit
adultery, but one must not sin by entertaining adultery in one's thought
life. Not only are we to not murder, but me must not entertain thoughts of
hatred and vengeance. The New Covenant elevates our ability with greater
grace, and therefore requires an application of Mosaic teaching that is more
exacting. While disagreeing with the Pharisees in some of their
applications, Yeshua removes certain accommodations of Moses that were due to
the "hardness of their hearts" and the weakness of the people (e.g. divorce).
The teaching of Yeshua (and the Apostles) constitutes New Covenant
<i>Halakhah</i>. Yeshua shows the meaning of the Sabbath, the legitimacy of
healing on the Sabbath, eating grain as one walks through the fields, and the
non-binding nature of the tradition of the elders (e.g. hand washing, <u>Mark
7</u>; <u>Matthew 15</u>). Yeshua makes authoritative <i>halakhah</i>, not
by appealing to past tradition (as did the scribes), but by pronouncement
according to his own authority (<u>Matthew 7:28</u>).
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This role of making authoritative <i>halakhah</i> is given to the disciples
who will supersede the Jewish Supreme Court, the Sanhedrin. In <u>Matthew
16</u>, Yeshua gives to Peter and the Apostles the keys to the Kingdom of
Heaven, which is the authority to bind and loose. This authority is the
right to forbid behavior (bind) and permit behavior (loose), and was
exercised at the Jerusalem Council concerning the requirements for Gentiles
to join the New Covenant Communities of faith. In addition, <u>Matthew 16
and 18</u> provide the foundation for enjoining congregational discipling for
the New Covenant community, a community of righteous order.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
In those passages where Yeshua is accused of violating the <i>Torah</i>,
closer observation shows that He was only violating Pharisaic traditions -
not any commandment of <i>Torah</i>. Where the teaching of Moses set a
standard, sometimes Yeshua would require more. Instead of being truthful in
oath taking, we are to fulfill every word we speak. Instead of allowing
unlimited revenge, the <i>Torah</i> prescribed "an eye for an eye" as the
