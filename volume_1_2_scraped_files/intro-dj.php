<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The word <i>halakhah</i> comes from the word <i>halak</i>, which means "way".
The first followers of Yeshua were called "followers of the way", i.e.
<i>halakhah</i> from Yeshua! <i>Halakhah</i> derives from two motives. One
is to apply the <i>Torah</i> (Law-instruction) of God to new situations where
the application is not obvious. The second is to protect the <i>Torah</i> by
adding regulations so that one will not get close to breaking the law. The
second motive is found in the ancient rabbinic text <i>Pirke Avot</i>, the
"Ethics of the Fathers". This is called "building a fence around the
<i>Torah</i>". At times, this endeavor led to an expanding legalism that was
far removed and even opposed to the original instruction of the <i>Torah</i>.
Sometimes it also gave grounds for violating the intent of the commandment
through ingenious ways of getting around it. We can see the controversy of
Yeshua and the Pharisees in this light. Pharisaic Judaism evolved into
Rabbinical Judaism with all of its pros and cons; perhaps much evolved in
opposition to Messianic Judaism and an attempt to gain total government over
the Jewish people (see Daniel Gruber's "<u>Rabbi Akiba's Messiah</u>"). Yet
the basic need for <i>halakhah</i> is unavoidable. Just as the United States
Constitution has to be applied to new situations by the courts of our land,
so the Mosaic Constitution needs to be applied to new situations as well
(e.g.: What do we do with all of the laws related to the Temple, now that
there is no Temple?).
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<i>Halakhah</i> (binding law upon the Jewish community) that was not
explicitly written in the <i>Torah</i>, was built through consensus that
arose out of intense debate in the early rabbinic <i>yeshivot</i> or
academies. If the academy was important enough and other academies were
swayed to its viewpoint, it became the universal practice among the Jewish
people. The decision for each academy was either made by the ruling rabbi
who was especially revered (and hence the others would concur), or it was
made by the majority opinion of all the rabbis of a particular school. The
<i>halakhah</i> was orally preserved and passed down (in the <i>Mishnah</i>
first of all) and in the application of the <i>Mishnah</i> called the
<i>Gemara</i>. The <i>Mishnah</i> contains the earlier of these decisions
and the tradition of law passed on from these. The <i>Mishnah</i> and the
<i>Gemara</i> together make up the <i>Talmud</i>. The rules of logic for
reaching decisions are sometimes in accord with the normal rules of logic
studied today. However, other rules are recipes for subjective conclusions
and provide ways to ingeniously defend any position desired by the proponent.
Rabbi Ishmael's early 13 rules of logic are simpler and more objective than
Akiba's expansive list of rules of interpretation. The amazing thing is the
claim that the Oral Law (which was later written down contrary to the Oral
Law itself) is from Moses, was handed down from generation to generation, and
has equal authority to the written <i>Torah</i>. Lawrence H. Shiffman of New
York University, an eminent authority on Rabbinic Judaism, argues in his book
"<u>From Text to
Tradition</u>"<sup><small><a name=fr1 href=#fn1>1</a></small></sup>,
that the Oral <i>Torah</i>, in traditional Judaism, in fact supersedes and
really replaces the written <i>Torah</i>.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The claim that the Oral Law is from Moses is really quite indefensible and is
certainly an attempt by the rabbinic community in the early centuries after
the first century to establish the authority of their traditions and their
conclusions. Indeed, two aspects of this claim are within the <i>Talmud</i>.
One is that the earlier tradition of the elders, perhaps a very small body of
material introduced by the phrase "we have a tradition" is seen as accurately
passed down. Indeed, the rest is sometimes seen as lost and rediscovered by
the application of rabbinic logic to the questions at hand; this is not
reasonably believable. That is not to say that rabbinic reflection is not
important in our own work in applying the <i>Torah</i>. The rabbis'
conclusions are sometimes very insightful and helpful to our own application
today. God's common grace is at work here.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The rabbis were correct concerning the need for applying the <i>Torah</i> to
new situations and having sufficient authority vested in some body to unify
the Jewish community in its basic practice. The process is akin to
legislation and constitutional law in the United States and other
constitutional nations. However, the Protestant Reformation position teaches
us that every generation must re-think and re-apply what is handed down in
every generation. Traditional application must be tested by the bar of the
Written Word and adjusted to be true to it. Reformed Churches were fond of
saying that they were reformed and always reforming, seeking to be truer to
the written word for all new situations. Hence, it is wrong to create a
fixed tradition of authoritative <i>halakhah</i> which so overlays the
Written Word that we can never get back to the Written Word to test the Oral
<i>Torah</i>. As Lawrence Shiffman shows, the appeal to the Written Word to
defend the Oral <i>Halakhah</i> is often strained and far-fetched, but done
to give some reverence to the Written Word.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Messianic Jews seek to apply the <i>Torah</i> (Law-instruction) of God to the
New Covenant situation and to the particular situation of life in which we
find ourselves. We use the words "Law-instruction" as a hyphenated
expression because the concept of <i>Torah</i> is more than law; it is God's
whole instruction or teaching. However, it is a mistake to not recognize
that a large part of this instruction consists of laws and commandments.
Some in response to Christian criticism of the idea of a cold impersonal law
have sought to strip <i>Torah</i> of its association with law. However, I
want to argue that the idea of law should be a happy and attractive concept.
Suffice it to say that all must grapple with the meaning as it applies to the
whole Bible and its application to our present lives. This includes the
Mosaic writings.
</font></p><hr width=150px style="margin-left: 10px">
