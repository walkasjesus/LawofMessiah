<font size=4 face="Times New Roman, Times, serif">
New Covenant <i>Torah</i>: What is the meaning of this phrase? New Covenant
<i>Torah</i> is a concept that recognizes the continuing place of
<i>Torah</i> (Law-instruction) of God as given in the Mosaic writings and
expanded in the Prophets and the writings of the New Covenant Scriptures. It
is a concept that also recognizes that we are in the New Covenant Era of
fulfillment. Though not under the Mosaic Covenant <i>per se</i>, we are
still to be instructed by the Mosaic writings and apply them, as fitting, to
the New Covenant order. This meaning will become clearer as I continue.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
As Thomas McComiskey argued in his excellent book "<u>Covenants of
Promise</u>", the Abrahamic Covenant is permanent. The Mosaic Covenant is a
temporary administration of the Abrahamic Covenant, and the New Covenant is
its permanent administration. However, the New Covenant teaches (explicitly
in some passages and implicitly throughout) that the Mosaic teaching is of
continuing validity; it is Scripture, and is "<i>profitable for teaching, for
reproof, for correction, and for training in righteousness, that the man of
God may be complete, equipped for every good work</i>" (<u>2 Timothy
3:16-17</u> RSV). This leads us to the unavoidable question of how it trains
us; what does its teaching require us to do if anything? This is the
question of <i>halakhah</i>, the Jewish concept of the way, or the
application of the <i>Torah</i> to new situations.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
It has been common to teach that Judaism is a religion of law and
Christianity a religion of grace. This is a distortion of both Judaism
(especially Biblical Judaism) and of Christianity. Judaism knows grace and
mercy; its prayers constantly appeal to God for forgiveness, not on the basis
of our merit, but solely on the basis of God's mercy and grace. Classical
Christianity also knows the place of the <i>Torah</i> (Law-instruction) and
has sought to apply the Law of God according to its understanding of what is
fitting to the New Covenant order. This has been so, even where it is not
clearly acknowledged that this is what is happening. For example,
Christianity has universally endorsed the teaching of Moses from Leviticus
concerning forbidden marriages which define incest. These standards are not
repeated in the New Testament. Christianity has an ethical tradition of
applying the Law which we could call its <i>halakhah</i>. The weakness of
Christianity in the west today is partly due to the loss of this ethical
tradition, and the gross abandonment of duty to train in it.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><a name=Ref1>THE CONCEPT OF HALAKHAH IN JEWISH THOUGHT</a></b>
</font>
<font size=3 face="Times New Roman, Times, serif">
&nbsp;&nbsp;
<a href="comment.php?d=intro-dj&h=<a name=Ref1>THE CONCEPT OF HALAKHAH IN JEWISH THOUGHT</a>"
onClick="return show_hide_box(this,400,3005,'1px solid')">[Make a Comment]</a>
</font>
