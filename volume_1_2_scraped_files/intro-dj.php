replacement theology of Classical Christianity where ethnic Israel has been
fully replaced in the purposes of God by the Church. On the other hand, we
oppose the antinomian heresy of Classical Dispensationalism. Messianic
Judaism does see discontinuity between the Mosaic Covenant and the New
Covenant. However, because Paul says that the gift and call of God to Israel
is irrevocable (<u>Romans 11:29</u>), we are oriented to greater continuity
than most Christian views. My little book <u>The Irrevocable Calling</u>
