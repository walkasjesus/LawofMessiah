<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The end of the 19<sup><small>th</small></sup> Century spawned a movement that
dramatically affected western Evangelical Protestantism. It broke from
Classical Christianity and presented a view of radical discontinuity between
the Mosaic Covenant and the New Covenant. Others had anticipated these
views, but were labeled antinomian heretics. Their movements came to little.
An Irish Anglican priest by the name of John Nelson Darby was troubled by the
"deadness" in the Anglican Church. In reflecting on the problem, Darby
concluded that the basic problem was confusion between law and grace. This
problem was rooted in the 39 Articles, the basic doctrinal statement of the
Anglican Communion. Darby asserted that the "Old Testament" was a
dispensation of law that was in total contrast to the New Testament period,
a dispensation of grace. Though having a place for ethnic Israel
in the future, all who become Christians are no longer "Jew" or "Gentile",
but part of the bride. Continued Jewish life in the New Covenant would
confuse law and grace. In Darby, the Mosaic Covenant offered salvation on
the basis of keeping the Law, and this was bound to lead to failure and
death. However, the sacrifices anticipated the coming of the Messiah so the
"Old Testament" saints were not without a grace element, and by believing in
the substitute sacrifices, they were preserved for the salvation that would
come in Yeshua. Darby radically redefined grace as only unmerited favor.
Darby taught that New Testament offers salvation by grace through faith with
a specific "Darby-ite" slant. It was that believing or faith in the death
and resurrection of Yeshua as one's personal Savior from sin is the only
necessary thing required for salvation. Repentance is not necessary.
Submitting to His holy lordship is not necessary. One may continue to live
in sin without showing life change or gratitude and still be saved. One
should not do so, however, but if salvation is by grace, it is a logical
possibility in his scheme. In Classical Christianity, however, the grace
concept included the content of empowerment and motivation to obedience.
This radical redefinition of grace produced a radical discontinuity between
the Mosaic Covenant and the New Covenant. Even the Sermon on the Mount was
seen as part of the dispensation of law. We are not duty-bound to anything
of this dispensation according to Darby and his followers. Even the Ten
Commandments do not have claim on us. We are only duty-bound to the teaching
of the epistles of the New Testament which do repeat the content of nine of
the Ten Commandments.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
In this radical discontinuity, Darby asserted the priesthood of all believers
and eschewed clergy. Lay ministers working in other professions would be the
elder-leaders. Ritual would be at a bare minimum (baptism and the Lord's
Supper). How did Darby see this as revitalizing the Church? In his view,
many would be grateful for the unmerited salvation, and they would dedicate
their entire lives. This is a second (but not required) step. The Church,
however, would then be populated by dedicated volunteers, not by people who
thought it was required of them for salvation. Carnal Christians would
largely be outside. In addition, the Church would enforce spiritual
discipline on the flock. Classical Dispensationalism made clear
distinctions. Israel has to do with the Law, the Church with grace. The
former has an earthly salvation, the latter a heavenly one. Today,
neo-dispensationalism is moderating this radical stance. Some teach that
repentance is needed and submission to His lordship (John McArthur). Others
now see the Sermon on the Mount as instruction for the New Covenant. A few
are even saying (from <u>2 Timothy 3:16-17</u>) that one can discern
principles in the Mosaic writings that have application to the dispensation
of grace.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The orientation of Dispensational theology has become part of the popular
sub-culture of American Evangelism. Many who could not even define the term
"Dispensationalism" reflect a popular culture born out of its theology.
Statements like "We are no longer under the Law," in a slant that is contrary
to Classical Christianity, are rooted in this movement. Those who say a
person was saved in a meeting reflect this sub-culture. Classical
Christians would only say a person made a profession and would not find
confidence in their salvation until the fruit of a changed life was brought
forth. Carnal Christianity has some rooting as well, since one may live in
sin and still hope for eternal security (contrary to both Calvinism and
Arminianism). Looseness and disrespect for the Law of God is a plague that
has affected Baptists, Charismatics, Pentecostals, Independent Churches, and
many smaller denominations and streams. Because America has been the leading
force for world missions, Dispensationalism is also found in many nations,
including (to my great chagrin) Israel. Many Israelis link the concept of
<i>Torah</i> with Rabbinical legalism. They unbiblically react in a negative
way to the idea of <i>Torah</i>. When some Israelis become believers in
Yeshua, they easily buy into the anti-Law dimensions of dispensational
theology from the past influence of Christian missions in the Land of Israel.
This is done without conscious realization. The Bible is read through
glasses without awareness that a Christian sub-culture has placed these
glasses upon them.
