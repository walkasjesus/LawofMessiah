therefore, we need to think through the meaning of clean and unclean laws
whose violation usually required temporary preclusion (until evening or for
seven days) from Temple involvement. Also, some of the clean-unclean laws, if
intentionally violated, required severe penalties. Today, these are
connected to higher moral meanings with contemporary application; an example
is the holiness of blood.) This gives some sense of where we see the New
Covenant as differing from the Mosaic. We should note that the New Covenant
replaces the administration of the Mosaic Covenant, but does not replace or
alter the Abrahamic Covenant. Rather, as McComiskey taught, the New Covenant
is the permanent administration of the Abrahamic Covenant. The New Covenant
is based on the fulfillments of the Messiah's crucifixion and resurrection,
