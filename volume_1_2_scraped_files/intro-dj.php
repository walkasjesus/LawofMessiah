This book suggests a Messianic Jewish <i>halakhic</i> approach to the various
commandments and laws (explicit and implied) that are found in the Bible. It
does not claim to be <i>halakhah</i> because it is not a body of rules that
any community has adopted as binding on its members. It nevertheless
presents a way of thinking that seeks to apply the Mosaic <i>Torah</i> (and
also other parts of the Bible) to the New Covenant order and to contemporary
life. As such, we have categorized the Law into basic subject divisions for
a more coherent presentation. One will see by this that the easy distinction
of Ceremonial and Moral Law does not fly, nor was it ever consistently
applied in any tradition of Christianity. Categorizing by subject does have
the disadvantage of not following the classical listing of 613 laws. In a
study of the laws and commandments, one will find that 613 is somewhat
arbitrary since some are repeated and not categorized well by subject.
However, because this is the classical Jewish way of putting forth the
biblical commandments, we will put traditional citations next to each
commandment or law being discussed, so that one can find it in Jewish lists
and commentaries.
