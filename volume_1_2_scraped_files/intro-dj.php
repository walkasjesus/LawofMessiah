<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Basically, it is understood that we are under condemnation due to our
violation of the universal dimensions of the Law of God as taught in
Scripture. However, Yeshua died to pay the penalty due us according to the
Law, and the record of our transgressions was cancelled. Not only so, but
when we repent and believe, submitting to Yeshua as Lord, we are transformed
within. The old man is crucified, and the body of sin is rendered powerless
(<u>Romans 6:6</u>). It is not only that He dies in our place, but we die in
Him and are raised to new life. This transformation of the inner man takes
place by identification with Messiah crucified and risen. Yeshua does not
die for us as separate from us, but He is part of the human race and our
corporate representative. In corporate reality, we are in Him, and He in us
by faith, just as we were in Adam. Furthermore, we can be filled with the
Spirit by faith in Him. Thereby do we enter the Kingdom of God. The Gospel
is the Gospel of the Kingdom, the invitation to come under the rule of the
King by submitting to His transforming grace rooted in His sacrificial death.
Having undergone this transformation, we are now capable of submitting to the
Law-instruction of God. Hence the biblical statements that by faith we
establish the Law (<u>Romans 3:31</u>) and the righteous requirement of the
Law is fulfilled in us who walk by the Spirit and not by the flesh (<u>Romans
8:3-4</u>). The Law is summarized in the commandments to love God with all
our hearts and to love our neighbors as ourselves (<u>Deuteronomy 6:4ff</u>;
<u>Leviticus 19:18</u>; <u>Mark 12:28-31</u>). Therefore "biblical ethics"
is part of the training of ministers and catechumens in Classical
Christianity.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Many understood that the Gospel of the Kingdom required that we come under
the rule of God. We seek to live out God's righteous order in every realm of
life. The righteous order is discerned by the teaching of the whole Bible.
It includes the order of God in marriage, in family, in business and
government, and in science and art! It, of course, includes His order for
the Church. Except for a post millennial minority which saw the Christians
taking over the whole world progressively and completely before the return of
Yeshua, Classical Christianity taught that we are to live out the Kingdom in
every sphere of life. Though we will not come into the fullness of the
Kingdom until the return of the Messiah, we are called to do all we can,
through the power of His Spirit, to establish the Law of God in every sphere
of human life and to thereby show as much of the Kingdom as possible. This
was seen as part of the believer's witness and as part of manifesting the
Glory of God in human life.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Many saw that the Ten Commandments were, for example, really two-fold. Some
of them summarized the requirement to love God exclusively, e.g., all of the
commandments against idolatry. All of the other commandments are expressions
of love for our fellow human beings created in His image. We love God in
rightly loving others. This was seen in the summary of both Yeshua and in
the Judaism of the first century (<u>Mark 12:28-31</u>; <u>Luke
10:25-28</u>). The love of our fellows is expressed in seeking justice and
in establishing God's righteous order. To pursue justice is therefore
equivalent to loving our neighbors, for human beings only find fulfillment in
God's order (<u>Micah 6:6-8</u>). Without the Law, love deteriorates into
mere humanistic sentiment. However, love motivates us to seek to establish
the order of God's righteousness, to maximize the fulfillment of human
beings, and more importantly, to show the wisdom and Glory of God in human
communities that live according to His ways. Justice is rectifying that
which is out of order by applying the blood of Yeshua for forgiveness and
establishing community on the basis of God's principles.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b>The Call for Justice</b>
</font>
<font size=3 face="Times New Roman, Times, serif">
&nbsp;&nbsp;
<a href="comment.php?d=intro-dj&h=The Call for Justice"
onClick="return show_hide_box(this,400,3005,'1px solid')">[Make a Comment]</a>
</font>
