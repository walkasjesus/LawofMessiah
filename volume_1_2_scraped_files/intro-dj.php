<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Martin Luther rejected much of Catholic doctrine. The priesthood of all
believers and the identification of the gathered community of saints (as a
temple) was asserted. However, the bread and wine of the Messiah's Supper
was seen as truly conveying the body and blood of Yeshua spiritually. The
liturgy that sets these elements apart, the role of the clergy, and the
symbolism of the Church itself still show marks of continuity to ancient
Israel. The seven-branch lamp, the eternal light, the priestly robes, and
the designation of the table of the Lord's Supper as the altar still show
continuity with the Ceremonial Law even if Lutheran theology too-easily
adopted the "ceremonial" and "moral" as distinctions. Luther was not
consistent in his approach to the Law. At one point he would assert that we
should love God and do as we please. He railed against those who held that
the Sunday Sabbath should be enforced and wrote that the English that were
enjoining sedate quiet and reflection, should be required to have sports and
recreation on Sunday. On the other hand, Luther's <u>Shorter and Larger
Catechism</u> put forth the Ten Commandments, with explanation and
application such that they should be obeyed. Luther's associate Philip
Melanchthon and the great Lutheran systematician Johannes Andreas Quenstedt,
both moved toward a more consistent position similar to Reform thought.
Luther's thought was, therefore, ambivalent to the Mosaic writings and was
inconsistent in approach. The relationship of the Law-Gospel dialectic was
never resolved; the ambivalence is still present in Lutheran thought today.
