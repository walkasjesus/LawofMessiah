<font size=4 face="Times New Roman, Times, serif">
Those of the Calvinist tradition projected a very high regard for
<i>Torah</i>. They accepted the distinction of the Ceremonial and Moral Law.
Calvin put forth a clear theology of the Law whereby it brings conviction of
sin and leads us to the Messiah; it restrains sin in society by becoming the
basis of civil law with its penalties, and it is a tool of discipleship for
the believer through the work of the Holy Spirit. This discipling tool
dimension for believers was called the "third use of the Law". Calvin found
this in <u>2 Timothy 3:16-17</u> where all Scripture was said to be valid for
training in righteousness. When the grace of God has been received, the
Spirit enables us to be obedient to the Law of God. The Sabbath (now
switched to Sunday according to their understanding of the New Testament) was
considered to be part of the Moral Law since it was in the Ten Commandments,
while (for example) <i>Yom Kippur</i> was seen as part of the Ceremonial Law.
Circumcision, the biblical calendar, and the Temple service have not passed
away. Dutch Reform Churches and Presbyterians followed in this tradition.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Puritans in the Calvinist tradition maintained the same basic stance whether
they became British Presbyterians or Congregationalist or Calvinist Baptists.
We see the same reflected in the writings of Increase Mather in the American
Colonies, and of Jonathan Edwards one hundred years later. The Colonial
Puritans, especially, wrestled with how to apply the Law of God and, more
than any other group, thought to build a society upon the basis of the Law.
The Moral Law was seen as including personal, civil, and business law.
Puritans exacted civil penalties, including capital punishment, based on the
Mosaic Law. They were a prosperous society, seeing themselves as a New
Israel, parallel to ancient Israel; yet they had special regard for ancient
Israel. Many saw a future purpose for ethnic Israel. Christmas celebration
in Puritan New England was forbidden as too pagan in its roots! On the
continuity-discontinuity issue, Puritans weighed in heavily on the side of
continuity. Applying the Law was not only abstract, but ordered Puritan life
for over a century.
