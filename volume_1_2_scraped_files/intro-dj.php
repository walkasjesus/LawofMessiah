<font size=4 face="Times New Roman, Times, serif">
In the 16<sup><small>th</small></sup> Century, several leaders believed that
the reformation did not go far enough. These were the Anabaptists, committed
to simple life, pacifism, adult baptism, and material sharing. While having
a high regard for the Law and seeing obedience as the outworking of grace,
they saw the teaching of the New Testament as a sufficient exposition and
application of the Law for Christians. The Sermon on the Mount was the
center of this understanding. Anabaptists saw continuity and discontinuity,
but thought that the New Covenant required a much higher standard than the
Mosaic. The Mosaic writings would be studied for ethical guidance, mostly to
supplement the New Testament. There was high regard for the principle of law
and holiness, but the New Covenant community in its pacifism, communal
sharing, openness to all peoples, and love for the enemy was seen in
discontinuity with the "Old Testament". Mennonites, Amish, Church of the
Brethren, Brethren in Christ and others come from the Anabaptist traditions.
In all of these traditions, we see that the Church has had to wrestle with
the issue of continuity and discontinuity. In doing so, the Church produced
varieties of Christian <i>halakhah</i>. Some saw great continuity and others
more discontinuity.
