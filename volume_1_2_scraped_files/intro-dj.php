<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The New Testament, which Messianic Jews often prefer to call the New Covenant
Scriptures, is <i>Torah</i>-positive. Unfortunately, there has been a gross
misunderstanding of the place of the Law of God in recent Christianity which
was not true in most of classical Christianity. This misunderstanding
derives from a theology called "dispensationalism" (in its classical form).
The problem is, in my opinion grave, but solely from the promise of God,
I do believe the Church will get it right before the return of the Lord
(<u>John 17:21</u>). Present theological trends, however, are not too
encouraging, though there are some glimmers of hope. Despite some
significant differences between classical Christianity and dispensationalism
(which we will outline later), classical Christianity came to a reasonably
correct conclusion concerning the place of the Law and the application of
Moses to the New Covenant order. This consensus basically yields a specific
theology of the relationship of spirit and law, gospel and law, and law and
grace. Perhaps John Calvin's summary in Book II Chapter 7 of his
"<u>Institutes of the Christian Religion</u>" is representative.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b>The Church Consensus</b>
</font>
<font size=3 face="Times New Roman, Times, serif">
&nbsp;&nbsp;
<a href="comment.php?d=intro-dj&h=The Church Consensus"
onClick="return show_hide_box(this,400,3005,'1px solid')">[Make a Comment]</a>
