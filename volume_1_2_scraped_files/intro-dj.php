<font size=4 face="Times New Roman, Times, serif">
<b><a name=Ref2>THE NEW TESTAMENT AND THE TORAH</a></b>
</font>
<font size=3 face="Times New Roman, Times, serif">
&nbsp;&nbsp;
<a href="comment.php?d=intro-dj&h=<a name=Ref2>THE NEW TESTAMENT AND THE TORAH</a>"
