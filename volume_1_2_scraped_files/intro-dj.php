Deuteronomy. Secondly, Kline asserted that the treaty form proves that the
Mosaic material and especially Deuteronomy is fully a covenant of grace.
Covenant - not law - is the basic category for understanding this material.
Law is a subsidiary concept. Other leading scholars picked up on this work
and now assert the same view as Kline. Samuel Schultz, the esteemed Old
