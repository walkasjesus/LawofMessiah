<font size=4 face="Times New Roman, Times, serif">
The following examples contain the word "<i>Torah</i>" in addition to one or
more of the above words, showing that "<i>Torah</i>" is connected to, but
distinguishable from, commandments, statutes, ordinances and judgments:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Numbers 19:2</u>: <i>This is the regulation from the Torah which Adonai
has commanded.</i><br>
Here, "<i>Torah</i>" cannot mean "regulation" or "ordinance".
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Numbers 31:21</u>: <i>... This is the regulation from the Torah which
Adonai has ordered Moshe.</i><br>
Here also, "<i>Torah</i>" cannot mean "regulation" or "ordinance".
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Deuteronomy 30:10</u>: <i>... so that you obey his mitzvot and
regulations which are written in this book of the Torah ...</i><br>
Some translations of this verse speak of "statutes" rather than
"regulations", so here, "<i>Torah</i>" cannot mean "<i>mitzvot</i>
(commandments)" or "regulations (statutes)".
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Psalm 89:31-32(30-31)</u>: <i>If his descendants abandon my Torah and
fail to live by my rulings, if they profane my regulations and don't obey my
mitzvot ...</i><br>
Here, "<i>Torah</i>" cannot mean "rulings (judgments)", "regulations
(statutes)" or "<i>mitzvot</i> (commandments)".
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Another way of distinguishing "<i>Torah</i>" (teaching) from "commandment",
"statute", "ordinance", and "judgment" is by its context. The following
Scriptures are best understood when "<i>Torah</i>" means "teaching":
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Exodus 13:9</u>: <i>Moreover, it will serve you as a sign on your hand
and as a reminder between your eyes, so that Adonai's Torah may be on your
lips; because with a strong hand Adonai brought you out of Egypt.</i>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Exodus 16:4</u>: <i>Adonai said to Moshe, 'Here, I will cause bread to
rain down from heaven for you. The people are to go out and gather a day's
ration every day. By this I will test whether they will observe my Torah or
not.'</i>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Psalm 1:2</u>: <i>Their delight is in Adonai's Torah; on his Torah they
meditate day and night.</i>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Psalm 37:31</u>: <i>The Torah of his God is in his heart; his footsteps
do not falter.</i>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Psalm 40:9(8)</u>: <i>Doing your will, my God, is my joy; your Torah is
in my inmost being.</i>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Proverbs 6:23</u>: <i>For the mitzvah is a lamp, Torah is light, and
reproofs that discipline are a way to life.</i>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Proverbs 7:2</u>: <i>Obey my commandments, and live; guard my teaching
like the pupil of your eye.</i>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Proverbs 13:14</u>: <i>The teaching of a wise man is a fountain of life,
enabling one to avoid deadly traps.</i>
