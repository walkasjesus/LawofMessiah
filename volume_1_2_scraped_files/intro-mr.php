<font size=4 face="Times New Roman, Times, serif">
<b><a name=Ref6>STATUTES OF THE MOSAIC LAW: DEPENDENT AND
TRANSCENDENT</a></b>
</font>
<font size=3 face="Times New Roman, Times, serif">
&nbsp;&nbsp;
<a href="comment.php?d=intro-mr&h=<a name=Ref6>STATUTES OF THE MOSAIC LAW: DEPENDENT AND
TRANSCENDENT</a>"
onClick="return show_hide_box(this,400,3005,'1px solid')">[Make a Comment]</a>
</font>
</p>
<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The Commandments contained in the <i>Tanakh</i> are God's explicit directives
and are, therefore, divine statutory
law<sup><small><a name=fr17 href=#fn17>17</a></small></sup>.
There are two categories of statutes commanded by God under the Mosaic
Covenant - those whose literal compliance depends upon the Covenant's
continued existence, and those whose literal compliance does not. I call the
first of these, "covenant-dependent", and the second of these,
"covenant-transcendent". Recognizing these two categories is important,
because the Book of Hebrews teaches:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Hebrews 8:13</u>: <i>By using the term, 'new', he has made the first
covenant 'old'; and something being made old, something in the process of
aging, is on its way to vanishing altogether.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Hebrews 8:13</u> suggests a diminishing literal role for
covenant-dependent statutes; consequently, such statutes are not incorporated
into New Covenant law in their original form and application, while statutes
that are covenant-transcendent are readily adopted without
change<sup><small><a name=fr18 href=#fn18>18</a></small></sup>.
So, for example, the literal application of statutes dealing with the
Levitical priesthood, the Temple, and the government of ancient Israel, are
in the process of vanishing (or have already
vanished<sup><small><a name=fr19 href=#fn19>19</a></small></sup>
as has the Mosaic Covenant been vanishing, but the principles they teach
remain
important,<sup><small><a name=fr20 href=#fn20>20</a></small></sup>
and we must not ignore them.
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Romans 7:6</u>: <i>But now we have been released from this aspect of the
Torah, because we have died to that which had us in its clutches, so that we
are serving in the new way provided by the Spirit and not in the old way of
outwardly following the letter of the law.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
It is my opinion that "oldness of the letter", as some translations read,
refers to the diminishing role of covenant-dependent statutes as literal law,
whereas our need for literal obedience to covenant-transcendent statutes
(e.g. <u>Exodus 20:1-17</u>) is timeless.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
In the <i>Tanakh</i>, statutes are not labeled as "covenant-dependent" or
"covenant-transcendent", and so one must exercise judgment in determining
which is which. A rule-of-thumb test is to ask: "Can or should this statute
be complied with in the New Covenant era exactly and literally as commanded?"
If the answer is "yes", it is "transcendent". If "no", it is "dependent".
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Sometimes, the answer to the above "test" question is obvious. No one would
disagree, for example, that it is as sinful to commit murder today, as in the
past. On the other hand, the question of whether Jews in the New Covenant
are still required to wear fringes on the corners of their garments
(<u>Numbers 15:38-41</u>), may evoke two opinions: (1) Yes, the original
reason for doing so still applies - to remind those who see the fringes of
the commandments of God, and (2) No, one can comply with the commandment's
principle by wearing an alternative item such as a necklace depicting two
tablets. In making such decisions for myself, I rely on the wisdom of a
saying that is not found in Scripture: "If it isn't broken, don't try to fix
it." So, if the literal commandment is as appropriately performable today as
during the time of Moses, then I do not search for an alternative.
</font></p><hr width=150px style="margin-left: 10px">
<p align=left style="margin-left: 30px; margin-right: 30px">
<font size=2>
<a name=fn17 href=#fr17>17.</a>
The terms "statutory law" and "statutes", as used here,
contemplate ordinances as well.
</font>
</p>
<p align=left style="margin-left: 30px; margin-right: 30px">
<font size=2>
<a name=fn18 href=#fr18>18.</a>
Mosaic statutes are not enforceable as law in the New
Covenant; it is their underlying teaching (<i>Torah</i>) and not the
statutes themselves that carry over (See "<i>Torah</i>" infra).
</font>
</p>
<p align=left style="margin-left: 30px; margin-right: 30px">
<font size=2>
<a name=fn19 href=#fr19>19.</a>
Some believe that the Mosaic Covenant has already
"vanished" as evidenced by Israel&#039;s two thousand-year inability to
conduct the Temple sacrifices (The Temple was destroyed in 70 A.D.).
</font>
</p>
<p align=left style="margin-left: 30px; margin-right: 30px">
<font size=2>
<a name=fn20 href=#fr20>20.</a>
The author does not believe that the temple,
priesthood, and statutes prophesied in <u>Ezekiel 40-48</u> indicate a
revitalization of the Mosaic Covenant. This is because (1) The priests
serving in that temple are limited to the sons of Zadok, (2) the High Priest
in that temple is "the prince" - not the sons of Aaron - and (3)
revitalization of the Mosaic Covenant would contradict <u>Hebrews 8:13</u>.
That being the case, the statutes appearing in the <u>Ezekiel chapters</u>
are, even when similar to Mosaic statutes, newly enacted and not
continuations of those in the Mosaic Covenant.
</font>
</p>
