<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The matter of proper application of Scripture looms very large. I have
approached this work prayerfully, and my interpretations are derived from my
sincere attempt to receive the wisdom of God through the <i>Ruach
HaKodesh</i>. I began with the <i>mitzvot</i> promulgated by Rabbi Yisrael
Meir HaKohen (the Chafetz Chaiyim) in his <i><u>Sefer haMitzvot
haKatzar</u></i><sup><small><a name=fr51 href=#fn51>51</a></small></sup>,
determined which <i>mitzvot</i> in Maimonides&#039;<sup><small><a name=fr52 href=#fn52>52</a>,<a name=fr53 href=#fn53>53</a></small></sup>,
and
HaChinuch&#039;s<sup><small><a name=fr54 href=#fn54>54</a></small></sup>
compilations corresponded, and then compared each of them with the Scriptures
they were intended to interpret. As a final step, I consulted a third
translator of
Maimonides<sup><small><a name=fr55 href=#fn55>55</a></small></sup>,
as well as New Covenant Scriptures that appeared relevant. Differences in
the chapter and verse citations among the various translated sources can be
explained by the fact that the translators and editors - not Maimonides,
Meir, or HaChinuch - placed them
there<sup><small><a name=fr56 href=#fn56>56</a></small></sup>.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Not being bound by Talmudic authority, I did not attempt to arrive at
<i>Taryag</i> (613) <i>mitzvot</i> and I did not follow Maimonides' practice
of listing <i>mitzvot</i> in categories of positive and negative.
<i>Mitzvot</i> that addressed the same or similar issues were combined, and I
made independent judgments regarding their applications and preferred means
of expression. All of this was done in collaboration with Dr. Daniel C.
Juster whose comments are liberally included.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
After merging the work of Meir and Maimonides and eliminating duplications, I
reduced the resulting list by eliminating those <i>mitzvot</i> that I judged
to be not literally observable in the New
Covenant<sup><small><a name=fr57 href=#fn57>57</a></small></sup>.
For example, Meir's Positive <i>Mitzvah</i> #MP52 (Deuteronomy 18:4) requires
that we give the <i>kohen</i> (Levitical priest) the first wool of our sheep.
