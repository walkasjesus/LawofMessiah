<font size=4 face="Times New Roman, Times, serif">
The Bible can be appreciated from many points of view. It may be thought of
as the biography of God in His dealings with mankind. It may be read as a
treatise on the history of the world and, in particular, the history of
Israel and the heathen nations. It may also be studied as a prophetic book,
revealing our future here on earth and in eternity. In this work, however,
it is mainly presented as a book of <i>Torah law</i>, containing God's
instructions for holy, moral, and victorious living.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
