vanished (<u>Hebrews
8:13</u>)<sup><small><a name=fr58 href=#fn58>58</a></small></sup>,
I conclude that there is no longer a reason to support the Levitical
<i>kohanim</i> with such
gifts<sup><small><a name=fr59 href=#fn59>59</a></small></sup>.
