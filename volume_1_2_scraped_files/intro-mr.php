<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Codifications written after <i><u>Sefer haMitzvot</u></i> have added to our
overall understanding of God's commandments, but no other codifier ever
attained the prestige and influence of Maimonides. As already stated,
Yisrael Meir haKohen's work, <i><u>Sefer haMitzvot haKatzar</i></u>, was
chosen here as a major comparison to Maimonides because he attempted to limit
his list of commandments to those he deemed performable in the Twentieth
Century. Although a complete listing and in-depth discussion of other law
codifiers are beyond the scope of this book, several nevertheless deserve
special mention:
</font></p><ol style="margin-left:30px" type=a>
<li><font size=4 face="Times New Roman, Times, serif">
<i><u>Mishneh Torah</u></i>, written by Maimonides, is a fourteen-book
compilation of laws gleaned from both Scripture and Talmud. It is reputed
to be complete, taking no account of a law's applicability in the
post-Temple era. Maimonides built the <i><u>Mishneh Torah</u></i> work
around his <i><u>Sefer haMitzvot</u></i>, so he reiterated all the
<i>mitzvot</i> in an introductory
list<sup><small><a name=fr42 href=#fn42>42</a></small></sup>,
and the relevant ones again before each of the fourteen books. The
relevance of <i><u>Mishneh Torah</u></i> for the present work is for its
expansive commentary and its subject-oriented organization.
<br><br></font></li>
<li><font size=4 face="Times New Roman, Times, serif">
<i><u>Sefer Mitzvot Gadol</u></i> (Big Book of Commandments), Moses of
Coucy (1<sup><small>st</small></sup> half of the
13<sup><small>th</small></sup> century).
<br><br></font></li>
<li><font size=4 face="Times New Roman, Times, serif">
<i><u>Sefer Mitzvot Katan</u></i> (Little Book of Commandments), Isaac ben
Joseph of Corveil (2<sup><small>nd</small></sup> half of the
13<sup><small>th</small></sup> century).
<br><br></font></li>
<li><font size=4 face="Times New Roman, Times, serif">
<i><u>Sefer HaChinuch</u></i> (The Book of Education) is attributed to
Aaron haLevi of Barcelona (c. 1257) and is still in common use today. It
was most probably based upon Abraham ibn Chasdai's translation of
Maimonides' first Arabic edition and, although basically organized
according to the chapter and verse sequence of the
<i>Tanakh</i><sup><small><a name=fr43 href=#fn43>43</a></small></sup>,
his first manuscript retained some positive and negative commandment
groupings as part of its order. In contrast, the <i><u>Sefer
HaChinuch</u></i> version that is in print today contains none of the
positive/negative groupings. This is the result (according to Charles
Wengrov<sup><small><a name=fr44 href=#fn44>44</a></small></sup>)
of an early printer's decision to reorder the <i>mitzvot</i>, causing them
to appear in exactly the same sequence as the verses in the <i>Tanakh</i>;
and so it remains today. The importance of <i><u>Sefer HaChinuch</u></i>
for the present work, besides its order and historical content, is its
commentary on Scripture application which is superb.
