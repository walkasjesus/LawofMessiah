The New Covenant was prophesied in <u>Jeremiah
31:30-33(31-34)</u><sup><small><a name=fr8 href=#fn8>8</a></small></sup>:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>'Here the days are coming,' says Adonai, 'when I will make a new covenant
with the house of Isra'el and with the house of Y'hudah. It will not be like
the covenant I made with their fathers on the day I took them by their hand
and brought them out of the land of Egypt; because they, for their part,
violated my covenant, even though I, for my part, was a husband to them,'
says Adonai. 'For this is the covenant I will make with the house of Isra'el
after those days,' says Adonai: 'I will put my Torah within them and write it
on their hearts; I will be their God, and they will be my people. No longer
will any of them teach his fellow community member or his brother, "Know
Adonai"; for all will know me, from the least of them to the greatest;
because I will forgive their wickednesses and remember their sins no
more.'</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Since in the Hebrew text of this Scripture, the word for "law" is
<img src=images/word-torah-1.gif height=24 align=top> (<i>Torah</i>),
Jeremiah is quoting God as saying "<b>I will put My <i>Torah</i> in their
minds, and write it on their hearts</b>". Handily, this Scripture is
repeated in the Greek New Covenant Scriptures in <u>Hebrews 8:8-12</u>.
There, the Greek word corresponding to "<i>Torah</i>"
(<u>verse 10</u>) is <i>nomos</i> (Strong's No. 3551). This word is defined
as "<i>Torah</i>" by <u>Friberg's New Testament
Lexicon</u><sup><small><a name=fr9 href=#fn9>9</a></small></sup>
as follows:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<img src=images/word-nomos-2.gif height=18 align=top> w. a basic mng. of what
is assigned or proper <i>law</i>; (1) gener. any <i>law</i> in the judicial
sphere (<u>Ro 7.1</u>); (2) as rule governing one's conduct <i>principle,
law</i> (<u>Ro 7.23</u>); (3) more specif. in the NT, of the Mosaic system of
legislation as revealing the divine will (<b>the Torah</b>) <i>the law (of
Moses)</i> (<u>Lk 2.22</u>); in an expanded sense, Jewish relig. laws
developed fr. the Mosaic law <i>(Jewish) law</i> (<u>Jn 18.31</u>; <u>Ac
23.29</u>); (4) as the collection of writings considered sacred by the Jews;
(a) in a narrower sense, the Pentateuch, the first five books of the Bible,
as comprising <i>the law</i> (<u>Mt 12.5</u>; <u>Ga 3.10b</u>); (b) in a
wider sense, the OT scriptures as a whole (<u>Mt 5.18</u>; <u>Ro 3.19</u>);
(5) fig. as the Christian Gospel, the New Covenant, as furnishing a new
principle to govern spiritual life <i>law</i> (<u>Ro 8.2a</u>; <u>He
10.16</u>).
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
A word related to "<i>nomos</i>", the Greek root word, "<i>nomotheteo</i>"
(<img src=images/word-nomos-3.gif height=18 align=top> - Strong's No. 3549),
occurs in <u>Hebrews 8:6</u> as <img src=images/word-nomos-4.gif height=18
align=top>. This word is translated "established" in the New King James
Version:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>But now He has obtained a more excellent ministry, inasmuch as He is also
