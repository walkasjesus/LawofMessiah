<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The Law of Messiah <i>Mitzvot</i> in this book are of two kinds; they are (1)
inclusive of one or more of the classical <i>mitzvot</i> (those from
Maimonides, Meir, or HaChinuch), or (2) completely unrelated to the classical
<i>mitzvot</i>. The classical <i>mitzvot</i> are based only upon Scriptures
from the <i>Torah</i>, whereas the <i>Mitzvot</i> written by the authors
herein are based upon Scriptures throughout the Bible. Those containing
Scripture references from the classical <i>mitzvah</i> compilations are
marked by an asterisk in the Table of Contents, and one or more of the
referenced Scriptures carry the label "Key Scriptures". In the
<i>Mitzvot</i> that are unrelated, their "Key Scriptures" are those that
appear to the authors as most relevant; these are not necessarily presented
