or Meir, and not necessarily because I deem them critical (or even logical)
for supporting a <i>mitzvah</i> under discussion. Finally, when I quote
Scripture, I quote entire verses even where Maimonides or Meir quote them
only in part, and even when I am only relying on part of a verse to prove or
support a <i>mitzvah</i>.
