According to Rabbinical understanding, the Holy Scriptures are organized into
three parts - the <i>Torah</i> (<i>Pentateuch</i>, Law), the <i>Nevi'im</i>
(Prophets), and the <i>Ketuvim</i> (Holy
Writings)<sup><small><a name=fr16 href=#fn16>16</a></small></sup>.
Rabbinical Judaism does not acknowledge the current existence of the "New
Covenant" prophesied in <u>Jeremiah 31:31(30)</u>, or the inspiration of the
New Covenant Scriptures. One of the ways that Judaism has historically
viewed "<i>Torah</i>" has been as synonymous with the Law of Moses - the law
to which it considers itself bound today. Its further understanding is that
all of the Mosaic Law is contained in "the <i>Torah</i>" (Pentateuch), and
that all remaining Scriptures of the <i>Tanakh</i>, while inspired, merely
historicize, exemplify, and embellish. Consequently, Rabbinical Judaism
devotes most of its attention to the "<i>Torah</i>" and to the oral tradition
(<i>Talmud</i>), to which it looks for interpretation.
</font></p><hr width=150px style="margin-left: 10px">
<p align=left style="margin-left: 30px; margin-right: 30px">
<font size=2>
<a name=fn16 href=#fr16>16.</a>
This categorization, to wit, <i>Torah</i>,
<i>Nevi&#039;im</i> and <i>Ketuvim</i>, has resulted in the Rabbinical
canon of the Bible being known by the acronym <i>Tanakh</i>. It is
noteworthy that Yeshua Himself referred to Scripture by these categories
(<u>Luke 24:44</u>).
</font>
</p>
