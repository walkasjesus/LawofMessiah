</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
We have seen that there are two components of Law in the <i>Tanakh</i> that
are eternal; they are (1) covenant-transcendent statutes, and (2)
<i>Torah</i> (teaching) that is revealed through all Scripture. Since, by
definition, covenant-dependent statutes vanish along with their vanishing
covenant, it is the covenant-transcendent statutes, plus all <i>Torah</i>
contained in the <i>Tanakh</i>, plus all law added by the New Covenant
Scriptures, which form the "Law of Messiah" to which we are accountable
today. Hence, the Law of Messiah is the New Covenant counterpart of the Law
of Moses.
