<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The Theological Wordbook of the Old Testament defines "<i>Torah</i>"
(<img src=images/word-torah-1.gif height=24 align=top> - Strong's number
8451) as follows<sup><small><a name=fr1 href=#fn1>1</a></small></sup>:
</p>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
The word t&ocirc;r&acirc; means basically &quot;teaching&quot; whether it is
the wise man instructing his son or God instructing Israel. The wise give
insight into all aspects of life so that the young may know how to conduct
themselves and to live a long blessed life (<u>Prov 3:1 f.</u>). So too God,
motivated by love, reveals to man basic insight into how to live with each
other and how to approach God. Through the law God shows his interest in all
aspects of man's life which is to be lived under his direction and care. Law
of God stands parallel to word of the Lord to signify that law is the
revelation of God's will (e.g. <u>Isa 1:10</u>). In this capacity it becomes
the nation's wisdom and understanding so that others will marvel at the
quality of Israel's distinctive life style (<u>Deut 4:6</u>). Thus there is a
very similar understanding of the role of teaching with its results in the
wisdom school, in the priestly instruction, and the role of the law with its
results for all the people of the covenant.
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
Specifically law refers to any set of regulations; e.g., <u>Exo 12</u>
contains the law in regard to observing the Passover; some other specific
laws include those for the various offerings (<u>Lev 7:37</u>), for leprosy
(<u>Lev 14:57</u>) and for jealousy (<u>Num 5:29</u>). In this light law is
often considered to consist of statutes, ordinances, precepts, commandments,
and testimonies.
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
The meaning of the word gains further perspective in the light of
<u>Deut</u>. According to <u>Deut 1:5</u> Moses sets about to explain the
law; law here would encompass the moral law, both in its apodictic and
casuistic formulation, and the ceremonial law. The genius of <u>Deut</u> is
that it interprets the external law in the light of its desired effect on
man's inner attitudes. In addition, the book of <u>Deut</u> itself shows
that the law has a broad meaning to encompass history, regulations and their
interpretation, and exhortations. It is not merely the listing of casuistic
statements as is the case in Hammurabi's code. Later the word extended to
include the first five books of the Bible in all their variety.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
In addition, The <u>New Brown-Driver-Briggs-Gesenius Hebrew and English
Lexicon</u> gives "direction, instruction,
law"<sup><small><a name=fr2 href=#fn2>2</a></small></sup>
as the primary definitions of "<i>Torah</i>", and the <u>Encyclopaedia
Judaica</u>
states:<sup><small><a name=fr3 href=#fn3>3</a></small></sup>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>Torah</i> is derived from the root <img src=images/word-yarah-1.gif
height=20 align=top> which in the <i>hifil</i> conjugation means "to teach"
(cf. <u>Lev. 10:11</u>). The meaning of the word is therefore "teaching",
"doctrine", or "instructions"; the commonly accepted "law" gives a wrong
impression. The word is used in different ways but the underlying idea of
"teaching" is common to all.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
