<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
To bring consensus, Judaism needed a scholar of such prestige that he could
define 613 <i>mitzvot</i> that would be acceptable to a majority of the
Jewish community. Such a scholar emerged in the person of Moshe ben Maimon
(Maimonides, aka "the RAMABAM") who, sometime prior to 1170 c.e., wrote his
compilation of <i>Torah</i> law in Arabic under the title <i><u>Kitab
Al-Fara'id</u></i> (<u>The Book of Divine Precepts</u>). He subsequently
revised his work and so, by the end of his life, there were two Arabic texts
or versions of <i><u>Kitab Al-Fara'id</u></i> in existence. Unlike his
predecessors, Maimonides was careful to follow defined principles (fourteen)
to justify his conclusions. This made all the difference, and his work
received almost universal
acceptance<sup><small><a name=fr34 href=#fn34>34</a></small></sup>.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Three contemporaries of Maimonides translated his texts into Hebrew, and
these translations became known as <i>Sefer haMitzvot</i>. Abraham ibn
Chasdai made his translation from Maimonides' first
version<sup><small><a name=fr35 href=#fn35>35</a></small></sup>.
Maimonides' second text (translated into Hebrew by Solomon ibn Job of Granada
and separately by Moses ibn Tibbon) is, however, considered the "standard
Arabic text" today. More recently, Dr. Chaim Heller published a "corrected"
Hebrew text in which he compared and reconciled Maimonides' Arabic texts with
that of ibn Job, and an even more recent translation (Jerusalem Hebrew Text)
was made by Rabbi Joseph Kapach.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
As an academic achievement, Maimonides' enumeration of <i>mitzvot</i> was
huge. However, it was too exhaustive to be a convenient tool in the
post-Temple era, when many of the <i>mitzvot</i> dealing with sacrifices and
the Levitical priesthood could no longer be performed.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Enter Rabbi Yisrael Meir
haKohen<sup><small><a name=fr36 href=#fn36>36</a></small></sup>.
In 1931, Rabbi Meir published <i>Sefer haMitzvot haKatzar</i> (The Concise
Book of
<i>Mitvoth</i>)<sup><small><a name=fr37 href=#fn37>37</a></small></sup>,
in which he extracted from Maimonides' list, Two Hundred Ninety Seven (297)
<i>mitzvot</i> - Seventy-seven (77) positive, One Hundred Ninety-four (194)
negative, and Twenty-six (26) applicable only in the Land of Israel. Rabbi
Meir intended his book to be a compilation of <i>mitzvot</i> that could be
observed by Jews in the post-Temple era and, particularly in the Diaspora.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Besides listing fewer <i>mitzvot</i> than Maimonides, Rabbi Meir differs from
him in other ways as well. To begin with, the two compilers number their
<i>mitzvot</i> differently and present them in a different order. As a
consequence, it is not always easy to determine which one of Meir's
<i>mitzvot</i> corresponds to a given <i>mitzvah</i> of Maimonides. Second,
while they usually agree on the Scriptures that define a given
<i>mitzvah</i>, it is not always the case (nor is it the case with other
commentators). It is also important to note that, while both Maimonides and
Meir quote Hebrew Scripture as proof texts for their respective
<i>mitzvot</i>, neither of their original writings give supportive chapter
and verse numbers<sup><small><a name=fr38 href=#fn38>38</a>,<a name=fr39 href=#fn39>39</a></small></sup>
and their quotations are not always of the entire verses as they appear in
modern Hebrew Bibles. Translators and editors of both their works added
chapter and verse citations that did not appear in the
originals<sup><small><a name=fr40 href=#fn40>40</a></small></sup>
and, in some cases, they quoted entire verses of Scripture where the original
writings quoted only parts of
verses<sup><small><a name=fr41 href=#fn41>41</a></small></sup>.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Finally, Maimonides and Meir do not always agree on the statement of the
<i>mitzvah</i> that they extract from a given Scripture. For example, in
response to <u>Exodus 12:18</u>, Maimonides' Positive <i>Mitzvah</i> #RP158
states that we are to eat unleavened bread on the evening of the
15<sup><small>th</small></sup> day of <i>Nisan</i>, while Meir's Positive
<i>Mitzvah</i> #MP23 states that we are to eat unleavened bread on the
evening of the 14<sup><small>th</small></sup> day of <i>Nisan</i>.
Presumably, the interpretive difference is in whether "evening" is understood
to be before or after sundown. Another example is presented by
<u>Deuteronomy 6:13</u>. The Scripture itself states that we are to "fear the
LORD your God and serve Him". That notwithstanding, Maimonides' Positive
<i>Mitzvah</i> #RP5 interprets it as "worshipping" God, while Rabbi Meir's
Positive <i>Mitzvah</i> #MP7 says it means to "pray" to God. Clearly, the
ways the two compilers interpret Scripture and write their respective
<i>mitzvot</i> reflect both their judgment and their theology.
</font></p><hr width=150px style="margin-left: 10px">
<p align=left style="margin-left: 30px; margin-right: 30px">
<font size=2>
<a name=fn34 href=#fr34>34.</a>
