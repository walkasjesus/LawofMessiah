</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Most English translations of this verse of Scripture employ the expression
"the Law of
Messiah",<sup><small><a name=fr27 href=#fn27>27</a></small></sup>
which is from where I derive the title of this
book.<sup><small><a name=fr28 href=#fn28>28</a></small></sup>
