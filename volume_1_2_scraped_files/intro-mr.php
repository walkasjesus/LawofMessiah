There are basically two ways in which "<i>Torah</i>" is used in the
<i>Tanakh</i>. In one usage, "<i>Torah</i>" refers to "the Law" - God's legal
code which provides instructions on specific matters. Examples of this usage
are:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
"This is the <b><i>Torah</i></b> of the burnt offering..." (<u>Leviticus</u>
<u>6:2(9)ff</u>);<sup><small><a name=fr4 href=#fn4>4</a></small></sup>
"grain offering..." (<u>Leviticus 6:7(14)ff</u>); "sin offering..."
(<u>Leviticus 6:18(25)ff)</u>; "trespass offering..." (<u>Leviticus
7:1ff</u>); "peace offering..." (<u>Leviticus 7:11ff</u>); "leprous
plague..." (<u>Leviticus 13:59ff</u>); "jealousy..." (<u>Numbers 5:29ff</u>).
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
In most instances, however, "<i>Torah</i>" refers broadly to God's teaching -
His universal and eternal standard for conduct and life. Where "<i>Torah</i>"
means "law", it is usually accompanied by other Hebrew words having to do
with law, such as:
</font></p>
