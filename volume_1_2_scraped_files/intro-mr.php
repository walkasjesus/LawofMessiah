of the <i>Tanakh</i> - and, when more than one Scripture is cited, it is the
first that governs the order; unfortunately, there are several occasions in
which the order of Chill's <i>mitzvot</i> depart from this plan, but the
reader is alerted where that happens. Chill's Scripture proof texts for the
various <i>mitzvot</i> he lists are more numerous and diverse than those of
either Maimonides or Meir, but his <i>mitzvot</i> are not easy to reference
because they are not numbered.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
