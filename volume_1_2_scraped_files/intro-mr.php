<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
As used in this book, a "<i>dibeyr</i>" is an explicit commandment of God in
Scripture - an imperative directive (usually a statute or ordinance of the
Mosaic Law) which may be either covenant-dependent or transcendent.
"<i>Torah</i>" can mean several things but, in the absence of the article
"the" (e.g. "the <i>Torah</i>"), it most often means God's essential and
eternal teaching or instruction. As used in this book, <i>Torah</i>
underlies all of God's
<i>dibrot</i>,<sup><small><a name=fr29 href=#fn29>29</a></small></sup>
and is revealed (explicitly or implicitly) by all Scripture - not just the
Scriptures of the <i>Pentateuch</i>. A "<i>Mitzvah</i>" is an interpreted
statement of God's will, derived from Scripture, and expressed in the form of
a commandment. A Mosaic statute or ordinance does not become a
<i>mitzvah</i> (as used herein) until it is interpreted and restated.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
There have been several attempts in history to codify God's Word into
numbered
<i>mitzvot</i><sup><small><a name=fr30 href=#fn30>30</a></small></sup>.
The earliest of these was <i>Hilchot Gedolot</i>, a work by Simon Kairo
published sometime in the 8<sup><small>th</small></sup> century. By that
time, a principle had already been established in the <i>Talmud</i>, that the
total number of <i>mitzvot</i> in the <i>Torah</i> was <i>Taryag</i>
(613)<sup><small><a name=fr31 href=#fn31>31</a></small></sup>;
and of these, two hundred forty-eight (248) were positive (<i>mitzvot
aseh</i>), and three hundred sixty-five (365) were negative (<i>mitzvot
lo ta&#039;aseh</i>)<sup><small><a name=fr32 href=#fn32>32</a></small></sup>.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Anyone who attempts to enumerate <i>mitzvot</i> in the <i>Torah</i> soon
realizes that there are decisions to be made. What, for example, should one
consider to be a
<i>mitzvah</i>?<sup><small><a name=fr33 href=#fn33>33</a></small></sup>
What level of departure from the plain meaning of the Biblical text is
permissible? What level of inference is allowable? How does one count
similar expressions of God's will that are stated differently at different
places in the Scriptures? Do we count as two <i>mitzvot</i>, those that are
expressed both in the positive and in the negative in different verses of
Scripture, or do we count them as one? It is not surprising that those who
have attempted this work have sometimes come to different conclusions.
</font></p><hr width=150px style="margin-left: 10px">
<p align=left style="margin-left: 30px; margin-right: 30px">
<font size=2>
<a name=fn29 href=#fr29>29.</a>
The plural of <i>dibeyr</i>.
</font>
</p>
<p align=left style="margin-left: 30px; margin-right: 30px">
<font size=2>
<a name=fn30 href=#fr30>30.</a>
The plural of <i>mitzvah</i>.
</font>
</p>
<p align=left style="margin-left: 30px; margin-right: 30px">
<font size=2>
<a name=fn31 href=#fr31>31.</a>
Shab. 87a
</font>
