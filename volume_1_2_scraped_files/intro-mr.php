The term "Law of Messiah" appears only once in the Bible, and that is in
<u>Galatians 6:2</u>. That notwithstanding, the term is extremely
significant in signaling that there exists such a law, that it is related to
"<i>Torah</i>" and, as we shall see, that both it and "<i>Torah</i>" are
alive and well under the New Covenant.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<b><a name=Ref1>TORAH AND THE LAW</a></b>
</font>
<font size=3 face="Times New Roman, Times, serif">
&nbsp;&nbsp;
<a href="comment.php?d=intro-mr&h=<a name=Ref1>TORAH AND THE LAW</a>"
