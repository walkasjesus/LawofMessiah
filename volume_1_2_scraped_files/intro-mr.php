<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Underlying each of God's commandments is <i>Torah</i>, which we have seen is
broadly defined as His essential
teaching<sup><small><a name=fr21 href=#fn21>21</a>,<a name=fr22 href=#fn22>22</a></small></sup>.
Although not all <i>Torah</i> is statutory in origin, all <i>torah</i> is
God's law because it is the essence of His will. Even if a commandment is
covenant-dependent, its <i>torah</i> transcends the change in covenant
because God's values never change (<u>Numbers 23:19</u>; <u>Psalm
102:26-28(25-27)</u>; <u>Hebrews 13:8</u>). You will notice that sometimes
I capitalize "<i>Torah</i>" and sometimes I do not and write it
"<i>torah.</i>" It is just my way of indicating whether I am speaking of
God's teaching generically or more specifically as His law (or do I mean
"Law?"). You see? That is why you should not take my capitalizations
seriously, and Hebrew is not capitalized anyway.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<i>Torah</i> is not only revealed through Mosaic commandments, but through
all
Scripture<sup><small><a name=fr23 href=#fn23>23</a></small></sup>.
The apostle Paul expressed this in <u>2 Timothy 3:16-17</u>:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>All Scripture is God-breathed and is valuable for teaching the truth,
convicting of sin, correcting faults and training in right living; thus
anyone who belongs to God may be fully equipped for every good work.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
And of the <i>Torah</i> that underlies covenant-dependent statutes contained
in the Law of Moses, Paul wrote in <u>Galatians 3:24-25</u>:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>Accordingly, the Torah functioned as a custodian until the Messiah came,
so that we might be declared righteous on the ground of trusting and being
faithful. But now that the time for this trusting faithfulness has come, we
are no longer under a custodian.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Finally, in <u>Jeremiah 31:30-33(31-34)</u><sup><small><a name=fr24 href=#fn24>24</a>,<a name=fr25 href=#fn25>25</a></small></sup>
Jeremiah prophesied that God would make a New Covenant based upon existing
<i>Torah</i>:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>'Here the days are coming,' says Adonai, 'when I will make a new covenant
with the house of Isra'el and with the house of Y'hudah. It will not be like
the covenant I made with their fathers on the day I took them by their hand
and brought them out of the land of Egypt; because they, for their part,
violated my covenant, even though I, for my part, was a husband to them,'
says Adonai. For this is the covenant I will make with the house of Isra'el
after those days,' says Adonai: 'I will put my Torah within them and write it
on their hearts; I will be their God, and they will be my people. No longer
will any of them teach his fellow community member or his brother, 'Know
Adonai'; for all will know me, from the least of them to the greatest;
because I will forgive their wickednesses and remember their sins no
more.'</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Thus, not only does <i>Torah</i> pass into the New Covenant, it defines it,
and without it there would be no New
Covenant<sup><small><a name=fr26 href=#fn26>26</a></small></sup>.
