<b>New Covenant Literal Application Code</b>
</font>
</p>
<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Each <i>mitzvah</i> in this compilation includes a New Covenant Literal
Application Code (abbreviated <b>NCLA</b>) that consists of three parts. The
first part specifies Jew, <i>K'rov Yisrael</i>, or Gentile. The second part
specifies Male, or Female. The third part specifies one of seven possible
levels of literal compliance that applies to each. The NCLA Code
accompanying each <i>Mitzvah</i> reflects the author's interpretive opinion,
and is provided for the reader's prayerful consideration only.
