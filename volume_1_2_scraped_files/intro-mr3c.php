<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
In <u>Romans 7:4-6</u>, Paul says:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>Therefore, my brethren, you also have become dead to the law through the
body of Messiah, that you may be married to another -- to Him who was
raised from the dead, that we should bear fruit to God. For when we were
in the flesh, the sinful passions which were aroused by the law were at
work in our members to bear fruit to death. But now we have been delivered
from the law, having died to what we were held by, so that we should serve
in the newness of the Spirit and not in the oldness of the letter.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Also, in <u>Galatians 2:19:20</u>, Paul says:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
For I through the law died to the law that I might live to God. I have
been crucified with Messiah; it is no longer I who live, but Messiah lives
in me; and the life which I now live in the flesh I live by faith in the
Son of God, who loved me and gave Himself for me.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The expressions "dead to the law", "died to the law", and "delivered from
the law" have led many to believe that God's Law no longer applies to us.
That is not at all what Paul is saying.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
In <u>Romans 7:4-6</u>, Paul is comparing our relationship with God to the
intimate relationship of marriage. Prior to the New Covenant, our
"marriage" relationship with God was mostly through our obedience to the
statutes of <i>Torah</i> because we did not yet have Yeshua's sacrifice or
the Holy Spirit to draw us closer. It was a wonderful relationship, but
God made it even better by sending Yeshua who sacrificed Himself for us.
This brought us into a "marriage" relationship with Yeshua that was more
intimate than our marriage relationship through the Law. But because it is
not God's will that we be in two marriages at the same time, Paul explains
that we died during our marriage to the Law in order to clear the way for
our "marriage" to Yeshua. That is what Paul meant when he said:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
"<i>you also have become dead to the law through the body of Messiah, that
you may be married to another</i>" (<u>Romans 7:4</u>) and his other
saying: "<i>I through the law died to the law that I might live to God</i>"
(<u>Galatians 2:19</u>).
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Paul's reference to being "delivered from the Law" and being "held" by the
law is a continuation of his metaphor that we have moved from one marriage
relationship to another. It is not as some believe - Paul stating that
God's Law held us in bondage. I said "God's Law" instead of just "Law"
purposely, in order to illustrate how much easier it is to accept that some
nondescript "Law" held us in bondage, than to think that "God's Law" held
us in bondage, which we know cannot be true. Also, Paul's reference to the
"newness of the Spirit" and the "oldness of the letter" is completely
consistent with the Scriptures and everything else Paul said because, in
the New Covenant, our principal way of relating to God has changed. Paul
recognizes our changed relationship to the Law, but at no time infers that
the <i>Torah</i> has been done away with or has been rendered useless.
</font></p>
