<font size=3 face="Times New Roman, Times, serif">
<u>Romans 7:6</u><br>
<i>But now we have been delivered from the law, having died to what we were
held by, so that we should serve in the newness of the Spirit and not in
the oldness of the letter.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
The <u>2 Corinthians</u> Scripture refers to the written Law as a "Ministry
of Death", and says that "the letter (presumably of the Law) kills". It
certainly appears to support the view of those who would preach doing away
with the Law, yet the Scripture also says that the written Law was glorious
- an apparent contradiction. What then is Paul trying to say? Paul is
reflecting about the Old Covenant, when the Holy Spirit was not accessible
to the average Israelite, and when literal obedience to the Law was indeed
the prescribed path to life. But it ceased to be the path to life in the
New Covenant when Yeshua's sacrifice gave us direct access to God, and the
Holy Spirit was given to us to be the Law's interpreter. So Paul is
warning us that today our approach to God's Law must be through the Holy
Spirit and not through mere literal observance. He is warning us that
although literal obedience to the Law led us to life under the Old
Covenant, by-passing the Holy Spirit to pursue literal obedience will lead
us to death under the New. That is not to say that today the letter of the
Law has no application. It does, but it must be the Holy Spirit who gives
us the application.
