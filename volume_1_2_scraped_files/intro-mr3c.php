us that Messiah is to whom the Law brings us. As for <u>Galatians 5:14</u>,
both the Law of Moses and the Gospel of Yeshua stand for selfless sacrifice
as against self-interest. When we "<i>love 'our' neighbor as
'ourself'</i>", we fulfill the ultimate of what the Law stands for, but we
are not released from obeying God's specific and detailed Commandments. If
we were, He would have told us.
