<p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Galatians 3:10-13</u>:
<i>For as many as are of the works of the law are under the curse; for it
is written, "Cursed is everyone who does not continue in all things which
are written in the book of the law, to do them. But that no one is
justified by the law in the sight of God is evident, for "the just shall
live by faith." Yet the law is not of faith, but "the man who does them
shall live by them." Messiah has redeemed us from the curse of the law,
having become a curse for us (for it is written, "Cursed is everyone who
hangs on a tree"), ...</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Once again, the words that Paul uses are often misconstrued - in this case,
the word "curse", which some would have us believe means that God's Law is
a curse. No, the curse to which Paul is referring originates in
<u>Deuteronomy 30:19</u>, which reads:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>I call heaven and earth as witnesses today against you, that I have set
before you life and death, blessing and cursing; therefore choose life,
that both you and your descendants may live</i>.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
When these words were given by God to Moses under the previous covenant,
the context was 'covering over sin' through obedience to the Law and animal
sacrifice. Israel's choice to either obey the Law and live, or disobey and
not live, was a reference to salvation that, unbeknown to the Israelites at
Mt. Sinai, would be granted in the future as a result of Yeshua's
sacrifice. Paul's words paralleling <u>Deuteronomy</u> were spoken many
centuries after Sinai under the New Covenant, when our path to salvation
had been transferred from conducting animal sacrifices to having faith in
Yeshua, the ultimate sacrifice. Paul was warning the Galatians not to
seek covering over of their sins in the old way of obedience to the Law
with attendant animal sacrifices, but rather to seek salvation in the new
way which was through Yeshua. Not only was the old way no longer
authorized but, under it, even a single violation would result in the
cursing referred to in <u>Deuteronomy 30:19</u> that led to death. But
Paul is clear in <u>Galatians 3:11 (NKJ)</u> that in the New Covenant:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>no one is justified by the law</i>,
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
and he quotes <u>Habakkuk 2:4</u> (see also, <u>Romans 1:7</u> and
<u>Hebrews 10:38</u>):
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
"<i>the just shall live by his faith</i>".
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Bottom line: It is not a curse to seek to keep the Law; the curse comes
from trying to keep it as a means of acquiring salvation.
