<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
In <u>Romans 3:20 &amp; 28</u>, Paul says:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>Therefore by the deeds of the law no flesh will be justified in His
sight, for by the law is the knowledge of sin ... Therefore we conclude
that a man is justified by faith apart from the deeds of the law.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
And in <u>Galatians 2:15-16 &amp; 21</u>:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>We who are Jews by nature, and not sinners of the Gentiles, knowing that
a man is not justified by the works of the law but by faith in Yeshua the
Messiah, even we have believed in Messiah Yeshua, that we might be
justified by faith in Messiah and not by the works of the law; for by the
works of the law no flesh shall be justified ... I do not set aside the
grace of God; for if righteousness comes through the law, then Messiah died
in vain.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
These Scriptures and others like them are often quoted to deny the Law's
continuing value in the New Covenant. After all (some think), if
righteousness cannot be acquired from performing the deeds of the Law, then
why do them? The answer is that, with the power and discernment that the
Holy Spirit allows us, we perform the deeds of the Law because they are
God's will - not in order to become righteous and acquire salvation.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Paul is very clear that no one is justified (i.e. made righteous - saved)
through merely obeying commandments. Justification comes only through our
reliance on Yeshua's sacrifice that expunges our sins if we receive Him
and repent; that's why <u>Galatians 2:16</u> states:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
"<i>... a man is not justified by the works of the law but by faith in
Yeshua the Messiah</i>".
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Romans 3:28</u> says the same thing but differently; it says:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
"<i>... a man is justified by faith apart from the deeds of the law</i>".
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
None of this contradicts Paul's contention that God's Law continues to have
value in the New Covenant. We can be certain of this because in <u>Romans
3:31</u> Paul says:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
"<i>Do we then make void the law through faith? Certainly not! On the
contrary, we establish the law</i>".
</font></p>
