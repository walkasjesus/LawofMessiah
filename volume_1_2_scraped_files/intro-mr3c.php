<p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Colossians 2:16-17</u>:
<i>So let no one judge you in food or in drink, or regarding a festival or
a new moon or sabbaths, which are a shadow of things to come, but the
substance is of Messiah.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This Scripture is sometimes used to assert that God's food laws, appointed
times, and other special days commanded in Scripture are no longer in
effect, and that those who adhere to them are putting themselves back under
the "Law of sin and death", and even denying Yeshua. Now that's a pretty
ominous charge for merely resting on the Sabbath and abstaining from pork.
To see what is actually being said here, let's widen our search to verses
of Scripture that come both before and after:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<b>Before</b>: <u>Colossians 2:8</u>:
<i>Beware lest anyone cheat you through philosophy and empty deceit,
according to the tradition of men, according to the basic principles of the
world, and not according to Messiah.</i>
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<b>After</b>: <u>Colossians 2:20-22</u>:
<i>Therefore, if you died with Messiah from the basic principles of the
world, why, as though living in the world, do you subject yourselves to
regulations -- "Do not touch, do not taste, do not handle", which all
concern things which perish with the using -- according to the commandments
and doctrines of men?</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Notice that the words in <u>verse 16</u>,
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
let no one judge you in food or in drink, or regarding a festival or a new
moon or sabbaths,
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
is sandwiched between <u>verse 8</u> and <u>verses 20-22</u> that warn
against becoming captive to the traditions, commandments, and doctrines of
men. But God's <i>Torah</i> regarding permissible foods and how to keep
His festivals, new moons, and Sabbaths, are not the traditions of men, so
Paul was not, therefore, advocating disregard for the <i>Torah</i>. Rather,
he was warning us against being in bondage to "fences" that some would
place around the <i>Torah</i> - rules promulgated by men that are beyond
and more stringent than those commanded by God.
</font></p>
