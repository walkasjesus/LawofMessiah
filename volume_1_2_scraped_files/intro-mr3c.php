<p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<u>Hebrews 8:6-7 &amp;13</u>:
<i>But now He has obtained a more excellent ministry, inasmuch as He is
also Mediator of a better covenant, which was established on better
promises. For if that first covenant had been faultless, then no place
would have been sought for a second ... In that He says, "A new covenant",
He has made the first obsolete. Now what is becoming obsolete and growing
old is ready to vanish away.</i>
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
These verses of Scripture are sometimes used, in conjunction with others,
to assert the Mosaic Law's inadequacy, defectiveness, and obsolescence.
But if those making the assertion would look at the Scriptures carefully,
they would see that what is being spoken of is the Mosaic Covenant - not
the Mosaic Law. A covenant is a relationship - an agreement between
parties. The Mosaic Covenant was the relationship that was consummated at
Mt. Sinai between God and the Israelites, where God gave the Israelites His
Laws (the <i>Torah</i>), and the Israelites promised to obey. Well, they
didn't obey, and they consequently breached the Covenant; the word "breach"
and "fault" are the same. The Covenant between God and the Israelites
developed a fault that was not intrinsic to the covenant agreement, but
rather one that was created by the Israelites' disobedience. We see this
clearly in <u>Hebrews 8:8</u> that says:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
<i>Because finding fault with THEM, He says: "Behold, the days are coming,
says the LORD, when I will make a new covenant with the house of Israel and
with the house of Judah ...</i>"
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Notice how the Scripture says "<i>finding fault with THEM</i>". The fault
was with the Israelites - not in the design of the covenant. Then we read
in both <u>Hebrews 8:8</u> and <u>Jeremiah 31:30-31(31-32)</u> that, rather
than God leaving Israel without a covenant (which He could have done and
which they deserved), He graciously gave them a New and different covenant
that was based on better promises. In the process of doing that, He
allowed the First Covenant to gradually pass into oblivion; that is what
Paul means by the First Covenant becoming obsolete.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
So, do we need to obey the Mosaic Law? Well, yes and no! Part of the
Mosaic Law can no longer be complied with because it has lost its
covenantal infrastructure. We have no operative Levitical Priesthood, no
Jerusalem Temple in which to conduct animal sacrifices, and no unified
leadership of Israel. Yeshua should be recognized by everyone as the King
of Israel, and His sacrifice as the reason why many commandments connected
to the Temple can no longer be performed. Still, there are many
commandments that remain doable, but they have now come under New Covenant
administration and are subject to New Covenant enforcement. When added to
other mandates of the New Covenant, they collectively become what
<u>Galatians 6:2</u> calls "the Law of Messiah".
