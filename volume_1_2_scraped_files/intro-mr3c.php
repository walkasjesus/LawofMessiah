<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
<u>Romans 6:14</u>:
"</i>For sin shall not have dominion over you, for you are not under law
but under grace</i>."
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
This sounds like Paul is saying that God's "grace" releases us to do
anything we want, and from having to obey any of God's laws. Why would the
Apostle Paul, a confessed keeper of the Law (<u>Acts 21:17-26</u>), say
such a thing? Did Yeshua's appearance on earth and subsequent sacrifice
cause God to change into a permissive liberal? I knew that couldn't be the
case, but still, what Paul said perplexed me, so I decided to pray and ask
God about it.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
What I believe I heard prophetically in reply is this: Being "under grace"
is not automatically permissive as some would like it to be. Grace does
sometimes imply permissive allowance or forgiveness, but being under God's
grace does not mean that He always grants it to us. Rather, it means that
we are subject to the <u>operation</u> of God's grace, and that He (and
only He) decides when to extend it and when to withhold it.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Theologians often define "grace" (<i>kharis</i>) as "God's unmerited favor"
(which is correct) but, in his writings, Paul uses it metaphorically to
mean God Himself, whose very person embodies grace. <u>Romans 6:14</u> is
therefore informing us that our accountability is no longer merely to the
"statutes" of God's written Word (His <i>Torah</i> or Law), but is now
directly to God Himself through the living Word, who is Yeshua (<u>John
1:14</u>). Our being "under grace" is Paul's way of saying that, in this
direct accountability to God, God may either extend His grace to us or
withhold it, on a case by case basis, according to His supreme sense of
mercy and justice. Our being "not under law", (the Greek word here for
"under" is <i>hupo</i>, meaning "underneath") connotes that we are no
longer "underneath" the Law's enforcement; that is, we no longer
automatically receive the Law's penalties for disobedience, but there is no
doubt that we continue to be (and are now directly) subject to God's
correction and punishment (as well as His praise and reward) when we are
deserving of them. Meanwhile, the commandments of <i>Torah</i>, as
interpreted by the Holy Spirit, retain their relevance in being a main
source for our determining God's Will, but our "obedience" relationship
with God is now direct rather than indirect. That is why Paul says in
<u>Galatians 5:18</u>:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
"<i>But if you are led by the Spirit, you are not under the law</i>".
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
And similarly in <u>Galatians 3:24-25</u>:
</font></p><p align=justify style="margin-left: 30px; margin-right: 30px">
<font size=3 face="Times New Roman, Times, serif">
"<i>Therefore the law was our tutor to bring us to Messiah, that we might
be justified by faith. But after faith </i>[meaning "Messiah"]<i> has
come, we are no longer under a tutor</i>".
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
What I have said about "grace" thus far has been in response to Paul's
juxtaposing "grace" and "law". I would, however, be remiss were I not to
mention another kind of grace that God <u>never</u> withholds. It is God's
empowerment, through the Holy Spirit, to do as well and as much as God, in
His sovereignty, is willing to release us to. So it would seem, for
example, that God always extends grace to a person who seeks His power for
obedience, and to do good. We depend on God's grace to perform His will.
</font></p>
