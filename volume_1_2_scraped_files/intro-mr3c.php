But as we seek the Holy Spirit for how to keep the Law, we are hindered by
ever-present "elephants in the room". The "elephants" of which I speak are
the well-known writings of the Apostle Paul which seem to say that obeying
the Mosaic Law is no longer profitable and may even cause spiritual harm. I
knew it was not so, but because I have had to restudy and rethink Paul's
statements every time someone raised them to me in debate, I decided to write
this short commentary in order to explain what I believe Paul really meant
in his writings.
</font></p><p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Each numbered caption that follows is a statement one often hears from
opponents of observing Biblical Law. Then following each caption are
Scriptures (translated in the New King James), authored by Paul, that are
often used to support erroneous views of the Law, and after that comes my
commentary. The New King James translation is used (with "Messiah" replacing
"Christ" and "Yeshua" replacing "Jesus") because it and the NIV are among
the most popular, and the most egregious in misrepresenting Paul.
