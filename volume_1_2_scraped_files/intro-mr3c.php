</p>
<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Messianic Judaism is similar to other Judaisms in seeking to apply the laws
of <i>Torah</i> that God gave to the Israelites at Mt. Sinai. Since those
early days, many changes have occurred that impact our ability to keep the
Law as our ancestors did. They include (1) a change in the Covenant, (2)
Yeshua's birth, ministry on earth, death, and resurrection, (3) universal
accessibility to the Holy Spirit, (4) expansion of the Scriptures to include
the New Testament, (5) lack of a functioning Levitical Priesthood, (6)
absence of the Temple of God and, most important of all, (7) salvation
through faith in Yeshua. These are the New Covenant realities under which we
now live. Consequently the way we keep the Law today cannot be solely
according to the letter; rather, it must be guided by the Holy Spirit for
both wisdom and application.
