<font size=4 face="Times New Roman, Times, serif">
The <i>mitzvot</i> compiled by the Classical Commentators were for the
ancient nation of Israel that had at its disposal everything it needed to
comply with each of them literally. Not only that, but for the Israelites
who lived back then, every <i>mitzvah</i> given to Moses made sense because
it met some need within their community. Today, however, things are very
different. The Messiah has come and has become our sacrifice for sin, there
is no Holy Tabernacle or Temple where sacrifices are conducted, there is no
functioning Levitical priesthood, we live under secular governments, and we
have advanced in science and technology in ways that preclude the need for
certain precautions that were important for the Israelites.
