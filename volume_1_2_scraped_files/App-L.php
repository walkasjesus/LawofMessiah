<font size=6>
<b>Explicit <i>Mitzvot</i> of the New Covenant <i>Shlichim</i></b>
</font>
</p>
<p align=justify style="margin-left: 10px; margin-right: 10px">
<font size=4 face="Times New Roman, Times, serif">
Whereas New Covenant Scriptures are liberally cited in support of
<i>Mitzvot</i> elsewhere in this book, this Appendix lists those words of the
<i>Shlichim</i> that were spoken in the imperative and can therefore be
considered commandments in their own
right.<sup><small><a name=fr1 href=#fn1>1</a></small></sup>
Some of these are unique, and some repeats of <i>mitzvot</i> given earlier
through Moses or spoken by Yeshua.
</font></p><hr width=150px style="margin-left: 10px">
<p align=left style="margin-left: 30px; margin-right: 30px">
<font size=2>
<a name=fn1 href=#fr1>1.</a>
The <i>mitzvot</i> in this list are those that are
explicitly expressed in the imperative case. That notwithstanding, many
other <i>mitzvot</i> can be implied from the <i>Shlichim</i>&#039;s
teachings that are not stated in the imperative.
</font>
</p>
